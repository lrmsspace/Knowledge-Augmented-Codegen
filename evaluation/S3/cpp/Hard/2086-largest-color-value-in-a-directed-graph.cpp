class Solution {
public:
    int largestPathValue(string colors, vector<vector<int>>& edges) {
        int n = colors.size();
        vector<vector<int>> graph(n);
        vector<int> inDegree(n, 0);
        for (const auto& edge : edges) {
            graph[edge[0]].push_back(edge[1]);
            inDegree[edge[1]]++;
        }

        queue<int> q;
        for (int i = 0; i < n; ++i) {
            if (inDegree[i] == 0) {
                q.push(i);
            }
        }

        vector<vector<int>> colorCount(n, vector<int>(26, 0));
        int visitedNodes = 0;
        int maxColorValue = 0;

        while (!q.empty()) {
            int node = q.front();
            q.pop();
            visitedNodes++;

            int colorIndex = colors[node] - 'a';
            colorCount[node][colorIndex]++;
            maxColorValue = max(maxColorValue, colorCount[node][colorIndex]);

            for (int neighbor : graph[node]) {
                for (int c = 0; c < 26; ++c) {
                    colorCount[neighbor][c] = max(colorCount[neighbor][c], colorCount[node][c]);
                }
                inDegree[neighbor]--;
                if (inDegree[neighbor] == 0) {
                    q.push(neighbor);
                }
            }
        }

        return visitedNodes == n ? maxColorValue : -1;
    }
};
