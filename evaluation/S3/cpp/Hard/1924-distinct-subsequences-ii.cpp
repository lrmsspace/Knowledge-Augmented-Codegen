class Solution {
public:
    int distinctSubseqII(string s) {
        int mod = 1000000007;
        vector<int> last(26, -1);
        vector<long long> dp(s.size() + 1, 0);
        dp[0] = 1;

        for (int i = 0; i < s.size(); ++i) {
            int c = s[i] - 'a';
            dp[i + 1] = (2 * dp[i]) % mod;
            if (last[c] != -1) {
                dp[i + 1] = (dp[i + 1] - dp[last[c]] + mod) % mod;
            }
            last[c] = i;
        }

        return (int)(dp[s.size()] - 1);         
    }
};
