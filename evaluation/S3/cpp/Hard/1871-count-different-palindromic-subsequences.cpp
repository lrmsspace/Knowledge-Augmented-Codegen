class Solution {
public:
    int countPalindromicSubsequences(string s) {
        int n = s.size();
        vector<vector<int>> dp(n, vector<int>(n, 0));
        vector<int> next(n), prev(n);
        vector<int> last(4, -1);

        for (int i = 0; i < n; ++i) {
            int c = s[i] - 'a';
            prev[i] = last[c];
            last[c] = i;
        }

        fill(last.begin(), last.end(), -1);
        for (int i = n - 1; i >= 0; --i) {
            int c = s[i] - 'a';
            next[i] = last[c];
            last[c] = i;
        }

        for (int i = n - 1; i >= 0; --i) {
            dp[i][i] = 1;
            for (int j = i + 1; j < n; ++j) {
                if (s[i] == s[j]) {
                    int low = next[i];
                    int high = prev[j];
                    if (low > high) {
                        dp[i][j] = dp[i + 1][j - 1] * 2 + 2;
                    } else if (low == high) {
                        dp[i][j] = dp[i + 1][j - 1] * 2 + 1;
                    } else {
                        dp[i][j] = dp[i + 1][j - 1] * 2 - dp[low + 1][high - 1];
                    }
                } else {
                    dp[i][j] = dp[i + 1][j] + dp[i][j - 1] - dp[i + 1][j - 1];
                }
                dp[i][j] = (dp[i][j] + 1000000007) % 1000000007;
            }
        }

        return dp[0][n - 1];            
    }
};
