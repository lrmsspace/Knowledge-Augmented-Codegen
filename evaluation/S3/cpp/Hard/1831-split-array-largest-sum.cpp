class Solution {
public:
    int splitArray(vector<int>& nums, int k) {
        int low = 0;
        int high = 0;

        for (int num : nums) {
            low = max(low, num);
            high += num;
        }

        while (low < high) {
            int mid = low + (high - low) / 2;
            if (canSplit(nums, k, mid)) {
                high = mid;
            } else {
                low = mid + 1;
            }
        }

        return low;         
    }
};
