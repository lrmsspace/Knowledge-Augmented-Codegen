class Solution {
public:
    int longestValidSubstring(string word, vector<string>& forbidden) {
        // Implementing the solution using string hashing and a sliding window approach.
        // We will use a hash set to store the forbidden substrings and a rolling hash to check substrings efficiently.

        unordered_set<string> forbiddenSet(forbidden.begin(), forbidden.end());
        int maxLength = 0;
        int left = 0;

        for (int right = 0; right < word.size(); ++right) {
            // Check the current substring from left to right
            string currentSubstring = word.substr(left, right - left + 1);

            // If the current substring is forbidden, move the left pointer
            while (forbiddenSet.count(currentSubstring) > 0) {
                ++left;
                currentSubstring = word.substr(left, right - left + 1);
            }

            // Update the maximum length of valid substring
            maxLength = max(maxLength, right - left + 1);
        }

        return maxLength;           
    }
};
