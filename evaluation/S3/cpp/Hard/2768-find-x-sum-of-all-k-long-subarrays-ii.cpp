class Solution {
public:
    vector<long long> findXSum(vector<int>& nums, int k, int x) {
        int n = (int)nums.size();
        vector<long long> result(n - k + 1, 0);
        unordered_map<int, int> freq;
        priority_queue<pair<int, int>> pq; // (frequency, value)

        for (int i = 0; i < k; ++i) {
            freq[nums[i]]++;
        }
        for (const auto& [num, count] : freq) {
            pq.emplace(count, num);
        }

        for (int i = 0; i <= n - k; ++i) {
            long long xSum = 0;
            int count = 0;
            vector<pair<int, int>> temp;

            while (!pq.empty() && count < x) {
                auto [freqCount, value] = pq.top();
                pq.pop();
                xSum += (long long)freqCount * value;
                temp.emplace_back(freqCount, value);
                count++;
            }
            result[i] = xSum;

            // Restore the priority queue
            for (const auto& p : temp) {
                pq.push(p);
            }

            if (i + k < n) {
                // Remove the leftmost element of the current window
                int leftNum = nums[i];
                freq[leftNum]--;
                if (freq[leftNum] == 0) {
                    freq.erase(leftNum);
                }
                pq.emplace(freq[leftNum], leftNum);

                // Add the new element to the right of the window
                int rightNum = nums[i + k];
                freq[rightNum]++;
                pq.emplace(freq[rightNum], rightNum);
            }
        }

        return result;          
    }
};
