class Solution {
public:
    int maxValue(vector<vector<int>>& events, int k) {
        int n = (int)events.size();
        vector<vector<int>> dp(k + 1, vector<int>(n + 1, 0));
        vector<int> startDays(n);
        for (int i = 0; i < n; ++i) {
            startDays[i] = events[i][0];
        }
        sort(events.begin(), events.end(), [](const vector<int>& a, const vector<int>& b) {
            return a[1] < b[1];
        });

        for (int i = 1; i <= n; ++i) {
            int startDay = events[i - 1][0];
            int endDay = events[i - 1][1];
            int value = events[i - 1][2];
            int idx = (int)(upper_bound(startDays.begin(), startDays.end(), startDay - 1) - startDays.begin());
            for (int j = 1; j <= k; ++j) {
                dp[j][i] = max(dp[j][i - 1], dp[j - 1][idx] + value);
            }
        }

        return dp[k][n];            
    }
};
