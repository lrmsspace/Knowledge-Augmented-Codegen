class Solution {
public:
    int minimumTimeRequired(vector<int>& jobs, int k) {
        int n = jobs.size();
        int low = *max_element(jobs.begin(), jobs.end());
        int high = accumulate(jobs.begin(), jobs.end(), 0);
        int result = high;

        auto canFinish = [&](int maxTime) {
            vector<int> workers(k, 0);
            return backtrack(jobs, workers, maxTime, 0);
        };

        while (low <= high) {
            int mid = low + (high - low) / 2;
            if (canFinish(mid)) {
                result = mid;
                high = mid - 1;
            } else {
                low = mid + 1;
            }
        }

        return result;        
    }
};
