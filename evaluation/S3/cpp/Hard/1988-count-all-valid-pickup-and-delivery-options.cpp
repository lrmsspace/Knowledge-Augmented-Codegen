class Solution {
public:
    int countOrders(int n) {
        long long ans = 1;
        int mod = 1000000007;

        for (int i = 1; i <= n; ++i) {
            ans = (ans * (i * 2 - 1) * i) % mod;
        }

        return (int)ans;            
    }
};
