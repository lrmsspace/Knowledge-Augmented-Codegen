class Solution {
public:
    int dieSimulator(int n, vector<int>& rollMax) {
        const int MOD = 1'000'000'007;
        vector<vector<long long>> dp(n + 1, vector<long long>(7, 0));
        vector<long long> sum(n + 1, 0);

        for (int i = 1; i <= 6; ++i) {
            dp[1][i] = 1;
            sum[1] = (sum[1] + dp[1][i]) % MOD;
        }

        for (int i = 2; i <= n; ++i) {
            for (int j = 1; j <= 6; ++j) {
                int k = rollMax[j - 1];
                if (i - k - 1 >= 0) {
                    dp[i][j] = (sum[i - 1] - sum[i - k - 1] + MOD) % MOD;
                } else {
                    dp[i][j] = sum[i - 1];
                }
                sum[i] = (sum[i] + dp[i][j]) % MOD;
            }
        }

        return sum[n];          
    }
};
