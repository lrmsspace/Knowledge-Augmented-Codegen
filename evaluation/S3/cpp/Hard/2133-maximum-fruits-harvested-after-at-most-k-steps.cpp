class Solution {
public:
    int maxTotalFruits(vector<vector<int>>& fruits, int startPos, int k) {
        int n = fruits.size();
        int maxPos = 0;
        for (const auto& f : fruits) {
            maxPos = max(maxPos, f[0]);
        }
        vector<int> prefix(maxPos + 1, 0);
        for (const auto& f : fruits) {
            prefix[f[0]] += f[1];
        }
        for (int i = 1; i <= maxPos; ++i) {
            prefix[i] += prefix[i - 1];
        }

        int ans = 0;
        for (int leftSteps = 0; leftSteps <= k; ++leftSteps) {
            int rightSteps = k - leftSteps;
            int leftPos = startPos - leftSteps;
            int rightPos = startPos + rightSteps;
            if (leftPos < 0) {
                leftPos = 0;
            }
            if (rightPos > maxPos) {
                rightPos = maxPos;
            }
            ans = max(ans, prefix[rightPos] - (leftPos > 0 ? prefix[leftPos - 1] : 0));
        }
        return ans;         
    }
};
