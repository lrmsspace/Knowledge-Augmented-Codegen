class Solution {
public:
    string reformat(string s) {
        string digits, letters;
        for (char c : s) {
            if (isdigit(c)) {
                digits += c;
            } else {
                letters += c;
            }
        }

        if (abs((int)digits.size() - (int)letters.size()) > 1) {
            return "";
        }

        string result;
        int i = 0, j = 0;
        while (i < digits.size() || j < letters.size()) {
            if (i < digits.size()) {
                result += digits[i++];
            }
            if (j < letters.size()) {
                result += letters[j++];
            }
        }

        return result;          
    }
};
