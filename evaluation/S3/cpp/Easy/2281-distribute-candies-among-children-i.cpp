class Solution {
public:
    int distributeCandies(int n, int limit) {
        int count = 0;
        for (int i = 0; i <= n; ++i) {
            for (int j = 0; j <= n; ++j) {
                int k = n - i - j;
                if (k >= 0 && k <= limit && i <= limit && j <= limit) {
                    ++count;
                }
            }
        }
        return count;           
    }
};
