class Solution {
public:
    int maxContainers(int n, int w, int maxWeight) {
        // The maximum number of containers that can be loaded is limited by either the number of cells on the deck (n*n) or the weight capacity of the ship (maxWeight / w).
        // We take the minimum of these two values to ensure we do not exceed either constraint.
        return std::min(n * n, maxWeight / w);          
    }
};
