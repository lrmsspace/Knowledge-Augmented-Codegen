class Solution {
public:
    int possibleStringCount(string word) {
        int count = 1; // Start with the original string itself
        for (size_t i = 1; i < word.size(); ++i) {
            if (word[i] == word[i - 1]) {
                count++; // Increment count for consecutive characters
            }
        }
        return count;           
    }
};
