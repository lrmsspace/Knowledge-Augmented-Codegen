class Solution {
public:
    vector<int> preorder(Node* root) {
        vector<int> result;
        if (root == nullptr) {
            return result;
        }
        
        stack<Node*> s;
        s.push(root);
        
        while (!s.empty()) {
            Node* node = s.top();
            s.pop();
            result.push_back(node->val);
            
            // Push children in reverse order to maintain left-to-right traversal
            for (auto it = node->children.rbegin(); it != node->children.rend(); ++it) {
                s.push(*it);
            }
        }
        
        return result;          
    }
};
