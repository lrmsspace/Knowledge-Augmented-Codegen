class Solution {
public:
    vector<int> shuffle(vector<int>& nums, int n) {
        vector<int> result(2 * n);
        for (int i = 0; i < n; ++i) {
            result[2 * i] = nums[i];       // Place x_i at even index
            result[2 * i + 1] = nums[i + n]; // Place y_i at odd index
        }
        return result;          
    }
};
