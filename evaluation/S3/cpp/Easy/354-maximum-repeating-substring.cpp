class Solution {
public:
    int maxRepeating(string sequence, string word) {
        int maxCount = 0;
        string current = word;

        while (sequence.find(current) != string::npos) {
            maxCount++;
            current += word; // Append the word to itself for the next check
        }

        return maxCount;            
    }
};
