class Solution {
public:
    vector<int> evenOddBit(int n) {
        vector<int> res(2, 0);
        for (int i = 0; n > 0; ++i, n >>= 1) {
            if ((n & 1) == 1) {
                res[i % 2]++;
            }
        }
        return res;         
    }
};
