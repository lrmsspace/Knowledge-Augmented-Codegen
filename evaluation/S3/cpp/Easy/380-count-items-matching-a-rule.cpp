class Solution {
public:
    int countMatches(vector<vector<string>>& items, string ruleKey, string ruleValue) {
        int keyIndex;
        if (ruleKey == "type") {
            keyIndex = 0;
        } else if (ruleKey == "color") {
            keyIndex = 1;
        } else { // ruleKey == "name"
            keyIndex = 2;
        }

        int count = 0;
        for (const auto& item : items) {
            if (item[keyIndex] == ruleValue) {
                count++;
            }
        }
        return count;           
    }
};
