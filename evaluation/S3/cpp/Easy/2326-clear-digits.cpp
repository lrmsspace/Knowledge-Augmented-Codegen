class Solution {
public:
    string clearDigits(string s) {
        string result;
        for (char c : s) {
            if (isdigit(c)) {
                if (!result.empty()) {
                    result.pop_back(); // Remove the closest non-digit character to the left
                }
            } else {
                result.push_back(c); // Add non-digit characters to the result
            }
        }
        return result;          
    }
};
