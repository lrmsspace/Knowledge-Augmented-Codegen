class Solution {
public:
    int secondHighest(string s) {
        int maxDigit = -1;
        int secondMaxDigit = -1;

        for (char c : s) {
            if (isdigit(c)) {
                int digit = c - '0';
                if (digit > maxDigit) {
                    secondMaxDigit = maxDigit;
                    maxDigit = digit;
                } else if (digit > secondMaxDigit && digit < maxDigit) {
                    secondMaxDigit = digit;
                }
            }
        }

        return secondMaxDigit;          
    }
};
