class Solution {
public:
    vector<int> twoOutOfThree(vector<int>& nums1, vector<int>& nums2, vector<int>& nums3) {
        vector<int> count(101, 0);
        for (int num : nums1) count[num] |= 1;
        for (int num : nums2) count[num] |= 2;
        for (int num : nums3) count[num] |= 4;

        vector<int> result;
        for (int i = 1; i <= 100; ++i) {
            if (__builtin_popcount(count[i]) >= 2) {
                result.push_back(i);
            }
        }
        return result;          
    }
};
