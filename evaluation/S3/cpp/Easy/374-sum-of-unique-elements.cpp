class Solution {
public:
    int sumOfUnique(vector<int>& nums) {
        int count[101] = {0}; // Since nums[i] is in the range [1, 100]
        for (int num : nums) {
            count[num]++;
        }
        
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            if (count[i] == 1) { // Only add unique elements
                sum += i;
            }
        }
        
        return sum;         
    }
};
