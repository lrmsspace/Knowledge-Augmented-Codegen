class Solution {
public:
    int getMinDistance(vector<int>& nums, int target, int start) {
        int minDistance = INT_MAX;
        for (size_t i = 0; i < nums.size(); ++i) {
            if (nums[i] == target) {
                int distance = std::abs(static_cast<int>(i) - start);
                minDistance = std::min(minDistance, distance);
            }
        }
        return minDistance;         
    }
};
