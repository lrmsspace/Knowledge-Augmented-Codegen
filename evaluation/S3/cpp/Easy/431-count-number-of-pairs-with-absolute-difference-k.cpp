class Solution {
public:
    int countKDifference(vector<int>& nums, int k) {
        int count = 0;
        for (size_t i = 0; i < nums.size(); ++i) {
            for (size_t j = i + 1; j < nums.size(); ++j) {
                if (abs(nums[i] - nums[j]) == k) {
                    ++count;
                }
            }
        }
        return count;           
    }
};
