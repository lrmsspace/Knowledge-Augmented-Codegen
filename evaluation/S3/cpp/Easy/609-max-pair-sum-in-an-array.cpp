class Solution {
public:
    int maxSum(vector<int>& nums) {
        vector<int> maxDigit(10, -1);
        int result = -1;

        for (int num : nums) {
            int largestDigit = 0;
            int temp = num;

            while (temp > 0) {
                largestDigit = max(largestDigit, temp % 10);
                temp /= 10;
            }

            if (maxDigit[largestDigit] != -1) {
                result = max(result, num + maxDigit[largestDigit]);
            }

            maxDigit[largestDigit] = max(maxDigit[largestDigit], num);
        }

        return result;          
    }
};
