class Solution {
public:
    bool checkPerfectNumber(int num) {
        if (num <= 1) {
            return false; // Perfect numbers are positive integers greater than 1
        }
        
        int sum = 1; // Start with 1, which is a divisor of every number
        for (int i = 2; i <= sqrt(num); ++i) {
            if (num % i == 0) {
                sum += i; // Add the divisor
                if (i != num / i) {
                    sum += num / i; // Add the complementary divisor if it's different
                }
            }
        }
        
        return sum == num; // Check if the sum of divisors equals the original number           
    }
};
