class Solution {
public:
    int similarPairs(vector<string>& words) {
        int count = 0;
        for (size_t i = 0; i < words.size(); ++i) {
            for (size_t j = i + 1; j < words.size(); ++j) {
                if (areSimilar(words[i], words[j])) {
                    ++count;
                }
            }
        }
        return count;           
    }
};
