class Solution {
public:
    int findLucky(vector<int>& arr) {
        int count[501] = {0}; // Since arr[i] is in the range [1, 500]
        for (int num : arr) {
            if (num <= 500) {
                count[num]++;
            }
        }
        
        int lucky = -1;
        for (int i = 1; i <= 500; i++) {
            if (count[i] == i) {
                lucky = i; // Update lucky to the largest found
            }
        }
        
        return lucky;           
    }
};
