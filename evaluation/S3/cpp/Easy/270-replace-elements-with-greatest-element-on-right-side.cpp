class Solution {
public:
    vector<int> replaceElements(vector<int>& arr) {
        int n = arr.size();
        vector<int> result(n);
        result[n - 1] = -1; // The last element is always -1

        // Traverse the array from the second last element to the beginning
        for (int i = n - 2; i >= 0; i--) {
            // The greatest element to the right is the maximum of the current rightmost element and the previously stored greatest
            result[i] = max(arr[i + 1], result[i + 1]);
        }

        return result;          
    }
};
