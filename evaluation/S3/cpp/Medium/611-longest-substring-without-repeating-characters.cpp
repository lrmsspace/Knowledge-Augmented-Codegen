class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        int n = s.size();
        vector<int> last(128, -1);
        int ans = 0, start = 0;
        for (int i = 0; i < n; ++i) {
            start = max(start, last[s[i]] + 1);
            ans = max(ans, i - start + 1);
            last[s[i]] = i;
        }
        return ans;         
    }
};
