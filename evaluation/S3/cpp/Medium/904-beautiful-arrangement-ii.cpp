class Solution {
public:
    vector<int> constructArray(int n, int k) {
        vector<int> ans(n);
        for (int i = 0; i < n - k - 1; ++i) {
            ans[i] = i + 1;
        }
        for (int i = n - k - 1; i < n; ++i) {
            ans[i] = (i % 2 == n % 2 ? n - (i - (n - k - 1)) / 2 : n - k + (i - (n - k - 1)) / 2);
        }
        return ans;         
    }
};
