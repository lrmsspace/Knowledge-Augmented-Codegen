class Solution {
public:
    string frequencySort(string s) {
        unordered_map<char, int> freq;
        for (char c : s) {
            freq[c]++;
        }

        vector<pair<char, int>> char_freq(freq.begin(), freq.end());
        sort(char_freq.begin(), char_freq.end(), [](const pair<char, int>& a, const pair<char, int>& b) {
            return a.second > b.second; // Sort by frequency in descending order
        });

        string result;
        for (const auto& p : char_freq) {
            result += string(p.second, p.first); // Append character p.second times
        }

        return result;          
    }
};
