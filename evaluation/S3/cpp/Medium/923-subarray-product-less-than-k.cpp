class Solution {
public:
    int numSubarrayProductLessThanK(vector<int>& nums, int k) {
        if (k <= 1) return 0; // Since all nums[i] >= 1, product will never be < 1

        int count = 0;
        int product = 1;
        int left = 0;

        for (int right = 0; right < nums.size(); ++right) {
            product *= nums[right];

            while (product >= k) {
                product /= nums[left++];
            }

            count += right - left + 1; // All subarrays ending at 'right' and starting from 'left' to 'right'
        }

        return count;           
    }
};
