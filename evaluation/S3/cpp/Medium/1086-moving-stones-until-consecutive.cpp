class Solution {
public:
    vector<int> numMovesStones(int a, int b, int c) {
        vector<int> stones = {a, b, c};
        sort(stones.begin(), stones.end());
        int left_gap = stones[1] - stones[0] - 1;
        int right_gap = stones[2] - stones[1] - 1;
        int min_moves = (left_gap == 0 && right_gap == 0) ? 0 : (left_gap <= 1 || right_gap <= 1) ? 1 : 2;
        int max_moves = left_gap + right_gap;
        return {min_moves, max_moves};          
    }
};
