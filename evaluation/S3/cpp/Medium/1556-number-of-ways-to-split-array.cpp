class Solution {
public:
    int waysToSplitArray(vector<int>& nums) {
        int count = 0, n = nums.size();
        long sum = 0, leftSum = 0;
        for (int num : nums) {
            sum += num;
        }
        for (int i = 0; i < n - 1; ++i) {
            leftSum += nums[i];
            if (leftSum >= sum - leftSum) {
                ++count;
            }
        }
        return count;           
    }
};
