class Solution {
public:
    vector<vector<int>> intervalIntersection(vector<vector<int>>& firstList, vector<vector<int>>& secondList) {
        vector<vector<int>> result;
        int i = 0, j = 0;

        while (i < firstList.size() && j < secondList.size()) {
            int start1 = firstList[i][0], end1 = firstList[i][1];
            int start2 = secondList[j][0], end2 = secondList[j][1];

            // Check if the intervals intersect
            if (end1 >= start2 && end2 >= start1) {
                // Calculate the intersection
                int intersectStart = max(start1, start2);
                int intersectEnd = min(end1, end2);
                result.push_back({intersectStart, intersectEnd});
            }

            // Move to the next interval in the list that ends first
            if (end1 < end2) {
                i++;
            } else {
                j++;
            }
        }

        return result;          
    }
};
