class Solution {
public:
    vector<int> closestDivisors(int num) {
        auto closestDivisorsHelper = [](int n) -> vector<int> {
            int sqrt_n = (int) sqrt(n);
            for (int i = sqrt_n; i >= 1; --i) {
                if (n % i == 0) {
                    return {i, n / i};
                }
            }
            return {};
        };

        vector<int> divisors1 = closestDivisorsHelper(num + 1);
        vector<int> divisors2 = closestDivisorsHelper(num + 2);

        if (abs(divisors1[0] - divisors1[1]) <= abs(divisors2[0] - divisors2[1])) {
            return divisors1;
        } else {
            return divisors2;
        }           
    }
};
