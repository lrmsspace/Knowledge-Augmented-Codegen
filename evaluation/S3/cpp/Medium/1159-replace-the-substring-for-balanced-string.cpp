class Solution {
public:
    int balancedString(string s) {
        int n = s.size();
        unordered_map<char, int> freq;
        for (char c : s) {
            freq[c]++;
        }

        int target = n / 4;
        int left = 0, minLength = n;

        for (int right = 0; right < n; right++) {
            freq[s[right]]--;

            while (left <= right && freq['Q'] <= target && freq['W'] <= target && freq['E'] <= target && freq['R'] <= target) {
                minLength = min(minLength, right - left + 1);
                freq[s[left]]++;
                left++;
            }
        }

        return minLength;           
    }
};
