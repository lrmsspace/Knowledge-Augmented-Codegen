class Solution {
public:
    int countSubIslands(vector<vector<int>>& grid1, vector<vector<int>>& grid2) {
        int m = grid1.size(), n = grid1[0].size();
        int count = 0;

        function<void(int, int)> dfs = [&](int i, int j) {
            if (i < 0 || i >= m || j < 0 || j >= n || grid2[i][j] == 0)
                return;
            grid2[i][j] = 0; // Mark as visited
            dfs(i + 1, j);
            dfs(i - 1, j);
            dfs(i, j + 1);
            dfs(i, j - 1);
        };

        for (int i = 0; i < m; ++i) {
            for (int j = 0; j < n; ++j) {
                if (grid2[i][j] == 1) {
                    if (grid1[i][j] == 1) {
                        count++;
                        dfs(i, j);
                    } else {
                        dfs(i, j); // Mark the non-sub-island as visited
                    }
                }
            }
        }

        return count;           
    }
};
