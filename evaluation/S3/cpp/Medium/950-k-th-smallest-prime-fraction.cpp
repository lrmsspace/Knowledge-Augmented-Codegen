class Solution {
public:
    vector<int> kthSmallestPrimeFraction(vector<int>& arr, int k) {
        int n = arr.size();
        int left = 0, right = 1;
        int p = 0, q = 1;

        while (left < right) {
            double mid = (left + right) / 2.0;
            int count = 0;
            p = 0, q = 1;

            for (int i = 0; i < n; ++i) {
                for (int j = i + 1; j < n; ++j) {
                    double fraction = static_cast<double>(arr[i]) / arr[j];
                    if (fraction < mid) {
                        count++;
                        if (fraction > static_cast<double>(p) / q) {
                            p = arr[i];
                            q = arr[j];
                        }
                    } else {
                        break;
                    }
                }
            }

            if (count < k) {
                left = mid;
            } else {
                right = mid;
            }
        }

        return {p, q};          
    }
};
