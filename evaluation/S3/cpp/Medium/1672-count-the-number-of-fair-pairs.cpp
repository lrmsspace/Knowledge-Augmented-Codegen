class Solution {
public:
    long long countFairPairs(vector<int>& nums, int lower, int upper) {
        sort(nums.begin(), nums.end());
        long long count = 0;
        for (int i = 0; i < nums.size(); ++i) {
            int left = lower - nums[i];
            int right = upper - nums[i];
            auto leftIt = lower_bound(nums.begin() + i + 1, nums.end(), left);
            auto rightIt = upper_bound(nums.begin() + i + 1, nums.end(), right);
            count += distance(leftIt, rightIt);
        }
        return count;           
    }
};
