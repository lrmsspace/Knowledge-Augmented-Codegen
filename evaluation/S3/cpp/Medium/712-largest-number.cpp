class Solution {
public:
    string largestNumber(vector<int>& nums) {
        vector<string> strNums;
        for (int num : nums) {
            strNums.push_back(to_string(num));
        }

        sort(strNums.begin(), strNums.end(), [](const string& a, const string& b) {
            return a + b > b + a; // Sort based on concatenation result
        });

        if (strNums[0] == "0") { // If the largest number is "0", return "0"
            return "0";
        }

        string result;
        for (const string& str : strNums) {
            result += str;
        }

        return result;          
    }
};
