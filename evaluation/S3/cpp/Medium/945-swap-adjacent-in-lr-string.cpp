class Solution {
public:
    bool canTransform(string start, string result) {
        if (start.size() != result.size()) return false;

        int n = start.size();
        int i = 0, j = 0;
        while (i < n || j < n) {
            while (i < n && start[i] == 'X') i++;
            while (j < n && result[j] == 'X') j++;

            if (i == n || j == n) return i == n && j == n; // Both should reach the end

            if (start[i] != result[j]) return false; // Mismatched characters

            if (start[i] == 'L' && i < j) return false; // 'L' can only move left
            if (start[i] == 'R' && i > j) return false; // 'R' can only move right

            i++;
            j++;
        }

        return true;            
    }
};
