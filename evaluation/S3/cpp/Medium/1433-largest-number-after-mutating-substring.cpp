class Solution {
public:
    string maximumNumber(string num, vector<int>& change) {
        string result = num;
        bool mutated = false;

        for (size_t i = 0; i < num.size(); ++i) {
            int digit = num[i] - '0';
            int mapped_digit = change[digit];

            if (mapped_digit > digit) {
                result[i] = '0' + mapped_digit;
                mutated = true;
            } else if (mapped_digit < digit && mutated) {
                break; // Stop mutating if we encounter a smaller digit after mutation has started
            }
        }

        return result;          
    }
};
