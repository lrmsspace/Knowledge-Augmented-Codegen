class Solution {
public:
    int magicalString(int n) {
        if (n <= 0) return 0;
        if (n <= 3) return 1;

        string s = "122";
        int count = 1; // Count of '1's in the first n characters
        int index = 2; // Index to read from s

        while (s.size() < n) {
            char nextChar = (s.back() == '1') ? '2' : '1'; // Alternate between '1' and '2'
            int repeatCount = s[index] - '0'; // Number of times to repeat nextChar
            s.append(repeatCount, nextChar); // Append nextChar repeatCount times

            if (nextChar == '1') {
                count += repeatCount; // Increment count by the number of '1's added
            }
            index++; // Move to the next character in s
        }

        return count;           
    }
};
