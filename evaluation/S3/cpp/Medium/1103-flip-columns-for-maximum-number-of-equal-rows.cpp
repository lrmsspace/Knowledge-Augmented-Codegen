class Solution {
public:
    int maxEqualRowsAfterFlips(vector<vector<int>>& matrix) {
        unordered_map<string, int> count;
        int max_rows = 0;

        for (const auto& row : matrix) {
            string pattern;
            for (int cell : row) {
                pattern += to_string(cell);
            }
            count[pattern]++;
            max_rows = max(max_rows, count[pattern]);
        }

        return max_rows;            
    }
};
