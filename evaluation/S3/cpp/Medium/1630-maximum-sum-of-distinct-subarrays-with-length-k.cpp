class Solution {
public:
    long long maximumSubarraySum(vector<int>& nums, int k) {
        unordered_set<int> seen;
        long long sum = 0, ans = 0;

        for (int i = 0; i < nums.size(); ++i) {
            if (i >= k) {
                seen.erase(nums[i - k]);
                sum -= nums[i - k];
            }

            if (seen.count(nums[i])) {
                continue;
            }

            seen.insert(nums[i]);
            sum += nums[i];
            ans = max(ans, sum);
        }

        return ans;         
    }
};
