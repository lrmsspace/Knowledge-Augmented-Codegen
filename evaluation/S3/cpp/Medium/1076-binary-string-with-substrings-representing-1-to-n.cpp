class Solution {
public:
    bool queryString(string s, int n) {
        for (int i = 1; i <= n; ++i) {
            string binary = bitset<32>(i).to_string(); // Convert integer to binary string
            binary.erase(0, binary.find('1')); // Remove leading zeros
            if (s.find(binary) == string::npos) { // Check if binary substring is in s
                return false;
            }
        }
        return true;            
    }
};
