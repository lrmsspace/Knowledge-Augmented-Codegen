class Solution {
public:
    string shiftingLetters(string s, vector<int>& shifts) {
        int n = s.size();
        vector<long long> prefix(n);
        prefix[0] = shifts[0] % 26;
        for (int i = 1; i < n; ++i) {
            prefix[i] = (prefix[i - 1] + shifts[i]) % 26;
        }
        for (int i = 0; i < n; ++i) {
            s[i] = (s[i] - 'a' + prefix[i]) % 26 + 'a';
        }
        return s;           
    }
};
