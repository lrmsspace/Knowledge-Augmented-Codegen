class Solution {
public:
    long long maximumTotalDamage(vector<int>& power) {
        long long ans = 0;
        unordered_map<int, int> freq;
        for (int p : power) {
            freq[p]++;
        }
        for (auto& [p, f] : freq) {
            ans += (long long)p * f * f;
        }
        return ans;         
    }
};
