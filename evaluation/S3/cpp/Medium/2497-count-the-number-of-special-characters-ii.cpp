class Solution {
public:
    int numberOfSpecialChars(string word) {
        int count = 0;
        vector<bool> seenLower(26, false);
        vector<bool> seenUpper(26, false);

        for (char c : word) {
            if (islower(c)) {
                seenLower[c - 'a'] = true;
            } else {
                seenUpper[c - 'A'] = true;
            }
        }

        for (int i = 0; i < 26; ++i) {
            if (seenLower[i] && seenUpper[i]) {
                count++;
            }
        }

        return count;           
    }
};
