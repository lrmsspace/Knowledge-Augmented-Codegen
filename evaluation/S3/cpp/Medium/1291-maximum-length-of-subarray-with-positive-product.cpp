class Solution {
public:
    int getMaxLen(vector<int>& nums) {
        int max_len = 0, positive_len = 0, negative_len = 0;

        for (int num : nums) {
            if (num > 0) {
                positive_len++;
                negative_len = negative_len > 0 ? negative_len + 1 : 0;
            } else if (num < 0) {
                int temp = positive_len;
                positive_len = negative_len > 0 ? negative_len + 1 : 0;
                negative_len = temp + 1;
            } else {
                positive_len = 0;
                negative_len = 0;
            }
            max_len = max(max_len, positive_len);
        }

        return max_len;         
    }
};
