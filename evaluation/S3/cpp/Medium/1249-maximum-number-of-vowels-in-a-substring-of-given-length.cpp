class Solution {
public:
    int maxVowels(string s, int k) {
        int count = 0, maxCount = 0;
        for (int i = 0; i < s.size(); ++i) {
            if (isVowel(s[i])) {
                ++count;
            }
            if (i >= k && isVowel(s[i - k])) {
                --count;
            }
            maxCount = max(maxCount, count);
        }
        return maxCount;            
    }
};
