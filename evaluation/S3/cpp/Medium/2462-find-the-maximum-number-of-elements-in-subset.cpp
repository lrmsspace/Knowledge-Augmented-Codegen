class Solution {
public:
    int maximumLength(vector<int>& nums) {
        unordered_map<int, int> count;
        for (int num : nums) {
            count[num]++;
        }

        int maxLength = 0;
        for (const auto& [x, freq] : count) {
            if (freq > 1) {
                int length = 2 * freq - 1; // x, x2, x4, ..., xk/2, xk, xk/2, ..., x4, x2, x
                maxLength = max(maxLength, length);
            }
        }

        return maxLength;           
    }
};
