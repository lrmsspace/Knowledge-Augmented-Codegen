class Solution {
public:
    int maxDistance(vector<int>& nums1, vector<int>& nums2) {
        int maxDist = 0;
        for (int i = 0, j = 0; i < nums1.size() && j < nums2.size();) {
            if (nums1[i] > nums2[j]) {
                i++;
            } else {
                maxDist = max(maxDist, j - i);
                j++;
            }
        }
        return maxDist;         
    }
};
