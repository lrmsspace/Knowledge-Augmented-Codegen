class Solution {
public:
    long long maxStrength(vector<int>& nums) {
        long long ans = LLONG_MIN, product = 1;
        int n = nums.size(), negativeCount = 0, zeroCount = 0, minNegative = INT_MIN;

        for (int num : nums) {
            if (num == 0) {
                zeroCount++;
                continue;
            }
            if (num < 0) {
                negativeCount++;
                minNegative = max(minNegative, num);
            }
            product *= num;
        }

        if (zeroCount > 0 && negativeCount % 2 == 0) {
            return max(ans, 0LL);
        }

        if (negativeCount % 2 != 0) {
            product /= minNegative;
        }

        return max(ans, product);           
    }
};
