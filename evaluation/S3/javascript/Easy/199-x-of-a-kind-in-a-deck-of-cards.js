/**
 * @param {number[]} deck
 * @return {boolean}
 */
var hasGroupsSizeX = function(deck) {
    const countMap = new Map();
    for (const card of deck) {
        countMap.set(card, (countMap.get(card) || 0) + 1);
    }
    
    let gcd = 0;
    for (const count of countMap.values()) {
        gcd = gcd === 0 ? count : gcd(gcd, count);
    }
    
    return gcd >= 2;
};
