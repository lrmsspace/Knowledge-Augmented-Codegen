/**
 * @param {string[]} words
 * @param {string} s
 * @return {boolean}
 */
var isAcronym = function(words, s) {
    let acronym = '';
    for (const word of words) {
        if (word.length > 0) {
            acronym += word[0]; // Append the first character of each word to form the acronym
        }
    }
    return acronym === s; // Return true if the formed acronym matches the given string s, otherwise false
};
