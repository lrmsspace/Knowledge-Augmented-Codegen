/**
 * @param {string} num1
 * @param {string} num2
 * @return {string}
 */
var addStrings = function(num1, num2) {
    let carry = 0;
    let result = '';
    let i = num1.length - 1, j = num2.length - 1;

    while (i >= 0 || j >= 0 || carry > 0) {
        const digit1 = i >= 0 ? num1.charCodeAt(i) - '0'.charCodeAt(0) : 0;
        const digit2 = j >= 0 ? num2.charCodeAt(j) - '0'.charCodeAt(0) : 0;
        const sum = digit1 + digit2 + carry;
        result = (sum % 10) + result; // Append the last digit to the result
        carry = Math.floor(sum / 10); // Calculate new carry
        i--;
        j--;
    }

    return result;
};
