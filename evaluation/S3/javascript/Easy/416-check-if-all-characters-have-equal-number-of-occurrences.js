/**
 * @param {string} s
 * @return {boolean}
 */
var areOccurrencesEqual = function(s) {
    const charCount = {}; // Object to count occurrences of each character
    for (let char of s) {
        charCount[char] = (charCount[char] || 0) + 1; // Increment count for the character
    }
    const counts = Object.values(charCount);
    const firstCount = counts[0];
    for (let count of counts) {
        if (count !== firstCount) {
            return false;
        }
    }
    return true;
};
