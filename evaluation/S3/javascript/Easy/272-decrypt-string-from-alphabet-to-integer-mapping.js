/**
 * @param {string} s
 * @return {string}
 */
var freqAlphabets = function(s) {
    let result = '';    
    for (let i = 0; i < s.length; i++) {
        if (i + 2 < s.length && s[i + 2] === '#') {
            // If we encounter a pattern like "10#", "11#", ..., "26#"
            const num = parseInt(s.substring(i, i + 2)); // Get the two-digit number
            result += String.fromCharCode(num - 1 + 'a'.charCodeAt(0)); // Map to corresponding character
            i += 2; // Skip the next two characters
        } else {
            // Single digit case
            const num = parseInt(s[i]); // Get the single-digit number
            result += String.fromCharCode(num - 1 + 'a'.charCodeAt(0)); // Map to corresponding character
        }
    }
    return result;
    
};
