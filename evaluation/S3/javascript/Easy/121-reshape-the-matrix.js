/**
 * @param {number[][]} mat
 * @param {number} r
 * @param {number} c
 * @return {number[][]}
 */
var matrixReshape = function(mat, r, c) {
    const m = mat.length;
    const n = mat[0].length;
    if (m * n !== r * c) {
        return mat; // Reshape not possible, return original matrix
    }
    const reshaped = new Array(r).fill(0).map(() => new Array(c).fill(0));
    for (let i = 0; i < m * n; i++) {
        reshaped[Math.floor(i / c)][i % c] = mat[Math.floor(i / n)][i % n];
    }
    return reshaped;            
};
