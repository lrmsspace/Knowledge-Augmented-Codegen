/**
 * @param {string} num
 * @return {string}
 */
var largestGoodInteger = function(num) {
    let largest = ""; // Initialize largest good integer as an empty string
    for (let i = 0; i < num.length - 2; i++) { // Iterate through the string, stopping 2 characters before the end
        // Check if the current character and the next two characters are the same
        if (num[i] === num[i + 1] && num[i] === num[i + 2]) {
            const goodInteger = num[i].repeat(3); // Create the good integer by repeating the character 3 times
            if (goodInteger > largest) { // Compare with the largest found so far
                largest = goodInteger; // Update largest if the current good integer is greater
            }
        }
    }
    return largest;
};
