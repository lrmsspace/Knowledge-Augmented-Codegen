/**
 * @param {string} word
 * @return {number}
 */
var numDifferentIntegers = function(word) {
    const uniqueIntegers = new Set(); // Set to store unique integers
    let currentNumber = ''; // String to build the current number

    for (let i = 0; i < word.length; i++) {
        const char = word[i];
        if (char >= '0' && char <= '9') { // If the character is a digit
            currentNumber += char; // Append it to the current number
        } else { // If it's a non-digit character
            if (currentNumber) { // If we have built a number
                uniqueIntegers.add(parseInt(currentNumber)); // Add the integer value to the set (automatically removes leading zeros)
                currentNumber = ''; // Reset for the next number
            }
        }
    }

    // Check if there is a number at the end of the string
    if (currentNumber) {
        uniqueIntegers.add(parseInt(currentNumber));
    }

    return uniqueIntegers.size; // Return the count of unique integers
};
