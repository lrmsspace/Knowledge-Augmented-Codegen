/**
 * @param {number[]} digits
 * @return {number[]}
 */
var findEvenNumbers = function(digits) {
    const uniqueNumbers = new Set(); // Set to store unique 3-digit even numbers    
    const len = digits.length;
    for (let i = 0; i < len; i++) {
        for (let j = 0; j < len; j++) {
            for (let k = 0; k < len; k++) {
                if (i !== j && j !== k && i !== k) {
                    const num = digits[i] * 100 + digits[j] * 10 + digits[k];
                    if (num >= 100 && num % 2 === 0) {
                        uniqueNumbers.add(num);
                    }
                }
            }
        }
    }
    return Array.from(uniqueNumbers).sort((a, b) => a - b);     
};
