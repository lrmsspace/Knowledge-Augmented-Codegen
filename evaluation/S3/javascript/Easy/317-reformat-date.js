/**
 * @param {string} date
 * @return {string}
 */
var reformatDate = function(date) {
    const [day, month, year] = date.split(' '); // Split the input date into components
    const monthMap = {
        'Jan': '01',
        'Feb': '02',
        'Mar': '03',
        'Apr': '04',
        'May': '05',
        'Jun': '06',
        'Jul': '07',
        'Aug': '08',
        'Sep': '09',
        'Oct': '10',
        'Nov': '11',
        'Dec': '12'
    };
    const dayNum = parseInt(day); // Extract the numeric part of the day
    const dayStr = dayNum < 10 ? '0' + dayNum : '' + dayNum; // Format the day to be two digits
    const monthStr = monthMap[month];
    return `${year}-${monthStr}-${dayStr}`; // Return the reformatted date
};
