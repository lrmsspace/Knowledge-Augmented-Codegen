/**
 * @param {number[]} gain
 * @return {number}
 */
var largestAltitude = function(gain) {
    let maxAltitude = 0; // Initialize max altitude to the starting point
    let currentAltitude = 0; // Initialize current altitude to the starting point
    for (let i = 0; i < gain.length; i++) {
        currentAltitude += gain[i]; // Update current altitude with the gain
        maxAltitude = Math.max(maxAltitude, currentAltitude); // Update max altitude if current is higher
    }
    return maxAltitude;
};
