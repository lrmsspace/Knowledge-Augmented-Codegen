/**
 * @param {string} ransomNote
 * @param {string} magazine
 * @return {boolean}
 */
var canConstruct = function(ransomNote, magazine) {
    const charCount = new Array(26).fill(0); // Count of each character in magazine

    // Count the frequency of each character in magazine
    for (const char of magazine) {
        charCount[char.charCodeAt(0) - 'a'.charCodeAt(0)]++;
    }

    // Check if ransomNote can be constructed from magazine
    for (const char of ransomNote) {
        const index = char.charCodeAt(0) - 'a'.charCodeAt(0);
        if (charCount[index] === 0) {
            return false; // Not enough of this character in magazine
        }
        charCount[index]--; // Use one occurrence of this character
    }

    return true; // All characters in ransomNote can be constructed from magazine
};
