/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var backspaceCompare = function(s, t) {
    const buildString = (str) => {
        const result = [];
        for (let char of str) {
            if (char === '#') {
                result.pop();
            } else {
                result.push(char);
            }
        }
        return result.join('');
    };
    return buildString(s) === buildString(t);
};
