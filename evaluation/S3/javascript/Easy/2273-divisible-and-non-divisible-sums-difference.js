/**
 * @param {number} n
 * @param {number} m
 * @return {number}
 */
var differenceOfSums = function(n, m) {
    let num1 = 0; // Initialize num1 to store the sum of integers not divisible by m
    let num2 = 0; // Initialize num2 to store the sum of integers divisible by m
    for (let i = 1; i <= n; i++) { // Iterate through integers from 1 to n
        if (i % m === 0) { // Check if the integer is divisible by m
            num2 += i; // If divisible, add it to num2
        } else {
            num1 += i; // If not divisible, add it to num1
        }
    }
    return num1 - num2; // Return the difference num1 - num2
};
