/**
 * @param {number} n
 * @param {number} time
 * @return {number}
 */
var passThePillow = function(n, time) {
    const cycleLength = 2 * (n - 1); // Calculate the length of one full cycle of passing the pillow
    const effectiveTime = time % cycleLength; // Find the effective time within the current cycle
    if (effectiveTime < n) { // If the effective time is less than n, the pillow is being passed forward
        return effectiveTime + 1; // Return the index of the person holding the pillow (1-based)
    } else { // If the effective time is greater than or equal to n, the pillow is being passed backward
        return cycleLength - effectiveTime + 1; // Calculate and return the index of the person holding the pillow (1-based)
    }
};
