/**
 * @param {string[]} words
 * @return {number}
 */
var countPrefixSuffixPairs = function(words) {
    let count = 0; // Initialize a counter to keep track of valid pairs
    const n = words.length;
    for (let i = 0; i < n; i++) {
        for (let j = i + 1; j < n; j++) {
            if (isPrefixAndSuffix(words[i], words[j])) {
                count++;
            }
        }
    }
    return count;
};

function isPrefixAndSuffix(prefix, suffix) {
    if (prefix.length > suffix.length) return false;
    return suffix.startsWith(prefix) && suffix.endsWith(prefix);
}