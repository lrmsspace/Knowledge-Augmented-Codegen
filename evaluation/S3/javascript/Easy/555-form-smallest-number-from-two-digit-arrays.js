/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number}
 */
var minNumber = function(nums1, nums2) {
    const set1 = new Set(nums1); // Create a set from the first array for O(1) lookups
    let minCommon = Infinity;
    for (const num of nums2) {
        if (set1.has(num)) {
            minCommon = Math.min(minCommon, num);
        }
    }
    return minCommon === Infinity ? 0 : minCommon;
};
