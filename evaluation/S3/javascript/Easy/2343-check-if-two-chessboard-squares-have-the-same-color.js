/**
 * @param {string} coordinate1
 * @param {string} coordinate2
 * @return {boolean}
 */
var checkTwoChessboards = function(coordinate1, coordinate2) {
    const color1 = (coordinate1.charCodeAt(0) - 97 + parseInt(coordinate1[1])) % 2; // Calculate the color of the first square using its column and row
    const color2 = (coordinate2.charCodeAt(0) - 97 + parseInt(coordinate2[1])) % 2; // Calculate the color of the second square using its column and row
    return color1 === color2; // Return true if both squares have the same color, otherwise false
};
