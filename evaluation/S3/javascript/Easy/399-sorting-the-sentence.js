/**
 * @param {string} s
 * @return {string}
 */
var sortSentence = function(s) {
    const words = s.split(' '); // Split the input string into words
    const sortedWords = new Array(words.length); // Create an array to hold the sorted words
    for (let word of words) {
        const index = parseInt(word[word.length - 1]) - 1; // Get the position from the last character of the word
        sortedWords[index] = word.slice(0, -1); // Place the word (without the number) in the correct position
    }
    return sortedWords.join(' '); // Join the sorted words back into a sentence
};
