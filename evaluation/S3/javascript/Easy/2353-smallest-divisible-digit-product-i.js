/**
 * @param {number} n
 * @param {number} t
 * @return {number}
 */
var smallestNumber = function(n, t) {
    const productOfDigits = (num) => {
        let product = 1;
        while (num > 0) {
            product *= num % 10;
            num = Math.floor(num / 10);
        }
        return product;
    };
    let num = n;
    while (productOfDigits(num) % t !== 0) {
        num++;
    }
    return num;
};
