/**
 * @param {number[]} nums
 * @return {number}
 */
var maximumDifference = function(nums) {
    let minValue = nums[0]; // Initialize minValue to the first element of the array
    let maxDiff = -1; // Initialize maxDiff to -1 to handle the case where no valid pair is found
    for (let i = 1; i < nums.length; i++) { // Start iterating from the second element
        if (nums[i] > minValue) { // Check if the current number is greater than minValue
            maxDiff = Math.max(maxDiff, nums[i] - minValue); // Update maxDiff if the current difference is greater
        } else {
            minValue = nums[i]; // Update minValue if the current number is smaller
        }
    }
    return maxDiff; // Return the maximum difference found
};
