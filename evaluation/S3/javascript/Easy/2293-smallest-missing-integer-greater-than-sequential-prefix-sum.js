/**
 * @param {number[]} nums
 * @return {number}
 */
var missingInteger = function(nums) {
    let prefixSum = 0; // Initialize prefix sum to keep track of the sum of the longest sequential prefix
    let longestPrefixLength = 0; // Initialize the length of the longest sequential prefix
    for (let i = 0; i < nums.length; i++) {
        if (i === 0 || nums[i] === nums[i - 1] + 1) { // Check if the current number continues the sequential prefix
            prefixSum += nums[i]; // Update the prefix sum
            longestPrefixLength++; // Increment the length of the longest sequential prefix
        } else {
            break; // If the sequence is broken, stop checking further
        }
    }
    const missingInteger = prefixSum; // The smallest missing integer is initially set to the prefix sum
    while (nums.includes(missingInteger)) { // Check if the missing integer is present in the array
        missingInteger++; // If it is present, increment to find the next candidate
    }
    return missingInteger; // Return the smallest missing integer greater than or equal to the prefix sum
};
