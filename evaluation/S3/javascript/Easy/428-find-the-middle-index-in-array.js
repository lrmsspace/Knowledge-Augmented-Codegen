/**
 * @param {number[]} nums
 * @return {number}
 */
var findMiddleIndex = function(nums) {
    const totalSum = nums.reduce((acc, num) => acc + num, 0); // Calculate the total sum of the array
    let leftSum = 0; // Initialize left sum to 0
    for (let i = 0; i < nums.length; i++) {
        const rightSum = totalSum - leftSum - nums[i]; // Calculate right sum by subtracting left sum and current element from total sum
        if (leftSum === rightSum) { // Check if left sum equals right sum
            return i; // Return the current index if it is the middle index
        }
        leftSum += nums[i]; // Update left sum by adding the current element
    }
    return -1; // Return -1 if no middle index is found
};
