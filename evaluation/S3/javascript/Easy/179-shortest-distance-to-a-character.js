/**
 * @param {string} s
 * @param {character} c
 * @return {number[]}
 */
var shortestToChar = function(s, c) {
    const result = new Array(s.length).fill(Infinity);
    let prev = -Infinity;
    
    // Forward pass: for each character, calculate distance to the previous occurrence of c
    for (let i = 0; i < s.length; i++) {
        if (s[i] === c) {
            prev = i;
        }
        result[i] = i - prev;
    }
    
    // Backward pass: for each character, update with distance to the next occurrence of c
    let next = Infinity;
    for (let i = s.length - 1; i >= 0; i--) {
        if (s[i] === c) {
            next = i;
        }
        result[i] = Math.min(result[i], next - i);
    }
    
    return result;
};
