/**
 * @param {number[][]} mat
 * @param {number[][]} target
 * @return {boolean}
 */
var findRotation = function(mat, target) {
    const n = mat.length;
    
    // Function to rotate the matrix 90 degrees clockwise
    const rotate90 = (matrix) => {
        const rotated = Array.from({ length: n }, () => Array(n).fill(0));
        for (let i = 0; i < n; i++) {
            for (let j = 0; j < n; j++) {
                rotated[j][n - 1 - i] = matrix[i][j];
            }
        }
        return rotated;
    };
    
    // Check if mat is already equal to target
    if (JSON.stringify(mat) === JSON.stringify(target)) {
        return true;
    }
    
    // Rotate mat up to 3 times and check against target
    let rotatedMat = mat;
    for (let i = 0; i < 3; i++) {
        rotatedMat = rotate90(rotatedMat);
        if (JSON.stringify(rotatedMat) === JSON.stringify(target)) {
            return true;
        }
    }
    
    return false; // If none of the rotations match target
};
