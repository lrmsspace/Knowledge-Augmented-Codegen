/**
 * @param {number[]} hours
 * @param {number} target
 * @return {number}
 */
var numberOfEmployeesWhoMetTarget = function(hours, target) {
    let count = 0; // Initialize a counter to keep track of employees who met the target
    for (const hour of hours) { // Iterate through each employee's hours
        if (hour >= target) { // Check if the employee's hours are greater than or equal to the target
            count++; // Increment the counter if the employee met the target
        }
    }    return count; // Return the total count of employees who met the target
};
