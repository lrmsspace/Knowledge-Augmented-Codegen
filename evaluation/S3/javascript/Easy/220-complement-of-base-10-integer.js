/**
 * @param {number} n
 * @return {number}
 */
var bitwiseComplement = function(n) {
    if (n === 0) {
        return 1; // The complement of 0 is 1
    }
    
    // Find the number of bits in n
    let bits = 0;
    let temp = n;
    while (temp > 0) {
        bits++;
        temp >>= 1;
    }
    
    // Create a mask with all bits set to 1 for the number of bits in n
    const mask = (1 << bits) - 1;
    
    // Return the bitwise complement of n using the mask
    return n ^ mask;
};
