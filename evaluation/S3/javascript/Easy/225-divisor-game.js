var divisorGame = function(n) {
    // Alice wins if n is even, and loses if n is odd.
    // This is because if n is even, Alice can always choose x = 1 (which is a divisor of n) to make n odd for Bob. Then Bob will be in the losing position.
    // If n is odd, Alice can only choose an odd divisor, which will make n even for Bob, giving Bob the winning position.
    return n % 2 === 0;
};
