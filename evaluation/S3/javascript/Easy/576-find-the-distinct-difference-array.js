/**
 * @param {number[]} nums
 * @return {number[]}
 */
var distinctDifferenceArray = function(nums) {
    const n = nums.length;
    const prefixSet = new Set();
    const result = [];
    for (let i = 0; i < n; i++) {
        prefixSet.add(nums[i]);
        const prefixCount = prefixSet.size;
        const suffixCount = new Set(nums.slice(i + 1)).size;
        result.push(prefixCount - suffixCount);
    }
    return result;
};
