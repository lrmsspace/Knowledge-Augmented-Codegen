/**
 * @param {number} m
 * @param {number} n
 * @param {number[][]} indices
 * @return {number}
 */
var oddCells = function(m, n, indices) {
    // Create arrays to track the number of increments for each row and column
    const rowIncrements = new Array(m).fill(0);
    const colIncrements = new Array(n).fill(0);
    
    // Process each index in the indices array
    for (const [row, col] of indices) {
        rowIncrements[row]++;
        colIncrements[col]++;
    }
    
    // Count cells with odd values
    let oddCount = 0;
    for (let i = 0; i < m; i++) {
        for (let j = 0; j < n; j++) {
            if ((rowIncrements[i] + colIncrements[j]) % 2 === 1) {
                oddCount++;
            }
        }
    }
    
    return oddCount;
};
