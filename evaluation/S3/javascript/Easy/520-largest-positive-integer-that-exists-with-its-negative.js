/**
 * @param {number[]} nums
 * @return {number}
 */
var findMaxK = function(nums) {
    const numSet = new Set(nums); // Create a set for O(1) lookups
    let maxK = -1;
    for (let num of nums) {
        if (num > 0 && numSet.has(-num)) {
            maxK = Math.max(maxK, num);
        }
    }
    return maxK;
};
