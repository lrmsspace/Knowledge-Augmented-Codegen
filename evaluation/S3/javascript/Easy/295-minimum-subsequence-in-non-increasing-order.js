/**
 * @param {number[]} nums
 * @return {number[]}
 */
var minSubsequence = function(nums) {
    // Sort the array in non-increasing order   nums.sort((a, b) => b - a);
    nums.sort((a, b) => b - a);
    
    const totalSum = nums.reduce((sum, num) => sum + num, 0);
    let currentSum = 0;
    const result = [];
    
    for (let i = 0; i < nums.length; i++) {
        currentSum += nums[i];
        result.push(nums[i]);
        if (currentSum > totalSum / 2) {
            break;
        }
    }
    
    return result;
};
