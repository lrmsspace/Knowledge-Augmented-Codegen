/**
 * @param {number[]} nums
 * @return {number}
 */
var minimumRightShifts = function(nums) {
    const n = nums.length;
    let shiftCount = 0;
    let sorted = false;
    while (!sorted) {
        sorted = true;
        for (let i = 0; i < n - 1; i++) {
            if (nums[i] > nums[i + 1]) {
                // Perform a right shift
                const lastElement = nums[n - 1];
                for (let j = n - 1; j > 0; j--) {
                    nums[j] = nums[j - 1];
                }
                nums[0] = lastElement;
                shiftCount++;
                sorted = false;
            }
        }
    }
    return shiftCount;
};
