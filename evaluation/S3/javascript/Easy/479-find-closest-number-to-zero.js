/**
 * @param {number[]} nums
 * @return {number}
 */
var findClosestNumber = function(nums) {
    let closest = nums[0]; // Initialize closest to the first element of the array
    for (let num of nums) { // Iterate through each number in the array
        // Check if the current number is closer to 0 than the closest found so far
        // If they are equally close, prefer the larger number
        if (Math.abs(num) < Math.abs(closest) || (Math.abs(num) === Math.abs(closest) && num > closest)) {
            closest = num; // Update closest if the current number is closer or equally close but larger
        }
    }
    return closest;
};
