/**
 * @param {number[]} nums
 * @return {number}
 */
var sumCounts = function(nums) {
    const n = nums.length;
    let totalSum = 0;
    for (let i = 0; i < n; i++) {
        const distinctElements = new Set();
        for (let j = i; j < n; j++) {
            distinctElements.add(nums[j]);
            totalSum += distinctElements.size * distinctElements.size;
        }
    }
    return totalSum;
};
