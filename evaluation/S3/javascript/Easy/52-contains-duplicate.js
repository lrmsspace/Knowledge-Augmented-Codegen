/**
 * @param {number[]} nums
 * @return {boolean}
 */
var containsDuplicate = function(nums) {
    const seen = new Set();
    for (const num of nums) {
        if (seen.has(num)) {
            return true; // Found a duplicate
        }
        seen.add(num); // Add the number to the set
    }
    return false; // No duplicates found
};
