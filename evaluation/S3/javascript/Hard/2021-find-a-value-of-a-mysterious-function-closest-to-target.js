/**
 * @param {number[]} arr
 * @param {number} target
 * @return {number}
 */
var closestToTarget = function(arr, target) {
    let ans = Infinity;
    for (let i = 0; i < arr.length; i++) {
        let currentAnd = arr[i];
        ans = Math.min(ans, Math.abs(currentAnd - target));
        for (let j = i + 1; j < arr.length; j++) {
            currentAnd &= arr[j];
            ans = Math.min(ans, Math.abs(currentAnd - target));
        }
    }
    return ans;
        
};
