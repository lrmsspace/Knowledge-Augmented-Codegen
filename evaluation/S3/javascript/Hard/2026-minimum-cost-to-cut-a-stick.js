/**
 * @param {number} n
 * @param {number[]} cuts
 * @return {number}
 */
var minCost = function(n, cuts) {
    cuts.push(0, n);
    cuts.sort((a, b) => a - b);
    const m = cuts.length;
    const dp = Array.from({ length: m }, () => Array(m).fill(0));
    for (let len = 2; len < m; len++) {
        for (let l = 0; l < m - len; l++) {
            const r = l + len;
            dp[l][r] = Infinity;
            for (let k = l + 1; k < r; k++) {
                dp[l][r] = Math.min(dp[l][r], dp[l][k] + dp[k][r] + cuts[r] - cuts[l]);
            }
        }
    }
    return dp[0][m - 1];
        
};
