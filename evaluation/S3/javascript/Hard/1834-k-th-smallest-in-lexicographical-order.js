/**
 * @param {number} n
 * @param {number} k
 * @return {number}
 */
var findKthNumber = function(n, k) {
    const countPrefix = (prefix, n) => {
        let count = 0;
        let p = prefix;
        while (p <= n) {
            count += Math.min(n + 1, p * 10) - p;
            p *= 10;
        }
        return count;
    };

    let prefix = 1;
    k--;
    while (k > 0) {
        const count = countPrefix(prefix, n);
        if (count <= k) {
            prefix++;
            k -= count;
        }
        else {
            prefix *= 10;
            k--;
        }
    }
    return prefix;
            
};
