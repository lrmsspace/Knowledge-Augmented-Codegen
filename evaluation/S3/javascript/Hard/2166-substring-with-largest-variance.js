/**
 * @param {string} s
 * @return {number}
 */
var largestVariance = function(s) {
    const freq = new Array(26).fill(0);
    for (const ch of s) {
        freq[ch.charCodeAt(0) - 97]++;
    }
    let maxVariance = 0;
    for (let i = 0; i < 26; i++) {
        for (let j = 0; j < 26; j++) {
            if (i === j || freq[i] === 0 || freq[j] === 0) continue;
            let count1 = 0, count2 = 0;
            for (const ch of s) {
                if (ch.charCodeAt(0) - 97 === i) {
                    count1++;
                } else if (ch.charCodeAt(0) - 97 === j) {
                    count2++;
                }
                if (count1 > 0 && count2 > 0) {
                    maxVariance = Math.max(maxVariance, Math.abs(count1 - count2));
                }
                if (count2 > count1) {
                    count1 = 0;
                    count2 = 0;
                }
            }
        }
    }
    return maxVariance;
    
};
