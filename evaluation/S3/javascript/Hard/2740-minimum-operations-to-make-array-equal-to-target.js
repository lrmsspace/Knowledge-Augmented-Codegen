/**
 * @param {number[]} nums
 * @param {number[]} target
 * @return {number}
 */
var minimumOperations = function(nums, target) {
    const n = nums.length;
    let operations = 0;
    for (let i = 0; i < n; i++) {
        if (nums[i] !== target[i]) {
            operations++;
        }
    }
    return operations;
};
