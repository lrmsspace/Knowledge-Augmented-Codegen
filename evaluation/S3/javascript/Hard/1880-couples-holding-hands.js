/**
 * @param {number[]} row
 * @return {number}
 */
var minSwapsCouples = function(row) {
    const n = row.length / 2;
    const parent = new Array(n).fill(0).map((_, i) => i);
        
    function find(x) {
        if (parent[x] !== x) {
            parent[x] = find(parent[x]);
        }
        return parent[x];
    }
        
    function union(x, y) {
        const rootX = find(x);
        const rootY = find(y);
        if (rootX !== rootY) {
            parent[rootX] = rootY;
        }
    }
        
    for (let i = 0; i < n; i++) {
        const person1 = row[2 * i];
        const person2 = row[2 * i + 1];
        union(Math.floor(person1 / 2), Math.floor(person2 / 2));
    }
        
    let swaps = 0;
    for (let i = 0; i < n; i++) {
        if (find(i) !== find(0)) {
            swaps++;
        }
    }
        
    return swaps;
}   ;
    
    