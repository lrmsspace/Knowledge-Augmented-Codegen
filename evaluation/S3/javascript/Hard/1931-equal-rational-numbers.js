/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var isRationalEqual = function(s, t) {
    const parseRational = (str) => {
        let [integerPart, decimalPart] = str.split('.');
        if (!decimalPart) {
            return { integerPart, nonRepeatingPart: '', repeatingPart: '' };
        }   
        let nonRepeatingPart = '';
        let repeatingPart = '';
        const repeatStart = decimalPart.indexOf('(');
        if (repeatStart !== -1) {
            nonRepeatingPart = decimalPart.substring(0, repeatStart);
            repeatingPart = decimalPart.substring(repeatStart + 1, decimalPart.length - 1);
        }
        else {
            nonRepeatingPart = decimalPart;
        }
        return { integerPart, nonRepeatingPart, repeatingPart };
    }
    
    const getDecimalValue = (integerPart, nonRepeatingPart, repeatingPart) => {
        let value = integerPart;
        if (nonRepeatingPart) {
            value += parseFloat('0.' + nonRepeatingPart);
        }
        if (repeatingPart) {
            const repeatingLength = repeatingPart.length;
            const repeatingValue = parseFloat('0.' + repeatingPart);
            value += repeatingValue / (Math.pow(10, repeatingLength) - 1);
        }
        return value;
    };
    
    const sParsed = parseRational(s);
    const tParsed = parseRational(t);
    return getDecimalValue(sParsed.integerPart, sParsed.nonRepeatingPart, sParsed.repeatingPart) === getDecimalValue(tParsed.integerPart, tParsed.nonRepeatingPart, tParsed.repeatingPart);
                
};
