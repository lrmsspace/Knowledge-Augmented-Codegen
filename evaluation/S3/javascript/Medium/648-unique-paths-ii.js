/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
var uniquePathsWithObstacles = function(obstacleGrid) {
    const m = obstacleGrid.length, n = obstacleGrid[0].length;
    const dp = Array.from({ length: m }, () => new Array(n).fill(0));

    // If the starting cell has an obstacle, return 0 as there are no paths.
    if (obstacleGrid[0][0] === 1) return 0;

    dp[0][0] = 1; // Start at the top-left corner

    // Fill the first column
    for (let i = 1; i < m; i++) {
        dp[i][0] = (obstacleGrid[i][0] === 1) ? 0 : dp[i - 1][0];
    }

    // Fill the first row
    for (let j = 1; j < n; j++) {
        dp[0][j] = (obstacleGrid[0][j] === 1) ? 0 : dp[0][j - 1];
    }

    // Fill the rest of the dp table
    for (let i = 1; i < m; i++) {
        for (let j = 1; j < n; j++) {
            if (obstacleGrid[i][j] === 1) {
                dp[i][j] = 0; // No paths through an obstacle
            } else {
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1]; // Paths from above and left
            }
        }
    }

    return dp[m - 1][n - 1]; // Return the number of unique paths to the bottom-right corner
};
