/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @param {number} k
 * @return {number}
 */
var numberOfPairs = function(nums1, nums2, k) {
    const countMap = new Map();
    for (const num of nums2) {
        const key = num * k;
        countMap.set(key, (countMap.get(key) || 0) + 1);
    }
    
    let count = 0;
    for (const num of nums1) {
        if (countMap.has(num)) {
            count += countMap.get(num);
        }
    }
    return count;
};
