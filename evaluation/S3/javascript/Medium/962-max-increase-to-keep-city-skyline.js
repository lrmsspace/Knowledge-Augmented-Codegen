/**
 * @param {number[][]} grid
 * @return {number}
 */
var maxIncreaseKeepingSkyline = function(grid) {
    const n = grid.length;
    const maxRow = new Array(n).fill(0); // Array to store maximum height of buildings in each row
    const maxCol = new Array(n).fill(0); // Array to store maximum height of buildings in each column
        
    // Find the maximum height in each row and column
    for (let i = 0; i < n; i++) {
        for (let j = 0; j < n; j++) {
            maxRow[i] = Math.max(maxRow[i], grid[i][j]);
            maxCol[j] = Math.max(maxCol[j], grid[i][j]);
        }
    }
        
    let totalIncrease = 0;
    // Calculate the maximum increase for each building
    for (let i = 0; i < n; i++) {
        for (let j = 0; j < n; j++) {
            const maxHeight = Math.min(maxRow[i], maxCol[j]);
            totalIncrease += maxHeight - grid[i][j];
        }
    }
        
    return totalIncrease;
};
