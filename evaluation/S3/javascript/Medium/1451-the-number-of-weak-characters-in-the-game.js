/**
 * @param {number[][]} properties
 * @return {number}
 */
var numberOfWeakCharacters = function(properties) {
    // Sort by attack ascending, and if tie, by defense descending
    properties.sort((a, b) => a[0] - b[0] || b[1] - a[1]);
    
    let maxDefense = 0;
    let weakCount = 0;
    
    // Traverse from the end to count weak characters
    for (let i = properties.length - 1; i >= 0; i--) {
        const [attack, defense] = properties[i];
        if (defense < maxDefense) {
            weakCount++;
        }
        maxDefense = Math.max(maxDefense, defense);
    }
    
    return weakCount;
};
