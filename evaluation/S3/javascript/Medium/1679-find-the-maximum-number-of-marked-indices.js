/**
 * @param {number[]} nums
 * @return {number}
 */
var maxNumOfMarkedIndices = function(nums) {
    nums.sort((a, b) => a - b);
    
    let left = 0;
    let right = Math.floor(nums.length / 2);
    let count = 0;
    
    while (left < nums.length / 2 && right < nums.length) {
        if (2 * nums[left] <= nums[right]) {
            count += 2; // Mark both indices
            left++;
            right++;
        } else {
            right++; // Try the next larger number for the right pointer
        }
    }
    
    return count;
};
