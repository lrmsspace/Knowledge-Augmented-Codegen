/**
 * @param {number[]} nums
 * @return {number[]}
 */
var productExceptSelf = function(nums) {
    const n = nums.length;
    const output = new Array(n).fill(1);

    // Calculate prefix products and store in output
    let prefixProduct = 1;
    for (let i = 0; i < n; i++) {
        output[i] = prefixProduct; // Store product of all elements to the left of i
        prefixProduct *= nums[i]; // Update prefix product for the next iteration
    }

    // Calculate suffix products and multiply with existing values in output
    let suffixProduct = 1;
    for (let i = n - 1; i >= 0; i--) {
        output[i] *= suffixProduct; // Multiply by product of all elements to the right of i
        suffixProduct *= nums[i]; // Update suffix product for the next iteration
    }

    return output;
};
