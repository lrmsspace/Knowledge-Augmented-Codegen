/**
 * @param {number[]} arr
 * @return {number[]}
 */
var prevPermOpt1 = function(arr) {
  const n = arr.length;
  
  // Step 1: Find the rightmost pair where arr[i] > arr[i + 1]
  let i = n - 2;
  while (i >= 0 && arr[i] <= arr[i + 1]) {
    i--;
  }
  
  // If no such pair exists, return the original array
  if (i < 0) {
    return arr;
  }
  
  // Step 2: Find the rightmost element that is smaller than arr[i]
  let j = n - 1;
  while (arr[j] >= arr[i]) {
    j--;
  }
  
  // Step 3: Swap arr[i] and arr[j]
  [arr[i], arr[j]] = [arr[j], arr[i]];
  
  return arr;
};
