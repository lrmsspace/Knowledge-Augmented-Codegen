/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var zigzagLevelOrder = function(root) {
    if (!root) return [];
    
    const result = [];
    const queue = [root];
    let leftToRight = true; // Flag to indicate the direction of traversal

    while (queue.length > 0) {
        const levelSize = queue.length;
        const currentLevel = new Array(levelSize);

        for (let i = 0; i < levelSize; i++) {
            const node = queue.shift();

            // Determine the index to fill based on the current direction
            const index = leftToRight ? i : (levelSize - 1 - i);
            currentLevel[index] = node.val;

            // Add child nodes to the queue for the next level
            if (node.left) queue.push(node.left);
            if (node.right) queue.push(node.right);
        }

        result.push(currentLevel);
        leftToRight = !leftToRight; // Toggle the direction for the next level
    }

    return result;
};
