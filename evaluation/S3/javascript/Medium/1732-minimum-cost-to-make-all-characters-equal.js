/**
 * @param {string} s
 * @return {number}
 */
var minimumCost = function(s) {
    const n = s.length;
    let cost = 0;
    let left = 0;
    let right = n - 1;
    while (left < right) {
        if (s[left] !== s[right]) {
            cost += 1;
        }
        left++;
        right--;
    }
    return cost;
};
