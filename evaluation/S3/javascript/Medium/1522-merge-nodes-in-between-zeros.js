/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var mergeNodes = function(head) {
    let current = head;
    let sum = 0;
    let newHead = null;
    let newCurrent = null;
    
    while (current !== null) {
        if (current.val === 0) {
            if (sum !== 0) {
                const newNode = new ListNode(sum);
                if (newHead === null) {
                    newHead = newNode;
                } else {
                    newCurrent.next = newNode;
                }
                newCurrent = newNode;
            }
            sum = 0;
        } else {
            sum += current.val;
        }
        current = current.next;
    }
    
    return newHead;
};
