/**
 * @param {number[]} asteroids
 * @return {number[]}
 */
var asteroidCollision = function(asteroids) {
    const stack = [];

    for (const asteroid of asteroids) {
        let collision = true;

        while (collision && asteroid < 0 && stack.length > 0 && stack[stack.length - 1] > 0) {
            const top = stack[stack.length - 1];

            if (top < -asteroid) {
                stack.pop(); // The top asteroid explodes
            } else if (top === -asteroid) {
                stack.pop(); // Both asteroids explode
                collision = false; // No more collisions for the current asteroid
            } else {
                collision = false; // The current asteroid explodes
            }
        }

        if (collision) {
            stack.push(asteroid); // Add the current asteroid to the stack if it survives
        }
    }

    return stack; // Return the state of asteroids after all collisions
};
