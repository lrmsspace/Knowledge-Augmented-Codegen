/**
 * @param {number[]} nums
 * @return {number}
 */
var minMoves = function(nums) {
    // The minimum moves to equalize the array is the total sum of differences between each element and the minimum element
    const minNum = Math.min(...nums); // Find the minimum element in the array
    let moves = 0;

    for (const num of nums) {
        moves += num - minNum; // Increment moves by the difference from the minimum
    }

    return moves;
};
