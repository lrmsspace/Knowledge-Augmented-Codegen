/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number[]}
 */
var advantageCount = function(nums1, nums2) {
    const n = nums1.length;
    const sortedNums1 = [...nums1].sort((a, b) => a - b);
    const result = [];
    let left = 0;
    let right = n - 1;
    
    for (let i = n - 1; i >= 0; i--) {
        if (sortedNums1[right] > nums2[i]) {
            result[i] = sortedNums1[right];
            right--;
        } else {
            result[i] = sortedNums1[left];
            left++;
        }
    }
    
    return result;
};
