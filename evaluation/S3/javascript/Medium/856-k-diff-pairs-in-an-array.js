/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var findPairs = function(nums, k) {
    if (k < 0) return 0; // k-diff pairs cannot exist if k is negative

    const numSet = new Set(nums); // Store unique numbers for O(1) lookups
    let count = 0;

    if (k === 0) {
        // Count duplicates when k is 0
        const freqMap = new Map();
        for (const num of nums) {
            freqMap.set(num, (freqMap.get(num) || 0) + 1);
        }
        for (const [num, freq] of freqMap.entries()) {
            if (freq > 1) count++; // Only count numbers that appear more than once
        }
    } else {
        // Count pairs when k is positive
        for (const num of numSet) {
            if (numSet.has(num + k)) count++; // Check for the complementary number
        }
    }

    return count;
};
