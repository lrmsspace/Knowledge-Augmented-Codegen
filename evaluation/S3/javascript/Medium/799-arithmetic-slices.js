/**
 * @param {number[]} nums
 * @return {number}
 */
var numberOfArithmeticSlices = function(nums) {
    let totalSlices = 0;
    let currentSlices = 0;

    for (let i = 2; i < nums.length; i++) {
        // Check if the current three elements form an arithmetic sequence
        if (nums[i] - nums[i - 1] === nums[i - 1] - nums[i - 2]) {
            currentSlices++; // Extend the existing arithmetic sequence
            totalSlices += currentSlices; // Add the new slices formed by extending
        } else {
            currentSlices = 0; // Reset if the sequence breaks
        }
    }

    return totalSlices;
};
