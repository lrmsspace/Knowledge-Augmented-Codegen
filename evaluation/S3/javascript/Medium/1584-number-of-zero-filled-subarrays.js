/**
 * @param {number[]} nums
 * @return {number}
 */
var zeroFilledSubarray = function(nums) {
    let count = 0;  
    let currentCount = 0;
    for (const num of nums) {
        if (num === 0) {
            currentCount++;
            count += currentCount;
        } else {
            currentCount = 0;
        }
    }
    return count;
};
