/**
 * @param {number[]} queries
 * @param {number} m
 * @return {number[]}
 */
var processQueries = function(queries, m) {
    const P = Array.from({ length: m }, (_, i) => i + 1);
    const result = [];
    
    for (const query of queries) {
        const index = P.indexOf(query);
        result.push(index);
        P.splice(index, 1); // Remove the element at index
        P.unshift(query);   // Add it to the beginning
    }
    
    return result;
};
