/**
 * @param {number[]} prices
 * @return {number}
 */
var maxProfit = function(prices) {
    let totalProfit = 0;

    for (let i = 1; i < prices.length; i++) {
        // If the price has increased from the previous day, we can profit by buying yesterday and selling today
        if (prices[i] > prices[i - 1]) {
            totalProfit += prices[i] - prices[i - 1];
        }
    }

    return totalProfit;
};
