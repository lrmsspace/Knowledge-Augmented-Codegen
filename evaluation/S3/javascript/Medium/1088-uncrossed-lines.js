/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number}
 */
var maxUncrossedLines = function(nums1, nums2) {
  const m = nums1.length;
  const n = nums2.length;
  
  // Create a DP table where dp[i][j] represents the maximum number of uncrossed lines
  // between the first i elements of nums1 and the first j elements of nums2
  const dp = Array.from({ length: m + 1 }, () => new Array(n + 1).fill(0));
  
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      if (nums1[i - 1] === nums2[j - 1]) {
        dp[i][j] = dp[i - 1][j - 1] + 1; // Match found, extend the line count
      } else {
        dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]); // No match, take the max from previous states
      }
    }
  }
  
  return dp[m][n]; // The bottom-right cell contains the answer
};
