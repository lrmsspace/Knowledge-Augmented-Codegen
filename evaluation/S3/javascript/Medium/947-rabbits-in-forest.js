/**
 * @param {number[]} answers
 * @return {number}
 */
var numRabbits = function(answers) {
    const freq = {}; // Frequency map to count occurrences of each answer
    let totalRabbits = 0; // Variable to store the total number of rabbits

    for (const answer of answers) {
        if (!freq[answer]) {
            freq[answer] = 0; // Initialize frequency for this answer if not already present
        }
        freq[answer]++; // Increment the frequency for this answer
    }

    for (const [answer, count] of Object.entries(freq)) {
        const groupSize = parseInt(answer) + 1; // Each group can have at most 'answer + 1' rabbits
        const groupsNeeded = Math.ceil(count / groupSize); // Calculate how many groups are needed for this answer
        totalRabbits += groupsNeeded * groupSize; // Add the total rabbits needed for these groups to the overall count
    }

    return totalRabbits; // Return the minimum number of rabbits in the forest
};
