/**
 * @param {number[]} nums
 * @return {number}
 */
var maximumTripletValue = function(nums) {
    let maxValue = 0;
    let minLeft = nums[0];
    
    for (let j = 1; j < nums.length - 1; j++) {
        minLeft = Math.min(minLeft, nums[j - 1]);
        const currentValue = (minLeft - nums[j]) * Math.max(...nums.slice(j + 1));
        maxValue = Math.max(maxValue, currentValue);
    }
    
    return maxValue;
};
