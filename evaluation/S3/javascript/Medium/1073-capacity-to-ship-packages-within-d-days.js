/**
 * @param {number[]} weights
 * @param {number} days
 * @return {number}
 */
var shipWithinDays = function(weights, days) {
  const canShip = (capacity) => {
    let currentLoad = 0;
    let requiredDays = 1; // Start with day 1
    for (const weight of weights) {
      if (currentLoad + weight > capacity) {
        requiredDays++; // Need another day
        currentLoad = weight; // Start loading the new day with the current weight
      } else {
        currentLoad += weight; // Continue loading the current day
      }
    }
    return requiredDays <= days; // Check if we can ship within the given days
  };
  
  let left = Math.max(...weights); // Minimum capacity must be at least the heaviest package
  let right = weights.reduce((a, b) => a + b, 0); // Maximum capacity is the sum of all weights
  
  while (left < right) {
    const mid = Math.floor((left + right) / 2);
    if (canShip(mid)) {
      right = mid; // Try to find a smaller capacity
    } else {
      left = mid + 1; // Need a larger capacity
    }
  }
  
  return left; // The minimum capacity needed to ship within days
};
