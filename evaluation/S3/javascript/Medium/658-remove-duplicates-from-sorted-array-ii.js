/**
 * @param {number[]} nums
 * @return {number}
 */
var removeDuplicates = function(nums) {
    let k = 0; // Pointer for the position of the next unique element

    for (let i = 0; i < nums.length; i++) {
        // Check if the current element can be added to the result
        if (k < 2 || nums[i] !== nums[k - 2]) {
            nums[k] = nums[i]; // Place the current element at position k
            k++; // Move the pointer for the next unique element
        }
    }

    return k; // Return the length of the modified array
};
