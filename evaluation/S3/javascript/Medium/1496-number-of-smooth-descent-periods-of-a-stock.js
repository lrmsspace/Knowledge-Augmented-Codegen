/**
 * @param {number[]} prices
 * @return {number}
 */
var getDescentPeriods = function(prices) {
    let count = 0;
    let length = 1; // Length of the current smooth descent period
    
    for (let i = 1; i < prices.length; i++) {
        if (prices[i] === prices[i - 1] - 1) {
            length++;
        } else {
            length = 1; // Reset length if the descent is broken
        }
        count += length; // Add the number of new periods ending at the current day
    }
    
    return count;
};
