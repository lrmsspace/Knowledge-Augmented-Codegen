/**
 * @param {string} s
 * @return {number}
 */
var minAnagramLength = function(s) {
    const charCount = new Array(26).fill(0);
    for (const char of s) {
        charCount[char.charCodeAt(0) - 'a'.charCodeAt(0)]++;
    }
    let maxCount = 0;
    for (const count of charCount) {
        maxCount = Math.max(maxCount, count);
    }
    return maxCount;
};
