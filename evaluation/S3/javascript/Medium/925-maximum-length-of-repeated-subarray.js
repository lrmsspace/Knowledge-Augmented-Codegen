/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number}
 */
var findLength = function(nums1, nums2) {
    const m = nums1.length, n = nums2.length;
    const dp = new Array(m + 1).fill(0).map(() => new Array(n + 1).fill(0)); // DP table to store lengths of longest common suffixes
    let maxLength = 0; // Variable to track the maximum length of repeated subarray

    for (let i = 1; i <= m; i++) {
        for (let j = 1; j <= n; j++) {
            if (nums1[i - 1] === nums2[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1] + 1; // Extend the previous common suffix
                maxLength = Math.max(maxLength, dp[i][j]); // Update maxLength if a longer common suffix is found
            }
        }
    }

    return maxLength; // Return the maximum length of repeated subarray
};
