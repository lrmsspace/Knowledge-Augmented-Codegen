/**
 * @param {number} bottom
 * @param {number} top
 * @param {number[]} special
 * @return {number}
 */
var maxConsecutive = function(bottom, top, special) {
    // Sort the special floors    special.sort((a, b) => a - b);
    
    let maxConsecutiveFloors = 0;
    special.sort((a, b) => a - b);
    let prev = bottom - 1;
    for (const floor of special) {
        maxConsecutiveFloors = Math.max(maxConsecutiveFloors, floor - prev - 1);
        prev = floor;
    }
    maxConsecutiveFloors = Math.max(maxConsecutiveFloors, top - prev);
    return maxConsecutiveFloors;
};
