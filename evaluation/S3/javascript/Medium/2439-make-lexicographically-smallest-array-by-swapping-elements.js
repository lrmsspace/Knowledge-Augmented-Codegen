/**
 * @param {number[]} nums
 * @param {number} limit
 * @return {number[]}
 */
var lexicographicallySmallestArray = function(nums, limit) {
    const n = nums.length;
    const result = new Array(n);
    const sortedIndices = Array.from({ length: n }, (_, i) => i).sort((a, b) => nums[a] - nums[b]);
    const unionFind = new DisjointSetUnion(n);
    
    for (const index of sortedIndices) {
        let current = index;
        while (current !== -1) {
            result[current] = nums[index];
            const nextIndex = unionFind.find(current + 1);
            if (nextIndex !== -1 && Math.abs(nums[current] - nums[nextIndex]) <= limit) {
                unionFind.union(current, nextIndex);
                current = unionFind.find(current);
            } else {
                break;
            }
        }
    }
    
    return result;
};
