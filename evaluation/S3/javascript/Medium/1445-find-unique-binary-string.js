/**
 * @param {string[]} nums
 * @return {string}
 */
var findDifferentBinaryString = function(nums) {
    const n = nums.length;
    const numSet = new Set(nums);
    
    for (let i = 0; i < (1 << n); i++) {
        const binaryString = i.toString(2).padStart(n, '0');
        if (!numSet.has(binaryString)) {
            return binaryString;
        }
    }
    
    return ""; // This line should never be reached since there will always be a valid answer
};
