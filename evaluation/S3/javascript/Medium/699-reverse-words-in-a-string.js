/**
 * @param {string} s
 * @return {string}
 */
var reverseWords = function(s) {
    // Split the string by spaces, filter out empty strings, reverse the array and join with a single space
    return s.split(' ').filter(word => word.length > 0).reverse().join(' ');
};
