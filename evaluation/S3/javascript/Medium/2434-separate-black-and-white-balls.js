/**
 * @param {string} s
 * @return {number}
 */
var minimumSteps = function(s) {
    let steps = 0;
    let whiteCount = 0;
    
    for (const char of s) {
        if (char === '0') {
            whiteCount++;
        } else {
            steps += whiteCount; // Each black ball needs to swap with all previously counted white balls
        }
    }
    
    return steps;
};
