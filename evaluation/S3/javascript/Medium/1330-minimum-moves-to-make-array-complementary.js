/**
 * @param {number[]} nums
 * @param {number} limit
 * @return {number}
 */
var minMoves = function(nums, limit) {
    const n = nums.length;
    const count = new Array(2 * limit + 2).fill(0);
    
    for (let i = 0; i < n / 2; i++) {
        const a = nums[i];
        const b = nums[n - 1 - i];
        count[a + b]++;
    }
    
    let minMoves = n;
    for (let sum = 2; sum <= 2 * limit; sum++) {
        let moves = 0;
        for (let i = 0; i < n / 2; i++) {
            const a = nums[i];
            const b = nums[n - 1 - i];
            if (a + b === sum) {
                continue;
            }
            if (a + b > sum) {
                moves += 1;
            } else {
                moves += 1;
            }
        }
        minMoves = Math.min(minMoves, moves);
    }
    
    return minMoves;
};
