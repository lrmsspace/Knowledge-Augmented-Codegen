/**
 * @param {number[]} citations
 * @return {number}
 */
var hIndex = function(citations) {
    const n = citations.length;
    const count = new Array(n + 1).fill(0);

    // Count how many papers have a certain number of citations
    for (const c of citations) {
        if (c >= n) {
            count[n]++; // Count all papers with citations >= n in the last bucket
        } else {
            count[c]++;
        }
    }

    let total = 0;
    // Iterate from the highest possible h-index down to 0
    for (let h = n; h >= 0; h--) {
        total += count[h]; // Total papers with at least h citations
        if (total >= h) {
            return h; // Found the maximum h such that at least h papers have at least h citations
        }
    }

    return 0; // If no valid h-index found, return 0
};
