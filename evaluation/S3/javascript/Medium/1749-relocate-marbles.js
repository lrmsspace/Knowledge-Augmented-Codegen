/**
 * @param {number[]} nums
 * @param {number[]} moveFrom
 * @param {number[]} moveTo
 * @return {number[]}
 */
var relocateMarbles = function(nums, moveFrom, moveTo) {
    const positionSet = new Set(nums);
    
    for (let i = 0; i < moveFrom.length; i++) {
        const from = moveFrom[i];
        const to = moveTo[i];
        positionSet.delete(from);
        positionSet.add(to);
    }
    return Array.from(positionSet).sort((a, b) => a - b);
};
