/**

 * @param {number} n

 * @param {number} start

 * @return {number[]}

 */

var circularPermutation = function(n, start) {
    const total = 1 << n;
    const result = [];
    let current = start;
    
    for (let i = 0; i < total; i++) {
        result.push(current);
        current = (current + 1) % total;
    }
    
    return result;      

};
