/**
 * @param {number} n
 * @return {number}
 */
var integerBreak = function(n) {
    const dp = new Array(n + 1).fill(0);
    dp[1] = 1; // Base case: The maximum product for integer 1 is 1

    for (let i = 2; i <= n; i++) {
        for (let j = 1; j < i; j++) {
            // For each split of i into j and i-j, we consider both the product of the split and the product of the split with the remaining part
            dp[i] = Math.max(dp[i], Math.max(j, dp[j]) * Math.max(i - j, dp[i - j]));
        }
    }

    return dp[n];
};
