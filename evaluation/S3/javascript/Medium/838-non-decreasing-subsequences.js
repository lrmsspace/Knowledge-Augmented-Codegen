/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var findSubsequences = function(nums) {
    const res = [];
    const backtrack = (start, path) => {
        if (path.length > 1) {
            res.push(path); // Add the current subsequence to the result if it has at least 2 elements
        }

        for (let i = start; i < nums.length; i++) {
            // Skip duplicates in the same recursion level
            if (i > start && nums[i] === nums[i - 1]) continue;

            // Only continue if the current number is greater than or equal to the last number in the path
            if (path.length === 0 || nums[i] >= path[path.length - 1]) {
                backtrack(i + 1, [...path, nums[i]]); // Recurse with the next element
            }
        }
    };

    nums.sort((a, b) => a - b); // Sort to handle duplicates
    backtrack(0, []); // Start backtracking from index 0 with an empty path

    return res;
};
