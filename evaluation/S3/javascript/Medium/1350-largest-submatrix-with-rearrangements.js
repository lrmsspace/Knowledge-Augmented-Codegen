/**
 * @param {number[][]} matrix
 * @return {number}
 */
var largestSubmatrix = function(matrix) {
    const rows = matrix.length;
    const cols = matrix[0].length;
    const heights = new Array(cols).fill(0);
    let maxArea = 0;
    
    for (let i = 0; i < rows; i++) {
        for (let j = 0; j < cols; j++) {
            if (matrix[i][j] === 1) {
                heights[j]++;
            } else {
                heights[j] = 0;
            }
        }
        
        // Create a copy of heights and sort it in descending order
        const sortedHeights = [...heights].sort((a, b) => b - a);
        
        // Calculate the area for each column in sorted order
        for (let j = 0; j < cols; j++) {
            maxArea = Math.max(maxArea, sortedHeights[j] * (j + 1));
        }
    }
    
    return maxArea;
};
