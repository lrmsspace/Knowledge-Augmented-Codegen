/**
 * @param {character[]} tasks
 * @param {number} n
 * @return {number}
 */
var leastInterval = function(tasks, n) {
    const taskCounts = new Array(26).fill(0); // Count occurrences of each task (A-Z)

    for (const task of tasks) {
        taskCounts[task.charCodeAt(0) - 'A'.charCodeAt(0)]++; // Increment count for the task
    }
    taskCounts.sort((a, b) => b - a); // Sort counts in descending order

    const maxCount = taskCounts[0];
    let idleSlots = (maxCount - 1) * n; // Calculate initial idle slots based on the most frequent task

    for (let i = 1; i < taskCounts.length; i++) {
        idleSlots -= Math.min(taskCounts[i], maxCount - 1); // Fill idle slots with other tasks
    }
    return idleSlots > 0 ? tasks.length + idleSlots : tasks.length; // Total intervals is tasks plus any remaining idle slots
};
