class Solution(object):
    def maxSubsequence(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: List[int]
        """
        # Step 1: Find the k largest elements using a min-heap
        if k == len(nums):
            return nums
        
        import heapq
        min_heap = []
        
        for num in nums:
            if len(min_heap) < k:
                heapq.heappush(min_heap, num)
            else:
                if num > min_heap[0]:
                    heapq.heapreplace(min_heap, num)
        
        # Step 2: Create a set of the k largest elements for O(1) lookups
        largest_k_set = set(min_heap)
        
        # Step 3: Collect the subsequence of length k with the largest sum
        result = []
        
        for num in nums:
            if num in largest_k_set:
                result.append(num)
                largest_k_set.remove(num)  # Remove to handle duplicates correctly
                if len(result) == k:
                    break
        
        return result