class Solution(object):
    def minBitwiseArray(self, nums):
        """
        :type nums: List[int]
        :rtype: List[int]
        """
        ans = []
        for num in nums:
            if num & (num + 1) == 0:  # Check if num is of the form 2^k - 1
                ans.append(num - 1)    # If so, the answer is num - 1
            else:
                ans.append(-1)         # Otherwise, it's impossible to find such a number
        return ans