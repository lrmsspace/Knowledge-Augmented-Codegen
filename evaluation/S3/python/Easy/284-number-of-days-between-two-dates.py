class Solution(object):
    def daysBetweenDates(self, date1, date2):
        """
        :type date1: str
        :type date2: str
        :rtype: int
        """
        from datetime import datetime

        # 定义日期格式
        date_format = "%Y-%m-%d"

        # 将字符串转换为日期对象
        d1 = datetime.strptime(date1, date_format)
        d2 = datetime.strptime(date2, date_format)

        # 计算两个日期之间的绝对差值
        return abs((d2 - d1).days)