class Solution(object):
    def maximumUnits(self, boxTypes, truckSize):
        """
        :type boxTypes: List[List[int]]
        :type truckSize: int
        :rtype: int
        """
        # Sort the box types by units per box in descending order
        boxTypes.sort(key=lambda x: x[1], reverse=True)

        total_units = 0

        for numberOfBoxes, numberOfUnitsPerBox in boxTypes:
            if truckSize <= 0:
                break

            # Determine how many boxes of this type we can take
            boxes_to_take = min(numberOfBoxes, truckSize)
            total_units += boxes_to_take * numberOfUnitsPerBox
            truckSize -= boxes_to_take

        return total_units