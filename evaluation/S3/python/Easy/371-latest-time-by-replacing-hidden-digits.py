class Solution(object):
    def maximumTime(self, time):
        """
        :type time: str
        :rtype: str
        """
        # Convert the time string to a list for easy manipulation
        time_list = list(time)

        # Handle hours
        if time_list[0] == '?':
            if time_list[1] == '?' or time_list[1] < '4':
                time_list[0] = '2'
            else:
                time_list[0] = '1'
        if time_list[1] == '?':
            if time_list[0] == '2':
                time_list[1] = '3'
            else:
                time_list[1] = '9'

        # Handle minutes
        if time_list[3] == '?':
            time_list[3] = '5'
        if time_list[4] == '?':
            time_list[4] = '9'

        return ''.join(time_list)