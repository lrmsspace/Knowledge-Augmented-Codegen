class Solution(object):
    def minOperations(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: int
        """
        if k not in nums:
            return -1

        nums.sort()
        operations = 0
        for i in range(len(nums) - 1, -1, -1):
            if nums[i] > k:
                operations += 1
            else:
                break

        return operations