class Solution(object):
    def minChanges(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: int
        """
        from collections import Counter
        n = len(nums)
        count = [Counter() for _ in range(k)]
        for i, num in enumerate(nums):
            count[i % k][num] += 1

        changes = 0
        for i in range(k):
            max_freq = max(count[i].values()) if count[i] else 0
            changes += (n // k + (1 if i < n % k else 0)) - max_freq

        return changes