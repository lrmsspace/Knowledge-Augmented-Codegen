class Solution(object):
    def tallestBillboard(self, rods):
        """
        :type rods: List[int]
        :rtype: int
        """
        dp = {0: 0}  # dp[diff] = max height of the taller support with that diff

        for rod in rods:
            new_dp = dp.copy()
            for diff, taller in dp.items():
                shorter = taller - diff

                # Case 1: Add rod to the taller support
                new_dp[diff + rod] = max(new_dp.get(diff + rod, 0), taller + rod)

                # Case 2: Add rod to the shorter support
                new_diff = abs(diff - rod)
                new_dp[new_diff] = max(new_dp.get(new_diff, 0), taller + rod if diff >= rod else shorter + rod)

            dp = new_dp

        return dp.get(0, 0)