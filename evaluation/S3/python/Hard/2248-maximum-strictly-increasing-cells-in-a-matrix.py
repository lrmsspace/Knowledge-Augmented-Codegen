class Solution(object):
    def maxIncreasingCells(self, mat):
        """
        :type mat: List[List[int]]
        :rtype: int
        """
        from collections import defaultdict
        import bisect

        m, n = len(mat), len(mat[0])
        cells = [(mat[i][j], i, j) for i in range(m) for j in range(n)]
        cells.sort()

        row_max = [0] * m
        col_max = [0] * n
        ans = 1

        for value, r, c in cells:
            length = max(row_max[r], col_max[c]) + 1
            row_max[r] = max(row_max[r], length)
            col_max[c] = max(col_max[c], length)
            ans = max(ans, length)

        return ans