class Solution(object):
    def numberOfPowerfulInt(self, start, finish, limit, s):
        """
        :type start: int
        :type finish: int
        :type limit: int
        :type s: str
        :rtype: int
        """
        def count_powerful_integers(n):
            if n < 0:
                return 0
            
            s_len = len(s)
            s_int = int(s)
            count = 0
            
            # Check for powerful integers that end with s
            for i in range(10 ** (s_len - 1) * limit, n + 1, 10 ** s_len):
                if i % (10 ** s_len) == s_int and all(int(digit) <= limit for digit in str(i)):
                    count += 1
            
            return count
        
        return count_powerful_integers(finish) - count_powerful_integers(start - 1)