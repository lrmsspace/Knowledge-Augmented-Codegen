class Solution(object):
    def minNumberOperations(self, target):
        """
        :type target: List[int]
        :rtype: int
        """
        # The minimum number of operations is equal to the sum of the positive differences between consecutive elements in the target array, plus the first element (if it's greater than 0).
        if not target:
            return 0
        
        operations = target[0]  # Start with the first element
        for i in range(1, len(target)):
            if target[i] > target[i - 1]:
                operations += target[i] - target[i - 1]
        
        return operations