class Solution(object):
    def maxArrayValue(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        # Greedy approach: Iterate from the end of the array, merging elements when possible.
        for i in range(len(nums) - 2, -1, -1):
            if nums[i] <= nums[i + 1]:
                nums[i] += nums[i + 1]
                del nums[i + 1]
        
        return max(nums)