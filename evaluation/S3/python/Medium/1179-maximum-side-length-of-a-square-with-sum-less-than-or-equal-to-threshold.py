class Solution(object):
    def maxSideLength(self, mat, threshold):
        """
        :type mat: List[List[int]]
        :type threshold: int
        :rtype: int
        """
        m, n = len(mat), len(mat[0])
        prefix_sum = [[0] * (n + 1) for _ in range(m + 1)]

        # Compute prefix sums
        for i in range(1, m + 1):
            for j in range(1, n + 1):
                prefix_sum[i][j] = mat[i - 1][j - 1] + prefix_sum[i - 1][j] + prefix_sum[i][j - 1] - prefix_sum[i - 1][j - 1]

        max_side = 0

        # Check for squares of increasing size
        for side in range(1, min(m, n) + 1):
            found_valid_square = False
            for i in range(side, m + 1):
                for j in range(side, n + 1):
                    square_sum = prefix_sum[i][j] - prefix_sum[i - side][j] - prefix_sum[i][j - side] + prefix_sum[i - side][j - side]
                    if square_sum <= threshold:
                        found_valid_square = True
                        break
                if found_valid_square:
                    break
            if found_valid_square:
                max_side = side
            else:
                break

        return max_side