class Solution(object):
    def maxSubstringLength(self, s, k):
        """
        :type s: str
        :type k: int
        :rtype: bool
        """
        from collections import Counter

        char_count = Counter(s)
        unique_chars = [char for char, count in char_count.items() if count == 1]

        return len(unique_chars) >= k