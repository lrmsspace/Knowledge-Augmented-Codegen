class Solution(object):
    def escapeGhosts(self, ghosts, target):
        """
        :type ghosts: List[List[int]]
        :type target: List[int]
        :rtype: bool
        """
        # Calculate the Manhattan distance from the start to the target
        player_distance = abs(target[0]) + abs(target[1])
        
        # Check each ghost's distance to the target
        for ghost in ghosts:
            ghost_distance = abs(ghost[0] - target[0]) + abs(ghost[1] - target[1])
            # If any ghost can reach the target as fast or faster than the player, return False
            if ghost_distance <= player_distance:
                return False
        
        # If all ghosts are farther from the target than the player, return True
        return True