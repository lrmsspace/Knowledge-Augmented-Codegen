class Solution(object):
    def findTheLongestSubstring(self, s):
        """
        :type s: str
        :rtype: int
        """
        vowel_to_bit = {'a': 1, 'e': 2, 'i': 4, 'o': 8, 'u': 16}
        state_to_index = {0: -1}  # 初始状态：所有元音出现偶数次
        max_length = 0
        current_state = 0

        for i, char in enumerate(s):
            if char in vowel_to_bit:
                current_state ^= vowel_to_bit[char]  # 切换当前元音的状态

            if current_state in state_to_index:
                max_length = max(max_length, i - state_to_index[current_state])
            else:
                state_to_index[current_state] = i

        return max_length