class Solution(object):
    def numTriplets(self, nums1, nums2):
        """
        :type nums1: List[int]
        :type nums2: List[int]
        :rtype: int
        """
        from collections import Counter

        def count_triplets(arr1, arr2):
            count = 0
            freq = Counter(arr2)
            for num in arr1:
                target = num * num
                for key in freq:
                    if target % key == 0:
                        complement = target // key
                        if complement in freq:
                            if complement == key:
                                count += freq[key] * (freq[key] - 1) // 2
                            else:
                                count += freq[key] * freq[complement]
            return count

        return count_triplets(nums1, nums2) + count_triplets(nums2, nums1)