class Solution(object):
    def kClosest(self, points, k):
        """
        :type points: List[List[int]]
        :type k: int
        :rtype: List[List[int]]
        """
        # Sort points by squared distance from origin (to avoid floating point operations)
        points.sort(key=lambda p: p[0]**2 + p[1]**2)
        return points[:k]