class Solution(object):
    def maxAbsValExpr(self, arr1, arr2):
        """
        :type arr1: List[int]
        :type arr2: List[int]
        :rtype: int
        """
        max_val = 0
        for i in range(4):
            max_val = max(max_val, self.get_max(arr1, arr2, i))
        return max_val