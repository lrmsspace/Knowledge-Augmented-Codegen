class Solution(object):
    def maxScore(self, cardPoints, k):
        """
        :type cardPoints: List[int]
        :type k: int
        :rtype: int
        """
        n = len(cardPoints)
        total_points = sum(cardPoints)
        
        # If k is equal to the length of cardPoints, return the total points
        if k == n:
            return total_points
        
        # The number of cards to exclude from the middle
        exclude_length = n - k
        
        # Calculate the sum of the first 'exclude_length' cards
        current_exclude_sum = sum(cardPoints[:exclude_length])
        min_exclude_sum = current_exclude_sum
        
        # Use a sliding window to find the minimum sum of any contiguous subarray of length 'exclude_length'
        for i in range(exclude_length, n):
            current_exclude_sum += cardPoints[i] - cardPoints[i - exclude_length]
            min_exclude_sum = min(min_exclude_sum, current_exclude_sum)
        
        # The maximum score is the total points minus the minimum points excluded
        return total_points - min_exclude_sum