class Solution(object):
    def makeTheIntegerZero(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        if num1 == 0:
            return 0
        if num1 < num2:
            return -1

        count = 0
        while num1 > 0:
            power_of_two = 1 << count  # This is equivalent to 2**count
            num1 -= power_of_two + num2
            count += 1

        return count if num1 == 0 else -1