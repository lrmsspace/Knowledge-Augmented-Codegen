class Solution(object):

    def maxAlternatingSum(self, nums):

        """

        :type nums: List[int]

        :rtype: int

        """
        even_sum = 0
        odd_sum = 0

        for i in range(len(nums)):
            if i % 2 == 0:
                even_sum = max(even_sum, odd_sum + nums[i])
            else:
                odd_sum = max(odd_sum, even_sum - nums[i])

        return even_sum