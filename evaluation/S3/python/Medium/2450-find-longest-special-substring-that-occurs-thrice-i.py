class Solution(object):
    def maximumLength(self, s):
        """
        :type s: str
        :rtype: int
        """
        n = len(s)
        max_len = -1

        for i in range(n):
            char = s[i]
            count = 0
            for j in range(i, n):
                if s[j] == char:
                    count += 1
                else:
                    break
            if count >= 3:
                max_len = max(max_len, count)

        return max_len