class Solution {
    public List<String> summaryRanges(int[] nums) {
        List<String> res = new ArrayList<>();
        if (nums.length == 0) {
            return res;
        }

        int start = nums[0];
        for (int i = 1; i < nums.length; i++) {
            if (nums[i] != nums[i - 1] + 1) {
                res.add(formatRange(start, nums[i - 1]));
                start = nums[i];
            }
        }
        res.add(formatRange(start, nums[nums.length - 1]));

        return res;         
    }
}
