class Solution {
    public String generateTag(String caption) {
        StringBuilder tag = new StringBuilder("#");
        boolean capitalizeNext = false;

        for (char c : caption.toCharArray()) {
            if (c == ' ') {
                capitalizeNext = true; // Next character should be capitalized
            } else if (Character.isLetter(c)) {
                if (capitalizeNext) {
                    tag.append(Character.toUpperCase(c));
                    capitalizeNext = false; // Reset after capitalizing
                } else {
                    tag.append(Character.toLowerCase(c));
                }
            }
        }

        // Truncate to a maximum of 100 characters
        return tag.length() > 100 ? tag.substring(0, 100) : tag.toString();         
    }
}
