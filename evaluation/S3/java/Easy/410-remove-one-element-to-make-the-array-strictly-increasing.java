// The array nums is strictly increasing if nums[i - 1] < nums[i] for each index (1 <= i < nums.length).
//
// Example:
// Input: nums = [1,2,10,5,7]
// Output: true
// Explanation: By removing 10 at index 2 from nums, it becomes [1,2,5,7].
