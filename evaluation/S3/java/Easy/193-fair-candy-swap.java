class Solution {
    public int[] fairCandySwap(int[] aliceSizes, int[] bobSizes) {
        int sumAlice = 0, sumBob = 0;
        for (int size : aliceSizes) {
            sumAlice += size;
        }
        for (int size : bobSizes) {
            sumBob += size;
        }

        int delta = (sumAlice - sumBob) / 2; // The difference divided by 2

        // Use a HashSet to store Bob's candy sizes for O(1) lookups
        Set<Integer> bobSet = new HashSet<>();
        for (int size : bobSizes) {
            bobSet.add(size);
        }

        // Find the pair of candies to swap
        for (int size : aliceSizes) {
            if (bobSet.contains(size - delta)) {
                return new int[]{size, size - delta};
            }
        }

        // This line should never be reached because the problem guarantees at least one answer exists
        return new int[0];          
    }
}
