class Solution {
    public int numberOfPoints(List<List<Integer>> nums) {
        boolean[] covered = new boolean[101]; // To track covered points from 1 to 100

        for (List<Integer> car : nums) {
            int start = car.get(0);
            int end = car.get(1);
            for (int i = start; i <= end; i++) {
                covered[i] = true; // Mark points covered by the car
            }
        }

        int count = 0;
        for (boolean isCovered : covered) {
            if (isCovered) {
                count++; // Count the number of covered points
            }
        }

        return count;           
    }
}
