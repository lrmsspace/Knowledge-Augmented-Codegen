class Solution {
    public boolean canMakeArithmeticProgression(int[] arr) {
        if (arr.length < 2) {
            return true; // An array with less than 2 elements is trivially an arithmetic progression
        }

        Arrays.sort(arr); // Sort the array to check for arithmetic progression

        int commonDifference = arr[1] - arr[0]; // Calculate the common difference

        for (int i = 2; i < arr.length; i++) {
            if (arr[i] - arr[i - 1] != commonDifference) {
                return false; // If any consecutive difference is not equal to the common difference, return false
            }
        }

        return true; // All differences are equal, return true          
    }
}
