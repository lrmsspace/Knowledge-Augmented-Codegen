class Solution {
    public String makeSmallestPalindrome(String s) {
        char[] chars = s.toCharArray();
        int left = 0, right = chars.length - 1;
        while (left < right) {
            if (chars[left] != chars[right]) {
                char smaller = (char) Math.min(chars[left], chars[right]);
                chars[left] = smaller;
                chars[right] = smaller;
            }
            left++;
            right--;
        }
        return new String(chars);           
    }
}
