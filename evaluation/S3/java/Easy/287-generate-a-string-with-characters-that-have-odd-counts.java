class Solution {
    public String generateTheString(int n) {
        StringBuilder sb = new StringBuilder();
        char c = 'a';
        for (int i = 0; i < n - 1; i++) {
            sb.append(c);
        }
        if (n % 2 == 0) {
            sb.append((char) (c + 1));
        } else {
            sb.append(c);
        }
        return sb.toString();           
    }
}
