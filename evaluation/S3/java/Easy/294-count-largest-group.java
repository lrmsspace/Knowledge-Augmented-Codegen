class Solution {
    public int countLargestGroup(int n) {
        Map<Integer, Integer> groupSizeMap = new HashMap<>();

        for (int i = 1; i <= n; i++) {
            int sumOfDigits = getSumOfDigits(i);
            groupSizeMap.put(sumOfDigits, groupSizeMap.getOrDefault(sumOfDigits, 0) + 1);
        }

        int maxSize = Collections.max(groupSizeMap.values());
        int count = 0;

        for (int size : groupSizeMap.values()) {
            if (size == maxSize) {
                count++;
            }
        }

        return count;           
    }
}
