class Solution {
    public int countStudents(int[] students, int[] sandwiches) {
        int count0 = 0, count1 = 0;
        for (int student : students) {
            if (student == 0) {
                count0++;
            } else {
                count1++;
            }
        }

        for (int sandwich : sandwiches) {
            if (sandwich == 0) {
                if (count0 > 0) {
                    count0--;
                } else {
                    break;
                }
            } else {
                if (count1 > 0) {
                    count1--;
                } else {
                    break;
                }
            }
        }

        return count0 + count1;         
    }
}
