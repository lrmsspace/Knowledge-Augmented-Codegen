class Solution {
    public List<Integer> addToArrayForm(int[] num, int k) {
        List<Integer> result = new ArrayList<>();
        int i = num.length - 1;
        while (i >= 0 || k > 0) {
            int sum = (i >= 0 ? num[i] : 0) + (k % 10);
            result.add(sum % 10);
            k /= 10;
            if (sum >= 10) {
                k++;
            }
            i--;
        }
        Collections.reverse(result);
        return result;          
    }
}
