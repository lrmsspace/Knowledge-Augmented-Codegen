class Solution {
    public int largestSumAfterKNegations(int[] nums, int k) {
        // Sort the array to bring negative numbers to the front
        Arrays.sort(nums);

        // Negate the negative numbers first
        for (int i = 0; i < nums.length && k > 0; i++) {
            if (nums[i] < 0) {
                nums[i] = -nums[i];
                k--;
            } else {
                break; // No more negative numbers to negate
            }
        }

        // If there are still negations left, and k is odd, negate the smallest number
        if (k % 2 == 1) {
            Arrays.sort(nums); // Sort again to find the smallest number
            nums[0] = -nums[0]; // Negate the smallest number
        }

        // Calculate the sum of the modified array
        int sum = 0;
        for (int num : nums) {
            sum += num;
        }

        return sum;         
    }
}
