class Solution {
    public boolean canAliceWin(int[] nums) {
        int singleDigitSum = 0;
        int doubleDigitSum = 0;

        for (int num : nums) {
            if (num < 10) {
                singleDigitSum += num;
            } else {
                doubleDigitSum += num;
            }
        }

        // Alice can choose either all single-digit numbers or all double-digit numbers
        // Alice wins if the sum of her numbers is strictly greater than the sum of Bob's numbers
        return singleDigitSum > doubleDigitSum || doubleDigitSum > singleDigitSum;          
    }
}
