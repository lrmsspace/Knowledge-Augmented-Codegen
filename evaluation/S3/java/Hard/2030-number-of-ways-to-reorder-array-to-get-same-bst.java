class Solution {
    public int numOfWays(int[] nums) {
        int n = nums.length;
        long[][] dp = new long[n + 1][n + 1];
        for (int i = 0; i <= n; i++) {
            dp[i][0] = 1;
            for (int j = 1; j <= i; j++) {
                dp[i][j] = (dp[i - 1][j - 1] + dp[i - 1][j]) % 1000000007;
            }
        }
        return (int) (dfs(nums, dp) - 1);           
    }
}
