class Solution {
    public long maximumScore(int[][] grid) {
        int n = grid.length;
        long maxScore = 0;

        // Calculate the score for each column
        for (int j = 0; j < n; j++) {
            long columnScore = 0;
            for (int i = 0; i < n; i++) {
                if (grid[i][j] > 0) {
                    columnScore += grid[i][j];
                }
            }
            maxScore += columnScore;
        }

        return maxScore;            
    }
}
