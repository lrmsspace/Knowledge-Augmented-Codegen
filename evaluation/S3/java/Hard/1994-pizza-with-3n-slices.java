class Solution {
    public int maxSizeSlices(int[] slices) {
        int n = slices.length;
        int k = n / 3;
        return Math.max(getMaxSize(slices, 0, n - 2, k), getMaxSize(slices, 1, n - 1, k));          
    }
}
