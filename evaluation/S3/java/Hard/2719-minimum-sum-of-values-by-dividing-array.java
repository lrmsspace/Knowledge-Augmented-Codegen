class Solution {
    public int minimumValueSum(int[] nums, int[] andValues) {
        int n = nums.length, m = andValues.length;
        int[][] dp = new int[m + 1][n + 1];
        for (int i = 0; i <= m; i++) {
            Arrays.fill(dp[i], Integer.MAX_VALUE);
        }
        dp[0][0] = 0;

        for (int i = 1; i <= m; i++) {
            for (int j = i; j <= n; j++) {
                int andValue = nums[j - 1];
                for (int k = j - 1; k >= i - 1; k--) {
                    if (andValue == andValues[i - 1]) {
                        dp[i][j] = Math.min(dp[i][j], dp[i - 1][k] + nums[j - 1]);
                    }
                    andValue &= nums[k - 1];
                }
            }
        }

        return dp[m][n] == Integer.MAX_VALUE ? -1 : dp[m][n];           
    }
}
