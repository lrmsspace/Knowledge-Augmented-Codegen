class Solution {
    public int countDigitOne(int n) {
        int count = 0;
        for (long i = 1; i <= n; i *= 10) {
            long divider = i * 10;
            long quotient = n / divider;
            long remainder = n % divider;

            count += quotient * i;
            if (remainder >= 2 * i - 1) {
                count += i;
            } else if (remainder >= i - 1) {
                count += remainder - i + 1;
            }
        }
        return count;           
    }
}
