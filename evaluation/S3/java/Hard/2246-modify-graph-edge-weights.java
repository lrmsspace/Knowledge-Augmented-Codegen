class Solution {
    public int[][] modifiedGraphEdges(int n, int[][] edges, int source, int destination, int target) {
        // Step 1: Build the graph and identify edges with weight -1
        List<List<int[]>> graph = new ArrayList<>();
        List<int[]> negativeEdges = new ArrayList<>();
        for (int i = 0; i < n; i++) {
            graph.add(new ArrayList<>());
        }
        for (int[] edge : edges) {
            int u = edge[0], v = edge[1], w = edge[2];
            graph.get(u).add(new int[]{v, w});
            graph.get(v).add(new int[]{u, w});
            if (w == -1) {
                negativeEdges.add(edge);
            }
        }

        // Step 2: Use Dijkstra's algorithm to find the shortest path from source to destination
        int[] dist = dijkstra(graph, source, n);
        if (dist[destination] < target) {
            return new int[0][];
        }

        // Step 3: Modify edges with weight -1 to meet the target distance
        for (int[] edge : negativeEdges) {
            edge[2] = 1; // Start with the smallest positive weight
        }

        // Recalculate distances after modification
        dist = dijkstra(graph, source, n);
        if (dist[destination] != target) {
            return new int[0][];
        }

        return edges;           
    }
}
