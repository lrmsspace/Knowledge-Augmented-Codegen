class Solution {
    public int[] maximumSumQueries(int[] nums1, int[] nums2, int[][] queries) {
        int n = nums1.length, m = queries.length;
        int[][] pairs = new int[n][2];
        for (int i = 0; i < n; i++) {
            pairs[i][0] = nums1[i];
            pairs[i][1] = nums2[i];
        }
        Arrays.sort(pairs, (a, b) -> b[0] - a[0]);

        int[][] sortedQueries = new int[m][3];
        for (int i = 0; i < m; i++) {
            sortedQueries[i][0] = queries[i][0];
            sortedQueries[i][1] = queries[i][1];
            sortedQueries[i][2] = i;
        }
        Arrays.sort(sortedQueries, (a, b) -> b[0] - a[0]);

        TreeMap<Integer, Integer> map = new TreeMap<>();
        int[] answer = new int[m];
        Arrays.fill(answer, -1);
        int j = 0;
        for (int[] query : sortedQueries) {
            while (j < n && pairs[j][0] >= query[0]) {
                map.put(pairs[j][1], Math.max(map.getOrDefault(pairs[j][1], 0), pairs[j][0] + pairs[j][1]));
                j++;
            }
            Map.Entry<Integer, Integer> entry = map.ceilingEntry(query[1]);
            if (entry != null) {
                answer[query[2]] = entry.getValue();
            }
        }
        return answer;          
    }
}
