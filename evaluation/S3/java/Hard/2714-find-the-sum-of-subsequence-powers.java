class Solution {
    public int sumOfPowers(int[] nums, int k) {
        int mod = (int) 1e9 + 7;
        int n = nums.length;
        Arrays.sort(nums);
        long sum = 0;

        // Generate all combinations of size k using bit manipulation
        for (int mask = 0; mask < (1 << n); mask++) {
            if (Integer.bitCount(mask) == k) {
                List<Integer> subsequence = new ArrayList<>();
                for (int i = 0; i < n; i++) {
                    if ((mask & (1 << i)) != 0) {
                        subsequence.add(nums[i]);
                    }
                }
                sum = (sum + calculatePower(subsequence)) % mod;
            }
        }

        return (int) sum;           
    }
}
