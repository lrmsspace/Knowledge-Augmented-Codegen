class Solution {
    public int numSimilarGroups(String[] strs) {
        int n = strs.length;
        int m = strs[0].length();
        boolean[] visited = new boolean[n];
        int groups = 0;

        for (int i = 0; i < n; i++) {
            if (!visited[i]) {
                dfs(strs, visited, i, m);
                groups++;
            }
        }

        return groups;          
    }
}
