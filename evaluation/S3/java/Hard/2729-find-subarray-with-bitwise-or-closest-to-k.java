class Solution {
    public int minimumDifference(int[] nums, int k) {
        int n = nums.length;
        int ans = Integer.MAX_VALUE;

        for (int i = 0; i < n; i++) {
            int orValue = 0;
            for (int j = i; j < n; j++) {
                orValue |= nums[j];
                ans = Math.min(ans, Math.abs(orValue - k));
            }
        }

        return ans;         
    }
}
