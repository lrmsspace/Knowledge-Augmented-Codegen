class Solution {
    public boolean canMouseWin(String[] grid, int catJump, int mouseJump) {
        int rows = grid.length, cols = grid[0].length();
        int catPos = 0, mousePos = 0, foodPos = 0;
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                char c = grid[i].charAt(j);
                if (c == 'C') catPos = i * cols + j;
                else if (c == 'M') mousePos = i * cols + j;
                else if (c == 'F') foodPos = i * cols + j;
            }
        }
        int[][] graph = new int[rows * cols][];
        for (int i = 0; i < rows * cols; i++) {
            graph[i] = getNextPositions(i, grid, rows, cols);
        }
        return canMouseWin(graph, catPos, mousePos, foodPos, catJump, mouseJump);           
    }
}
