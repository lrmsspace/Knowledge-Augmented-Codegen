class Solution {
    public int maxEqualFreq(int[] nums) {
        int n = nums.length;
        int[] freq = new int[n + 1];
        int[] count = new int[n + 1];
        int maxFreq = 0;
        int result = 0;

        for (int i = 0; i < n; i++) {
            int num = nums[i];
            if (freq[num] > 0) {
                count[freq[num]]--;
            }
            freq[num]++;
            count[freq[num]]++;
            maxFreq = Math.max(maxFreq, freq[num]);

            boolean valid = (maxFreq == 1) ||
                            (count[maxFreq] * maxFreq + count[maxFreq - 1] * (maxFreq - 1) == i + 1 && count[maxFreq] > 0) ||
                            (count[maxFreq] * maxFreq + count[maxFreq - 1] * (maxFreq - 1) == i && count[maxFreq - 1] > 0);
            if (valid) {
                result = i + 1;
            }
        }

        return result;          
    }
}
