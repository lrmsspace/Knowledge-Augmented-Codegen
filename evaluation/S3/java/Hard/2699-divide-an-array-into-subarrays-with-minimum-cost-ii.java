class Solution {
    public long minimumCost(int[] nums, int k, int dist) {
        int n = nums.length;
        int[] extended = new int[2 * n];
        for (int i = 0; i < n; i++) {
            extended[i] = extended[i + n] = nums[i];
        }

        long[][] dp = new long[2 * n][2 * n];
        for (int len = 1; len <= n; len++) {
            for (int l = 0; l + len - 1 < 2 * n; l++) {
                int r = l + len - 1;
                if (len == 1) {
                    dp[l][r] = extended[l];
                } else {
                    dp[l][r] = Long.MAX_VALUE;
                    for (int m = l; m < r; m++) {
                        dp[l][r] = Math.min(dp[l][r], dp[l][m] + dp[m + 1][r]);
                    }
                }
            }
        }

        long minCost = Long.MAX_VALUE;
        for (int start = 0; start < n; start++) {
            int end = start + dist + k - 2;
            if (end < start + n) {
                minCost = Math.min(minCost, dp[start][end]);
            }
        }

        return minCost;         
    }
}
