class Solution {
    public long minMaxSubarraySum(int[] nums, int k) {
        int n = nums.length;
        long totalSum = 0;

        // Calculate the sum of maximum elements in all subarrays of size at most k
        for (int i = 0; i < n; i++) {
            int maxVal = nums[i];
            for (int j = i; j < Math.min(i + k, n); j++) {
                maxVal = Math.max(maxVal, nums[j]);
                totalSum += maxVal;
            }
        }

        // Calculate the sum of minimum elements in all subarrays of size at most k
        for (int i = 0; i < n; i++) {
            int minVal = nums[i];
            for (int j = i; j < Math.min(i + k, n); j++) {
                minVal = Math.min(minVal, nums[j]);
                totalSum += minVal;
            }
        }

        return totalSum;            
    }
}
