class Solution {
    public long countSubstrings(String s) {
        int n = s.length();
        long count = 0;

        // Check substrings of length 1 to 10 (since last digit can be at most 9)
        for (int len = 1; len <= Math.min(10, n); len++) {
            for (int i = 0; i <= n - len; i++) {
                String substring = s.substring(i, i + len);
                int lastDigit = substring.charAt(substring.length() - 1) - '0';
                if (lastDigit != 0 && isDivisible(substring, lastDigit)) {
                    count++;
                }
            }
        }

        return count;           
    }
}
