class Solution {
    public int minPatches(int[] nums, int n) {
        int patches = 0;
        long maxCovered = 0; // Use long to avoid overflow
        int i = 0;

        while (maxCovered < n) {
            if (i < nums.length && nums[i] <= maxCovered + 1) {
                // If the current number can extend the coverage
                maxCovered += nums[i];
                i++;
            } else {
                // Patch the array with maxCovered + 1
                maxCovered += (maxCovered + 1);
                patches++;
            }
        }

        return patches;         
    }
}
