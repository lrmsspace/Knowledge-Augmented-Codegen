class Solution {
    public String getPermutation(int n, int k) {
        // Step 1: Create a list of numbers from 1 to n
        List<Integer> numbers = new ArrayList<>();
        for (int i = 1; i <= n; i++) {
            numbers.add(i);
        }

        // Step 2: Calculate the factorial values for numbers from 0 to n-1
        int[] factorials = new int[n];
        factorials[0] = 1;
        for (int i = 1; i < n; i++) {
            factorials[i] = factorials[i - 1] * i;
        }

        // Step 3: Build the k-th permutation
        StringBuilder permutation = new StringBuilder();
        k--; // Convert k to zero-based index

        for (int i = n - 1; i >= 0; i--) {
            int factorial = factorials[i];
            int index = k / factorial;
            permutation.append(numbers.get(index));
            numbers.remove(index);
            k %= factorial;
        }

        return permutation.toString();          
    }
}
