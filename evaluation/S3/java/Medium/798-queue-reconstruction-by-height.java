class Solution {
    public int[][] reconstructQueue(int[][] people) {
        // Sort the people by height in descending order. If two people have the same height, sort them by the number of people in front in ascending order.
        Arrays.sort(people, (a, b) -> {
            if (a[0] != b[0]) {
                return b[0] - a[0]; // Sort by height in descending order
            } else {
                return a[1] - b[1]; // Sort by number of people in front in ascending order
            }
        });

        List<int[]> queue = new LinkedList<>();

        // Insert each person into the queue based on their number of people in front.
        for (int[] person : people) {
            queue.add(person[1], person); // Insert person at the index equal to the number of people in front
        }

        return queue.toArray(new int[queue.size()][]);          
    }
}
