class Solution {
    public int maxTwoEvents(int[][] events) {
        // Sort events by their end time
        Arrays.sort(events, (a, b) -> Integer.compare(a[1], b[1]));

        int maxValue = 0; // To store the maximum value of a single event
        int result = 0; // To store the maximum sum of two non-overlapping events

        for (int[] event : events) {
            // Binary search to find the rightmost event that ends before the current event starts
            int left = 0, right = maxValue;
            while (left < right) {
                int mid = left + (right - left) / 2;
                if (events[mid][1] < event[0]) {
                    left = mid + 1;
                } else {
                    right = mid;
                }
            }

            // Update the result with the maximum value of a non-overlapping event plus the current event's value
            if (left > 0) {
                result = Math.max(result, events[left - 1][2] + event[2]);
            }

            // Update maxValue to be the maximum value of an event that ends before the current event
            maxValue = Math.max(maxValue, event[2]);
        }

        return result;          
    }
}
