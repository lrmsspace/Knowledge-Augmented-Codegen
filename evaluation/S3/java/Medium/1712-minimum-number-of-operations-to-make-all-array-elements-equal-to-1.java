class Solution {
    public int minOperations(int[] nums) {
        int n = nums.length;
        int minGCD = Integer.MAX_VALUE;
        for (int num : nums) {
            minGCD = gcd(minGCD, num);
        }
        if (minGCD > 1) {
            return -1; // If the minimum GCD is greater than 1, it's impossible to make all elements equal to 1
        }

        int operations = 0;
        for (int num : nums) {
            if (num > 1) {
                operations += countOperations(num);
            }
        }
        return operations;          
    }
}
