class Solution {
    public int rangeBitwiseAnd(int left, int right) {
        // The bitwise AND of a range of numbers will only have the common prefix of the binary representations of the left and right numbers.
        // We can find this common prefix by right-shifting both numbers until they are equal, counting the number of shifts.
        // Then we left-shift the result back to its original position.

        int shiftCount = 0;

        // Right shift until left and right are the same
        while (left != right) {
            left >>= 1;
            right >>= 1;
            shiftCount++;
        }

        // Left shift back to restore the original position
        return left << shiftCount;          
    }
}
