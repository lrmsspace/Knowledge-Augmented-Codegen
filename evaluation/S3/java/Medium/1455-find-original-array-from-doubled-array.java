class Solution {
    public int[] findOriginalArray(int[] changed) {
        if (changed.length % 2 != 0) {
            return new int[0];
        }

        Map<Integer, Integer> freq = new HashMap<>();
        for (int num : changed) {
            freq.put(num, freq.getOrDefault(num, 0) + 1);
        }

        int[] original = new int[changed.length / 2];
        int index = 0;

        // Sort keys to ensure we process smaller numbers first
        List<Integer> keys = new ArrayList<>(freq.keySet());
        Collections.sort(keys);

        for (int key : keys) {
            while (freq.get(key) > 0) {
                int doubleKey = key * 2;
                if (!freq.containsKey(doubleKey) || freq.get(doubleKey) <= 0) {
                    return new int[0];
                }
                original[index++] = key;
                freq.put(key, freq.get(key) - 1);
                freq.put(doubleKey, freq.get(doubleKey) - 1);
            }
        }

        return original;            
    }
}
