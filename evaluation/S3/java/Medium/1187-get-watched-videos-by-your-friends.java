class Solution {
    public List<String> watchedVideosByFriends(List<List<String>> watchedVideos, int[][] friends, int id, int level) {
        int n = friends.length;
        boolean[] used = new boolean[n];
        Queue<Integer> queue = new LinkedList<>();
        queue.offer(id);
        used[id] = true;

        for (int i = 0; i < level; i++) {
            int size = queue.size();
            for (int j = 0; j < size; j++) {
                int curr = queue.poll();
                for (int friend : friends[curr]) {
                    if (!used[friend]) {
                        used[friend] = true;
                        queue.offer(friend);
                    }
                }
            }
        }

        Map<String, Integer> freqMap = new HashMap<>();
        while (!queue.isEmpty()) {
            int friendId = queue.poll();
            for (String video : watchedVideos.get(friendId)) {
                freqMap.put(video, freqMap.getOrDefault(video, 0) + 1);
            }
        }

        List<String> result = new ArrayList<>(freqMap.keySet());
        Collections.sort(result, (a, b) -> {
            int freqCompare = Integer.compare(freqMap.get(a), freqMap.get(b));
            return freqCompare != 0 ? freqCompare : a.compareTo(b);
        });

        return result;          
    }
}
