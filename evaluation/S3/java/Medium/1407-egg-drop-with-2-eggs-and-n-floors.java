class Solution {
    public int twoEggDrop(int n) {
        // The idea is to find the smallest integer k such that k*(k+1)/2 >= n.
        // This is because in the worst case, we can drop the first egg from the k-th floor,
        // then if it breaks, we have k-1 drops left to check the remaining floors below.
        // If it doesn't break, we can move up to the next floor and repeat the process.
        // The total number of drops in the worst case will be k.

        int k = 0;
        while (k * (k + 1) / 2 < n) {
            k++;
        }
        return k;           
    }
}
