class Solution {
    public boolean checkIfCanBreak(String s1, String s2) {
        char[] arr1 = s1.toCharArray();
        char[] arr2 = s2.toCharArray();
        Arrays.sort(arr1);
        Arrays.sort(arr2);

        boolean canS1BreakS2 = true;
        boolean canS2BreakS1 = true;

        for (int i = 0; i < arr1.length; i++) {
            if (arr1[i] < arr2[i]) {
                canS1BreakS2 = false;
            }
            if (arr1[i] > arr2[i]) {
                canS2BreakS1 = false;
            }
        }

        return canS1BreakS2 || canS2BreakS1;            
    }
}
