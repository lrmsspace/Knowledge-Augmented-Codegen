class Solution {
    public boolean predictTheWinner(int[] nums) {
        int n = nums.length;
        int[][] dp = new int[n][n];

        // Base case: When there is only one number, the player takes it.
        for (int i = 0; i < n; i++) {
            dp[i][i] = nums[i];
        }

        // Fill the DP table for subarrays of increasing length.
        for (int length = 2; length <= n; length++) {
            for (int i = 0; i <= n - length; i++) {
                int j = i + length - 1;
                // The player can choose either the left end or the right end.
                // The opponent will also play optimally, so we subtract the opponent's best score from the current choice.
                dp[i][j] = Math.max(nums[i] - dp[i + 1][j], nums[j] - dp[i][j - 1]);
            }
        }

        // If the final score is non-negative, player 1 can win or tie.
        return dp[0][n - 1] >= 0;           
    }
}
