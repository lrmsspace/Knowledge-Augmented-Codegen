class Solution {
    public int findLUSlength(String[] strs) {
        int n = strs.length;
        int res = -1;

        for (int i = 0; i < n; i++) {
            boolean isUncommon = true;

            for (int j = 0; j < n; j++) {
                if (i != j && isSubsequence(strs[i], strs[j])) {
                    isUncommon = false;
                    break;
                }
            }

            if (isUncommon) {
                res = Math.max(res, strs[i].length());
            }
        }

        return res;         
    }
}
