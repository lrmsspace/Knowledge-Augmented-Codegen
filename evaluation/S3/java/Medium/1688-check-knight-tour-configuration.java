class Solution {
    public boolean checkValidGrid(int[][] grid) {
        int n = grid.length;
        int[][] moves = {{-2, -1}, {-2, 1}, {-1, -2}, {-1, 2}, {1, -2}, {1, 2}, {2, -1}, {2, 1}};
        int[] pos = new int[2];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                if (grid[i][j] == 0) {
                    pos[0] = i;
                    pos[1] = j;
                    break;
                }
            }
        }
        for (int step = 1; step < n * n; step++) {
            boolean foundNextMove = false;
            for (int[] move : moves) {
                int nextRow = pos[0] + move[0];
                int nextCol = pos[1] + move[1];
                if (nextRow >= 0 && nextRow < n && nextCol >= 0 && nextCol < n && grid[nextRow][nextCol] == step) {
                    pos[0] = nextRow;
                    pos[1] = nextCol;
                    foundNextMove = true;
                    break;
                }
            }
            if (!foundNextMove) {
                return false;
            }
        }
        return true;            
    }
}
