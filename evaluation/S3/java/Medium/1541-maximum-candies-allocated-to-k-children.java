class Solution {
    public int maximumCandies(int[] candies, long k) {
        int low = 1, high = 0;
        for (int candy : candies) {
            high = Math.max(high, candy);
        }

        while (low < high) {
            int mid = low + (high - low) / 2;
            if (canAllocate(candies, k, mid)) {
                low = mid + 1;
            } else {
                high = mid;
            }
        }

        return low - 1;         
    }
}
