class Solution {
    public boolean canSortArray(int[] nums) {
        int n = nums.length;
        int[] setBitsCount = new int[n];
        for (int i = 0; i < n; i++) {
            setBitsCount[i] = Integer.bitCount(nums[i]);
        }

        for (int i = 0; i < n - 1; i++) {
            if (setBitsCount[i] == setBitsCount[i + 1]) {
                if (nums[i] > nums[i + 1]) {
                    // Swap the elements
                    int tempNum = nums[i];
                    nums[i] = nums[i + 1];
                    nums[i + 1] = tempNum;

                    // Swap the corresponding set bits count
                    int tempSetBits = setBitsCount[i];
                    setBitsCount[i] = setBitsCount[i + 1];
                    setBitsCount[i + 1] = tempSetBits;
                }
            }
        }

        // Check if the array is sorted
        for (int i = 0; i < n - 1; i++) {
            if (nums[i] > nums[i + 1]) {
                return false;
            }
        }

        return true;            
    }
}
