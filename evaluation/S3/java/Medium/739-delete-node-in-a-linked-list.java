/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public void deleteNode(ListNode node) {
        // The idea is to copy the value of the next node into the current node and then bypass the next node.
        // This effectively deletes the current node by overwriting it with the next node's value and removing the next node from the list.

        if (node == null || node.next == null) {
            return; // Cannot delete the last node or a null node
        }

        node.val = node.next.val; // Copy the value of the next node to the current node
        node.next = node.next.next; // Bypass the next node, effectively deleting it            
    }
}
