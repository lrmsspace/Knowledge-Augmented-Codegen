class Solution {
    public long countBadPairs(int[] nums) {
        // The number of pairs (i, j) with i < j is n * (n - 1) / 2.
        // We can count the number of good pairs and subtract from total pairs to get bad pairs.
        int n = nums.length;
        long totalPairs = (long) n * (n - 1) / 2;

        // Map to count occurrences of nums[i] - i
        Map<Integer, Long> countMap = new HashMap<>();
        for (int i = 0; i < n; i++) {
            int key = nums[i] - i;
            countMap.put(key, countMap.getOrDefault(key, 0L) + 1);
        }

        // Count good pairs
        long goodPairs = 0;
        for (long count : countMap.values()) {
            if (count > 1) {
                goodPairs += count * (count - 1) / 2; // C(count, 2)
            }
        }

        // Bad pairs are total pairs minus good pairs
        return totalPairs - goodPairs;          
    }
}
