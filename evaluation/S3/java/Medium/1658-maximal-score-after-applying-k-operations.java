class Solution {
    public long maxKelements(int[] nums, int k) {
        PriorityQueue<Integer> maxHeap = new PriorityQueue<>(Collections.reverseOrder());
        for (int num : nums) {
            maxHeap.offer(num);
        }

        long score = 0;
        for (int i = 0; i < k; i++) {
            int currentMax = maxHeap.poll();
            score += currentMax;
            maxHeap.offer((currentMax + 2) / 3); // ceil(currentMax / 3)
        }

        return score;           
    }
}
