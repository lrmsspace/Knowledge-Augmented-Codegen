class Solution {
    public int numPairsDivisibleBy60(int[] time) {
        int[] count = new int[60];
        int result = 0;

        for (int t : time) {
            int remainder = t % 60;
            int complement = (60 - remainder) % 60; // Handle the case when remainder is 0
            result += count[complement];
            count[remainder]++;
        }

        return result;          
    }
}
