class Solution {
    public int maxDistToClosest(int[] seats) {
        int maxDist = 0;
        int lastOccupied = -1;

        for (int i = 0; i < seats.length; i++) {
            if (seats[i] == 1) {
                if (lastOccupied == -1) {
                    maxDist = i; // Distance from the start to the first occupied seat
                } else {
                    maxDist = Math.max(maxDist, (i - lastOccupied) / 2); // Max distance in the middle
                }
                lastOccupied = i;
            }
        }

        // Check distance from the last occupied seat to the end
        maxDist = Math.max(maxDist, seats.length - 1 - lastOccupied);

        return maxDist;         
    }
}
