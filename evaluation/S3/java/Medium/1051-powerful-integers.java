class Solution {
    public List<Integer> powerfulIntegers(int x, int y, int bound) {
        Set<Integer> powerfulInts = new HashSet<>();
        int maxXPower = 1, maxYPower = 1;

        // Calculate the maximum powers of x and y that are less than or equal to bound
        while (maxXPower < bound) {
            maxXPower *= x;
        }
        while (maxYPower < bound) {
            maxYPower *= y;
        }

        // Generate powerful integers
        for (int i = 1; i < maxXPower; i *= x) {
            for (int j = 1; j < maxYPower; j *= y) {
                int sum = i + j;
                if (sum <= bound) {
                    powerfulInts.add(sum);
                } else {
                    break; // No need to continue if the sum exceeds the bound
                }
            }
        }

        return new ArrayList<>(powerfulInts);           
    }
}
