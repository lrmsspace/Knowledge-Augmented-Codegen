class Solution {
    public int maxWidthRamp(int[] nums) {
        int n = nums.length;
        int[] sortedIndices = new int[n];
        for (int i = 0; i < n; i++) {
            sortedIndices[i] = i;
        }
        Arrays.sort(sortedIndices, (a, b) -> Integer.compare(nums[a], nums[b]));

        int maxWidth = 0;
        int minIndex = sortedIndices[0];
        for (int i = 1; i < n; i++) {
            int currentIndex = sortedIndices[i];
            if (currentIndex < minIndex) {
                minIndex = currentIndex;
            } else {
                maxWidth = Math.max(maxWidth, currentIndex - minIndex);
            }
        }
        return maxWidth;            
    }
}
