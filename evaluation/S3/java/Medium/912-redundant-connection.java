class Solution {
    public int[] findRedundantConnection(int[][] edges) {
        int n = edges.length;
        UnionFind uf = new UnionFind(n + 1); // Nodes are labeled from 1 to n

        for (int[] edge : edges) {
            int u = edge[0];
            int v = edge[1];

            if (!uf.union(u, v)) {
                return edge; // This edge creates a cycle
            }
        }

        return new int[0]; // Should never reach here since there's guaranteed to be a redundant connection         
    }
}
