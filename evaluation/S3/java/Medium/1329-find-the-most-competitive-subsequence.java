class Solution {
    public int[] mostCompetitive(int[] nums, int k) {
        int n = nums.length;
        int[] result = new int[k];
        int idx = 0; // Index for the result array
        // We can remove at most n - k elements to get a subsequence of length k
        int toRemove = n - k;

        for (int num : nums) {
            // While we can remove more elements and the last element in result is greater than the current number
            while (toRemove > 0 && idx > 0 && result[idx - 1] > num) {
                idx--; // Remove the last element from result
                toRemove--; // Decrease the number of removals left
            }
            if (idx < k) { // If we still have space in result, add the current number
                result[idx++] = num;
            } else {
                toRemove--; // If we can't add more elements, we consider it as removed
            }
        }

        return result;          
    }
}
