class Solution {
    public int findCheapestPrice(int n, int[][] flights, int src, int dst, int k) {
        // Create a graph from the flights data
        Map<Integer, List<int[]>> graph = new HashMap<>();
        for (int[] flight : flights) {
            graph.computeIfAbsent(flight[0], x -> new ArrayList<>()).add(new int[]{flight[1], flight[2]});
        }

        // Min-heap to store (cost, current city, stops)
        PriorityQueue<int[]> pq = new PriorityQueue<>(Comparator.comparingInt(a -> a[0]));
        pq.offer(new int[]{0, src, 0}); // cost, city, stops

        while (!pq.isEmpty()) {
            int[] current = pq.poll();
            int cost = current[0];
            int city = current[1];
            int stops = current[2];

            // If we reached the destination, return the cost
            if (city == dst) {
                return cost;
            }

            // If we have made more than k stops, skip further exploration
            if (stops > k) {
                continue;
            }

            // Explore neighbors
            if (graph.containsKey(city)) {
                for (int[] neighbor : graph.get(city)) {
                    int nextCity = neighbor[0];
                    int price = neighbor[1];
                    pq.offer(new int[]{cost + price, nextCity, stops + 1});
                }
            }
        }

        // If we exhaust the priority queue without reaching the destination, return -1
        return -1;          
    }
}
