class Solution {
    public int maxProfit(int[] prices, int fee) {
        int n = prices.length;
        int hold = -prices[0]; // Max profit when holding a stock
        int cash = 0;          // Max profit when not holding a stock

        for (int i = 1; i < n; i++) {
            hold = Math.max(hold, cash - prices[i]); // Either keep holding or buy today
            cash = Math.max(cash, hold + prices[i] - fee); // Either keep cash or sell today
        }

        return cash; // Maximum profit is when not holding any stock at the end         
    }
}
