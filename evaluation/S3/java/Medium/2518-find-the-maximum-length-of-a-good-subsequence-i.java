class Solution {
    public int maximumLength(int[] nums, int k) {
        int n = nums.length;
        int maxLength = 1; // A single element is always a good subsequence
        for (int i = 0; i < n; i++) {
            int changes = 0;
            for (int j = i + 1; j < n; j++) {
                if (nums[j] != nums[j - 1]) {
                    changes++;
                }
                if (changes > k) {
                    break;
                }
                maxLength = Math.max(maxLength, j - i + 1);
            }
        }
        return maxLength;           
    }
}
