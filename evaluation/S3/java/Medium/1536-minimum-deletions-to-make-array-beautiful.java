class Solution {
    public int minDeletion(int[] nums) {
        int deletions = 0;
        int n = nums.length;

        for (int i = 0; i < n - 1; i++) {
            if (i % 2 == 0 && nums[i] == nums[i + 1]) {
                deletions++;
            }
        }

        // If the length of the array after deletions is odd, we need to delete one more element
        if ((n - deletions) % 2 != 0) {
            deletions++;
        }

        return deletions;           
    }
}
