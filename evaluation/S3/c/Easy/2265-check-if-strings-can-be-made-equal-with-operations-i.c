bool canBeEqual(char* s1, char* s2) {
    int count[26] = {0}; // Count of each character in s1 and s2

    // Count characters in s1
    for (int i = 0; i < 4; i++) {
        count[s1[i] - 'a']++;
    }

    // Subtract counts based on characters in s2
    for (int i = 0; i < 4; i++) {
        count[s2[i] - 'a']--;
    }

    // Check if all counts are zero
    for (int i = 0; i < 26; i++) {
        if (count[i] != 0) {
            return false; // If any count is not zero, s1 and s2 cannot be made equal
        }
    }

    return true; // All counts are zero, s1 and s2 can be made equal        
}
