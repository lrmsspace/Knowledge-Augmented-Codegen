int findCenter(int** edges, int edgesSize, int* edgesColSize) {
    // The center of a star graph will be the node that appears in all edges.
    // Since it's a star graph, the center node will be connected to every other node.
    // We can simply check the first two edges to find the common node, which will be the center.

    int firstEdgeNode1 = edges[0][0];
    int firstEdgeNode2 = edges[0][1];
    
    int secondEdgeNode1 = edges[1][0];
    int secondEdgeNode2 = edges[1][1];

    // Check if the first node of the first edge is the center
    if (firstEdgeNode1 == secondEdgeNode1 || firstEdgeNode1 == secondEdgeNode2) {
        return firstEdgeNode1;
    }
    
    // Otherwise, the second node of the first edge is the center
    return firstEdgeNode2;      
}
