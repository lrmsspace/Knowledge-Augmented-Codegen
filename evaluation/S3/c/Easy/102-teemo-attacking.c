int findPoisonedDuration(int* timeSeries, int timeSeriesSize, int duration) {
    if (timeSeriesSize == 0) {
        return 0;
    }
    
    int totalDuration = 0;
    
    for (int i = 0; i < timeSeriesSize - 1; i++) {
        int attackTime = timeSeries[i];
        int nextAttackTime = timeSeries[i + 1];
        
        // Calculate the end time of the current poison effect
        int poisonEndTime = attackTime + duration;
        
        // If the next attack happens before the current poison effect ends,
        // we only count the duration until the next attack.
        if (nextAttackTime < poisonEndTime) {
            totalDuration += nextAttackTime - attackTime;
        } else {
            totalDuration += duration;
        }
    }
    
    // Add the duration for the last attack
    totalDuration += duration;
    
    return totalDuration;    
}
