int minimumMoves(char* s) {
    int moves = 0;
    for (int i = 0; s[i] != '\0'; i++) {
        if (s[i] == 'X') {
            moves++;
            i += 2; // Skip the next two characters as they will be converted to 'O'
        }
    }
    return moves;       
}
