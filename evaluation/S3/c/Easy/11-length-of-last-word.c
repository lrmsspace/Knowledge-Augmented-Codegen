int lengthOfLastWord(char* s) {
    int length = 0;
    int i = 0;

    // Skip trailing spaces
    while (s[i] != '\0') {
        i++;
    }
    i--; // Move back to the last character

    // Count the length of the last word
    while (i >= 0 && s[i] == ' ') {
        i--;
    }
    while (i >= 0 && s[i] != ' ') {
        length++;
        i--;
    }

    return length;      
}
