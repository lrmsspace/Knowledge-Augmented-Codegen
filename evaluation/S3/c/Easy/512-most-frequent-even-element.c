int mostFrequentEven(int* nums, int numsSize) {
    int freq[100001] = {0}; // Frequency array for even numbers up to 10^5
    int maxFreq = 0; // To track the maximum frequency of even numbers
    int result = -1; // To store the most frequent even number

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] % 2 == 0) { // Check if the number is even
            freq[nums[i]]++; // Increment frequency of the even number
            if (freq[nums[i]] > maxFreq) {
                maxFreq = freq[nums[i]]; // Update max frequency
                result = nums[i]; // Update result to the current number
            } else if (freq[nums[i]] == maxFreq && nums[i] < result) {
                result = nums[i]; // Update result to the smaller number in case of a tie
            }
        }
    }

    return result; // Return the most frequent even number or -1 if none found      
}
