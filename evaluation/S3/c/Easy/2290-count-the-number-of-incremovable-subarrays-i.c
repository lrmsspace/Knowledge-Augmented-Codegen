int incremovableSubarrayCount(int* nums, int numsSize) {
    int count = 0;
    for (int start = 0; start < numsSize; ++start) {
        for (int end = start; end < numsSize; ++end) {
            int prev = -1;
            int is_incremovable = 1;
            for (int i = 0; i < numsSize; ++i) {
                if (i >= start && i <= end) {
                    continue; // Skip the subarray [start, end]
                }
                if (prev != -1 && nums[i] <= prev) {
                    is_incremovable = 0; // Not strictly increasing
                    break;
                }
                prev = nums[i];
            }
            if (is_incremovable) {
                count++;
            }
        }
    }
    return count;       
}
