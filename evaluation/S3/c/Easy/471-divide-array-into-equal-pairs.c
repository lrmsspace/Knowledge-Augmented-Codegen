bool divideArray(int* nums, int numsSize) {
    int freq[501] = {0}; // Frequency array to count occurrences of each number

    // Count the frequency of each number in the input array
    for (int i = 0; i < numsSize; i++) {
        freq[nums[i]]++;
    }

    // Check if each number has an even frequency
    for (int i = 1; i <= 500; i++) {
        if (freq[i] % 2 != 0) {
            return false; // If any number has an odd frequency, return false
        }
    }

    return true; // All numbers have even frequency, return true        
}
