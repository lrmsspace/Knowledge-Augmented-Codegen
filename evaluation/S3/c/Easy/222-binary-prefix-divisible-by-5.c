/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
bool* prefixesDivBy5(int* nums, int numsSize, int* returnSize) {
    *returnSize = numsSize;
    bool* result = (bool*)malloc(numsSize * sizeof(bool));
    int prefixValue = 0;

    for (int i = 0; i < numsSize; i++) {
        // Update the prefix value by shifting left and adding the current bit
        prefixValue = ((prefixValue << 1) + nums[i]) % 5;
        // Check if the current prefix value is divisible by 5
        result[i] = (prefixValue == 0);
    }

    return result;      
}
