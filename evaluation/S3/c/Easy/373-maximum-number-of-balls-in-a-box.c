int countBalls(int lowLimit, int highLimit) {
    int boxCount[46] = {0}; // Maximum sum of digits for numbers up to 10^5 is 45 (9*5)
    int maxBalls = 0;

    for (int ball = lowLimit; ball <= highLimit; ball++) {
        int sumDigits = 0;
        int num = ball;

        // Calculate the sum of digits for the current ball number
        while (num > 0) {
            sumDigits += num % 10;
            num /= 10;
        }

        // Increment the count of balls in the corresponding box
        boxCount[sumDigits]++;
        if (boxCount[sumDigits] > maxBalls) {
            maxBalls = boxCount[sumDigits];
        }
    }

    return maxBalls;        
}
