int minTimeToType(char* word) {
    int time = 0;
    char current_char = 'a';

    for (int i = 0; word[i] != '\0'; i++) {
        char target_char = word[i];
        int distance = abs(target_char - current_char);
        time += distance < 13 ? distance : 26 - distance; // Move in the shorter direction
        time++; // Time to type the character
        current_char = target_char; // Update the current character
    }

    return time;        
}
