char* makeGood(char* s) {
    char* stack = (char*)malloc((strlen(s) + 1) * sizeof(char));
    int top = -1;

    for (int i = 0; s[i] != '\0'; i++) {
        char current = s[i];
        if (top >= 0 && abs(stack[top] - current) == 32) {
            top--; // Pop the last character from the stack
        } else {
            stack[++top] = current; // Push the current character onto the stack
        }
    }
    stack[top + 1] = '\0'; // Null-terminate the resulting string
    return stack;       
}
