int minCostToMoveChips(int* position, int positionSize) {
    int oddCount = 0;
    int evenCount = 0;

    for (int i = 0; i < positionSize; i++) {
        if (position[i] % 2 == 0) {
            evenCount++;
        } else {
            oddCount++;
        }
    }

    return oddCount < evenCount ? oddCount : evenCount;     
}
