int numberOfSteps(int num) {
    int steps = 0;
    while (num > 0) {
        if (num % 2 == 0) {
            num /= 2; // If num is even, divide it by 2
        } else {
            num -= 1; // If num is odd, subtract 1 from it
        }
        steps++; // Increment the step count
    }
    return steps;       
}
