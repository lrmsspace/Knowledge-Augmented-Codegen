char repeatedCharacter(char* s) {
    int freq[26] = {0}; // Frequency array for lowercase English letters

    for (int i = 0; s[i] != '\0'; i++) {
        char c = s[i];
        freq[c - 'a']++; // Increment frequency of the current character

        if (freq[c - 'a'] == 2) {
            return c; // Return the first character that appears twice
        }
    }

    return '\0'; // This line should never be reached due to problem constraints        
}
