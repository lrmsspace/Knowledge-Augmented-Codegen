int longestPalindrome(char* s) {
    int charCount[128] = {0}; // Assuming ASCII character set
    int length = 0;
    int hasOddCount = 0;

    // Count the frequency of each character
    for (char* p = s; *p; p++) {
        charCount[(int)(*p)]++;
    }

    // Calculate the length of the longest palindrome
    for (int i = 0; i < 128; i++) {
        if (charCount[i] % 2 == 0) {
            length += charCount[i]; // Add even counts fully
        } else {
            length += charCount[i] - 1; // Add the largest even count
            hasOddCount = 1; // Mark that we have at least one odd count
        }
    }

    // If there's at least one odd count, we can place one odd character in the center
    if (hasOddCount) {
        length += 1;
    }

    return length;      
}
