/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */
int getMinimumDifference(struct TreeNode* root) {
    int min_diff = __INT_MAX__; // Initialize min_diff to the maximum integer value
    struct TreeNode* prev = NULL; // To keep track of the previous node in in-order traversal

    // Helper function for in-order traversal
    void inOrderTraversal(struct TreeNode* node) {
        if (node == NULL) {
            return; // Base case: if the node is NULL, return
        }
        
        inOrderTraversal(node->left); // Traverse the left subtree
        
        if (prev != NULL) { // If there is a previous node, calculate the difference
            int diff = node->val - prev->val; // Calculate the absolute difference
            if (diff < min_diff) { // Update min_diff if the current difference is smaller
                min_diff = diff;
            }
        }
        prev = node; // Update prev to the current node
        
        inOrderTraversal(node->right); // Traverse the right subtree
    }

    inOrderTraversal(root); // Start in-order traversal from the root
    return min_diff; // Return the minimum absolute difference found        
}
