int smallestIndex(int* nums, int numsSize) {
    for (int i = 0; i < numsSize; i++) {
        int sum = 0, n = nums[i];
        while (n > 0) {
            sum += n % 10;
            n /= 10;
        }
        if (sum == i) {
            return i;
        }
    }
    return -1;      
}
