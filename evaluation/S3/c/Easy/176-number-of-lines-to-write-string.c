/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* numberOfLines(int* widths, int widthsSize, char * s, int* returnSize){
    int *result = (int *)malloc(2 * sizeof(int));
    result[0] = 1; // Start with one line
    result[1] = 0; // Start with zero width on the current line

    for (char *p = s; *p != '\0'; p++) {
        int charIndex = *p - 'a'; // Get the index for the character
        int charWidth = widths[charIndex]; // Get the width of the character

        if (result[1] + charWidth > 100) {
            // If adding this character exceeds the line width, move to a new line
            result[0]++; // Increment line count
            result[1] = charWidth; // Start new line with this character's width
        } else {
            // Otherwise, add the character's width to the current line
            result[1] += charWidth;
        }
    }

    *returnSize = 2; // Set the return size to 2
    return result;
}
