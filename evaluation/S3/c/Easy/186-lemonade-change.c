bool lemonadeChange(int* bills, int billsSize) {
    int five = 0, ten = 0; // Track the count of $5 and $10 bills
    for (int i = 0; i < billsSize; i++) {
        if (bills[i] == 5) {
            five++; // Collect a $5 bill
        } else if (bills[i] == 10) {
            if (five > 0) {
                five--; // Give one $5 as change
                ten++;  // Collect a $10 bill
            } else {
                return false; // Cannot give change
            }
        } else { // bills[i] == 20
            if (ten > 0 && five > 0) {
                ten--; // Give one $10 as change
                five--; // Give one $5 as change
            } else if (five >= 3) {
                five -= 3; // Give three $5 bills as change
            } else {
                return false; // Cannot give change
            }
        }
    }
    return true; // Successfully provided change to all customers       
}
