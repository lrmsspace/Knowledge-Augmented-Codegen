int findNumbers(int* nums, int numsSize) {
    int count = 0;
    for (int i = 0; i < numsSize; i++) {
        int numDigits = 0;
        int n = nums[i];
        while (n > 0) {
            n /= 10;
            numDigits++;
        }
        if (numDigits % 2 == 0) {
            count++;
        }
    }
    return count;       
}
