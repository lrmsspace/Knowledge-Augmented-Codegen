long long minimumReplacement(int* nums, int numsSize) {
    long long operations = 0;
    for (int i = numsSize - 2; i >= 0; i--) {
        if (nums[i] > nums[i + 1]) {
            int parts = (nums[i] + nums[i + 1] - 1) / nums[i + 1]; // Calculate how many parts we need to split nums[i] into
            operations += parts - 1; // We need (parts - 1) operations to split into 'parts'
            nums[i] = nums[i] / parts; // Update nums[i] to the new value after splitting
        }
    }
    return operations;      
}
