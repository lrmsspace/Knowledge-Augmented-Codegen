long long incremovableSubarrayCount(int* nums, int numsSize) {
    long long count = 0;
    int start = 0;

    for (int i = 1; i < numsSize; i++) {
        if (nums[i] <= nums[i - 1]) {
            // Calculate the number of subarrays in the current increasing segment
            long long length = i - start;
            count += (length * (length + 1)) / 2; // Number of subarrays in a segment of length 'length'
            start = i; // Start a new segment
        }
    }

    // Count the last increasing segment
    long long length = numsSize - start;
    count += (length * (length + 1)) / 2;

    return count;       
}
