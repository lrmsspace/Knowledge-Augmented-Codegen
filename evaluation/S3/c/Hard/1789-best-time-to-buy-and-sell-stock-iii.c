int maxProfit(int* prices, int pricesSize) {
    if (pricesSize < 2) {
        return 0;
    }

    int maxProfit = 0;
    int minPrice1 = prices[0];
    int maxProfit1 = 0;
    int minPrice2 = prices[0];
    int maxProfit2 = 0;

    for (int i = 1; i < pricesSize; i++) {
        minPrice1 = minPrice1 < prices[i] ? minPrice1 : prices[i];
        maxProfit1 = maxProfit1 > (prices[i] - minPrice1) ? maxProfit1 : (prices[i] - minPrice1);
        minPrice2 = minPrice2 < (prices[i] - maxProfit1) ? minPrice2 : (prices[i] - maxProfit1);
        maxProfit2 = maxProfit2 > (prices[i] - minPrice2) ? maxProfit2 : (prices[i] - minPrice2);
    }

    return maxProfit2;      
}
