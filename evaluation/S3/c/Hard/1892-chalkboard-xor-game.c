bool xorGame(int* nums, int numsSize) {
    int xorSum = 0;
    for (int i = 0; i < numsSize; i++) {
        xorSum ^= nums[i];
    }
    // If the XOR sum is 0, Alice wins immediately.
    if (xorSum == 0) {
        return true;
    }
    // If there are an even number of elements, Alice can always win by making a move that leaves an odd number of elements for Bob.
    return numsSize % 2 == 0;       
}
