long long maximumValueSum(int** board, int boardSize, int* boardColSize) {
    int m = boardSize, n = boardColSize[0];
    long long maxSum = LLONG_MIN;

    // Generate all combinations of 3 rows
    for (int i = 0; i < m - 2; i++) {
        for (int j = i + 1; j < m - 1; j++) {
            for (int k = j + 1; k < m; k++) {
                // For each combination of rows, find the maximum sum of values in those rows
                long long currentSum = 0;
                for (int col = 0; col < n; col++) {
                    currentSum += board[i][col] + board[j][col] + board[k][col];
                }
                if (currentSum > maxSum) {
                    maxSum = currentSum;
                }
            }
        }
    }

    return maxSum;      
}
