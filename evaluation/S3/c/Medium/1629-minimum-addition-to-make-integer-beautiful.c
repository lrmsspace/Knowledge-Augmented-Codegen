long long makeIntegerBeautiful(long long n, int target) {
    long long x = 0, p = 1;
    while (n > 0) {
        int sum = 0, m = n;
        while (m > 0) {
            sum += m % 10;
            m /= 10;
        }
        if (sum <= target) {
            break;
        }
        int d = n % 10;
        x += ((10 - d) % 10) * p;
        n += ((10 - d) % 10);
        p *= 10;
    }
    return x;       
}
