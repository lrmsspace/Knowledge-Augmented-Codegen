/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */
bool btreeGameWinningMove(struct TreeNode* root, int n, int x) {
    struct TreeNode *xNode = NULL;
    int leftCount = 0, rightCount = 0;

    // Helper function to find the node with value x and count the nodes in its left and right subtrees
    void dfs(struct TreeNode* node) {
        if (!node) return;
        if (node->val == x) {
            xNode = node;
        }
        dfs(node->left);
        dfs(node->right);
    }

    dfs(root);

    // Count the nodes in the left subtree of x
    void countNodes(struct TreeNode* node, int* count) {
        if (!node) return;
        (*count)++;
        countNodes(node->left, count);
        countNodes(node->right, count);
    }

    if (xNode) {
        countNodes(xNode->left, &leftCount);
        countNodes(xNode->right, &rightCount);
    }

    int parentCount = n - (leftCount + rightCount + 1); // Nodes not in x's subtree

    // The second player can win if they can control more than half of the nodes
    return (parentCount > n / 2) || (leftCount > n / 2) || (rightCount > n / 2);        
}
