// Problem Description:
// Given two strings s1 and s2, return the lowest ASCII sum of deleted characters to make two strings equal.
//
// Example:
// Input: s1 = "sea", s2 = "eat"
// Output: 231
// Explanation: Deleting "s" from "sea" adds the ASCII value of "s" (115) to the sum.
