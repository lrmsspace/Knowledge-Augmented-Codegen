bool checkInclusion(char* s1, char* s2) {
    int len1 = strlen(s1), len2 = strlen(s2);
    if (len1 > len2) return false;

    int count[26] = {0};
    for (int i = 0; i < len1; i++) {
        count[s1[i] - 'a']++;
        count[s2[i] - 'a']--;
    }

    for (int i = 0; i < 26; i++) {
        if (count[i] != 0) return false;
    }

    for (int i = len1; i < len2; i++) {
        count[s2[i - len1] - 'a']++;
        count[s2[i] - 'a']--;

        bool match = true;
        for (int j = 0; j < 26; j++) {
            if (count[j] != 0) {
                match = false;
                break;
            }
        }
        if (match) return true;
    }

    return false;       
}
