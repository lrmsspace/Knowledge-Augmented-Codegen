int findNthDigit(int n) {
    long long digit_length = 1; // Current digit length (1 for 1-9, 2 for 10-99, etc.)
    long long count = 9;        // Count of numbers with the current digit length
    long long start = 1;        // Starting number for the current digit length

    // Find the range in which the nth digit falls
    while (n > digit_length * count) {
        n -= digit_length * count; // Decrease n by the number of digits in this range
        digit_length++;            // Move to the next digit length
        count *= 10;              // Update count for the next range
        start *= 10;              // Update start for the next range
    }

    // Calculate the exact number that contains the nth digit
    long long number = start + (n - 1) / digit_length; // The number that contains the nth digit
    int digit_index = (n - 1) % digit_length;          // The index of the digit in that number

    // Extract and return the nth digit
    return (number / (long long)pow(10, digit_length - 1 - digit_index)) % 10;      
}
