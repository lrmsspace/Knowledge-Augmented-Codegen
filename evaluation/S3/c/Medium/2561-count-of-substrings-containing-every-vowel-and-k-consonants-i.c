int countOfSubstrings(char* word, int k) {
    int count = 0;
    int n = strlen(word);
    for (int i = 0; i < n; i++) {
        int vowels[5] = {0}; // a, e, i, o, u
        int consonants = 0;
        for (int j = i; j < n; j++) {
            char c = word[j];
            if (c == 'a') vowels[0]++;
            else if (c == 'e') vowels[1]++;
            else if (c == 'i') vowels[2]++;
            else if (c == 'o') vowels[3]++;
            else if (c == 'u') vowels[4]++;
            else consonants++;

            // Check if all vowels are present and consonants count is k
            if (vowels[0] > 0 && vowels[1] > 0 && vowels[2] > 0 && vowels[3] > 0 && vowels[4] > 0 && consonants == k) {
                count++;
            }
        }
    }
    return count;       
}
