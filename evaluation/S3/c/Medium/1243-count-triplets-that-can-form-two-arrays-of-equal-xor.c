int countTriplets(int* arr, int arrSize) {
    int count = 0;
    for (int i = 0; i < arrSize; i++) {
        int xorSum = 0;
        for (int j = i; j < arrSize; j++) {
            xorSum ^= arr[j];
            if (xorSum == 0) {
                count += (j - i);
            }
        }
    }
    return count;       
}
