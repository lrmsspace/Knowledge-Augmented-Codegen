int minimumDeletions(char* s) {
    int countA = 0, countB = 0, deletions = 0;
    for (char* p = s; *p; p++) {
        if (*p == 'a') {
            countA++;
        } else {
            countB++;
            deletions = deletions < countA ? deletions + 1 : countA;
        }
    }
    return deletions;       
}
