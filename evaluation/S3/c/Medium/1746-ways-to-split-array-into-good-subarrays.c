int numberOfGoodSubarraySplits(int* nums, int numsSize) {
    int mod = 1000000007;
    long long result = 1; // Use long long to handle large results
    int count = 0; // Count of zeros between ones
    int onesCount = 0; // Count of ones in the array

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] == 1) {
            onesCount++;
            if (onesCount > 1) {
                result = (result * (count + 1)) % mod; // Multiply by the number of ways to split zeros
            }
            count = 0; // Reset count for the next segment of zeros
        } else {
            count++; // Increment count of zeros
        }
    }

    return onesCount == 0 ? 0 : result; // If there are no ones, return 0       
}
