int minExtraChar(char* s, char** dictionary, int dictionarySize) {
    int n = strlen(s);
    int dp[n + 1];
    memset(dp, 0, sizeof(dp));
    
    for (int i = 1; i <= n; i++) {
        dp[i] = dp[i - 1] + 1; // Assume the current character is extra
        for (int j = 0; j < dictionarySize; j++) {
            int len = strlen(dictionary[j]);
            if (i >= len && strncmp(s + i - len, dictionary[j], len) == 0) {
                dp[i] = fmin(dp[i], dp[i - len]); // Update dp[i] if we can form a valid substring
            }
        }
    }
    
    return dp[n];       
}
