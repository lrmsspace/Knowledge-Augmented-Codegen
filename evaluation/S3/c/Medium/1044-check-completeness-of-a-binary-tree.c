/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */
bool isCompleteTree(struct TreeNode* root) {
    if (!root) return true; // An empty tree is complete.

    // Use a queue for level order traversal.
    struct TreeNode* queue[100]; // Assuming max 100 nodes as per constraints.
    int front = 0, rear = 0;
    queue[rear++] = root;

    bool end = false; // Flag to indicate if we've encountered a null node.

    while (front < rear) {
        struct TreeNode* current = queue[front++];

        if (current) {
            if (end) return false; // If we have seen a null node before, it's not complete.
            queue[rear++] = current->left; // Enqueue left child.
            queue[rear++] = current->right; // Enqueue right child.
        } else {
            end = true; // Mark that we've seen a null node.
        }
    }

    return true; // If we finish level order traversal without issues, it's complete.       
}
