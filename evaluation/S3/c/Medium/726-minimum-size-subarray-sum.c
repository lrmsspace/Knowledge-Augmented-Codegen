int minSubArrayLen(int target, int* nums, int numsSize) {
    int min_length = 0;
    int sum = 0;
    int left = 0;

    for (int right = 0; right < numsSize; right++) {
        sum += nums[right];

        while (sum >= target) {
            int current_length = right - left + 1;
            if (min_length == 0 || current_length < min_length) {
                min_length = current_length;
            }
            sum -= nums[left];
            left++;
        }
    }

    return min_length;      
}
