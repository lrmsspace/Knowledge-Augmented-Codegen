long long countAlternatingSubarrays(int* nums, int numsSize) {
    long long count = 0;
    int start = 0;

    for (int i = 1; i < numsSize; i++) {
        if (nums[i] == nums[i - 1]) {
            int length = i - start;
            count += (long long)length * (length + 1) / 2;
            start = i;
        }
    }

    // Count the last segment
    int length = numsSize - start;
    count += (long long)length * (length + 1) / 2;

    return count;       
}
