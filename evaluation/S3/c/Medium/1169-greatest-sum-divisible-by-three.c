int maxSumDivThree(int* nums, int numsSize) {
    int dp[3] = {0, 0, 0}; // dp[i] will hold the maximum sum with remainder i when divided by 3
    for (int i = 0; i < numsSize; i++) {
        int num = nums[i];
        int mod = num % 3;
        int temp[3];
        for (int j = 0; j < 3; j++) {
            temp[j] = dp[j]; // Store current dp values to update them simultaneously
        }
        for (int j = 0; j < 3; j++) {
            int new_mod = (j + mod) % 3; // Calculate new remainder after adding num
            if (temp[j] + num > dp[new_mod]) { // Update dp if we get a better sum
                dp[new_mod] = temp[j] + num;
            }
        }
    }
    return dp[0]; // The maximum sum that is divisible by 3 will be in dp[0]        
}
