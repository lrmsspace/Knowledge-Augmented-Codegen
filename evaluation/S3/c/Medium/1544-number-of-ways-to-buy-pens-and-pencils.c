long long waysToBuyPensPencils(int total, int cost1, int cost2) {
    long long count = 0;
    for (int pens = 0; pens * cost1 <= total; pens++) {
        int remaining = total - pens * cost1;
        count += remaining / cost2 + 1; // +1 for the case of buying 0 pencils
    }
    return count;       
}
