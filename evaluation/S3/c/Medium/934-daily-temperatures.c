/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* dailyTemperatures(int* temperatures, int temperaturesSize, int* returnSize) {
    *returnSize = temperaturesSize;
    int* answer = (int*)malloc(temperaturesSize * sizeof(int));
    if (!answer) return NULL; // Handle malloc failure

    // Stack to store indices of temperatures
    int* stack = (int*)malloc(temperaturesSize * sizeof(int));
    if (!stack) {
        free(answer);
        return NULL; // Handle malloc failure
    }
    int top = -1; // Stack is initially empty

    for (int i = 0; i < temperaturesSize; i++) {
        // Pop from stack while current temperature is greater than the temperature at the index on top of the stack
        while (top >= 0 && temperatures[i] > temperatures[stack[top]]) {
            int idx = stack[top--]; // Get the index and pop it
            answer[idx] = i - idx; // Calculate the number of days until a warmer temperature
        }
        stack[++top] = i; // Push current index onto the stack
    }

    // For indices still in the stack, there are no warmer days ahead
    while (top >= 0) {
        answer[stack[top--]] = 0; // Set remaining indices to 0
    }

    free(stack); // Free the stack memory
    return answer;      
}
