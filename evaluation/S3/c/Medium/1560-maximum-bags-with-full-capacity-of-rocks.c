int maximumBags(int* capacity, int capacitySize, int* rocks, int rocksSize, int additionalRocks) {
    // Calculate the remaining capacity for each bag
    int* remaining = (int*)malloc(capacitySize * sizeof(int));
    for (int i = 0; i < capacitySize; i++) {
        remaining[i] = capacity[i] - rocks[i];
    }

    // Sort the remaining capacities
    qsort(remaining, capacitySize, sizeof(int), (int (*)(const void*, const void*))compare);

    // Fill the bags with additional rocks
    int count = 0;
    for (int i = 0; i < capacitySize; i++) {
        if (additionalRocks >= remaining[i]) {
            additionalRocks -= remaining[i];
            count++;
        } else {
            break;
        }
    }

    free(remaining);
    return count;       
}
