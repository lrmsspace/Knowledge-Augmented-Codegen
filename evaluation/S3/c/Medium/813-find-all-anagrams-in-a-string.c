/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* findAnagrams(char* s, char* p, int* returnSize) {
    int s_len = strlen(s);
    int p_len = strlen(p);
    if (s_len < p_len) {
        *returnSize = 0;
        return NULL;
    }

    int* result = (int*)malloc(s_len * sizeof(int));
    int result_index = 0;

    int p_count[26] = {0};
    int s_count[26] = {0};

    for (int i = 0; i < p_len; i++) {
        p_count[p[i] - 'a']++;
        s_count[s[i] - 'a']++;
    }

    for (int i = p_len; i < s_len; i++) {
        if (memcmp(p_count, s_count, sizeof(p_count)) == 0) {
            result[result_index++] = i - p_len;
        }
        s_count[s[i] - 'a']++;
        s_count[s[i - p_len] - 'a']--;
    }

    // Check the last window
    if (memcmp(p_count, s_count, sizeof(p_count)) == 0) {
        result[result_index++] = s_len - p_len;
    }

    *returnSize = result_index;
    return result;      
}
