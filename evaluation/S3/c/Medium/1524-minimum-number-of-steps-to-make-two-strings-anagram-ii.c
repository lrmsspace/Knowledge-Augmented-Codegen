int minSteps(char* s, char* t) {
    int count[26] = {0};
    int steps = 0;

    // Count frequency of characters in s
    for (int i = 0; s[i] != '\0'; i++) {
        count[s[i] - 'a']++;
    }

    // Subtract frequency based on characters in t
    for (int i = 0; t[i] != '\0'; i++) {
        count[t[i] - 'a']--;
    }

    // Calculate total steps needed
    for (int i = 0; i < 26; i++) {
        if (count[i] > 0) {
            steps += count[i]; // Characters to remove from s
        } else {
            steps -= count[i]; // Characters to remove from t
        }
    }

    return steps;       
}
