long long gridGame(int** grid, int gridSize, int* gridColSize) {
    int n = gridColSize[0];
    long long topSum = 0, bottomSum = 0;

    // Calculate the total points in the top row
    for (int c = 0; c < n; c++) {
        topSum += grid[0][c];
    }

    long long maxPointsForSecondRobot = 0;

    // Iterate through each column to find the optimal path for the first robot
    for (int c = 0; c < n; c++) {
        // Points collected by the second robot if the first robot goes down at column c
        long long pointsForSecondRobot = topSum - grid[0][c] + bottomSum;

        // Update the maximum points for the second robot
        if (pointsForSecondRobot > maxPointsForSecondRobot) {
            maxPointsForSecondRobot = pointsForSecondRobot;
        }

        // Update the sums for the next iteration
        topSum -= grid[0][c]; // First robot collects points from the top row
        bottomSum += grid[1][c]; // First robot collects points from the bottom row
    }

    return maxPointsForSecondRobot;     
}
