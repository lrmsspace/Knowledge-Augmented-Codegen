char* findLexSmallestString(char* s, int a, int b) {
    int n = strlen(s);
    char* best = strdup(s);
    char* current = strdup(s);

    // A set to track visited configurations
    char visited[100][101]; // Assuming max length of s is 100
    memset(visited, 0, sizeof(visited));

    while (1) {
        // Mark current configuration as visited
        if (visited[current[0] - '0'][current[1] - '0']) {
            break; // If we have already seen this configuration, we are done
        }
        visited[current[0] - '0'][current[1] - '0'] = 1;

        // Update best string if current is smaller
        if (strcmp(current, best) < 0) {
            strcpy(best, current);
        }

        // Apply the two operations
        char next[101];
        
        // Operation 1: Add a to all odd indices
        for (int i = 0; i < n; i++) {
            next[i] = current[i];
            if (i % 2 == 1) {
                next[i] = ((current[i] - '0' + a) % 10) + '0';
            }
        }
        next[n] = '\0';
        
        // Check if we have already visited this configuration
        if (!visited[next[0] - '0'][next[1] - '0']) {
            strcpy(current, next);
            continue;
        }

        // Operation 2: Rotate s to the right by b positions
        char rotated[101];
        for (int i = 0; i < n; i++) {
            rotated[(i + b) % n] = current[i];
        }
        rotated[n] = '\0';

        // Check if we have already visited this configuration
        if (!visited[rotated[0] - '0'][rotated[1] - '0']) {
            strcpy(current, rotated);
            continue;
        }

        break; // If both operations lead to visited configurations, we are done
    }

    free(current);
    return best;        
}
