char* countAndSay(int n) {
    if (n == 1) {
        char* result = (char*)malloc(2 * sizeof(char));
        result[0] = '1';
        result[1] = '\0';
        return result;
    }

    char* prev = countAndSay(n - 1);
    int len = strlen(prev);
    char* result = (char*)malloc((2 * len + 1) * sizeof(char)); // Worst case: "111...1" -> "31...1"
    int index = 0;

    for (int i = 0; i < len; ) {
        char current_char = prev[i];
        int count = 0;

        while (i < len && prev[i] == current_char) {
            count++;
            i++;
        }

        index += sprintf(result + index, "%d%c", count, current_char);
    }

    result[index] = '\0';
    free(prev); // Free the previous string to avoid memory leak
    return result;      
}
