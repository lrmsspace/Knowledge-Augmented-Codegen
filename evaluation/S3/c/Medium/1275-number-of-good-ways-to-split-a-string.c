int numSplits(char* s) {
    int left[26] = {0}, right[26] = {0};
    int leftCount = 0, rightCount = 0, goodSplits = 0;

    // Count the frequency of each character in the right array
    for (int i = 0; s[i] != '\0'; i++) {
        if (right[s[i] - 'a'] == 0) {
            rightCount++;
        }
        right[s[i] - 'a']++;
    }

    // Iterate through the string and update counts
    for (int i = 0; s[i] != '\0'; i++) {
        char c = s[i];
        // Move character from right to left
        right[c - 'a']--;
        if (right[c - 'a'] == 0) {
            rightCount--;
        }

        if (left[c - 'a'] == 0) {
            leftCount++;
        }
        left[c - 'a']++;

        // Check if the number of distinct characters is the same
        if (leftCount == rightCount) {
            goodSplits++;
        }
    }

    return goodSplits;      
}
