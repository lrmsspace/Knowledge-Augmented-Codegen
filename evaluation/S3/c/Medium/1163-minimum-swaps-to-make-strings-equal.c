int minimumSwap(char* s1, char* s2) {
    int countXY = 0, countYX = 0;
    for (int i = 0; s1[i] != '\0'; i++) {
        if (s1[i] == 'x' && s2[i] == 'y') {
            countXY++;
        } else if (s1[i] == 'y' && s2[i] == 'x') {
            countYX++;
        }
    }
    
    if ((countXY + countYX) % 2 != 0) {
        return -1; // If the total number of mismatches is odd, it's impossible to make them equal.
    }
    
    return (countXY / 2) + (countYX / 2) + (countXY % 2) + (countYX % 2);       
}
