int minimumAverageDifference(int* nums, int numsSize) {
    long long total_sum = 0;
    for (int i = 0; i < numsSize; i++) {
        total_sum += nums[i];
    }

    long long left_sum = 0;
    int min_diff = __INT_MAX__;
    int min_index = -1;

    for (int i = 0; i < numsSize; i++) {
        left_sum += nums[i];
        long long right_sum = total_sum - left_sum;

        int left_avg = left_sum / (i + 1);
        int right_avg = (i == numsSize - 1) ? 0 : right_sum / (numsSize - i - 1);

        int diff = left_avg > right_avg ? left_avg - right_avg : right_avg - left_avg;

        if (diff < min_diff) {
            min_diff = diff;
            min_index = i;
        }
    }

    return min_index;       
}
