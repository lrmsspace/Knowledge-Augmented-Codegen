int maximumLength(char* s) {
    int count[26] = {0};
    for (int i = 0; s[i]; i++) {
        count[s[i] - 'a']++;
    }
    int maxLength = -1;
    for (int i = 0; i < 26; i++) {
        if (count[i] >= 3) {
            int length = count[i] / 3;
            if (length > maxLength) {
                maxLength = length;
            }
        }
    }
    return maxLength;       
}
