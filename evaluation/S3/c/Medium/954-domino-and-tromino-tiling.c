int numTilings(int n) {
    if (n == 1) return 1;
    if (n == 2) return 2;
    int mod = 1000000007;
    int dp[n + 1];
    dp[0] = 1; // Base case: one way to tile an empty board
    dp[1] = 1; // Base case: one way to tile a 2x1 board
    dp[2] = 2; // Base case: two ways to tile a 2x2 board

    for (int i = 3; i <= n; i++) {
        dp[i] = ((long long)dp[i - 1] + dp[i - 2]) % mod; // Add the ways to tile by placing a domino vertically or horizontally
        for (int j = i - 3; j >= 0; j--) {
            dp[i] = (dp[i] + (long long)dp[j] * 2) % mod; // Add the ways to tile by placing a tromino
        }
    }
    
    return dp[n];       
}
