/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */
struct ListNode* swapNodes(struct ListNode* head, int k) {
    struct ListNode* first = head;
    struct ListNode* second = head;
    struct ListNode* current = head;
    int count = 1;

    // Traverse the list to find the kth node from the beginning and the end
    while (current != NULL) {
        if (count == k) {
            first = current; // kth node from the beginning
        }
        if (count > k) {
            second = second->next; // Move second pointer after k steps
        }
        current = current->next;
        count++;
    }

    // Swap values of the two nodes
    int temp = first->val;
    first->val = second->val;
    second->val = temp;

    return head;
}
