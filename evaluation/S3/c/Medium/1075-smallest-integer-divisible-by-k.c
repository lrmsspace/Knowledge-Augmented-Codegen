int smallestRepunitDivByK(int k) {
    if (k % 2 == 0 || k % 5 == 0) {
        return -1; // If k is divisible by 2 or 5, it cannot be formed by a number consisting of only '1's
    }
    
    int remainder = 0;
    for (int length = 1; length <= k; length++) {
        remainder = (remainder * 10 + 1) % k; // Generate the next number consisting of '1's and take modulo k
        if (remainder == 0) {
            return length; // If the remainder is 0, we found our answer
        }
    }
    
    return -1; // If we exhaust all possibilities up to k and don't find a solution, return -1      
}
