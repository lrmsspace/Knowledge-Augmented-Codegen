/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* resultsArray(int* nums, int numsSize, int k, int* returnSize) {
    *returnSize = numsSize - k + 1;
    int* results = (int*)malloc((*returnSize) * sizeof(int));
    if (!results) return NULL; // Handle malloc failure

    for (int i = 0; i <= numsSize - k; i++) {
        int max = nums[i];
        int min = nums[i];
        for (int j = 1; j < k; j++) {
            if (nums[i + j] > max) max = nums[i + j];
            if (nums[i + j] < min) min = nums[i + j];
        }
        // Check if the elements are consecutive and sorted
        if (max - min == k - 1) {
            results[i] = max;
        } else {
            results[i] = -1;
        }
    }

    return results;     
}
