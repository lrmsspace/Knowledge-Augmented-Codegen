/**
 * @param {number} n
 * @param {number[]} sums
 * @return {number[]}
 */
var recoverArray = function(n, sums) {
    sums.sort((a, b) => a - b);
    const result = [];
    
    for (let i = 0; i < n; i++) {
        const half = sums.length / 2;
        const diff = sums[half] - sums[0];
        result.push(diff);
        sums = sums.filter(x => x < sums[half]);
    }
    
    return result;
};
