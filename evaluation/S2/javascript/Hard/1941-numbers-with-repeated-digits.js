/**
 * @param {number} n
 * @return {number}
 */
var numDupDigitsAtMostN = function(n) {
    const digits = String(n).split('').map(Number);
    const m = digits.length;
    const dp = new Array(m + 1).fill(0).map(() => new Array(1 << 10).fill(-1));
    
    const countUniqueDigits = (index, mask, isTight) => {
        if (index === m) return 1;
        if (dp[index][mask] !== -1 && !isTight) return dp[index][mask];
        
        let result = 0;
        const maxDigit = isTight ? digits[index] : 9;
        for (let digit = 0; digit <= maxDigit; digit++) {
            if (mask & (1 << digit)) continue;
            const newMask = mask | (1 << digit);
            const newIsTight = isTight && (digit === maxDigit);
            result += countUniqueDigits(index + 1, newMask, newIsTight);
        }
        if (!isTight) dp[index][mask] = result;
        return result;
    };
    return n - countUniqueDigits(0, 0, true);
};
