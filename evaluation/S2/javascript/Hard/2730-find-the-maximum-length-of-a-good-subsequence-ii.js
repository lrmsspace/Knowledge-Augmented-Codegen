/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var maximumLength = function(nums, k) {
    const n = nums.length;
    const dp = new Array(n).fill(1);
    const map = new Map();
    let maxLength = 1;
    for (let i = 0; i < n; i++) {
        const num = nums[i];
        if (map.has(num - k)) {
            dp[i] = Math.max(dp[i], map.get(num - k) + 1);
        }
        map.set(num, Math.max(map.get(num) || 0, dp[i]));
        maxLength = Math.max(maxLength, dp[i]);
    }
    return maxLength;
};
