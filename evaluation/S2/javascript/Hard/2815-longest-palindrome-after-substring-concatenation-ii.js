/**
 * @param {string} s
 * @param {string} t
 * @return {number}
 */
var longestPalindrome = function(s, t) {
    const combined = s + '#' + t;
    const prefix = new Array(combined.length).fill(0);
    
    for (let i = 1; i < combined.length; i++) {
        let j = prefix[i - 1];
        while (j > 0 && combined[i] !== combined[j]) {
            j = prefix[j - 1];
        }
        if (combined[i] === combined[j]) {
            j++;
        }
        prefix[i] = j;
    }
    
    let maxLength = 0;
    for (let i = s.length + 1; i < combined.length; i++) {
        maxLength = Math.max(maxLength, prefix[i]);
    }
    
    return maxLength * 2;
};
