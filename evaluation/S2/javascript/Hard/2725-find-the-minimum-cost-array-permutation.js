/**
 * @param {number[]} nums
 * @return {number[]}
 */
var findPermutation = function(nums) {
    const n = nums.length;
    const perm = new Array(n);
    const indices = new Array(n);
    for (let i = 0; i < n; i++) {
        indices[i] = i;
    }
    indices.sort((a, b) => nums[a] - nums[b]);
    for (let i = 0; i < n; i++) {
        perm[indices[i]] = i;
    }
    return perm;
};
