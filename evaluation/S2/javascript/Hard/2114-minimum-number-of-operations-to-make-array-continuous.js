/**
 * @param {number[]} nums
 * @return {number}
 */
var minOperations = function(nums) {
    nums.sort((a, b) => a - b);
    let left = 0, maxLength = 1;
    for (let right = 0; right < nums.length; right++) {
        while (nums[right] - nums[left] >= nums.length) {
            left++;
        }
        maxLength = Math.max(maxLength, right - left + 1);
    }
    return nums.length - maxLength;
};
