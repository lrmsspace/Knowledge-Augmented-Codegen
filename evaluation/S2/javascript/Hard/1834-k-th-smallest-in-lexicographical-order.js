/**
 * @param {number} n
 * @param {number} k
 * @return {number}
 */
var findKthNumber = function(n, k) {
    let prefix = 1;
    k -= 1; // We start counting from the first number which is 1
    
    while (k > 0) {
        const count = countPrefix(n, prefix);
        
        if (count <= k) {
            // Move to the next prefix
            prefix += 1;
            k -= count;
        } else {
            // Move down to the next level of prefixes
            prefix *= 10;
            k -= 1; // Count the prefix itself
        }
    }
    
    return prefix;
};
