/**
 * @param {number[][]} points
 * @param {number} k
 * @return {number}
 */
var findMaxValueOfEquation = function(points, k) {
    let maxValue = -Infinity;
    const deque = [];
    
    for (const [x, y] of points) {
        while (deque.length > 0 && x - deque[0][0] > k) {
            deque.shift();
        }
        if (deque.length > 0) {
            maxValue = Math.max(maxValue, y + x + deque[0][1] - deque[0][0]);
        }
        while (deque.length > 0 && y - x >= deque[deque.length - 1][1] - deque[deque.length - 1][0]) {
            deque.pop();
        }
        deque.push([x, y]);
    }
    
    return maxValue;
};
