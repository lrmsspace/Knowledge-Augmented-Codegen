/**
 * @param {number[]} nums
 * @return {boolean}
 */
var canTraverseAllPairs = function(nums) {
    const maxNum = Math.max(...nums);
    const primeFactors = Array.from({ length: maxNum + 1 }, () => []);
    for (let i = 2; i <= maxNum; i++) {
        if (primeFactors[i].length === 0) {
            for (let j = i; j <= maxNum; j += i) {
                primeFactors[j].push(i);
            }
        }
    }
    
    const graph = new Map();
    for (let i = 0; i < nums.length; i++) {
        for (const prime of primeFactors[nums[i]]) {
            if (!graph.has(prime)) {
                graph.set(prime, []);
            }
            graph.get(prime).push(i);
        }
    }
    
    const visited = new Set();
    function dfs(node) {
        visited.add(node);
        for (const prime of primeFactors[nums[node]]) {
            for (const neighbor of graph.get(prime)) {
                if (!visited.has(neighbor)) {
                    dfs(neighbor);
                }
            }
        }
    }
    dfs(0);
    return visited.size === nums.length;
};
