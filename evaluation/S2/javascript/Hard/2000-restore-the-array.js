/**
 * @param {string} s
 * @param {number} k
 * @return {number}
 */
var numberOfArrays = function(s, k) {
    const mod = 1e9 + 7;
    const n = s.length;
    const dp = new Array(n + 1).fill(0);
    dp[0] = 1; // Base case: there's one way to decode an empty string

    for (let i = 1; i <= n; i++) {
        for (let j = i - 1; j >= 0; j--) {
            const numStr = s.slice(j, i);
            if (numStr[0] === '0') continue; // Skip numbers with leading zeros
            const num = parseInt(numStr, 10);
            if (num > k) break; // No need to check further if the number exceeds k
            dp[i] = (dp[i] + dp[j]) % mod;
        }
    }

    return dp[n];
};
