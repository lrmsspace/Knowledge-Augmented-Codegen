    const n = edges1.length + 1;
    const m = edges2.length + 1;
    const tree1 = buildTree(n, edges1);
    const tree2 = buildTree(m, edges2);
    const diameter1 = findDiameter(tree1);
    const diameter2 = findDiameter(tree2);
