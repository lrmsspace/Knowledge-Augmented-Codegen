/**
 * @param {number[]} balls
 * @return {number}
 */
var getProbability = function(balls) {
    const totalBalls = balls.reduce((a, b) => a + b, 0);
    const halfBalls = totalBalls / 2;
    const k = balls.length;
    const factorial = [1];
    for (let i = 1; i <= totalBalls; i++) {
        factorial[i] = factorial[i - 1] * i;
    }

    const countWays = (distribution) => {
        let ways = 1;
        for (let i = 0; i < k; i++) {
            ways *= factorial[distribution[i]];
        }
        return ways;
    }
    
    let totalWays = 0;
    let validWays = 0;

    function backtrack(distribution, index, sum) {
        if (index === k) {
            if (sum === halfBalls) {
                totalWays += countWays(distribution);
                if (distribution.reduce((a, b) => a + (b > 0 ? 1 : 0), 0) === k / 2) {
                    validWays += countWays(distribution);
                }
            }
            return;
        }

        for (let i = 0; i <= balls[index]; i++) {
            distribution[index] = i;
            backtrack(distribution, index + 1, sum + i);
        }
    }

    backtrack(new Array(k).fill(0), 0, 0);

    return validWays / totalWays;
};
