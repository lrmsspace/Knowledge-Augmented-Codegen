/**
 * @param {number[]} pizzas
 * @return {number}
 */
var maxWeight = function(pizzas) {
    pizzas.sort((a, b) => b - a);
    let totalWeight = 0;
    for (let i = 0; i < pizzas.length; i += 2) {
        totalWeight += pizzas[i];
    }
    return totalWeight;
};
