/**
 * @param {string} s
 * @param {number} numRows
 * @return {string}
 */
var convert = function(s, numRows) {
    
    if (numRows === 1) return s;

    const rows = new Array(Math.min(numRows, s.length)).fill('');
    let currentRow = 0;
    let goingDown = false;
    
    for (let i = 0; i < s.length; i++) {
        rows[currentRow] += s[i];
        if (currentRow === 0) {
            goingDown = true;
        } else if (currentRow === numRows - 1) {
            goingDown = false;
        }
        currentRow += goingDown ? 1 : -1;
    }
    
    return rows.join('');
};
