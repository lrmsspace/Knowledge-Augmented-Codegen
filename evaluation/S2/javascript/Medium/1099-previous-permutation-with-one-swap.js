/**
 * @param {number[]} arr
 * @return {number[]}
 */
var prevPermOpt1 = function(arr) {
    let i = arr.length - 2;
    while (i >= 0 && arr[i] <= arr[i + 1]) i--;
    if (i < 0) return arr;
    let j = arr.length - 1;
    while (arr[j] >= arr[i]) j--;
    while (j > 0 && arr[j] === arr[j - 1]) j--;
    [arr[i], arr[j]] = [arr[j], arr[i]];
    return arr;
};
