/**
 * @param {number[]} skill
 * @return {number}
 */
var dividePlayers = function(skill) {
    skill.sort((a, b) => a - b);
    const n = skill.length;
    const teamSkill = skill[0] + skill[n - 1];
    let chemistrySum = 0;
    for (let i = 0; i < n / 2; i++) {
        if (skill[i] + skill[n - 1 - i] !== teamSkill) {
            return -1;
        }
        chemistrySum += skill[i] * skill[n - 1 - i];
    }
    return chemistrySum;
};
