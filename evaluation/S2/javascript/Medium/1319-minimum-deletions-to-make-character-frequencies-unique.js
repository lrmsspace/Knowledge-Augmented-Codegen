/**
 * @param {string} s
 * @return {number}
 */
var minDeletions = function(s) {
    const freq = new Array(26).fill(0);
    for (const char of s) {
        freq[char.charCodeAt(0) - 'a'.charCodeAt(0)]++;
    }
    const usedFreq = new Set();
    let deletions = 0;
    for (const f of freq) {
        let currentFreq = f;
        while (currentFreq > 0 && usedFreq.has(currentFreq)) {
            currentFreq--;
            deletions++;
        }
        if (currentFreq > 0) {
            usedFreq.add(currentFreq);
        }
    }
    return deletions;
};
