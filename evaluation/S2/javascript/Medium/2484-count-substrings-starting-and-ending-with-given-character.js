/**
 * @param {string} s
 * @param {character} c
 * @return {number}
 */
var countSubstrings = function(s, c) {
    let count = 0;
    let occurrences = 0;
    for (let i = 0; i < s.length; i++) {
        if (s[i] === c) {
            occurrences++;
        }
        count += occurrences;
    }
    return count;
};
