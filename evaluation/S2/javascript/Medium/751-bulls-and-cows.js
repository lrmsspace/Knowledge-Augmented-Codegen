/**
 * @param {string} secret
 * @param {string} guess
 * @return {string}
 */
var getHint = function(secret, guess) {
    let bulls = 0;
    const count = Array(10).fill(0);
    for (let i = 0; i < secret.length; i++) {
        const sDigit = parseInt(secret[i]);
        const gDigit = parseInt(guess[i]);
        if (sDigit === gDigit) {
            bulls++;
        } else {
            if (count[sDigit] < 0) bulls++;
            if (count[gDigit] > 0) bulls++;
            count[sDigit]++;
            count[gDigit]--;
        }
    }
    const cows = guess.length - bulls - count.reduce((sum, c) => sum + Math.abs(c), 0) / 2;
    return `${bulls}A${cows}B`;
};
