/**
 * @param {number} n
 * @return {number}
 */
var lastRemaining = function(n) {
    let head = 1, step = 1, remaining = n, leftToRight = true;
    while (remaining > 1) {
        if (leftToRight || remaining % 2 === 1) {
            head += step;
        }
        step *= 2;
        remaining = Math.floor(remaining / 2);
        leftToRight = !leftToRight;
    }
    return head;
};
