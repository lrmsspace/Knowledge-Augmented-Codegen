/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number}
 */
var maxNonOverlapping = function(nums, target) {
    const prefixSums = new Set();
    prefixSums.add(0);
    let currentSum = 0;
    let count = 0;
    for (const num of nums) {
        currentSum += num;
        if (prefixSums.has(currentSum - target)) {
            count++;
            prefixSums.clear();
            currentSum = 0;
        }
        prefixSums.add(currentSum);
    }
    return count;
};
