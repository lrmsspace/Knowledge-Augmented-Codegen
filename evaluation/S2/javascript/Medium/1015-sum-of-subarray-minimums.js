/**
 * @param {number[]} arr
 * @return {number}
 */
var sumSubarrayMins = function(arr) {
    const stack = [];
    let sum = 0;
    const MOD = 1e9 + 7;
    for (let i = 0; i < arr.length; i++) {
        while (stack.length && arr[stack[stack.length - 1]] > arr[i]) {
            const j = stack.pop();
            const k = stack.length ? stack[stack.length - 1] : -1;
            sum = (sum + arr[j] * (i - j) * (j - k)) % MOD;
        }
        stack.push(i);
    }
    while (stack.length) {
        const j = stack.pop();
        const k = stack.length ? stack[stack.length - 1] : -1;
        sum = (sum + arr[j] * (arr.length - j) * (j - k)) % MOD;
    }
    return sum;
};
