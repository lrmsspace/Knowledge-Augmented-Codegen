/**
 * @param {number[]} arr
 * @return {number}
 */
var maximumElementAfterDecrementingAndRearranging = function(arr) {
    const n = arr.length;
    const count = Array(n + 1).fill(0);
    for (const num of arr) {
        count[Math.min(num, n)]++;
    }
    let result = 0;
    for (let i = 1; i <= n; i++) {
        count[i] += count[i - 1];
        result = Math.min(i, count[i]);
    }
    return result;
};
