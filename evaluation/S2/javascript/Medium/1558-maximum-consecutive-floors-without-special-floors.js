/**
 * @param {number} bottom
 * @param {number} top
 * @param {number[]} special
 * @return {number}
 */
var maxConsecutive = function(bottom, top, special) {
    special.sort((a, b) => a - b);
    let maxConsecutive = 0;
    let prev = bottom - 1;
    for (const floor of special) {
        maxConsecutive = Math.max(maxConsecutive, floor - prev - 1);
        prev = floor;
    }
    maxConsecutive = Math.max(maxConsecutive, top - prev);
    return maxConsecutive;
};
