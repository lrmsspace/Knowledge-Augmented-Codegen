/**
 * @param {string} s
 * @return {number}
 */
var minSwaps = function(s) {
    let balance = 0;
    let maxImbalance = 0;
    
    for (const char of s) {
        if (char === '[') {
            balance++;
        } else {
            balance--;
        }
        maxImbalance = Math.min(maxImbalance, balance);
    }
    
    return Math.ceil(-maxImbalance / 2);
};
