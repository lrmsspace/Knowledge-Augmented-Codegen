/**
 * @param {string} s
 * @return {string}
 */
var resultingString = function(s) {
    const stack = [];
    for (const char of s) {
        if (stack.length > 0) {
            const top = stack[stack.length - 1];
            if ((top === 'a' && char === 'b') || (top === 'b' && char === 'a') || (top === 'z' && char === 'a') || (top === 'a' && char === 'z')) {
                stack.pop();
                continue;
            }
        }
        stack.push(char);
    }
    return stack.join('');
};
