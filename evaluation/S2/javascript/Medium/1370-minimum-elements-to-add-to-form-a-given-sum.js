/**
 * @param {number[]} nums
 * @param {number} limit
 * @param {number} goal
 * @return {number}
 */
var minElements = function(nums, limit, goal) {
    const currentSum = nums.reduce((acc, num) => acc + num, 0);
    const diff = Math.abs(goal - currentSum);
    return Math.ceil(diff / limit);
};
