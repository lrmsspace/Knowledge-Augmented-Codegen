/**
 * @param {number[]} nums
 * @param {number} goal
 * @return {number}
 */
var numSubarraysWithSum = function(nums, goal) {
    const prefixSums = new Map();
    prefixSums.set(0, 1);
    let currentSum = 0;
    let count = 0;
    for (const num of nums) {
        currentSum += num;
        count += prefixSums.get(currentSum - goal) || 0;
        prefixSums.set(currentSum, (prefixSums.get(currentSum) || 0) + 1);
    }
    return count;
};
