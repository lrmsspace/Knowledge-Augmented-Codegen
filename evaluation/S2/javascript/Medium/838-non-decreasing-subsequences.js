/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var findSubsequences = function(nums) {
    const result = [];
    const backtrack = (start, path) => {
        if (path.length > 1) {
            result.push([...path]);
        }
        const used = new Set();
        for (let i = start; i < nums.length; i++) {
            if (used.has(nums[i])) continue;
            used.add(nums[i]);
            if (path.length === 0 || nums[i] >= path[path.length - 1]) {
                path.push(nums[i]);
                backtrack(i + 1, path);
                path.pop();
            }
        }
    };
    backtrack(0, []);
    return result;
};