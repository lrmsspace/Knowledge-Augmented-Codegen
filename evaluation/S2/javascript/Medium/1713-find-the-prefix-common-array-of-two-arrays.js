/**
 * @param {number[]} A
 * @param {number[]} B
 * @return {number[]}
 */
var findThePrefixCommonArray = function(A, B) {
    const n = A.length;
    const commonCount = new Array(n).fill(0);
    const seenInA = new Set();
    const seenInB = new Set();
    for (let i = 0; i < n; i++) {
        seenInA.add(A[i]);
        seenInB.add(B[i]);
        let count = 0;
        for (const num of seenInA) {
            if (seenInB.has(num)) {
                count++;
            }
        }
        commonCount[i] = count;
    }
    return commonCount;
};
