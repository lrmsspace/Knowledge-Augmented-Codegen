/**
 * @param {string} s
 * @param {number} x
 * @param {number} y
 * @return {number}
 */
var maximumGain = function(s, x, y) {
    const first = x >= y ? "ab" : "ba";
    const second = x >= y ? "ba" : "ab";
    const firstScore = Math.max(x, y);
    const secondScore = Math.min(x, y);
    
    let score = 0;
    let stack = [];
    
    for (const char of s) {
        if (stack.length > 0 && stack[stack.length - 1] + char === first) {
            stack.pop();
            score += firstScore;
        } else {
            stack.push(char);
        }
    }
    
    let newStack = [];
    for (const char of stack) {
        if (newStack.length > 0 && newStack[newStack.length - 1] + char === second) {
            newStack.pop();
            score += secondScore;
        } else {
            newStack.push(char);
        }
    }
    
    return score;
};
