/**
 * @param {string} s
 * @param {number[][]} queries
 * @return {boolean[]}
 */
var canMakePaliQueries = function(s, queries) {
    const prefix = new Array(s.length + 1).fill(0);
    for (let i = 0; i < s.length; i++) {
        prefix[i + 1] = prefix[i] ^ (1 << (s.charCodeAt(i) - 97));
    }
    const result = [];
    for (const query of queries) {
        const [left, right, k] = query;
        const xor = prefix[right + 1] ^ prefix[left];
        let oddCount = 0;
        for (let i = 0; i < 26; i++) {
            if ((xor & (1 << i)) !== 0) {
                oddCount++;
            }
        }
        result.push(oddCount <= 2 * k + 1);
    }
    return result;
};
