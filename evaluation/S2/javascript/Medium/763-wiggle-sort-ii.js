/**
 * @param {number[]} nums
 * @return {void} Do not return anything, modify nums in-place instead.
 */
var wiggleSort = function(nums) {
    const n = nums.length;
    const mid = Math.floor((n - 1) / 2);
    const sorted = [...nums].sort((a, b) => a - b);
    
    let j = mid, k = n - 1;
    for (let i = 0; i < n; i++) {
        nums[i] = (i % 2 === 0) ? sorted[j--] : sorted[k--];
    }

    
};
