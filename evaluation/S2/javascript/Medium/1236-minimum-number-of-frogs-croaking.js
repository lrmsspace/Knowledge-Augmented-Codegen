/**
 * @param {string} croakOfFrogs
 * @return {number}
 */
var minNumberOfFrogs = function(croakOfFrogs) {
    const count = new Array(5).fill(0);
    let maxFrogs = 0;
    for (const char of croakOfFrogs) {
        let index;
        switch (char) {
            case 'c':
                index = 0;
                break;
            case 'r':
                index = 1;
                break;
            case 'o':
                index = 2;
                break;
            case 'a':
                index = 3;
                break;
            case 'k':
                index = 4;
                break;
            default:
                return -1; // Invalid character
        }
        count[index]++;
        if (index > 0 && count[index] > count[index - 1]) {
            return -1; // Invalid sequence
        }
        maxFrogs = Math.max(maxFrogs, count[0] - count[4]);
    }
    return count[0] === count[1] && count[1] === count[2] && count[2] === count[3] && count[3] === count[4]
        ? maxFrogs
        : -1; // Unmatched croaks
};
