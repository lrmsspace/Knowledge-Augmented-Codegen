/**
 * @param {number} n
 * @param {number[][]} edges
 * @param {string} labels
 * @return {number[]}
 */
var countSubTrees = function(n, edges, labels) {
    const graph = Array.from({ length: n }, () => []);
    for (const [u, v] of edges) {
        graph[u].push(v);
        graph[v].push(u);
    }
    
    const result = new Array(n).fill(0);
    const visited = new Array(n).fill(false);
    
    function dfs(node) {
        visited[node] = true;
        const labelCount = new Map();
        labelCount.set(labels[node], 1);
        
        for (const neighbor of graph[node]) {
            if (!visited[neighbor]) {
                const childLabelCount = dfs(neighbor);
                for (const [label, count] of childLabelCount) {
                    labelCount.set(label, (labelCount.get(label) || 0) + count);
                }
            }
        }
        
        result[node] = labelCount.get(labels[node]);
        return labelCount;
    }
    
    dfs(0);
    return result;
};
