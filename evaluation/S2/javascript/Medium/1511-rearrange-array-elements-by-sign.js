/**
 * @param {number[]} nums
 * @return {number[]}
 */
var rearrangeArray = function(nums) {
    const positives = [];
    const negatives = [];
    for (const num of nums) {
        if (num > 0) {
            positives.push(num);
        } else {
            negatives.push(num);
        }
    }
    const result = [];
    let i = 0, j = 0;
    while (i < positives.length && j < negatives.length) {
        result.push(positives[i++]);
        result.push(negatives[j++]);
    }
    return result;
};
