/**
 * @param {number[]} nums
 * @return {number}
 */
var longestArithSeqLength = function(nums) {
    const n = nums.length;
    const dp = new Array(n).fill(0).map(() => new Map());
    let maxLength = 2;
    for (let i = 1; i < n; i++) {
        for (let j = 0; j < i; j++) {
            const diff = nums[i] - nums[j];
            const length = (dp[j].get(diff) || 1) + 1;
            dp[i].set(diff, length);
            maxLength = Math.max(maxLength, length);
        }
    }
    return maxLength;
};
