/**
 * @param {number[]} nums
 * @return {number}
 */
var zeroFilledSubarray = function(nums) {
    let count = 0;
    let currentZeroCount = 0;
    for (const num of nums) {
        if (num === 0) {
            currentZeroCount++;
            count += currentZeroCount;
        } else {
            currentZeroCount = 0;
        }
    }
    return count;
};
