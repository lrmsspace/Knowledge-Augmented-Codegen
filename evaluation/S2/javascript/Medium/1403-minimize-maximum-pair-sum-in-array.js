/**
 * @param {number[]} nums
 * @return {number}
 */
var minPairSum = function(nums) {
    nums.sort((a, b) => a - b);
    const n = nums.length;
    let maxPairSum = 0;
    for (let i = 0; i < n / 2; i++) {
        const pairSum = nums[i] + nums[n - 1 - i];
        maxPairSum = Math.max(maxPairSum, pairSum);
    }
    return maxPairSum;
};
