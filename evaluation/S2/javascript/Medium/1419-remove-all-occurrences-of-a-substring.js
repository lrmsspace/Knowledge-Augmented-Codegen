/**
 * @param {string} s
 * @param {string} part
 * @return {string}
 */
var removeOccurrences = function(s, part) {
    while (true) {
        const index = s.indexOf(part);
        if (index === -1) {
            break;
        }
        s = s.slice(0, index) + s.slice(index + part.length);
    }
    return s;
};
