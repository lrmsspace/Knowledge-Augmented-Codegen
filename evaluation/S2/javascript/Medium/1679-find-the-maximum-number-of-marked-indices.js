/**
 * @param {number[]} nums
 * @return {number}
 */
var maxNumOfMarkedIndices = function(nums) {
    nums.sort((a, b) => a - b);
    let left = 0;
    let right = Math.floor(nums.length / 2);
    let markedCount = 0;
    
    while (left < nums.length / 2 && right < nums.length) {
        if (2 * nums[left] <= nums[right]) {
            markedCount += 2; // Mark both indices
            left++;
            right++;
        } else {
            right++; // Move right pointer to find a valid pair
        }
    }
    
    return markedCount;
};
