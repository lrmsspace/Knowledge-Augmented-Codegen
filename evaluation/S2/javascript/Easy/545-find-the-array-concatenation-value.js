/**
 * @param {number[]} nums
 * @return {number}
 */
var findTheArrayConcVal = function(nums) {
    let concatenationValue = 0;
    let left = 0;
    let right = nums.length - 1;
    
    while (left < right) {
        concatenationValue += parseInt(nums[left].toString() + nums[right].toString());
        left++;
        right--;
    }
    
    // If there's an odd number of elements, add the middle element
    if (left === right) {
        concatenationValue += nums[left];
    }
    
    return concatenationValue;
};
