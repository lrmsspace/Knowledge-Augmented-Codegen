/**

 * @param {number[]} nums

 * @return {number}

 */

var maxProductDifference = function(nums) {

    // Sort the array to easily find the two largest and two smallest numbers
    nums.sort((a, b) => a - b);
    
    // The maximum product difference is (largest1 * largest2) - (smallest1 * smallest2)
    const n = nums.length;
    const maxProduct = nums[n - 1] * nums[n - 2];
    const minProduct = nums[0] * nums[1];
    
    return maxProduct - minProduct;

};
