/**
 * @param {number[]} nums
 * @return {number}
 */
var minimumRightShifts = function(nums) {
    const n = nums.length;
    let shifts = 0;
    
    while (shifts < n) {
        if (isSorted(nums)) {
            return shifts;
        }
        rightShift(nums);
        shifts++;
    }
    
    return -1;
};
