/**
 * @param {string} s1
 * @param {string} s2
 * @param {string} s3
 * @return {number}
 */
var findMinimumOperations = function(s1, s2, s3) {
    let i = s1.length - 1;
    let j = s2.length - 1;
    let k = s3.length - 1;
    let operations = 0;
    
    while (i >= 0 && j >= 0 && k >= 0 && s1[i] === s2[j] && s2[j] === s3[k]) {
        i--;
        j--;
        k--;
    }
    
    operations += (i + 1) + (j + 1) + (k + 1);
    
    return operations;
};
