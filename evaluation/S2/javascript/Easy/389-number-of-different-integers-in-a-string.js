/**
 * @param {string} word
 * @return {number}
 */
var numDifferentIntegers = function(word) {
    const uniqueIntegers = new Set();
    let currentNumber = '';
    for (let i = 0; i < word.length; i++) {
        if (word[i] >= '0' && word[i] <= '9') {
            currentNumber += word[i];
        } else {
            if (currentNumber !== '') {
                // Remove leading zeros
                currentNumber = currentNumber.replace(/^0+/, '') || '0';
                uniqueIntegers.add(currentNumber);
            }
            currentNumber = '';
        }
    }
    // Handle the last number if the string doesn't end with a non-digit character
    if (currentNumber !== '') {
        currentNumber = currentNumber.replace(/^0+/, '') || '0';
        uniqueIntegers.add(currentNumber);
    }
    return uniqueIntegers.size;
};
