/**
 * @param {number} n
 * @return {number}
 */
var smallestNumber = function(n) {
    let x = 1;
    
    while (x < n) {
        x = (x << 1) | 1; // Shift left and set the least significant bit to create numbers like 1, 3, 7, 15, etc.
    }
    
    return x;
};
