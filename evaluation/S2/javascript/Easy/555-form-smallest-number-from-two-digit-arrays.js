/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number}
 */
var minNumber = function(nums1, nums2) {
    const set1 = new Set(nums1);
    const set2 = new Set(nums2);
    
    // Find common elements
    const common = [];
    for (const num of set1) {
        if (set2.has(num)) {
            common.push(num);
        }
    }
    
    // If there are common elements, return the smallest one
    if (common.length > 0) {
        return Math.min(...common);
    }
    
    // If no common elements, form the smallest number by combining the smallest from each array
    const min1 = Math.min(...nums1);
    const min2 = Math.min(...nums2);
    
    return Math.min(min1 * 10 + min2, min2 * 10 + min1);
};
