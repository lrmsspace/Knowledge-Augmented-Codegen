/**
 * @param {number[]} nums
 * @return {number}
 */
var sumOfEncryptedInt = function(nums) {
    let totalSum = 0;
    
    for (const num of nums) {
        const digits = String(num).split('');
        const maxDigit = Math.max(...digits);
        totalSum += parseInt(maxDigit);
    }
    
    return totalSum;
};
