/**
 * @param {number[]} nums
 * @return {number}
 */
var missingInteger = function(nums) {
    let longestSeqSum = 0;
    for (let i = 0; i < nums.length; i++) {
        if (i === 0 || nums[i] === nums[i - 1] + 1) {
            longestSeqSum += nums[i];
        } else {
            break;
        }
    }
    let missing = longestSeqSum;
    while (nums.includes(missing)) {
        missing++;
    }
    return missing;
};
