/**
 * @param {number[]} arr
 * @return {number[]}
 */
var sortByBits = function(arr) {
    return arr.sort((a, b) => {
        const countA = countBits(a);
        const countB = countBits(b);
        
        if (countA === countB) {
            return a - b;
        }
        return countA - countB;
    });
};
