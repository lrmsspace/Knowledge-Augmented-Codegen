/**
 * @param {number[]} arr
 * @return {number}
 */
var trimMean = function(arr) {
    const n = arr.length;
    const k = n / 20;
    arr.sort((a, b) => a - b);
    let sum = 0;
    for (let i = k; i < n - k; i++) {
        sum += arr[i];
    }
    return sum / (n - 2 * k);
};
