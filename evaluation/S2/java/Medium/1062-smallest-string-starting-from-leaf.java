/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public String smallestFromLeaf(TreeNode root) {
        if (root == null) {
            return "";
        }
        String left = smallestFromLeaf(root.left);
        String right = smallestFromLeaf(root.right);
        if (left.isEmpty()) {
            return (char) ('a' + root.val) + right;
        }
        if (right.isEmpty()) {
            return (char) ('a' + root.val) + left;
        }
        return (char) ('a' + root.val) + (left.compareTo(right) < 0 ? left : right);            
    }
}
