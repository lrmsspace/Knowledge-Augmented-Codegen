class Solution {
    public int assignEdgeWeights(int[][] edges) {
        // Step 1: Build the adjacency list for the tree
        int n = edges.length + 1;
        List<List<Integer>> graph = new ArrayList<>();
        for (int i = 0; i < n; i++) {
            graph.add(new ArrayList<>());
        }
        for (int[] edge : edges) {
            graph.get(edge[0] - 1).add(edge[1] - 1);
            graph.get(edge[1] - 1).add(edge[0] - 1);
        }

        // Step 2: Perform DFS to find the maximum depth and count nodes at that depth
        int[] maxDepth = new int[1];
        int[] countAtMaxDepth = new int[1];
        dfs(graph, 0, -1, 0, maxDepth, countAtMaxDepth);

        // Step 3: Calculate the number of ways to assign weights
        // If the maximum depth is even, we can only assign weights in a way that results in an odd cost if we have an odd number of edges.
        // If the maximum depth is odd, we can assign weights in a way that results in an odd cost if we have an even number of edges.
        int totalEdgesInPath = maxDepth[0];
        if (totalEdgesInPath % 2 == 0) {
            return (int) ((Math.pow(2, countAtMaxDepth[0]) - Math.pow(2, countAtMaxDepth[0] - 1)) % (1_000_000_007));
        } else {
            return (int) ((Math.pow(2, countAtMaxDepth[0]) - Math.pow(2, countAtMaxDepth[0] - 1)) % (1_000_000_007));
        }           
    }
}
