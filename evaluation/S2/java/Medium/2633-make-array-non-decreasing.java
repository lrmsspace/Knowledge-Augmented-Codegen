class Solution {
    public int maximumPossibleSize(int[] nums) {
        int n = nums.length;
        int[] dp = new int[n];
        int size = 0;

        for (int num : nums) {
            int k = binarySearch(dp, size, num);
            dp[k] = num;
            if (k == size) {
                size++;
            }
        }

        return size;            
    }
}
