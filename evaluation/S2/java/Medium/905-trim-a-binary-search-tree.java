/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public TreeNode trimBST(TreeNode root, int low, int high) {
        if (root == null) {
            return null;
        }

        // If the current node's value is less than low, trim the left subtree
        if (root.val < low) {
            return trimBST(root.right, low, high);
        }

        // If the current node's value is greater than high, trim the right subtree
        if (root.val > high) {
            return trimBST(root.left, low, high);
        }

        // If the current node's value is within the range [low, high], trim both subtrees
        root.left = trimBST(root.left, low, high);
        root.right = trimBST(root.right, low, high);

        return root;            
    }
}
