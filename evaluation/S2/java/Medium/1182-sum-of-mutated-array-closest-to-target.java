class Solution {
    public int findBestValue(int[] arr, int target) {
        int low = 0, high = 0;
        for (int num : arr) {
            high = Math.max(high, num);
        }

        while (low < high) {
            int mid = low + (high - low) / 2;
            if (getSum(arr, mid) < target) {
                low = mid + 1;
            } else {
                high = mid;
            }
        }

        int sumLow = getSum(arr, low);
        int sumLowMinusOne = getSum(arr, low - 1);
        if (Math.abs(sumLowMinusOne - target) <= Math.abs(sumLow - target)) {
            return low - 1;
        }
        return low;         
    }
}
