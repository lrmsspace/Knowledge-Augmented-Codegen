class Solution {
    public boolean checkIfCanBreak(String s1, String s2) {
        char[] arr1 = s1.toCharArray();
        char[] arr2 = s2.toCharArray();
        Arrays.sort(arr1);
        Arrays.sort(arr2);

        boolean canBreakS1 = true;
        boolean canBreakS2 = true;

        for (int i = 0; i < arr1.length; i++) {
            if (arr1[i] < arr2[i]) {
                canBreakS1 = false;
            }
            if (arr1[i] > arr2[i]) {
                canBreakS2 = false;
            }
            if (!canBreakS1 && !canBreakS2) {
                return false;
            }
        }

        return true;            
    }
}
