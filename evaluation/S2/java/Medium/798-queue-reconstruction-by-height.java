class Solution {
    public int[][] reconstructQueue(int[][] people) {
        // Sort people by height in descending order, and by k in ascending order for the same height
        Arrays.sort(people, (a, b) -> {
            if (a[0] != b[0]) {
                return b[0] - a[0]; // Sort by height in descending order
            } else {
                return a[1] - b[1]; // Sort by k in ascending order
            }
        });

        List<int[]> queue = new LinkedList<>();
        for (int[] person : people) {
            queue.add(person[1], person); // Insert person at index k
        }

        return queue.toArray(new int[queue.size()][]);          
    }
}
