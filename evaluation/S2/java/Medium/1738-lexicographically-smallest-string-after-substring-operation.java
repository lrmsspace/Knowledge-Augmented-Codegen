class Solution {
    public String smallestString(String s) {
        char[] chars = s.toCharArray();
        int i = 0;
        while (i < chars.length && chars[i] == 'a') {
            i++;
        }
        if (i == chars.length) {
            chars[chars.length - 1] = 'z';
        } else {
            while (i < chars.length && chars[i] != 'a') {
                chars[i] = (char) (chars[i] - 1);
                i++;
            }
        }
        return new String(chars);           
    }
}
