class Solution {
    public int countSubmatrices(int[][] grid, int k) {
        int m = grid.length, n = grid[0].length;
        int count = 0;

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (grid[i][j] > k) continue;

                int sum = 0;
                for (int x = i; x < m; x++) {
                    for (int y = j; y < n; y++) {
                        sum += grid[x][y];
                        if (sum > k) break;
                        count++;
                    }
                }
            }
        }

        return count;           
    }
}
