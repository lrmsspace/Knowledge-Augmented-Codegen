class Solution {
    public int minimumOperations(int[][] grid) {
        int m = grid.length, n = grid[0].length;
        int[][] heights = new int[m][n];
        for (int j = 0; j < n; j++) {
            for (int i = 0; i < m; i++) {
                if (grid[i][j] == 0) {
                    heights[i][j] = (i > 0 ? heights[i - 1][j] : 0) + 1;
                }
            }
        }

        int maxArea = 0;
        for (int i = 0; i < m; i++) {
            maxArea = Math.max(maxArea, largestRectangleArea(heights[i]));
        }

        return m * n - maxArea;         
    }
}
