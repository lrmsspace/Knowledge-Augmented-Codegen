class Solution {
    public int removeCoveredIntervals(int[][] intervals) {
        // Sort intervals by start point ascending, and if tie, by end point descending
        Arrays.sort(intervals, (a, b) -> {
            if (a[0] != b[0]) {
                return Integer.compare(a[0], b[0]);
            } else {
                return Integer.compare(b[1], a[1]);
            }
        });

        int count = 0; // Count of non-covered intervals
        int maxEnd = 0; // Track the maximum end point of intervals seen so far

        for (int[] interval : intervals) {
            // If the current interval's end point is greater than maxEnd,
            // it means this interval is not covered by any previous interval
            if (interval[1] > maxEnd) {
                count++; // Increment count of non-covered intervals
                maxEnd = interval[1]; // Update maxEnd to the current interval's end point
            }
        }

        return count; // Return the number of remaining intervals           
    }
}
