class Solution {
    public int maxOperations(String s) {
        int count = 0;
        int onesCount = 0;

        for (char c : s.toCharArray()) {
            if (c == '1') {
                onesCount++;
            } else if (onesCount > 0) {
                count++;
                onesCount--;
            }
        }

        return count;           
    }
}
