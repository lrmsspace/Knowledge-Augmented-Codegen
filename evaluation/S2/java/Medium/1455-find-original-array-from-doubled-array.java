class Solution {
    public int[] findOriginalArray(int[] changed) {
        int n = changed.length;
        if (n % 2 != 0) {
            return new int[0];
        }

        int maxVal = 0;
        for (int num : changed) {
            maxVal = Math.max(maxVal, num);
        }

        int[] count = new int[maxVal + 1];
        for (int num : changed) {
            count[num]++;
        }

        int[] original = new int[n / 2];
        int index = 0;

        for (int i = 0; i <= maxVal; i++) {
            while (count[i] > 0) {
                if (i * 2 > maxVal || count[i * 2] == 0) {
                    return new int[0];
                }
                original[index++] = i;
                count[i]--;
                count[i * 2]--;
            }
        }

        return original;            
    }
}
