class Solution {
    public long maxScore(int[] nums) {
        long gcd = 0, lcm = 1;
        for (int num : nums) {
            gcd = gcd(gcd, num);
            lcm = lcm(lcm, num);
        }
        long maxScore = gcd * lcm;

        for (int num : nums) {
            long newGcd = gcd(num, gcd);
            long newLcm = lcm(num, lcm);
            maxScore = Math.max(maxScore, newGcd * newLcm);
        }

        return maxScore;            
    }
}
