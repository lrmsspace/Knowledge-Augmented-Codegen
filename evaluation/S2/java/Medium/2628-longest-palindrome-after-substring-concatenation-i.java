class Solution {
    public int longestPalindrome(String s, String t) {
        int maxLength = 0;
        int n = s.length();
        int m = t.length();

        // Check all substrings of s and t
        for (int i = 0; i <= n; i++) {
            for (int j = 0; j <= m; j++) {
                String combined = s.substring(0, i) + t.substring(0, j);
                if (isPalindrome(combined)) {
                    maxLength = Math.max(maxLength, combined.length());
                }
            }
        }

        return maxLength;           
    }
}
