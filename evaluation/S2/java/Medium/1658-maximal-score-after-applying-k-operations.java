class Solution {
    public long maxKelements(int[] nums, int k) {
        PriorityQueue<Integer> pq = new PriorityQueue<>(Collections.reverseOrder());
        for (int num : nums) {
            pq.offer(num);
        }

        long score = 0;
        for (int i = 0; i < k; i++) {
            int maxElement = pq.poll();
            score += maxElement;
            pq.offer((maxElement + 2) / 3); // Equivalent to ceil(maxElement / 3)
        }

        return score;           
    }
}
