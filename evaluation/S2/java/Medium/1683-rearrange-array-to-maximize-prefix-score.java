class Solution {
    public int maxScore(int[] nums) {
        // Sort the array in non-increasing order to maximize the prefix sums.
        Arrays.sort(nums);
        int score = 0;
        int prefixSum = 0;

        // Iterate through the sorted array and calculate the prefix sums.
        for (int i = nums.length - 1; i >= 0; i--) {
            prefixSum += nums[i];
            if (prefixSum > 0) {
                score++;
            } else {
                break; // No need to continue if the prefix sum is not positive.
            }
        }

        return score;           
    }
}
