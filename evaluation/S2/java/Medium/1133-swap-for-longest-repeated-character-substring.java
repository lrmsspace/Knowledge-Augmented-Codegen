class Solution {
    public int maxRepOpt1(String text) {
        int[] count = new int[26];
        for (char c : text.toCharArray()) {
            count[c - 'a']++;
        }

        int maxLen = 1;
        int left = 0;
        for (int right = 0; right < text.length(); right++) {
            if (text.charAt(right) != text.charAt(left)) {
                if (right - left > maxLen) {
                    maxLen = right - left;
                }
                if (count[text.charAt(left) - 'a'] > right - left) {
                    maxLen++;
                }
                left = right;
            }
        }

        if (text.length() - left > maxLen) {
            maxLen = text.length() - left;
        }
        if (count[text.charAt(left) - 'a'] > text.length() - left) {
            maxLen++;
        }

        return maxLen;          
    }
}
