class Solution {
    public int longestAwesome(String s) {
        int n = s.length();
        int ans = 1;
        int mask = 0;
        Map<Integer, Integer> map = new HashMap<>();
        map.put(0, -1);
        for (int i = 0; i < n; i++) {
            mask ^= 1 << (s.charAt(i) - '0');
            if (map.containsKey(mask)) {
                ans = Math.max(ans, i - map.get(mask));
            } else {
                map.put(mask, i);
            }
            for (int j = 0; j < 10; j++) {
                int mask2 = mask ^ (1 << j);
                if (map.containsKey(mask2)) {
                    ans = Math.max(ans, i - map.get(mask2));
                }
            }
        }
        return ans;         
    }
}
