class Solution {
    public int numOfWays(int n) {
        int mod = 1_000_000_007;
        long a = 6, b = 6; // a: 3-color patterns, b: 2-color patterns
        for (int i = 2; i <= n; i++) {
            long newA = (2 * a + 2 * b) % mod;
            long newB = (2 * a + 3 * b) % mod;
            a = newA;
            b = newB;
        }
        return (int)((a + b) % mod);            
    }
}
