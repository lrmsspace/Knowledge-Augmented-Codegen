class Solution {
    public int numOfWays(int[] nums) {
        int mod = (int)1e9 + 7;
        int n = nums.length;
        long[][] C = new long[n + 1][n + 1];
        for (int i = 0; i <= n; i++) {
            C[i][0] = C[i][i] = 1;
            for (int j = 1; j < i; j++) {
                C[i][j] = (C[i - 1][j - 1] + C[i - 1][j]) % mod;
            }
        }

        return (int)(dfs(nums, C, mod) - 1);            
    }
}
