class Solution {
    public int sumCounts(int[] nums) {
        int n = nums.length;
        int mod = (int) 1e9 + 7;

        // Step 1: Calculate the number of subarrays with at most k distinct elements
        long[] atMost = new long[n + 1];
        for (int k = 1; k <= n; k++) {
            atMost[k] = countAtMostK(nums, k, mod);
        }

        // Step 2: Calculate the sum of squares of distinct counts
        long result = 0;
        for (int k = 1; k <= n; k++) {
            long countK = (atMost[k] - atMost[k - 1] + mod) % mod;
            result = (result + countK * k % mod * k % mod) % mod;
        }

        return (int) result;            
    }
}
