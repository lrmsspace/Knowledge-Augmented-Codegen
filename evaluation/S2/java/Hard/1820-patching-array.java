class Solution {
    public int minPatches(int[] nums, int n) {
        int patches = 0;
        long coverage = 0; // Use long to avoid overflow
        int i = 0;

        while (coverage < n) {
            if (i < nums.length && nums[i] <= coverage + 1) {
                coverage += nums[i];
                i++;
            } else {
                coverage += coverage + 1; // Patch with the next number that extends coverage
                patches++;
            }
        }

        return patches;         
    }
}
