class Solution {
    public int[] leftmostBuildingQueries(int[] heights, int[][] queries) {
        int n = heights.length;
        int m = queries.length;
        int[] ans = new int[m];

        // Step 1: Build the graph based on the given conditions
        List<Integer>[] graph = new List[n];
        for (int i = 0; i < n; i++) {
            graph[i] = new ArrayList<>();
            for (int j = i + 1; j < n; j++) {
                if (heights[i] < heights[j]) {
                    graph[i].add(j);
                }
            }
        }

        // Step 2: For each query, perform a BFS to find the leftmost building where Alice and Bob can meet
        for (int i = 0; i < m; i++) {
            int ai = queries[i][0];
            int bi = queries[i][1];
            ans[i] = bfs(graph, ai, bi);
        }

        return ans;         
    }
}
