class Solution {
    public int countPartitions(int[] nums, int k) {
        int mod = 1_000_000_007;
        long sum = 0;
        for (int num : nums) {
            sum += num;
        }
        if (sum < 2L * k) {
            return 0;
        }
        int n = nums.length;
        long totalPartitions = modExp(2, n, mod);
        long invalidPartitions = countInvalidPartitions(nums, k, mod);
        return (int) ((totalPartitions - invalidPartitions + mod) % mod);           
    }
}
