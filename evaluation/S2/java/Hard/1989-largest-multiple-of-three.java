class Solution {
    public String largestMultipleOfThree(int[] digits) {
        int[] count = new int[10];
        int sum = 0;
        for (int digit : digits) {
            count[digit]++;
            sum += digit;
        }

        if (sum == 0) {
            return "0";
        }

        int remainder = sum % 3;
        if (remainder != 0) {
            if (!removeDigits(count, remainder, 1)) {
                removeDigits(count, 3 - remainder, 2);
            }
        }

        StringBuilder result = new StringBuilder();
        for (int i = 9; i >= 0; i--) {
            while (count[i] > 0) {
                result.append(i);
                count[i]--;
            }
        }

        return result.toString();           
    }
}
