class Solution {
    public int numberOfCombinations(String num) {
        int n = num.length();
        int mod = 1_000_000_007;

        // dp[i] will store the number of ways to split the substring num[0...i-1]
        int[] dp = new int[n + 1];
        dp[0] = 1; // Base case: there's one way to split an empty string

        for (int i = 1; i <= n; i++) {
            for (int j = 0; j < i; j++) {
                // Check if the substring num[j...i-1] is a valid number
                if (num.charAt(j) != '0' && compare(num, j, i - 1, num, 0, j - 1) >= 0) {
                    dp[i] = (dp[i] + dp[j]) % mod;
                }
            }
        }

        return dp[n];           
    }
}
