class Solution {
    public long countPairs(int[] nums, int k) {
        long count = 0;
        int[] freq = new int[k];

        for (int num : nums) {
            int gcd = gcd(num, k);
            int complement = k / gcd;
            count += freq[complement];
            freq[gcd]++;
        }

        return count;           
    }
}
