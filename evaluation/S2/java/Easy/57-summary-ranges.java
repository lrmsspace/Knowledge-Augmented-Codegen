class Solution {
    public List<String> summaryRanges(int[] nums) {
        List<String> result = new ArrayList<>();
        if (nums.length == 0) {
            return result;
        }

        int start = nums[0];
        for (int i = 1; i < nums.length; i++) {
            if (nums[i] != nums[i - 1] + 1) {
                addRange(result, start, nums[i - 1]);
                start = nums[i];
            }
        }
        addRange(result, start, nums[nums.length - 1]);
        return result;          
    }
}
