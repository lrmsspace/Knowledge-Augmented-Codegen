class Solution {
    public int numberOfPoints(List<List<Integer>> nums) {
        // Step 1: Create a boolean array to track covered points
        boolean[] covered = new boolean[101]; // Since points range from 1 to 100

        // Step 2: Mark the covered points for each car
        for (List<Integer> car : nums) {
            int start = car.get(0);
            int end = car.get(1);
            for (int i = start; i <= end; i++) {
                covered[i] = true; // Mark this point as covered
            }
        }

        // Step 3: Count the number of covered points
        int count = 0;
        for (boolean isCovered : covered) {
            if (isCovered) {
                count++;
            }
        }

        return count; // Return the total count of covered points           
    }
}
