class Solution {
    public int[] fairCandySwap(int[] aliceSizes, int[] bobSizes) {
        int sumAlice = 0, sumBob = 0;
        for (int x : aliceSizes) sumAlice += x;
        for (int x : bobSizes) sumBob += x;

        int delta = (sumAlice - sumBob) / 2; // The amount Alice needs to give to Bob

        // Use a HashSet for Bob's candy sizes for O(1) lookups
        Set<Integer> bobSet = new HashSet<>();
        for (int x : bobSizes) bobSet.add(x);

        // Find the pair of candies to swap
        for (int x : aliceSizes) {
            if (bobSet.contains(x - delta)) {
                return new int[]{x, x - delta};
            }
        }

        // This line should never be reached since the problem guarantees an answer exists
        return new int[0];          
    }
}
