/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean checkTree(TreeNode root) {
        if (root == null) {
            return false; // If the root is null, we cannot satisfy the condition.
        }
        int leftVal = (root.left != null) ? root.left.val : 0; // Get left child value or 0 if it doesn't exist.
        int rightVal = (root.right != null) ? root.right.val : 0; // Get right child value or 0 if it doesn't exist.
        return root.val == leftVal + rightVal; // Check if root value equals the sum of its children.           
    }
}
