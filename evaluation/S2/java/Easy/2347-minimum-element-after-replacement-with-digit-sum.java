class Solution {
    public int minElement(int[] nums) {
        int min = Integer.MAX_VALUE;
        for (int num : nums) {
            int sumDigits = sumOfDigits(num);
            min = Math.min(min, sumDigits);
        }
        return min;         
    }
}
