class Solution {
    public String mostCommonWord(String paragraph, String[] banned) {
        // Step 1: Normalize the paragraph by converting to lowercase and replacing punctuation with spaces
        String normalized = paragraph.toLowerCase().replaceAll("[!?',;\\.]", " ");
        
        // Step 2: Split the normalized paragraph into words
        String[] words = normalized.split("\\s+");
        
        // Step 3: Create a set of banned words for O(1) lookups
        Set<String> bannedSet = new HashSet<>(Arrays.asList(banned));
        
        // Step 4: Count the frequency of each non-banned word
        Map<String, Integer> wordCount = new HashMap<>();
        for (String word : words) {
            if (!bannedSet.contains(word)) {
                wordCount.put(word, wordCount.getOrDefault(word, 0) + 1);
            }
        }
        
        // Step 5: Find the most common non-banned word
        String mostCommon = "";
        int maxCount = 0;
        for (Map.Entry<String, Integer> entry : wordCount.entrySet()) {
            if (entry.getValue() > maxCount) {
                mostCommon = entry.getKey();
                maxCount = entry.getValue();
            }
        }
        
        return mostCommon;          
    }
}
