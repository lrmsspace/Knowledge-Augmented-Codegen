class Solution {
    public int addDigits(int num) {
        // The digital root can be calculated using the formula:
        // digital_root(n) = 1 + (n - 1) % 9 for n > 0
        // For n = 0, the digital root is 0.
        if (num == 0) {
            return 0;
        }
        return 1 + (num - 1) % 9;           
    }
}
