class Solution {
    public int[] answerQueries(int[] nums, int[] queries) {
        int n = nums.length, m = queries.length;
        int[] answer = new int[m];
        Arrays.sort(nums);
        for (int i = 1; i < n; i++) {
            nums[i] += nums[i - 1];
        }
        for (int i = 0; i < m; i++) {
            int q = queries[i];
            int idx = Arrays.binarySearch(nums, q);
            if (idx < 0) {
                idx = -(idx + 1);
            } else {
                idx++;
            }
            answer[i] = idx;
        }
        return answer;          
    }
}
