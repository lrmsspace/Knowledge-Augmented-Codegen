class Solution {
    public int countVowelSubstrings(String word) {
        int count = 0;
        for (int i = 0; i < word.length(); i++) {
            Set<Character> vowels = new HashSet<>();
            for (int j = i; j < word.length(); j++) {
                char c = word.charAt(j);
                if (!isVowel(c)) {
                    break;
                }
                vowels.add(c);
                if (vowels.size() == 5) {
                    count++;
                }
            }
        }
        return count;           
    }
}
