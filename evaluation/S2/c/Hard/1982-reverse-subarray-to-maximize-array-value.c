int maxValueAfterReverse(int* nums, int numsSize) {
    int i, sum = 0, max1 = 0, max2 = 0, min1 = 0, min2 = 0;
    for (i = 0; i < numsSize - 1; ++i) {
        int a = nums[i], b = nums[i + 1];
        sum += abs(a - b);
        if (a > b) {
            max1 = fmax(max1, b - a);
            min1 = fmin(min1, a - b);
        } else {
            max2 = fmax(max2, a - b);
            min2 = fmin(min2, b - a);
        }
    }
    return sum + fmax(max1 - min1, max2 - min2);        
}
