int minLengthAfterRemovals(int* nums, int numsSize) {
    int left = 0, right = numsSize - 1;

    while (left < right) {
        if (nums[left] < nums[right]) {
            left++;
            right--;
        } else {
            right--;
        }
    }

    return numsSize - 2 * left;     
}
