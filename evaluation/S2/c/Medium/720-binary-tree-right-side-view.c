/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */
/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* rightSideView(struct TreeNode* root, int* returnSize) {
    if (!root) {
        *returnSize = 0;
        return NULL;
    }

    int* result = (int*)malloc(100 * sizeof(int)); // Max nodes in the tree is 100
    int size = 0;

    struct TreeNode* queue[100]; // Queue for BFS
    int front = 0, rear = 0;

    queue[rear++] = root; // Enqueue root

    while (front < rear) {
        int levelSize = rear - front; // Number of nodes at current level
        result[size++] = queue[front]->val; // Add rightmost node of current level to result

        for (int i = 0; i < levelSize; i++) {
            struct TreeNode* node = queue[front++]; // Dequeue

            // Enqueue left child first, then right child
            if (node->left) {
                queue[rear++] = node->left;
            }
            if (node->right) {
                queue[rear++] = node->right;
            }
        }
    }

    *returnSize = size;
    return result;      
}
