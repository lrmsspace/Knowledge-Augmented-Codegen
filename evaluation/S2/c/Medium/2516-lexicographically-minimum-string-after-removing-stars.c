char* clearStars(char* s) {
    int n = 0;
    for (int i = 0; s[i]; i++) {
        if (s[i] == '*') {
            n--;
        } else {
            s[n++] = s[i];
        }
    }
    s[n] = '\0';
    return s;       
}
