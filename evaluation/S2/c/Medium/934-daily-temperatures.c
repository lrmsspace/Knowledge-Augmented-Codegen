/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* dailyTemperatures(int* temperatures, int temperaturesSize, int* returnSize) {
    int* answer = (int*)malloc(temperaturesSize * sizeof(int));
    int* stack = (int*)malloc(temperaturesSize * sizeof(int)); // Stack to hold indices
    int top = -1; // Stack pointer

    for (int i = temperaturesSize - 1; i >= 0; i--) {
        while (top >= 0 && temperatures[i] >= temperatures[stack[top]]) {
            top--; // Pop from stack until we find a warmer temperature
        }
        answer[i] = (top == -1) ? 0 : (stack[top] - i); // Calculate days to wait
        stack[++top] = i; // Push current index onto stack
    }

    free(stack); // Free the stack memory
    *returnSize = temperaturesSize; // Set return size
    return answer; // Return the answer array       
}
