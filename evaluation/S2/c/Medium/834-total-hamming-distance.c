int totalHammingDistance(int* nums, int numsSize) {
    int total = 0;

    // Iterate over each bit position (0 to 31)
    for (int i = 0; i < 32; i++) {
        int countOnes = 0;
        int countZeros = 0;

        // Count how many numbers have the current bit set and how many don't
        for (int j = 0; j < numsSize; j++) {
            if (nums[j] & (1 << i)) {
                countOnes++;
            } else {
                countZeros++;
            }
        }

        // Each pair of one and zero contributes to the Hamming distance
        total += countOnes * countZeros;
    }

    return total;       
}
