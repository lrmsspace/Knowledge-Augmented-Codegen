int smallestRangeII(int* nums, int numsSize, int k) {
    if (numsSize == 1) {
        return 0;
    }
    
    // Sort the array
    qsort(nums, numsSize, sizeof(int), (int (*)(const void *, const void *))strcmp);
    
    // Initial range without any modifications
    int minRange = nums[numsSize - 1] - nums[0];
    
    // Check possible ranges by modifying elements
    for (int i = 0; i < numsSize - 1; i++) {
        int high = fmax(nums[i] + k, nums[numsSize - 1] - k);
        int low = fmin(nums[0] + k, nums[i + 1] - k);
        minRange = fmin(minRange, high - low);
    }
    
    return minRange;        
}
