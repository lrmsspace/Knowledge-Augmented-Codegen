int subarraysDivByK(int* nums, int numsSize, int k) {
    int count = 0;
    int prefixSum = 0;
    int modCount[k]; // Array to count occurrences of each mod value
    for (int i = 0; i < k; i++) {
        modCount[i] = 0; // Initialize counts to zero
    }
    modCount[0] = 1; // There's one way to have a sum of zero (empty subarray)

    for (int i = 0; i < numsSize; i++) {
        prefixSum += nums[i];
        int modValue = ((prefixSum % k) + k) % k; // Handle negative values
        count += modCount[modValue]; // Add the count of previous prefix sums with the same mod value
        modCount[modValue]++; // Increment the count for this mod value
    }

    return count;       
}
