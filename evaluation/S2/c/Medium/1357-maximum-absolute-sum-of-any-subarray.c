int maxAbsoluteSum(int* nums, int numsSize) {
    int max_sum = 0, min_sum = 0, current_sum = 0;

    for (int i = 0; i < numsSize; i++) {
        current_sum += nums[i];
        if (current_sum > max_sum) {
            max_sum = current_sum;
        }
        if (current_sum < min_sum) {
            min_sum = current_sum;
        }
    }

    return max_sum > -min_sum ? max_sum : -min_sum;     
}
