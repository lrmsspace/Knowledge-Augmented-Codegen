long long gridGame(int** grid, int gridSize, int* gridColSize) {
    int n = gridColSize[0];
    long long topSum = 0, bottomSum = 0;
    for (int i = 0; i < n; i++) {
        topSum += grid[0][i];
        bottomSum += grid[1][i];
    }

    long long maxPointsForSecondRobot = 0;
    for (int i = 0; i < n; i++) {
        // Points collected by the second robot if the first robot goes through column i
        long long pointsForSecondRobot = (topSum - grid[0][i]) + (bottomSum - grid[1][i]);
        if (pointsForSecondRobot > maxPointsForSecondRobot) {
            maxPointsForSecondRobot = pointsForSecondRobot;
        }
    }

    return maxPointsForSecondRobot;     
}
