int numOfSubarrays(int* arr, int arrSize, int k, int threshold) {
    int count = 0;
    int sum = 0;
    int target = k * threshold;

    // Calculate the sum of the first 'k' elements
    for (int i = 0; i < k; i++) {
        sum += arr[i];
    }

    // Check if the average of the first 'k' elements meets the threshold
    if (sum >= target) {
        count++;
    }

    // Use a sliding window to calculate the sum of subsequent sub-arrays of size 'k'
    for (int i = k; i < arrSize; i++) {
        sum += arr[i] - arr[i - k]; // Add the new element and remove the old element

        // Check if the average of the current sub-array meets the threshold
        if (sum >= target) {
            count++;
        }
    }

    return count;       
}
