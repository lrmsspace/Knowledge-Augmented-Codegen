int numberOfGoodSubarraySplits(int* nums, int numsSize) {
    int mod = 1e9 + 7;
    long long result = 1; // Start with 1 to account for the first split
    int countZeroes = 0; // Count of zeroes between ones
    int foundOne = 0; // Flag to indicate if we've found the first one

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] == 1) {
            if (foundOne) {
                // For each subsequent one, multiply the result by the number of zeroes + 1
                result = (result * (countZeroes + 1)) % mod;
            }
            foundOne = 1; // Mark that we've found at least one '1'
            countZeroes = 0; // Reset zero count after finding a '1'
        } else if (foundOne) {
            countZeroes++; // Count zeroes only after finding the first '1'
        }
    }

    return foundOne ? result : 0; // If no '1' was found, return 0      
}
