bool canReach(int* arr, int arrSize, int start) {
    if (arr[start] == 0) {
        return true;
    }

    int jump = arr[start];
    arr[start] = -1; // mark as visited

    if (start + jump < arrSize && canReach(arr, arrSize, start + jump)) {
        return true;
    }
    if (start - jump >= 0 && canReach(arr, arrSize, start - jump)) {
        return true;
    }

    return false;       
}
