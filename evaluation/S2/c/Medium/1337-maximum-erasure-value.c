int maximumUniqueSubarray(int* nums, int numsSize) {
    int max_sum = 0, current_sum = 0;
    int left = 0, right = 0;
    int seen[10001] = {0}; // To track the count of each number in the current window

    while (right < numsSize) {
        if (seen[nums[right]] == 0) {
            // If the number is not seen, add it to the current sum and mark it as seen
            current_sum += nums[right];
            seen[nums[right]]++;
            right++;
        } else {
            // If the number is already seen, remove numbers from the left until we can include the current number
            while (seen[nums[right]] > 0) {
                current_sum -= nums[left];
                seen[nums[left]]--;
                left++;
            }
        }
        // Update the maximum sum found so far
        if (current_sum > max_sum) {
            max_sum = current_sum;
        }
    }

    return max_sum;     
}
