int minScoreTriangulation(int* values, int valuesSize) {
    int dp[50][50];
    for (int i = 0; i < valuesSize; i++) {
        dp[i][i] = 0;
    }
    for (int len = 2; len < valuesSize; len++) {
        for (int l = 0; l + len < valuesSize; l++) {
            int r = l + len;
            dp[l][r] = 0x3f3f3f3f;
            for (int k = l + 1; k < r; k++) {
                int score = dp[l][k] + dp[k][r] + values[l] * values[k] * values[r];
                if (score < dp[l][r]) {
                    dp[l][r] = score;
                }
            }
        }
    }
    return dp[0][valuesSize - 1];       
}
