bool sumOfNumberAndReverse(int num) {
    for (int i = 0; i <= num; ++i) {
        int sum = i, rev = 0;
        while (i > 0) {
            rev = rev * 10 + i % 10;
            i /= 10;
        }
        sum += rev;
        if (sum == num) {
            return true;
        }
    }
    return false;       
}
