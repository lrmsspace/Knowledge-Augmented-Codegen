int maxSumDivThree(int* nums, int numsSize) {
    int sum = 0, min1 = 10000, min2 = 10000;
    for (int i = 0; i < numsSize; ++i) {
        sum += nums[i];
        if (nums[i] % 3 == 1) {
            if (nums[i] < min1) {
                min2 = min1;
                min1 = nums[i];
            } else if (nums[i] < min2) {
                min2 = nums[i];
            }
        } else if (nums[i] % 3 == 2) {
            if (nums[i] < min1) {
                min2 = min1;
                min1 = nums[i];
            } else if (nums[i] < min2) {
                min2 = nums[i];
            }
        }
    }
    if (sum % 3 == 0) {
        return sum;
    } else if (sum % 3 == 1) {
        return sum - (min1 < min2 * 2 ? min1 : min2 * 2);
    } else {
        return sum - (min1 < min2 * 2 ? min1 : min2 * 2);
    }       
}
