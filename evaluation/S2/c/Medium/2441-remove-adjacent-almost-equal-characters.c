int removeAlmostEqualCharacters(char* word) {
    int count[26] = {0};
    for (int i = 0; word[i] != '\0'; i++) {
        count[word[i] - 'a']++;
    }

    int operations = 0;
    for (int i = 0; i < 26; i++) {
        if (count[i] > 1) {
            operations += count[i] - 1;
        }
    }
    return operations;      
}
