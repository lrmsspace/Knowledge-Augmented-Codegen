int distributeCookies(int* cookies, int cookiesSize, int k) {
    int unfairness = 0;
    for (int i = 0; i < cookiesSize; i++) {
        unfairness += cookies[i];
    }
    int distribution[k];
    for (int i = 0; i < k; i++) {
        distribution[i] = 0;
    }
    backtrack(cookies, cookiesSize, k, distribution, 0, &unfairness);
    return unfairness;      
}
