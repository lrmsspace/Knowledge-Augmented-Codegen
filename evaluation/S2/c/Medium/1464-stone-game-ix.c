bool stoneGameIX(int* stones, int stonesSize) {
    int count[3] = {0};  // Count of stones based on their value modulo 3

    // Count the number of stones in each modulo category
    for (int i = 0; i < stonesSize; i++) {
        count[stones[i] % 3]++;
    }

    // If there are no stones that are divisible by 3, Alice can always win by taking one from either category
    if (count[0] % 2 == 0) {
        return abs(count[1] - count[2]) <= 1;
    } else {
        return abs(count[1] - count[2]) <= 0;
    }       
}
