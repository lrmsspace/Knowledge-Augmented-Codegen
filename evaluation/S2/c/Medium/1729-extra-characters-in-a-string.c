int minExtraChar(char* s, char** dictionary, int dictionarySize) {
    int n = strlen(s);
    int dp[n + 1];
    memset(dp, 0, sizeof(dp));
    
    for (int i = 1; i <= n; i++) {
        dp[i] = dp[i - 1] + 1; // Assume the current character is extra
        for (int j = 0; j < dictionarySize; j++) {
            int dictLen = strlen(dictionary[j]);
            if (dictLen <= i && strncmp(s + i - dictLen, dictionary[j], dictLen) == 0) {
                dp[i] = fmin(dp[i], dp[i - dictLen]); // Update dp[i] if we can use a word from the dictionary
            }
        }
    }
    
    return dp[n];       
}
