char* kthLargestNumber(char** nums, int numsSize, int k) {
    // Sort the array of strings based on their integer values
    for (int i = 0; i < numsSize - 1; i++) {
        for (int j = 0; j < numsSize - i - 1; j++) {
            // Compare the two strings as integers
            if (strcmp(nums[j], nums[j + 1]) < 0) {
                // Swap the strings
                char* temp = nums[j];
                nums[j] = nums[j + 1];
                nums[j + 1] = temp;
            }
        }
    }
    
    // Return the k-1 indexed element as the kth largest
    return nums[k - 1];     
}
