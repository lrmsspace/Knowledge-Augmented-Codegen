char* removeDuplicates(char* s, int k) {
    int n = 0;
    char* stack = (char*)malloc(sizeof(char) * (strlen(s) + 1));
    int* count = (int*)malloc(sizeof(int) * (strlen(s) + 1));
    for (int i = 0; s[i]; ++i) {
        stack[n] = s[i];
        count[n] = (n > 0 && stack[n - 1] == stack[n]) ? count[n - 1] + 1 : 1;
        if (count[n] == k) {
            n -= k;
        } else {
            n++;
        }
    }
    stack[n] = '\0';
    free(count);
    return stack;       
}
