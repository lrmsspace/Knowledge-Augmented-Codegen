int findMinFibonacciNumbers(int k) {
    int fib[45] = {0, 1, 1}; // Precompute Fibonacci numbers up to F(44) which is > 10^9
    for (int i = 3; i < 45; i++) {
        fib[i] = fib[i - 1] + fib[i - 2];
    }

    int count = 0;
    for (int i = 44; i >= 2; i--) { // Start from the largest Fibonacci number
        while (k >= fib[i]) {
            k -= fib[i]; // Subtract the Fibonacci number from k
            count++; // Increment the count of Fibonacci numbers used
        }
    }
    
    return count;       
}
