long long countCompleteDayPairs(int* hours, int hoursSize) {
    long long count[24];
    for (int i = 0; i < 24; ++i) {
        count[i] = 0;
    }
    
    for (int i = 0; i < hoursSize; ++i) {
        count[hours[i] % 24]++;
    }
    
    long long result = 0;
    
    // Count pairs where both elements are from the same remainder group
    for (int i = 0; i < 24; ++i) {
        if (count[i] > 1) {
            result += (count[i] * (count[i] - 1)) / 2; // C(count[i], 2)
        }
    }
    
    return result;      
}
