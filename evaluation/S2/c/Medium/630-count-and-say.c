char* countAndSay(int n) {
    if (n <= 0) return NULL;

    char* result = (char*)malloc(2 * sizeof(char)); // Start with "1"
    strcpy(result, "1");

    for (int i = 1; i < n; i++) {
        int len = strlen(result);
        char* temp = (char*)malloc(2 * len * sizeof(char)); // Allocate enough space
        int index = 0;

        for (int j = 0; j < len; j++) {
            int count = 1;
            while (j + 1 < len && result[j] == result[j + 1]) {
                count++;
                j++;
            }
            // Append count and character to temp
            index += sprintf(temp + index, "%d%c", count, result[j]);
        }

        free(result); // Free the old result
        result = temp; // Move to the new result
    }

    return result;      
}
