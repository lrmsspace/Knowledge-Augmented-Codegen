int findTheWinner(int n, int k) {
    int winner = 0; // Base case: J(1, k) = 0 (0-indexed)
    for (int i = 2; i <= n; i++) {
        winner = (winner + k) % i; // Recursive relation: J(n, k) = (J(n-1, k) + k) % n
    }
    return winner + 1; // Convert to 1-indexed      
}
