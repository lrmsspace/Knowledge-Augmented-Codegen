long long maxSum(int** grid, int gridSize, int* gridColSize, int* limits, int limitsSize, int k) {
    int n = gridSize, m = gridColSize[0];
    long long dp[k + 1];
    for (int i = 0; i <= k; ++i) {
        dp[i] = 0;
    }

    for (int i = 0; i < n; ++i) {
        // Sort the current row in descending order
        int* row = grid[i];
        for (int j = 0; j < limits[i]; ++j) {
            for (int l = j + 1; l < limits[i]; ++l) {
                if (row[j] < row[l]) {
                    int temp = row[j];
                    row[j] = row[l];
                    row[l] = temp;
                }
            }
        }
        // Sum up the first k elements of the sorted row
        for (int j = 0; j < k && j < limits[i]; ++j) {
            dp[k] += row[j];
        }
    
    }
    
    return dp[k];
}
