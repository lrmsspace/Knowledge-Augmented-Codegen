int numSquares(int n) {
    // Dynamic programming array to store the least number of perfect squares for each number up to n
    int dp[n + 1];
    
    // Initialize the dp array with a large value (greater than any possible number of perfect squares)
    for (int i = 0; i <= n; i++) {
        dp[i] = i; // The worst case is i (1^2 + 1^2 + ... + 1^2)
    }
    
    // Fill the dp array using the bottom-up approach
    for (int i = 1; i * i <= n; i++) {
        int square = i * i;
        for (int j = square; j <= n; j++) {
            if (dp[j - square] + 1 < dp[j]) {
                dp[j] = dp[j - square] + 1;
            }
        }
    }
    
    return dp[n];       
}
