int minimumSubstringsInPartition(char* s) {
    int freq[26] = {0};
    int count = 0;

    for (int i = 0; s[i] != '\0'; i++) {
        int index = s[i] - 'a';
        freq[index]++;
        if (freq[index] == 1) {
            count++;
        }
    }

    return count;       
}
