int distributeCandies(int* candyType, int candyTypeSize) {
    int types[200001] = {0}; // Array to count occurrences of each candy type
    int uniqueTypes = 0; // Counter for unique candy types

    // Count the occurrences of each candy type
    for (int i = 0; i < candyTypeSize; i++) {
        int typeIndex = candyType[i] + 100000; // Shift index to handle negative values
        if (types[typeIndex] == 0) {
            uniqueTypes++; // Increment unique type count when we encounter a new type
        }
        types[typeIndex]++;
    }

    // The maximum number of different types Alice can eat is the minimum of unique types and half of the total candies
    return uniqueTypes < (candyTypeSize / 2) ? uniqueTypes : (candyTypeSize / 2);       
}
