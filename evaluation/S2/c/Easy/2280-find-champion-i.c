int findChampion(int** grid, int gridSize, int* gridColSize) {
    int champion = 0; // Start with the first team as the champion candidate

    // Iterate through each team to find the champion
    for (int i = 1; i < gridSize; i++) {
        // If the current champion is weaker than team i, update the champion
        if (grid[champion][i] == 0) {
            champion = i;
        }
    }

    return champion; // Return the identified champion team     
}
