char* makeGood(char* s) {
    int len = 0;
    char* stack = (char*)malloc((strlen(s) + 1) * sizeof(char));
    for (int i = 0; s[i] != '\0'; i++) {
        if (len > 0 && abs(stack[len - 1] - s[i]) == 32) {
            len--; // Pop the last character from the stack
        } else {
            stack[len++] = s[i]; // Push the current character onto the stack
        }
    }
    stack[len] = '\0'; // Null-terminate the resulting string
    return stack;       
}
