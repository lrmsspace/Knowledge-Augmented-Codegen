/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
char** findOcurrences(char* text, char* first, char* second, int* returnSize) {
    char** result = (char**)malloc(1000 * sizeof(char*)); // Allocate memory for up to 1000 results
    int count = 0; // Initialize count of results

    char* token = strtok(text, " "); // Tokenize the input text by spaces
    char* prev1 = NULL; // To store the previous word
    char* prev2 = NULL; // To store the word before the previous word

    while (token != NULL) {
        if (prev2 != NULL && strcmp(prev2, first) == 0 && strcmp(prev1, second) == 0) {
            result[count++] = strdup(token); // If the previous two words match first and second, add current token to result
        }
        prev2 = prev1; // Update prev2 to prev1
        prev1 = token; // Update prev1 to current token
        token = strtok(NULL, " "); // Get the next token
    }

    *returnSize = count; // Set the return size to the number of results found
    return result; // Return the array of results       
}
