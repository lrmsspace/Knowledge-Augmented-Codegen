int mostFrequentEven(int* nums, int numsSize) {
    int freq[100001] = {0}; // Frequency array for even numbers up to 100000
    int maxFreq = 0; // To track the maximum frequency of even numbers
    int result = -1; // To store the most frequent even element

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] % 2 == 0) { // Check if the number is even
            freq[nums[i]]++; // Increment the frequency of the even number
            if (freq[nums[i]] > maxFreq) { // If this number has a higher frequency than the current max
                maxFreq = freq[nums[i]]; // Update the maximum frequency
                result = nums[i]; // Update the result to this number
            } else if (freq[nums[i]] == maxFreq && nums[i] < result) { // If there's a tie in frequency, choose the smaller number
                result = nums[i];
            }
        }
    }

    return result; // Return the most frequent even element or -1 if none found     
}
