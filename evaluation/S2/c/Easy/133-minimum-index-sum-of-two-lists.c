char** findRestaurant(char** list1, int list1Size, char** list2, int list2Size, int* returnSize) {
    int minSum = list1Size + list2Size; // Maximum possible index sum
    char** result = (char**)malloc(sizeof(char*) * minSum); // Allocate memory for results
    int count = 0; // Count of common strings with minimum index sum

    for (int i = 0; i < list1Size; i++) {
        for (int j = 0; j < list2Size; j++) {
            if (strcmp(list1[i], list2[j]) == 0) { // Check for common string
                int indexSum = i + j;
                if (indexSum < minSum) {
                    minSum = indexSum; // Update minimum index sum
                    count = 0; // Reset count for new minimum
                    result[count++] = list1[i]; // Add to result
                } else if (indexSum == minSum) {
                    result[count++] = list1[i]; // Add to result if same minimum
                }
            }
        }
    }

    *returnSize = count; // Set the return size
    return result; // Return the array of common strings with minimum index sum     
}
