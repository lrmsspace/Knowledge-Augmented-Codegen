int unequalTriplets(int* nums, int numsSize) {
    int count[1001] = {0}; // Count occurrences of each number (1 to 1000)
    for (int i = 0; i < numsSize; i++) {
        count[nums[i]]++;
    }

    int result = 0;
    int prefixSum = 0; // Sum of counts of numbers processed so far

    for (int num = 1; num <= 1000; num++) {
        if (count[num] > 0) {
            // For the current number, we can choose one from its count,
            // and the other two from the remaining numbers.
            result += prefixSum * count[num] * (numsSize - prefixSum - count[num]);
            prefixSum += count[num]; // Update prefix sum with the current number's count
        }
    }

    return result;      
}
