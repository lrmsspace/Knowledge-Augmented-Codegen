int firstBadVersion(int n) {
    int low = 1, high = n;
    while (low < high) {
        int mid = low + (high - low) / 2; // Avoids potential overflow
        if (isBadVersion(mid)) {
            high = mid; // The first bad version is at mid or before
        } else {
            low = mid + 1; // The first bad version is after mid
        }
    }
    return low; // At the end of the loop, low == high and points to the first bad version      
}
