int countBalls(int lowLimit, int highLimit) {
    int boxCount[46] = {0}; // Max sum of digits for 10^5 is 45 (9*5)
    int maxBalls = 0;

    for (int ball = lowLimit; ball <= highLimit; ball++) {
        int sumDigits = 0, num = ball;
        while (num > 0) {
            sumDigits += num % 10;
            num /= 10;
        }
        boxCount[sumDigits]++;
        if (boxCount[sumDigits] > maxBalls) {
            maxBalls = boxCount[sumDigits];
        }
    }

    return maxBalls;        
}
