int maximumLengthSubstring(char* s) {
    int count[26] = {0}; // Count of each character
    int left = 0, maxLength = 0;

    for (int right = 0; s[right] != '\0'; right++) {
        count[s[right] - 'a']++; // Increment count for the current character

        // If any character occurs more than twice, move the left pointer
        while (count[s[right] - 'a'] > 2) {
            count[s[left] - 'a']--; // Decrement count for the left character
            left++; // Move the left pointer to the right
        }

        // Update maxLength if the current window is larger
        int currentLength = right - left + 1;
        if (currentLength > maxLength) {
            maxLength = currentLength;
        }
    }

    return maxLength;       
}
