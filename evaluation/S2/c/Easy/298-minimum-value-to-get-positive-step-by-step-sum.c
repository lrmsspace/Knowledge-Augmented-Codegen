int minStartValue(int* nums, int numsSize) {
    int min_prefix_sum_value = 0;
    int prefix_sum = 0;

    for (int i = 0; i < numsSize; i++) {
        prefix_sum += nums[i];
        if (prefix_sum < min_prefix_sum_value) {
            min_prefix_sum_value = prefix_sum;
        }
    }

    return 1 - min_prefix_sum_value;        
}
