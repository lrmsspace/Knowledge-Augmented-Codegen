int findPoisonedDuration(int* timeSeries, int timeSeriesSize, int duration) {
    if (timeSeriesSize == 0) {
        return 0;
    }

    int total_duration = 0;

    for (int i = 0; i < timeSeriesSize - 1; i++) {
        int attack_time = timeSeries[i];
        int next_attack_time = timeSeries[i + 1];
        total_duration += (next_attack_time - attack_time < duration) ? (next_attack_time - attack_time) : duration;
    }

    // Add the duration for the last attack
    total_duration += duration;

    return total_duration;      
}
