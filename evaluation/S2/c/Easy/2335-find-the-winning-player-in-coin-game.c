char* winningPlayer(int x, int y) {
    int totalValue = x * 75 + y * 10;
    
    // If the total value of coins is less than 115, Alice cannot make a move and loses immediately.
    if (totalValue < 115) {
        return "Bob";
    }
    
    // Calculate how many full turns (115 value) can be made with the available coins.
    int turns = totalValue / 115;
    
    // If the number of turns is odd, Alice wins; if even, Bob wins.
    return (turns % 2 == 1) ? "Alice" : "Bob";      
}
