class Solution(object):
    def numberOfWays(self, corridor):
        """
        :type corridor: str
        :rtype: int
        """
        MOD = 10**9 + 7
        seat_positions = [i for i, c in enumerate(corridor) if c == 'S']

        if len(seat_positions) % 2 != 0:
            return 0

        if not seat_positions:
            return 1

        ways = 1
        for i in range(2, len(seat_positions), 2):
            gap = seat_positions[i] - seat_positions[i - 1]
            ways = (ways * gap) % MOD

        return ways