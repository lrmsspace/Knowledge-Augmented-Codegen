class Solution(object):
    def stoneGameVII(self, stones):
        """
        :type stones: List[int]
        :rtype: int
        """
        n = len(stones)
        prefix_sum = [0] * (n + 1)
        for i in range(n):
            prefix_sum[i + 1] = prefix_sum[i] + stones[i]
        
        dp = [[0] * n for _ in range(n)]
        
        for length in range(2, n + 1):
            for left in range(n - length + 1):
                right = left + length - 1
                score_remove_left = prefix_sum[right + 1] - prefix_sum[left + 1] - dp[left + 1][right]
                score_remove_right = prefix_sum[right] - prefix_sum[left] - dp[left][right - 1]
                dp[left][right] = max(score_remove_left, score_remove_right)
        
        return dp[0][n - 1]