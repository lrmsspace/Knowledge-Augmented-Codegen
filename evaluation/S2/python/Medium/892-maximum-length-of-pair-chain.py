class Solution(object):
    def findLongestChain(self, pairs):
        """
        :type pairs: List[List[int]]
        :rtype: int
        """
        # Sort pairs based on the second element (righti)
        pairs.sort(key=lambda x: x[1])
        
        count = 0
        current_end = float('-inf')
        
        for pair in pairs:
            if pair[0] > current_end:
                count += 1
                current_end = pair[1]
        
        return count