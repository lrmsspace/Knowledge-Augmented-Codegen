class Solution(object):
    def maximumProduct(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: int
        """
        import heapq

        mod = 10**9 + 7
        min_heap = nums[:]
        heapq.heapify(min_heap)

        for _ in range(k):
            smallest = heapq.heappop(min_heap)
            smallest += 1
            heapq.heappush(min_heap, smallest)

        product = 1
        for num in min_heap:
            product = (product * num) % mod

        return product