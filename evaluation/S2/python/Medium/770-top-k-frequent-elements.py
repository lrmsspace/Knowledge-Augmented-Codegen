class Solution(object):
    def topKFrequent(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: List[int]
        """
        from collections import Counter
        import heapq

        # Step 1: Count the frequency of each element in nums
        count = Counter(nums)

        # Step 2: Use a min-heap to keep track of the top k elements
        heap = []
        for num, freq in count.items():
            heapq.heappush(heap, (freq, num))  # Push (frequency, number) to the heap
            if len(heap) > k:  # If the heap exceeds size k, remove the smallest element
                heapq.heappop(heap)

        # Step 3: Extract the numbers from the heap and return them
        return [num for freq, num in heap]