class Solution(object):
    def numberOfSets(self, n, k):
        """
        :type n: int
        :type k: int
        :rtype: int
        """
        mod = 10**9 + 7
        dp = [[0] * (k + 1) for _ in range(n + 1)]
        
        for i in range(2, n + 1):
            dp[i][0] = 1
        
        for i in range(2, n + 1):
            for j in range(1, k + 1):
                dp[i][j] = (dp[i - 1][j] + (i - 1) * dp[i - 2][j - 1]) % mod
        
        return dp[n][k]