class Solution(object):
    def maximumSubarraySum(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: int
        """
        from collections import defaultdict

        n = len(nums)
        prefix_sum = [0] * (n + 1)
        for i in range(n):
            prefix_sum[i + 1] = prefix_sum[i] + nums[i]

        max_sum = 0
        seen = defaultdict(list)

        for i in range(n + 1):
            if i > 0:
                seen[nums[i - 1]].append(i - 1)

            target1 = nums[i - 1] + k if i > 0 else None
            target2 = nums[i - 1] - k if i > 0 else None

            for target in [target1, target2]:
                if target is not None and target in seen:
                    for j in seen[target]:
                        if j < i:
                            current_sum = prefix_sum[i] - prefix_sum[j]
                            max_sum = max(max_sum, current_sum)

        return max_sum