class Solution(object):
    def largestTimeFromDigits(self, arr):
        """
        :type arr: List[int]
        :rtype: str
        """
        from itertools import permutations

        max_time = -1
        # Generate all permutations of the input digits
        for perm in permutations(arr):
            h1, h2, m1, m2 = perm
            hours = h1 * 10 + h2
            minutes = m1 * 10 + m2
            # Check if the generated time is valid
            if 0 <= hours < 24 and 0 <= minutes < 60:
                max_time = max(max_time, hours * 60 + minutes)

        if max_time == -1:
            return ""
        else:
            # Convert back to HH:MM format
            max_hours = max_time // 60
            max_minutes = max_time % 60
            return f"{max_hours:02d}:{max_minutes:02d}"