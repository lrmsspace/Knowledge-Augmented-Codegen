class Solution(object):
    def countDistinctIntegers(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        distinct = set(nums)
        for num in nums:
            rev = 0
            n = num
            while n > 0:
                rev = rev * 10 + n % 10
                n //= 10
            distinct.add(rev)
        return len(distinct)