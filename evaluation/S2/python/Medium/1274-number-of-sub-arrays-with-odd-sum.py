class Solution(object):
    def numOfSubarrays(self, arr):
        """
        :type arr: List[int]
        :rtype: int
        """
        MOD = 10**9 + 7
        count_odd, count_even = 0, 1  # Start with one even sum (the empty subarray)
        current_sum = 0
        result = 0

        for num in arr:
            current_sum += num
            if current_sum % 2 == 0:
                result = (result + count_odd) % MOD
                count_even += 1
            else:
                result = (result + count_even) % MOD
                count_odd += 1

        return result