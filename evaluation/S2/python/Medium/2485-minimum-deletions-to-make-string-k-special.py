class Solution(object):
    def minimumDeletions(self, word, k):
        """
        :type word: str
        :type k: int
        :rtype: int
        """
        from collections import Counter
        import heapq

        freq = Counter(word)
        max_heap = [-count for count in freq.values()]
        heapq.heapify(max_heap)

        deletions = 0
        while max_heap:
            largest = -heapq.heappop(max_heap)
            if not max_heap:
                break
            second_largest = -max_heap[0]

            if largest - second_largest > k:
                deletions += 1
                largest -= 1
                if largest > 0:
                    heapq.heappush(max_heap, -largest)
            else:
                break

        return deletions