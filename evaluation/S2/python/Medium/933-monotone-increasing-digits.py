class Solution(object):
    def monotoneIncreasingDigits(self, n):
        """
        :type n: int
        :rtype: int
        """
        digits = list(map(int, str(n)))
        i = len(digits) - 1

        while i > 0 and digits[i - 1] <= digits[i]:
            i -= 1

        if i == 0:
            return n

        while i > 0 and digits[i - 1] > digits[i]:
            digits[i - 1] -= 1
            i -= 1

        for j in range(i + 1, len(digits)):
            digits[j] = 9

        return int(''.join(map(str, digits)))