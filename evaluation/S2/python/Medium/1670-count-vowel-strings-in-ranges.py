class Solution(object):
    def vowelStrings(self, words, queries):
        """
        :type words: List[str]
        :type queries: List[List[int]]
        :rtype: List[int]
        """
        vowels = set('aeiou')
        prefix_count = [0] * (len(words) + 1)

        for i in range(len(words)):
            prefix_count[i + 1] = prefix_count[i] + (1 if words[i][0] in vowels and words[i][-1] in vowels else 0)

        result = []
        for l, r in queries:
            result.append(prefix_count[r + 1] - prefix_count[l])

        return result