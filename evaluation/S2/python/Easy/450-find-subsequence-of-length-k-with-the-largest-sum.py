class Solution(object):
    def maxSubsequence(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: List[int]
        """
        # Step 1: Identify the k largest elements and their indices
        indexed_nums = list(enumerate(nums))
        indexed_nums.sort(key=lambda x: x[1], reverse=True)  # Sort by value in descending order
        top_k = indexed_nums[:k]  # Get the top k elements
        top_k.sort(key=lambda x: x[0])  # Sort back by original index

        # Step 2: Extract the values of the top k elements in their original order
        result = [nums[i] for i, _ in top_k]
        
        return result