class Solution(object):
    def longestNiceSubstring(self, s):
        """
        :type s: str
        :rtype: str
        """
        def is_nice(sub):
            chars = set(sub)
            for c in chars:
                if c.islower() and c.upper() not in chars:
                    return False
                if c.isupper() and c.lower() not in chars:
                    return False
            return True

        longest = ""
        for i in range(len(s)):
            for j in range(i + 1, len(s) + 1):
                substring = s[i:j]
                if is_nice(substring) and len(substring) > len(longest):
                    longest = substring

        return longest