class Solution {
public:
    vector<int> canSeePersonsCount(vector<int>& heights) {
        int n = heights.size();
        vector<int> ans(n);
        stack<int> s; // Stack to store indices of people in the queue

        for (int i = n - 1; i >= 0; --i) {
            // Pop all people shorter than the current person
            while (!s.empty() && heights[i] > heights[s.top()]) {
                ans[i]++;
                s.pop();
            }
            // If there's still someone in the stack, they can see the current person
            if (!s.empty()) {
                ans[i]++;
            }
            // Push the current person's index onto the stack
            s.push(i);
        }

        return ans;         
    }
};
