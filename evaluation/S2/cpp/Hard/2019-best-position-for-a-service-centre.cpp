class Solution {
public:
    double getMinDistSum(vector<vector<int>>& positions) {
    	double ans = 1e20;
        for (int i = 0; i < positions.size(); i++) {
        	for (int j = 0; j < positions.size(); j++) {
        		double sum = 0;
        		for (int k = 0; k < positions.size(); k++) {
        			sum += sqrt(pow(positions[i][0] - positions[k][0], 2) + pow(positions[j][1] - positions[k][1], 2));
        		}
        		ans = min(ans, sum);
        	}
        }
        return ans;
    }
};
