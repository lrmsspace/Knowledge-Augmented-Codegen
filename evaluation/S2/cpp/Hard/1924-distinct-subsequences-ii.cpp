class Solution {
public:
    int distinctSubseqII(string s) {
        const int mod = 1e9 + 7;
        vector<int> last(26, -1);
        int n = s.size();
        vector<long long> dp(n + 1, 0);
        dp[0] = 1; // Base case: empty subsequence

        for (int i = 1; i <= n; ++i) {
            int charIndex = s[i - 1] - 'a';
            dp[i] = (2 * dp[i - 1]) % mod;

            if (last[charIndex] != -1) {
                dp[i] = (dp[i] - dp[last[charIndex]]) % mod;
            }

            last[charIndex] = i - 1; // Update the last occurrence of the character
        }

        return (dp[n] - 1 + mod) % mod; // Subtract the empty subsequence and ensure non-negative result            
    }
};
