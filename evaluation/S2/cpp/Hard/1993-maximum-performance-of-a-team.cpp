class Solution {
public:
    int maxPerformance(int n, vector<int>& speed, vector<int>& efficiency, int k) {
        vector<pair<int, int>> engineers(n);
        for (int i = 0; i < n; ++i) {
            engineers[i] = {efficiency[i], speed[i]};
        }
        sort(engineers.rbegin(), engineers.rend());

        long long max_perf = 0, speed_sum = 0;
        priority_queue<int, vector<int>, greater<int>> min_heap;

        for (const auto& [eff, spd] : engineers) {
            if (min_heap.size() == k) {
                speed_sum -= min_heap.top();
                min_heap.pop();
            }
            min_heap.push(spd);
            speed_sum += spd;
            max_perf = max(max_perf, speed_sum * eff);
        }

        return max_perf % 1000000007;           
    }
};
