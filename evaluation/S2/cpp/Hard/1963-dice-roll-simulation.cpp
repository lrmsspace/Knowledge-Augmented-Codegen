class Solution {
public:
    int dieSimulator(int n, vector<int>& rollMax) {
        const int M = 1e9 + 7;
        vector<vector<int>> dp(n + 1, vector<int>(6));
        for (int i = 0; i < 6; ++i) {
            dp[1][i] = 1;
        }
        for (int i = 2; i <= n; ++i) {
            for (int j = 0; j < 6; ++j) {
                for (int k = 1; k <= rollMax[j] && k < i; ++k) {
                    dp[i][j] = (dp[i][j] + dp[i - k][(j + 1) % 6]) % M;
                }
            }
        }
        int ans = 0;
        for (int i = 0; i < 6; ++i) {
            ans = (ans + dp[n][i]) % M;
        }
        return ans;         
    }
};
