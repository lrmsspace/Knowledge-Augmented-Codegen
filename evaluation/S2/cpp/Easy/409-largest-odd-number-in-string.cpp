class Solution {
public:
    string largestOddNumber(string num) {
        // Start from the end of the string and look for the first odd digit
        for (int i = num.size() - 1; i >= 0; --i) {
            if ((num[i] - '0') % 2 == 1) { // Check if the digit is odd
                return num.substr(0, i + 1); // Return the substring from the start to this index
            }
        }
        return ""; // If no odd digit is found, return an empty string          
    }
};
