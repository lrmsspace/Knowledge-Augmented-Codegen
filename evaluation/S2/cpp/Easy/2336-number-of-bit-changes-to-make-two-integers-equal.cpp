class Solution {
public:
    int minChanges(int n, int k) {
        if (n < k) {
            return -1;  // Impossible to make n equal to k if n is smaller
        }

        int changes = 0;
        while (n > 0) {
            if ((n & 1) == 1 && (k & 1) == 0) {
                changes++;  // Need to change this bit from 1 to 0
            }
            n >>= 1;  // Shift n right by 1 bit
            k >>= 1;  // Shift k right by 1 bit
        }

        return changes;         
    }
};
