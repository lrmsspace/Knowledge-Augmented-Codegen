class Solution {
public:
    void moveZeroes(vector<int>& nums) {
        int insert_pos = 0; // Position to insert the next non-zero element

        // First pass: Move all non-zero elements to the front
        for (int num : nums) {
            if (num != 0) {
                nums[insert_pos++] = num; // Place non-zero element at insert_pos and increment
            }
        }

        // Second pass: Fill the remaining positions with zeros
        while (insert_pos < nums.size()) {
            nums[insert_pos++] = 0; // Fill the rest of the array with zeros
        }           
    }
};
