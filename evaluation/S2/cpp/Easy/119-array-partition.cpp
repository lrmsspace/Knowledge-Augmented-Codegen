class Solution {
public:
    int arrayPairSum(vector<int>& nums) {
        // Sort the array to pair the smallest numbers together
        sort(nums.begin(), nums.end());
        
        int sum = 0;
        // Iterate through the sorted array, adding every second element (the smaller one in each pair)
        for (size_t i = 0; i < nums.size(); i += 2) {
            sum += nums[i];
        }
        
        return sum;         
    }
};
