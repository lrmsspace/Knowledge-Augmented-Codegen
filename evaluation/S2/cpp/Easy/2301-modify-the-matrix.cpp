class Solution {
public:
    vector<vector<int>> modifiedMatrix(vector<vector<int>>& matrix) {
        int m = matrix.size();
        int n = matrix[0].size();
        vector<int> maxInColumn(n, 0);

        // Find the maximum element in each column
        for (int j = 0; j < n; ++j) {
            for (int i = 0; i < m; ++i) {
                maxInColumn[j] = max(maxInColumn[j], matrix[i][j]);
            }
        }

        // Create the answer matrix and replace -1 with the maximum element in its column
        vector<vector<int>> answer = matrix;
        for (int i = 0; i < m; ++i) {
            for (int j = 0; j < n; ++j) {
                if (answer[i][j] == -1) {
                    answer[i][j] = maxInColumn[j];
                }
            }
        }

        return answer;          
    }
};
