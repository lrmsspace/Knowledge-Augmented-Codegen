class Solution {
public:
    vector<int> replaceElements(vector<int>& arr) {
        int n = arr.size();
        if (n == 0) return arr;

        vector<int> result(n);
        result[n - 1] = -1; // The last element is always -1

        for (int i = n - 2; i >= 0; --i) {
            result[i] = max(arr[i + 1], result[i + 1]);
        }

        return result;          
    }
};
