class Solution {
public:
    int sumOfUnique(vector<int>& nums) {
        unordered_map<int, int> count;
        for (int num : nums) {
            count[num]++;
        }

        int sum = 0;
        for (const auto& [num, freq] : count) {
            if (freq == 1) {
                sum += num;
            }
        }

        return sum;         
    }
};
