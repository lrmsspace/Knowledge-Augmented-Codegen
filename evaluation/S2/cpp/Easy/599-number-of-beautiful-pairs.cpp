class Solution {
public:
    int countBeautifulPairs(vector<int>& nums) {
        int count = 0;
        for (int i = 0; i < nums.size(); ++i) {
            int first_digit = getFirstDigit(nums[i]);
            for (int j = i + 1; j < nums.size(); ++j) {
                int last_digit = nums[j] % 10;
                if (gcd(first_digit, last_digit) == 1) {
                    ++count;
                }
            }
        }
        return count;           
    }
};
