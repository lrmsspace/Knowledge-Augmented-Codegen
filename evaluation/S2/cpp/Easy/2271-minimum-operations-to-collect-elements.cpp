class Solution {
public:
    int minOperations(vector<int>& nums, int k) {
        int n = nums.size();
        int count = 0;
        for (int i = n - 1; i >= 0; --i) {
            if (nums[i] <= k) {
                count++;
            } else {
                break;
            }
        }
        return k - count;           
    }
};
