class Solution {
public:
    int specialArray(vector<int>& nums) {
        int n = nums.size();
        vector<int> count(n + 1, 0);
        for (int num : nums) {
            if (num >= n) {
                count[n]++;
            } else {
                count[num]++;
            }
        }

        int cumulativeCount = 0;
        for (int x = n; x >= 0; --x) {
            cumulativeCount += count[x];
            if (cumulativeCount == x) {
                return x;
            }
        }

        return -1;          
    }
};
