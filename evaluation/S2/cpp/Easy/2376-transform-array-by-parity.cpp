class Solution {
public:
    vector<int> transformArray(vector<int>& nums) {
        vector<int> result(nums.size(), 0);
        for (size_t i = 0; i < nums.size(); ++i) {
            result[i] = nums[i] % 2;
        }
        sort(result.begin(), result.end());
        return result;          
    }
};
