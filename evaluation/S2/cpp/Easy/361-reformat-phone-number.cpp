class Solution {
public:
    string reformatNumber(string number) {
        string digits;
        for (char c : number) {
            if (isdigit(c)) {
                digits += c;
            }
        }

        string result;
        int n = digits.size();
        int i = 0;

        while (i < n) {
            if (n - i > 4) {
                result += digits.substr(i, 3) + '-';
                i += 3;
            } else if (n - i == 4) {
                result += digits.substr(i, 2) + '-' + digits.substr(i + 2, 2);
                break;
            } else {
                result += digits.substr(i);
                break;
            }
        }

        return result;          
    }
};
