class Solution {
public:
    int maximum69Number (int num) {
        string numStr = to_string(num);
        for (char& digit : numStr) {
            if (digit == '6') {
                digit = '9';
                break; // Change only the first occurrence of '6'
            }
        }
        return stoi(numStr);            
    }
};
