class Solution {
public:
    vector<int> getSumAbsoluteDifferences(vector<int>& nums) {
        int n = nums.size();
        vector<int> result(n, 0);
        vector<long long> prefixSum(n, 0);

        // Compute prefix sums
        prefixSum[0] = nums[0];
        for (int i = 1; i < n; ++i) {
            prefixSum[i] = prefixSum[i - 1] + nums[i];
        }

        for (int i = 0; i < n; ++i) {
            long long leftSum = (long long)nums[i] * i - (i > 0 ? prefixSum[i - 1] : 0);
            long long rightSum = (i < n - 1 ? prefixSum[n - 1] - prefixSum[i] : 0) - (long long)nums[i] * (n - 1 - i);
            result[i] = leftSum + rightSum;
        }

        return result;          
    }
};
