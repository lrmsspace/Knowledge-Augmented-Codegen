class Solution {
public:
    int leastBricks(vector<vector<int>>& wall) {
        unordered_map<int, int> edge_count;
        for (const auto& row : wall) {
            int cumulative_width = 0;
            for (size_t i = 0; i < row.size() - 1; ++i) { // Exclude the last brick to avoid counting the right edge
                cumulative_width += row[i];
                edge_count[cumulative_width]++;
            }
        }

        int max_edges_crossed = 0;
        for (const auto& [edge_position, count] : edge_count) {
            max_edges_crossed = max(max_edges_crossed, count);
        }

        return wall.size() - max_edges_crossed; // Total rows minus the maximum edges crossed           
    }
};
