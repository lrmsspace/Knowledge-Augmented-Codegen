class Solution {
public:
    int minSideJumps(vector<int>& obstacles) {
        int n = obstacles.size(), ans = 0, cur = 2;
        for (int i = 1; i < n; ++i) {
            if (obstacles[i] == cur) {
                ++ans;
                for (int j = 1; j <= 3; ++j) {
                    if (j != cur && obstacles[i] != j) {
                        cur = j;
                        break;
                    }
                }
            }
        }
        return ans;         
    }
};
