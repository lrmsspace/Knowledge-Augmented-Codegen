// The perimeter of a polygon is the sum of lengths of its sides.
//
// Return the largest possible perimeter of a polygon whose sides can be formed from nums, or -1 if it is not possible to create a polygon.
//
// Example:
// Input: nums = [5,5,5]
// Output: 15
