class Solution {
public:
    vector<int> sortArray(vector<int>& nums) {
        // Implementing Merge Sort
        if (nums.size() <= 1) return nums;

        vector<int> temp(nums.size());
        mergeSort(nums, temp, 0, nums.size() - 1);
        return nums;            
    }
};
