class Solution {
public:
    vector<int> pancakeSort(vector<int>& arr) {
        vector<int> res;
        int n = arr.size();
        for (int i = n; i > 0; --i) {
            int idx = find(arr.begin(), arr.end(), i) - arr.begin();
            if (idx == i - 1) continue; // Already in the correct position
            if (idx != 0) {
                res.push_back(idx + 1); // Flip to move i to the front
                reverse(arr.begin(), arr.begin() + idx + 1);
            }
            res.push_back(i); // Flip to move i to its correct position
            reverse(arr.begin(), arr.begin() + i);
        }
        return res;         
    }
};
