class Solution {
public:
    long long maxStrength(vector<int>& nums) {
        long long max_product = LLONG_MIN;
        int n = nums.size();
        for (int mask = 1; mask < (1 << n); ++mask) {
            long long product = 1;
            for (int i = 0; i < n; ++i) {
                if (mask & (1 << i)) {
                    product *= nums[i];
                }
            }
            max_product = max(max_product, product);
        }
        return max_product;         
    }
};
