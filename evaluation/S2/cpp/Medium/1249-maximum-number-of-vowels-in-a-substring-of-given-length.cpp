class Solution {
public:
    int maxVowels(string s, int k) {
        int maxVowels = 0, currentVowels = 0;
        for (int i = 0; i < s.size(); ++i) {
            if (isVowel(s[i])) {
                ++currentVowels;
            }
            if (i >= k) {
                if (isVowel(s[i - k])) {
                    --currentVowels;
                }
            }
            maxVowels = max(maxVowels, currentVowels);
        }
        return maxVowels;           
    }
};
