class Solution {
public:
    int getMaximumConsecutive(vector<int>& coins) {
        sort(coins.begin(), coins.end());
        int max_consecutive = 0;
        for (int coin : coins) {
            if (coin > max_consecutive + 1) {
                break;
            }
            max_consecutive += coin;
        }
        return max_consecutive + 1;         
    }
};
