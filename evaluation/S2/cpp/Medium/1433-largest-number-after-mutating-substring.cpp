class Solution {
public:
    string maximumNumber(string num, vector<int>& change) {
        string result = num;
        int n = num.size();
        bool mutated = false;

        for (int i = 0; i < n; ++i) {
            int digit = num[i] - '0';
            int mappedDigit = change[digit];

            if (mappedDigit > digit) {
                result[i] = mappedDigit + '0';
                mutated = true;
            } else if (mappedDigit < digit && mutated) {
                break; // Stop mutating once we encounter a smaller mapped digit after starting mutation
            }
        }

        return result;          
    }
};
