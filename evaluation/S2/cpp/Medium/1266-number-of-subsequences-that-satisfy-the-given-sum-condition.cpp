class Solution {
public:
    int numSubseq(vector<int>& nums, int target) {
        static const int MOD = 1e9 + 7;
        sort(nums.begin(), nums.end());
        int n = nums.size();
        vector<int> pow(n, 1);
        for (int i = 1; i < n; ++i) {
            pow[i] = (pow[i - 1] * 2) % MOD;
        }
        int left = 0, right = n - 1, ans = 0;
        while (left <= right) {
            if (nums[left] + nums[right] > target) {
                --right;
            } else {
                ans = (ans + pow[right - left]) % MOD;
                ++left;
            }
        }
        return ans;         
    }
};
