class Solution {
public:
    int minFlips(string target) {
        int flips = 0;
        char currentState = '0'; // Initial state of s is "000...0"

        for (char c : target) {
            if (c != currentState) {
                flips++; // We need to flip at this position
                currentState = c; // Update the current state after the flip
            }
        }

        return flips;           
    }
};
