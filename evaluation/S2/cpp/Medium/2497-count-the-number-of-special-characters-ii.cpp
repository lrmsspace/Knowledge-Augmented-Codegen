class Solution {
public:
    int numberOfSpecialChars(string word) {
        unordered_set<char> lowercase, uppercase;
        for (char c : word) {
            if (islower(c)) {
                lowercase.insert(c);
            } else {
                uppercase.insert(c);
            }
        }

        int count = 0;
        for (char c : lowercase) {
            if (uppercase.count(toupper(c))) {
                count++;
            }
        }

        return count;           
    }
};
