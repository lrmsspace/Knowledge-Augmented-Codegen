class Solution {
public:
    string removeKdigits(string num, int k) {
        string result;
        for (char digit : num) {
            while (!result.empty() && k > 0 && result.back() > digit) {
                result.pop_back();
                k--;
            }
            if (!result.empty() || digit != '0') {
                result.push_back(digit);
            }
        }
        while (!result.empty() && k > 0) {
            result.pop_back();
            k--;
        }
        return result.empty() ? "0" : result;           
    }
};
