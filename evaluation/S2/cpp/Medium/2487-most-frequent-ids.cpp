class Solution {
public:
    vector<long long> mostFrequentIDs(vector<int>& nums, vector<int>& freq) {
        int n = nums.size();
        vector<long long> ans(n);
        unordered_map<int, long long> count; // Map to track the count of each ID
        long long maxFreq = 0; // Variable to track the maximum frequency

        for (int i = 0; i < n; ++i) {
            int id = nums[i];
            long long change = freq[i];

            // Update the count of the current ID
            count[id] += change;

            // Update the maximum frequency if necessary
            maxFreq = max(maxFreq, count[id]);

            // Store the current maximum frequency in the answer array
            ans[i] = maxFreq;
        }

        return ans;         
    }
};
