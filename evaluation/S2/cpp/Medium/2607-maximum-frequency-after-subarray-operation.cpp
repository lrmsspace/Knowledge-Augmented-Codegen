class Solution {
public:
    int maxFrequency(vector<int>& nums, int k) {
        int n = nums.size();
        vector<long long> prefixSum(n + 1, 0);
        for (int i = 0; i < n; ++i) {
            prefixSum[i + 1] = prefixSum[i] + nums[i];
        }

        int maxFreq = 1;
        long long minPrefixSum = 0;
        for (int r = 1; r <= n; ++r) {
            minPrefixSum = min(minPrefixSum, prefixSum[r - 1]);
            if (prefixSum[r] - minPrefixSum <= k + (r - 1)) {
                maxFreq = max(maxFreq, r);
            }
        }

        return maxFreq;         
    }
};
