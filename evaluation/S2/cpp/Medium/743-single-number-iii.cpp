class Solution {
public:
    vector<int> singleNumber(vector<int>& nums) {
        int xor_all = 0;
        for (int num : nums) {
            xor_all ^= num;
        }

        // Find the rightmost set bit in xor_all
        int rightmost_set_bit = xor_all & (-xor_all);

        int num1 = 0, num2 = 0;
        for (int num : nums) {
            if (num & rightmost_set_bit) {
                num1 ^= num; // XOR of first group
            } else {
                num2 ^= num; // XOR of second group
            }
        }

        return {num1, num2};            
    }
};
