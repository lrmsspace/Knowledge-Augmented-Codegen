class Solution {
public:
    long long maxMatrixSum(vector<vector<int>>& matrix) {
        int n = matrix.size();
        long long sum = 0;
        int minAbsVal = INT_MAX;
        int negCount = 0;

        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < n; ++j) {
                int val = matrix[i][j];
                sum += abs(val);
                minAbsVal = min(minAbsVal, abs(val));
                if (val < 0) {
                    negCount++;
                }
            }
        }

        // If the count of negative numbers is odd, we can flip one more element to make it even
        if (negCount % 2 == 1) {
            sum -= 2LL * minAbsVal; // Flip the smallest absolute value element
        }

        return sum;         
    }
};
