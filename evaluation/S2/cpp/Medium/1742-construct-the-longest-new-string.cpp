class Solution {
public:
    int longestString(int x, int y, int z) {
        // We can use at most min(x, y) pairs of "AA" and "BB" to avoid "AAA" and "BBB".
        int pairs = min(x, y);
        
        // Each pair contributes 4 characters ("AA" + "BB").
        int length = pairs * 4;
        
        // After using pairs, we have (x - pairs) "AA" and (y - pairs) "BB" left.
        // We can add one more "AA" if we have any left, or one more "BB" if we have any left.
        if (x > pairs) {
            length += 2; // Add one "AA"
        } else if (y > pairs) {
            length += 2; // Add one "BB"
        }
        
        // We can add all "AB" strings without causing "AAA" or "BBB".
        length += z * 2; // Each "AB" contributes 2 characters
        
        return length;          
    }
};
