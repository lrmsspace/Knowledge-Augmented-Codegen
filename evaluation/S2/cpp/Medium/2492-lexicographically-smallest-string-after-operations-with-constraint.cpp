class Solution {
public:
    string getSmallestString(string s, int k) {
        string res = s;
        for (int i = 0; i < s.size(); ++i) {
            int dist = min((s[i] - 'a' + 26) % 26, ('z' - s[i] + 26) % 26);
            if (dist <= k) {
                k -= dist;
                res[i] = 'a';
            } else {
                res[i] = (s[i] - k - 'a' + 26) % 26 + 'a';
                break;
            }
        }
        return res;         
    }
};
