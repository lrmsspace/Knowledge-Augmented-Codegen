class Solution {
public:
    int minimumSum(vector<int>& nums) {
        int n = nums.size();
        vector<int> left(n, -1), right(n, -1);
        stack<int> st;

        // Find the largest element to the left of each index
        for (int i = 0; i < n; ++i) {
            while (!st.empty() && nums[st.top()] >= nums[i]) {
                st.pop();
            }
            if (!st.empty()) {
                left[i] = st.top();
            }
            st.push(i);
        }

        // Clear the stack to reuse it for finding the largest element to the right
        while (!st.empty()) {
            st.pop();
        }

        // Find the largest element to the right of each index
        for (int i = n - 1; i >= 0; --i) {
            while (!st.empty() && nums[st.top()] >= nums[i]) {
                st.pop();
            }
            if (!st.empty()) {
                right[i] = st.top();
            }
            st.push(i);
        }

        int min_sum = INT_MAX;
        // Calculate the minimum sum of a mountain triplet
        for (int j = 0; j < n; ++j) {
            if (left[j] != -1 && right[j] != -1) {
                min_sum = min(min_sum, nums[left[j]] + nums[j] + nums[right[j]]);
            }
        }

        return min_sum == INT_MAX ? -1 : min_sum;           
    }
};
