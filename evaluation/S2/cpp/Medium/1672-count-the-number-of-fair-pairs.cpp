class Solution {
public:
    long long countFairPairs(vector<int>& nums, int lower, int upper) {
        sort(nums.begin(), nums.end());
        long long count = 0;
        for (int i = 0; i < nums.size(); ++i) {
            int low_bound = lower - nums[i];
            int up_bound = upper - nums[i];
            auto low_it = lower_bound(nums.begin() + i + 1, nums.end(), low_bound);
            auto up_it = upper_bound(nums.begin() + i + 1, nums.end(), up_bound);
            count += distance(low_it, up_it);
        }
        return count;           
    }
};
