class Solution {
public:
    int longestSubsequence(vector<int>& arr, int difference) {
        unordered_map<int, int> dp; // dp[x] = length of longest arithmetic subsequence ending with x
        int max_length = 1;

        for (int num : arr) {
            // The previous element in the arithmetic sequence would be num - difference
            int prev_num = num - difference;
            dp[num] = dp[prev_num] + 1; // Extend the sequence ending with prev_num
            max_length = max(max_length, dp[num]); // Update max length if needed
        }

        return max_length;          
    }
};
