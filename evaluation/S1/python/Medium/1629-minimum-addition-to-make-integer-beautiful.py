class Solution(object):
    def makeIntegerBeautiful(self, n, target):
        """
        :type n: int
        :type target: int
        :rtype: int
        """
        def digit_sum(x):
            return sum(int(d) for d in str(x))
        
        addition = 0
        factor = 1
        
        while digit_sum(n + addition) > target:
            digit = (n // factor) % 10
            increment = (10 - digit) % 10
            addition += increment * factor
            n += increment * factor
            factor *= 10
            
        return addition