class Solution(object):
    def maximumCandies(self, candies, k):
        """
        :type candies: List[int]
        :type k: int
        :rtype: int
        """
        def canDistribute(mid):
            count = 0
            for candy in candies:
                count += candy // mid
            return count >= k

        left, right = 1, max(candies)
        answer = 0

        while left <= right:
            mid = (left + right) // 2
            if canDistribute(mid):
                answer = mid
                left = mid + 1
            else:
                right = mid - 1

        return answer