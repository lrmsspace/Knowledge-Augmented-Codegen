class Solution(object):
    def champagneTower(self, poured, query_row, query_glass):
        """
        :type poured: int
        :type query_row: int
        :type query_glass: int
        :rtype: float
        """
        # Create a 2D array to represent the tower
        tower = [[0.0 for _ in range(i + 1)] for i in range(101)]
        tower[0][0] = float(poured)

        # Simulate the pouring process
        for r in range(100):
            for g in range(r + 1):
                if tower[r][g] > 1.0:
                    excess = (tower[r][g] - 1.0) / 2.0
                    tower[r + 1][g] += excess
                    tower[r + 1][g + 1] += excess
                    tower[r][g] = 1.0

        return min(1.0, tower[query_row][query_glass])