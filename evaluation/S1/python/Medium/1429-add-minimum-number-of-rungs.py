class Solution(object):
    def addRungs(self, rungs, dist):
        """
        :type rungs: List[int]
        :type dist: int
        :rtype: int
        """
        prev = 0
        added = 0
        for rung in rungs:
            gap = rung - prev
            if gap > dist:
                # Calculate how many rungs needed to fill the gap
                # Each added rung can cover 'dist' distance
                # We need (gap - 1) // dist rungs to cover the gap
                added += (gap - 1) // dist
            prev = rung
        return added