class Solution(object):
    def minEnd(self, n, x):
        """
        :type n: int
        :type x: int
        :rtype: int
        """
        if n == 1:
            return x
        
        # Find the smallest number greater than x that has at least
        # (n - 1) bits set to 1 in positions where x has 0s.
        result = x
        bits_needed = n - 1
        bit_position = 0
        
        while bits_needed > 0:
            if (x & (1 << bit_position)) == 0:
                result |= (1 << bit_position)
                bits_needed -= 1
            bit_position += 1
        
        return result