class Solution(object):
    def queryString(self, s, n):
        """
        :type s: str
        :type n: int
        :rtype: bool
        """
        needed = set(range(1, n + 1))
        length = len(s)
        
        for i in range(length):
            if s[i] == '0':
                continue
            num = 0
            for j in range(i, min(length, i + 30)):
                num = (num << 1) | (1 if s[j] == '1' else 0)
                if num in needed:
                    needed.remove(num)
                    if not needed:
                        return True
        return False