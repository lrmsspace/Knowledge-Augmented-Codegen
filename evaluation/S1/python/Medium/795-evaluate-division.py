class Solution(object):
    def calcEquation(self, equations, values, queries):
        """
        :type equations: List[List[str]]
        :type values: List[float]
        :type queries: List[List[str]]
        :rtype: List[float]
        """
        from collections import defaultdict, deque

        # Build the graph
        graph = defaultdict(list)
        for (dividend, divisor), value in zip(equations, values):
            graph[dividend].append((divisor, value))
            graph[divisor].append((dividend, 1 / value))

        def bfs(start, end):
            if start not in graph or end not in graph:
                return -1.0
            if start == end:
                return 1.0

            queue = deque([(start, 1.0)])
            visited = set([start])

            while queue:
                current_node, current_product = queue.popleft()

                for neighbor, value in graph[current_node]:
                    if neighbor in visited:
                        continue
                    new_product = current_product * value
                    if neighbor == end:
                        return new_product
                    visited.add(neighbor)
                    queue.append((neighbor, new_product))

            return -1.0

        results = []
        for dividend, divisor in queries:
            result = bfs(dividend, divisor)
            results.append(result)

        return results