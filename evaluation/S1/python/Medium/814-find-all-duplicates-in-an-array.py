class Solution(object):
    def findDuplicates(self, nums):
        """
        :type nums: List[int]
        :rtype: List[int]
        """
        res = []
        for n in nums:
            index = abs(n) - 1
            if nums[index] < 0:
                res.append(abs(n))
            else:
                nums[index] = -nums[index]
        return res