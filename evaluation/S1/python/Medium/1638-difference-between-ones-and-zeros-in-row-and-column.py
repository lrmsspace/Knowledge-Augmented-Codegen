class Solution(object):
    def onesMinusZeros(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: List[List[int]]
        """
        m = len(grid)
        n = len(grid[0]) if m > 0 else 0

        row_ones = [0] * m
        col_ones = [0] * n

        for i in range(m):
            for j in range(n):
                if grid[i][j] == 1:
                    row_ones[i] += 1
                    col_ones[j] += 1

        result = [[0] * n for _ in range(m)]

        for i in range(m):
            for j in range(n):
                ones_count = row_ones[i] + col_ones[j]
                zeros_count = (n - row_ones[i]) + (m - col_ones[j])
                result[i][j] = ones_count - zeros_count

        return result