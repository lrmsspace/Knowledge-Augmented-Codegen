# Problem Description:
# Given an integer array nums, find the subarray with the largest sum, and return its sum.
#
# Example:
# Input: nums = [-2,1,-3,4,-1,2,1,-5,4]
# Output: 6
# Explanation: The subarray [4,-1,2,1] has the largest sum 6.
