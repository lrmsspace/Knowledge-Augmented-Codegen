class Solution(object):
    def maxSubArray(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        global_max = float('-inf')
        current_max = 0
        
        for num in nums:
            current_max += num
            if current_max > global_max:
                global_max = current_max
            if current_max < 0:
                current_max = 0
        
        return global_max