class Solution(object):
    def maximumLength(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        even_count = sum(1 for num in nums if num % 2 == 0)
        odd_count = len(nums) - even_count
        return max(even_count, odd_count)