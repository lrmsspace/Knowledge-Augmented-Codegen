class Solution(object):
    def magicalString(self, n):
        """
        :type n: int
        :rtype: int
        """
        if n <= 0:
            return 0
        if n <= 3:
            return 1  # "122"

        s = [1, 2, 2]
        head = 2  # 指向当前读数位置
        num = 1   # 下一个要写入的数字
        while len(s) < n:
            count = s[head]
            s.extend([num] * count)
            num = 3 - num  # 在1和2之间切换
            head += 1

        return s[:n].count(1)