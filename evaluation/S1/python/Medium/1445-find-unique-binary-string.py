class Solution(object):
    def findDifferentBinaryString(self, nums):
        """
        :type nums: List[str]
        :rtype: str
        """
        n = len(nums)
        result = []
        
        for i in range(n):
            # Flip the i-th bit of the i-th string
            if nums[i][i] == '0':
                result.append('1')
            else:
                result.append('0')
        
        return ''.join(result)