class Solution(object):
    def waysToSplitArray(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        n = len(nums)
        prefix_sum = [0] * n
        prefix_sum[0] = nums[0]
        
        for i in range(1, n):
            prefix_sum[i] = prefix_sum[i - 1] + nums[i]
        
        total_sum = prefix_sum[-1]
        valid_splits = 0
        
        for i in range(n - 1):
            if prefix_sum[i] >= total_sum - prefix_sum[i]:
                valid_splits += 1
        
        return valid_splits