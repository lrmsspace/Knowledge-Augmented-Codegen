class Solution(object):
    def minDeletions(self, s):
        """
        :type s: str
        :rtype: int
        """
        freq = {}
        for char in s:
            freq[char] = freq.get(char, 0) + 1

        used_frequencies = set()
        deletions = 0

        for char, count in freq.items():
            while count > 0 and count in used_frequencies:
                count -= 1
                deletions += 1
            if count > 0:
                used_frequencies.add(count)

        return deletions