# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution(object):
    def pseudoPalindromicPaths (self, root):
        """
        :type root: Optional[TreeNode]
        :rtype: int
        """
        def dfs(node, count):
            if not node:
                return 0
            
            # Toggle the bit corresponding to the current node's value
            count ^= (1 << node.val)
            
            # If it's a leaf node, check if at most one bit is set in count
            if not node.left and not node.right:
                # Check if count has at most one bit set
                if count & (count - 1) == 0:
                    return 1
                else:
                    return 0
            
            # Continue DFS on left and right children
            left_paths = dfs(node.left, count)
            right_paths = dfs(node.right, count)
            
            return left_paths + right_paths
        
        return dfs(root, 0)