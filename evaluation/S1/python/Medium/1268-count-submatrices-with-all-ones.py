class Solution(object):
    def numSubmat(self, mat):
        """
        :type mat: List[List[int]]
        :rtype: int
        """
        m, n = len(mat), len(mat[0])
        # Precompute the number of consecutive ones to the left for each cell
        left = [[0] * n for _ in range(m)]
        for i in range(m):
            for j in range(n):
                if mat[i][j] == 1:
                    left[i][j] = left[i][j - 1] + 1 if j > 0 else 1

        total_submatrices = 0
        # For each cell, count the number of submatrices with all ones ending at that cell
        for i in range(m):
            for j in range(n):
                if mat[i][j] == 1:
                    min_width = float('inf')
                    for k in range(i, -1, -1):
                        min_width = min(min_width, left[k][j])
                        total_submatrices += min_width

        return total_submatrices