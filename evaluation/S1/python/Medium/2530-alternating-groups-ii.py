class Solution(object):
    def numberOfAlternatingGroups(self, colors, k):
        """
        :type colors: List[int]
        :type k: int
        :rtype: int
        """
        n = len(colors)
        count = 0
        
        for start in range(n):
            is_alternating = True
            for i in range(k):
                if colors[(start + i) % n] == colors[(start + i - 1) % n]:
                    is_alternating = False
                    break
            if is_alternating:
                count += 1
                
        return count