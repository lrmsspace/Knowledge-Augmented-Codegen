class Solution(object):
    def maxProduct(self, words):
        """
        :type words: List[str]
        :rtype: int
        """
        n = len(words)
        bitmasks = [0] * n
        lengths = [0] * n
        
        for i in range(n):
            bitmask = 0
            for char in words[i]:
                bitmask |= 1 << (ord(char) - ord('a'))
            bitmasks[i] = bitmask
            lengths[i] = len(words[i])
        
        max_product = 0
        for i in range(n):
            for j in range(i + 1, n):
                if bitmasks[i] & bitmasks[j] == 0:
                    max_product = max(max_product, lengths[i] * lengths[j])
        
        return max_product