class Solution(object):
    def countAlternatingSubarrays(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        n = len(nums)
        total_count = 0
        length = 1  # Length of the current alternating subarray

        for i in range(1, n):
            if nums[i] != nums[i - 1]:
                length += 1
            else:
                # Calculate the number of alternating subarrays in the previous segment
                total_count += (length * (length + 1)) // 2
                length = 1  # Reset length for the new segment

        # Account for the last segment
        total_count += (length * (length + 1)) // 2

        return total_count