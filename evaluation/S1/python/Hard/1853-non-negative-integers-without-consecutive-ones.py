class Solution(object):
    def findIntegers(self, n):
        """
        :type n: int
        :rtype: int
        """
        # Convert n to binary and reverse it for easier processing
        bits = []
        while n > 0:
            bits.append(n & 1)
            n >>= 1
        
        # Memoization table: dp[pos][prev_bit][is_limit]
        memo = {}
        
        def dfs(pos, prev_bit, is_limit):
            if pos == len(bits):
                return 1
            
            if (pos, prev_bit, is_limit) in memo:
                return memo[(pos, prev_bit, is_limit)]
            
            limit = bits[pos] if is_limit else 1
            total = 0
            
            for bit in range(limit + 1):
                if bit == 1 and prev_bit == 1:
                    continue
                total += dfs(pos + 1, bit, is_limit and (bit == limit))
            
            memo[(pos, prev_bit, is_limit)] = total
            return total
        
        return dfs(0, 0, True)