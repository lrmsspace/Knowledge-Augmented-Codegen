class Solution(object):
    def maxSizeSlices(self, slices):
        """
        :type slices: List[int]
        :rtype: int
        """
        n = len(slices) // 3
        return max(self.helper(slices, 0, len(slices) - 2, n), self.helper(slices, 1, len(slices) - 1, n))