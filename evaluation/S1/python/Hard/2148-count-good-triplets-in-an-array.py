class Solution(object):
    def goodTriplets(self, nums1, nums2):
        """
        :type nums1: List[int]
        :type nums2: List[int]
        :rtype: int
        """
        n = len(nums1)
        pos_in_nums2 = [0] * n
        for i in range(n):
            pos_in_nums2[nums2[i]] = i

        bit = [0] * (n + 1)

        def update(index):
            while index <= n:
                bit[index] += 1
                index += index & -index

        def query(index):
            result = 0
            while index > 0:
                result += bit[index]
                index -= index & -index
            return result

        count = 0
        for i in range(n):
            pos = pos_in_nums2[nums1[i]]
            count += query(pos) * (i - query(pos))
            update(pos + 1)

        return count