class Solution(object):
    def sumOfEncryptedInt(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        total = 0
        for num in nums:
            max_digit = max(int(digit) for digit in str(num))
            encrypted_num = int(str(max_digit) * len(str(num)))
            total += encrypted_num
        return total