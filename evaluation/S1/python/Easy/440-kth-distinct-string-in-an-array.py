class Solution(object):
    def kthDistinct(self, arr, k):
        """
        :type arr: List[str]
        :type k: int
        :rtype: str
        """
        from collections import Counter

        count = Counter(arr)
        distinct_strings = [s for s in arr if count[s] == 1]

        if k <= len(distinct_strings):
            return distinct_strings[k - 1]
        else:
            return ""