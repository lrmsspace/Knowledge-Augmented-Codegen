class Solution(object):
    def kLengthApart(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: bool
        """
        last_one_index = -1  # 记录上一个1出现的位置
        for i in range(len(nums)):
            if nums[i] == 1:
                if last_one_index != -1 and i - last_one_index - 1 < k:
                    return False
                last_one_index = i
        return True