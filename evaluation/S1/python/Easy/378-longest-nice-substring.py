class Solution(object):
    def longestNiceSubstring(self, s):
        """
        :type s: str
        :rtype: str
        """
        def is_nice(subs):
            chars = set(subs)
            for c in chars:
                if c.swapcase() not in chars:
                    return False
            return True

        n = len(s)
        max_len = 0
        res = ""

        for i in range(n):
            for j in range(i + 1, n + 1):
                subs = s[i:j]
                if is_nice(subs):
                    if j - i > max_len:
                        max_len = j - i
                        res = subs

        return res