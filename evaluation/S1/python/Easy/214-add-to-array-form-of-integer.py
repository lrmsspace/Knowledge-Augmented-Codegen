class Solution(object):
    def addToArrayForm(self, num, k):
        """
        :type num: List[int]
        :type k: int
        :rtype: List[int]
        """
        n = len(num)
        carry = 0
        i = n - 1
        result = []

        while i >= 0 or k > 0 or carry > 0:
            digit_num = num[i] if i >= 0 else 0
            digit_k = k % 10

            total = digit_num + digit_k + carry
            carry = total // 10
            result.append(total % 10)

            k //= 10
            i -= 1

        result.reverse()
        return result