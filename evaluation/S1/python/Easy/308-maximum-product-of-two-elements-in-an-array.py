class Solution(object):
    def maxProduct(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        # Find the two largest numbers in the array
        first_max = second_max = 0
        for num in nums:
            if num > first_max:
                second_max = first_max
                first_max = num
            elif num > second_max:
                second_max = num
        
        # Calculate the maximum product
        return (first_max - 1) * (second_max - 1)