class Solution(object):
    def isGood(self, nums):
        """
        :type nums: List[int]
        :rtype: bool
        """
        n = len(nums) - 1
        count = [0] * (n + 1)
        
        for num in nums:
            if num > n:
                return False
            count[num] += 1
        
        for i in range(1, n):
            if count[i] != 1:
                return False
        
        if count[n] != 2:
            return False
        
        return True