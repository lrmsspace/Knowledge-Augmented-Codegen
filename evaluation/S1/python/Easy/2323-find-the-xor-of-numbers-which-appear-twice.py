class Solution(object):
    def duplicateNumbersXOR(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        xor_result = 0
        seen = set()

        for num in nums:
            if num in seen:
                xor_result ^= num
            else:
                seen.add(num)

        return xor_result