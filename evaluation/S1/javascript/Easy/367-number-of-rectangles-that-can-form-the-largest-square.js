/**
 * @param {number[][]} rectangles
 * @return {number}
 */
var countGoodRectangles = function(rectangles) {
    let maxLen = 0;
    let count = 0;
    for (const [length, width] of rectangles) {
        const side = Math.min(length, width);
        if (side > maxLen) {
            maxLen = side;
            count = 1;
        } else if (side === maxLen) {
            count++;
        }
    }
    return count;
};