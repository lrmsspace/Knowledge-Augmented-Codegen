/**
 * @param {number} n
 * @return {number}
 */
var alternateDigitSum = function(n) {
    const numStr = n.toString();
    let sum = 0;
    for (let i = 0; i < numStr.length; i++) {
        const digit = parseInt(numStr[i], 10);
        if (i % 2 === 0) {
            sum += digit;
        } else {
            sum -= digit;
        }
    }
    return sum;
};