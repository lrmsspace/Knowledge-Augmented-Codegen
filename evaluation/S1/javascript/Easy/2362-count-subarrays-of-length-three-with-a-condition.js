/**
 * @param {number[]} nums
 * @return {number}
 */
var countSubarrays = function(nums) {
    let count = 0;
    const n = nums.length;
    for (let i = 0; i < n - 2; i++) {
        if (nums[i] + nums[i + 2] === 2 * nums[i + 1]) {
            count++;
        }
    }
    return count;
};  