/**
 * @param {string[]} operations
 * @return {number}
 */
var finalValueAfterOperations = function(operations) {
    let X = 0;
    for (const operation of operations) {
        if (operation === '++X' || operation === 'X++') {
            X++;
        } else if (operation === '--X' || operation === 'X--') {
            X--;
        }
    }    return X;
};
