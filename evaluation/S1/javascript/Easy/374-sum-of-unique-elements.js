/**
 * @param {number[]} nums
 * @return {number}
 */
var sumOfUnique = function(nums) {
    const countMap = new Map();
    for (const num of nums) {
        countMap.set(num, (countMap.get(num) || 0) + 1);
    }
    let sum = 0;
    for (const [num, count] of countMap.entries()) {
        if (count === 1) {
            sum += num;
        }
    }
    return sum;
};