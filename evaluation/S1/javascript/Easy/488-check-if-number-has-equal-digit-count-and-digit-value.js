/**
 * @param {string} num
 * @return {boolean}
 */
var digitCount = function(num) {
    const count = new Array(10).fill(0);
    for (const char of num) {
        const digit = parseInt(char, 10);
        count[digit]++;
    }
    for (let i = 0; i < num.length; i++) {
        const digit = parseInt(num[i], 10);
        if (count[i] !== digit) {
            return false;
        }
    }
    return true;
};