/**
 * @param {number} red
 * @param {number} blue
 * @return {number}
 */
var maxHeightOfTriangle = function(red, blue) {
    let height = 0;
    let totalBalls = 0;
    while (true) {
        height++;
        totalBalls += height;
        const halfHeight = Math.floor((height + 1) / 2);
        const redNeeded = halfHeight;
        const blueNeeded = height - halfHeight;
        if (redNeeded > red || blueNeeded > blue) {
            return height - 1;
        }
    }
};
