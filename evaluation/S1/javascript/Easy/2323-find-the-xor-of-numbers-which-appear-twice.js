/**
 * @param {number[]} nums
 * @return {number}
 */
var duplicateNumbersXOR = function(nums) {
    const countMap = new Map();
    for (const num of nums) {
        countMap.set(num, (countMap.get(num) || 0) + 1);
    }
    let xorResult = 0;
    for (const [num, count] of countMap.entries()) {
        if (count === 2) {
            xorResult ^= num;
        }
    }
    return xorResult;   
};
