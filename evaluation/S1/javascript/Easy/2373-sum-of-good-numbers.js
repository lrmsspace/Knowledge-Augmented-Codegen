/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var sumOfGoodNumbers = function(nums, k) {
    let totalSum = 0;
    const n = nums.length;
    for (let i = 0; i < n; i++) {
        const leftIndex = i - k;
        const rightIndex = i + k;
        const leftValid = leftIndex >= 0 ? nums[i] > nums[leftIndex] : true;
        const rightValid = rightIndex < n ? nums[i] > nums[rightIndex] : true;
        if (leftValid && rightValid) {
            totalSum += nums[i];
        }
    }
    return totalSum;
};
