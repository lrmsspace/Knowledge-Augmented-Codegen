/**
 * @param {number[][]} grid
 * @return {number}
 */
var countNegatives = function(grid) {
    let negativeCount = 0;
    const rows = grid.length;
    const cols = grid[0].length;
    let row = 0;
    let col = cols - 1;
    while (row < rows && col >= 0) {
        if (grid[row][col] < 0) {
            negativeCount += (col + 1);
            row++;
        } else {
            col--;
        }
    }
    return negativeCount;
};