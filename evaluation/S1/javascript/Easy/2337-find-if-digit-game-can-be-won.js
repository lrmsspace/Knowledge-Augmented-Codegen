/**
 * @param {number[]} nums
 * @return {boolean}
 */
var canAliceWin = function(nums) {
    let singleDigitSum = 0;
    let doubleDigitSum = 0;
    for (const num of nums) {
        if (num < 10) {
            singleDigitSum += num;
        } else {
            doubleDigitSum += num;
        }
    }
    return singleDigitSum > doubleDigitSum || doubleDigitSum > singleDigitSum;
};
