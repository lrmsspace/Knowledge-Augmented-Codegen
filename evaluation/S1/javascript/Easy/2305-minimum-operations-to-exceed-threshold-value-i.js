/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var minOperations = function(nums, k) {
    nums.sort((a, b) => a - b);
    let operations = 0;
    for (const num of nums) {
        if (num < k) {
            operations++;
        } else {
            break;
        }
    }
    return operations;
};
