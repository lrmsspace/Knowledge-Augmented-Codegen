/**
 * @param {number[]} nums
 * @return {boolean}
 */
var hasTrailingZeros = function(nums) {
    let combinedOr = 0;
    for (const num of nums) {
        combinedOr |= num;
    }
    return (combinedOr & 1) === 0;
};
