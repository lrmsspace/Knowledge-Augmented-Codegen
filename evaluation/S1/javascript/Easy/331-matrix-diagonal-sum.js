/**
 * @param {number[][]} mat
 * @return {number}
 */
var diagonalSum = function(mat) {
    const n = mat.length;
    let totalSum = 0;
    for (let i = 0; i < n; i++) {
        totalSum += mat[i][i]; // Primary diagonal
        totalSum += mat[i][n - 1 - i]; // Secondary diagonal
    }
    if (n % 2 === 1) {
        totalSum -= mat[Math.floor(n / 2)][Math.floor(n / 2)]; // Subtract the middle element if n is odd
    }
    return totalSum;
};