/**
 * @param {number[][]} matrix
 * @return {number[]}
 */
var luckyNumbers = function(matrix) {
    const m = matrix.length;         
    const n = matrix[0].length;
    const luckyNumbers = [];

    for (let i = 0; i < m; i++) {
        let minVal = matrix[i][0];
        let minCol = 0;
        for (let j = 1; j < n; j++) {
            if (matrix[i][j] < minVal) {
                minVal = matrix[i][j];
                minCol = j;
            }
        }

        let isLucky = true;
        for (let k = 0; k < m; k++) {
            if (matrix[k][minCol] > minVal) {
                isLucky = false;
                break;
            }
        }

        if (isLucky) {
            luckyNumbers.push(minVal);
        }
    }

    return luckyNumbers;
};