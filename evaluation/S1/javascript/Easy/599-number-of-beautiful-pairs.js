/**
 * @param {number[]} nums
 * @return {number}
 */
var countBeautifulPairs = function(nums) {
    const gcd = (a, b) => {
        while (b !== 0) {
            const temp = b;
            b = a % b;
            a = temp;
        }

        return a;
    }   
    let beautifulPairCount = 0;
    const n = nums.length;
    for (let i = 0; i < n; i++) {
        const firstDigit = parseInt(nums[i].toString()[0], 10);
        for (let j = i + 1; j < n; j++) {   
            const lastDigit = nums[j] % 10;
            if (gcd(firstDigit, lastDigit) === 1) {
                beautifulPairCount++;
            }
        }
    }
    return beautifulPairCount;
    
};
