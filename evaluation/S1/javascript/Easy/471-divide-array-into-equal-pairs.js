/**
 * @param {number[]} nums
 * @return {boolean}
 */
var divideArray = function(nums) {
    const countMap = new Map();
    for (const num of nums) {
        countMap.set(num, (countMap.get(num) || 0) + 1);
    }
    for (const count of countMap.values()) {
        if (count % 2 !== 0) {
            return false;
        }
    }
    return true;
};