/**
 * @param {string} number
 * @return {string}
 */
var reformatNumber = function(number) {
    const digits = number.replace(/[\s-]/g, '');
    const n = digits.length;
    let result = '';
    let i = 0;

    while (n - i > 4) {
        result += digits.slice(i, i + 3) + '-';
        i += 3;
    }

    if (n - i === 4) {
        result += digits.slice(i, i + 2) + '-' + digits.slice(i + 2, i + 4);
    } else {
        result += digits.slice(i);
    }

    return result;
};