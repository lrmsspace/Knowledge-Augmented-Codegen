/**
 * @param {number} n
 * @return {boolean}
 */
var isThree = function(n) {
    if (n < 4) return false;
    const sqrtN = Math.sqrt(n);
    if (sqrtN % 1 !== 0) return false;

    for (let i = 2; i <= Math.sqrt(sqrtN); i++) {
        if (sqrtN % i === 0) {
            return false;
        }
    }
    return true;
};  