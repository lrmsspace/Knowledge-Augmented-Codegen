/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var smallestRangeI = function(nums, k) {
    const maxNum = Math.max(...nums);
    const minNum = Math.min(...nums);
    const range = maxNum - minNum;
    return Math.max(0, range - 2 * k);  
};
