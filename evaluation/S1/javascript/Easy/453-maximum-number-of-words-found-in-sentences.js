/**
 * @param {string[]} sentences
 * @return {number}
 */
var mostWordsFound = function(sentences) {
    let maxWords = 0;
    for (const sentence of sentences) {
        const wordCount = sentence.split(' ').length;
        if (wordCount > maxWords) {
            maxWords = wordCount;
        }
    }
    return maxWords;
};
