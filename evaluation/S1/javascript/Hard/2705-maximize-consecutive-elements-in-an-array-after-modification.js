/**
 * @param {number[]} nums
 * @return {number}
 */
var maxSelectedElements = function(nums) {
    nums.sort((a, b) => a - b);
    let maxCount = 0;
    let currentCount = 1;

    for (let i = 1; i < nums.length; i++) {
        if (nums[i] <= nums[i - 1] + 1) {
            currentCount++;
        } else {
            maxCount = Math.max(maxCount, currentCount);
            currentCount = 1;
        }
    }
    maxCount = Math.max(maxCount, currentCount);

    return maxCount;
};
