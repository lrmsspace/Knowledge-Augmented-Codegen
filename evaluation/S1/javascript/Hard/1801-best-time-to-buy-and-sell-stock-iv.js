/**
 * @param {number} k
 * @param {number[]} prices
 * @return {number}
 */
var maxProfit = function(k, prices) {
    const n = prices.length;
    if (n === 0) return 0;

    // If k is large enough, we can make unlimited transactions
    if (k >= Math.floor(n / 2)) {
        let maxProfit = 0;
        for (let i = 1; i < n; i++) {
            if (prices[i] > prices[i - 1]) {
                maxProfit += prices[i] - prices[i - 1];
            }
        }
        return maxProfit;
    }

    const dpBuy = new Array(k + 1).fill(-Infinity);
    const dpSell = new Array(k + 1).fill(0);

    for (const price of prices) {
        for (let j = 1; j <= k; j++) {
            dpBuy[j] = Math.max(dpBuy[j], dpSell[j - 1] - price);
            dpSell[j] = Math.max(dpSell[j], dpBuy[j] + price);
        }
    }

    return dpSell[k];
};
