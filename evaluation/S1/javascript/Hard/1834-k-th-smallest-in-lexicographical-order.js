/**
 * @param {number} n
 * @param {number} k
 * @return {number}
 */
var findKthNumber = function(n, k) {
    const countPrefix = (prefix) => {
        let count = 0;
        let currentPrefix = prefix;
        let nextPrefix = prefix + 1;

        while (currentPrefix <= n) {
            count += Math.min(n + 1, nextPrefix) - currentPrefix;
            currentPrefix *= 10;
            nextPrefix *= 10;
        }

        return count;
    };

    let current = 1;
    k--;

    while (k > 0) {
        const count = countPrefix(current);
        if (count <= k) {
            k -= count;
            current++;
        } else {
            current *= 10;
            k--;
        }
    }

    return current;
};
