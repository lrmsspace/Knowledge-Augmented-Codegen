/**
 * @param {string} s
 * @return {number}
 */
var distinctSubseqII = function(s) {
    const mod = 1e9 + 7;
    const n = s.length;
    const dp = new Array(n + 1).fill(0);
    const last = new Array(26).fill(-1);

    dp[0] = 1; // Base case: There's one subsequence of the empty string

    for (let i = 1; i <= n; i++) {
        const charIndex = s.charCodeAt(i - 1) - 'a'.charCodeAt(0);
        dp[i] = (2 * dp[i - 1]) % mod;

        if (last[charIndex] !== -1) {
            dp[i] = (dp[i] - dp[last[charIndex]]) % mod;
        }

        last[charIndex] = i - 1;
    }

    return (dp[n] - 1 + mod) % mod; // Subtract 1 to exclude the empty subsequence
};
