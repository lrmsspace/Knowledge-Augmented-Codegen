/**
 * @param {string} s
 * @return {number}
 */
var uniqueLetterString = function(s) {
    const n = s.length;
    const lastPos = new Array(26).fill(-1);
    const contribution = new Array(26).fill(0);
    let result = 0;

    for (let i = 0; i < n; i++) {
        const charIndex = s.charCodeAt(i) - 'A'.charCodeAt(0);
        contribution[charIndex] = (i - lastPos[charIndex]) * (n - i);
        lastPos[charIndex] = i;
        result += contribution[charIndex];
    }

    return result;
};
