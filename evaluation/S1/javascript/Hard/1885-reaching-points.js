/**
 * @param {number} sx
 * @param {number} sy
 * @param {number} tx
 * @param {number} ty
 * @return {boolean}
 */
var reachingPoints = function(sx, sy, tx, ty) {
    while (tx >= sx && ty >= sy) {
        if (tx === ty) break;
        if (tx > ty) {
            tx -= Math.max(1, Math.floor((tx - sx) / ty)) * ty;
        } else {
            ty -= Math.max(1, Math.floor((ty - sy) / tx)) * tx;
        }
    }
    return tx === sx && ty === sy;
};
