/**
 * @param {number[]} machines
 * @return {number}
 */
var findMinMoves = function(machines) {
    const totalDresses = machines.reduce((sum, dresses) => sum + dresses, 0);
    const n = machines.length;

    if (totalDresses % n !== 0) {
        return -1;
    }

    const target = totalDresses / n;
    let maxMoves = 0;
    let cumulativeDiff = 0;

    for (const dresses of machines) {
        const diff = dresses - target;
        cumulativeDiff += diff;
        maxMoves = Math.max(maxMoves, Math.abs(cumulativeDiff), diff);
    }

    return maxMoves;
};
