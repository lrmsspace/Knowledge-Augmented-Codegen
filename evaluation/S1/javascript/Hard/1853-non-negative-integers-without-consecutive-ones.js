/**
 * @param {number} n
 * @return {number}
 */
var findIntegers = function(n) {
    const memo = new Map();

    const count = (num, prev, sum) => {
        if (sum > n) {
            return 0;
        }
        if (num === 0) {
            return 1;
        }

        const key = `${num}-${prev}-${sum}`;
        if (memo.has(key)) {
            return memo.get(key);
        }

        let result = count(num - 1, false, sum << 1);
        if (!prev) {
            result += count(num - 1, true, (sum << 1) | 1);
        }

        memo.set(key, result);
        return result;
    };

    const maxBits = Math.floor(Math.log2(n)) + 1;
    return count(maxBits, false, 0);
};
