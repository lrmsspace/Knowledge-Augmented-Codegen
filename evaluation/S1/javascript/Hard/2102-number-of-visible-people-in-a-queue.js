/**
 * @param {number[]} heights
 * @return {number[]}
 */
var canSeePersonsCount = function(heights) {
    const n = heights.length;
    const result = new Array(n).fill(0);
    const stack = [];

    for (let i = n - 1; i >= 0; i--) {
        while (stack.length > 0 && stack[stack.length - 1] < heights[i]) {
            stack.pop();
            result[i]++;
        }
        if (stack.length > 0) {
            result[i]++;
        }
        stack.push(heights[i]);
    }

    return result;
};
