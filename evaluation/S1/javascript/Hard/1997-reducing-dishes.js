/**
 * @param {number[]} satisfaction
 * @return {number}
 */
var maxSatisfaction = function(satisfaction) {
    satisfaction.sort((a, b) => a - b);

    let maxLikeTimeCoefficient = 0;
    let currentLikeTimeCoefficient = 0;
    let totalSatisfaction = 0;

    for (let i = satisfaction.length - 1; i >= 0; i--) {
        totalSatisfaction += satisfaction[i];
        currentLikeTimeCoefficient += totalSatisfaction;

        maxLikeTimeCoefficient = Math.max(maxLikeTimeCoefficient, currentLikeTimeCoefficient);
    }

    return maxLikeTimeCoefficient;
};
