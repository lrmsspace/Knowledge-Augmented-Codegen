var countBalancedPermutations = function(num) {
    const mod = 1e9 + 7;
    const count = Array(10).fill(0);
    for (const digit of num) {
        count[digit]++;
    }
    
    const evenCount = Math.ceil(num.length / 2);
    const oddCount = Math.floor(num.length / 2);
    
    let result = 0;
    for (let digit = 0; digit <= 9; digit++) {
        if (count[digit] > 0) {
            count[digit]--;
            result = (result + countPermutations(count, evenCount - 1, oddCount)) % mod;
            count[digit]++;
        }
    }
    
    return result;
};
