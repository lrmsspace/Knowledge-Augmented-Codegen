/**
 * @param {number[]} nums
 * @return {boolean}
 */
var increasingTriplet = function(nums) {
	// We can maintain two variables, `first` and `second`, to track the smallest and second smallest values found so far in the array. 
	// As we iterate through the array, we update these variables accordingly. If we find a number greater than both `first` and `second`, it means we have found an increasing triplet subsequence.
	let first = Infinity;
	let second = Infinity;

	for (const num of nums) {
		if (num <= first) {
			first = num; // Update first if num is smaller than or equal to first
		} else if (num <= second) {
			second = num; // Update second if num is smaller than or equal to second but greater than first
		} else {
			return true; // Found a number greater than both first and second
		}
	}

	return false; // No increasing triplet subsequence found
};
