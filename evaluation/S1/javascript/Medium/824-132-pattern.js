/**
 * @param {number[]} nums
 * @return {boolean}
 */
var find132pattern = function(nums) {
	let maxK = -Infinity;
	const stack = [];

	for (let i = nums.length - 1; i >= 0; i--) {
		if (nums[i] < maxK) {
			return true;
		}
		
		while (stack.length > 0 && stack[stack.length - 1] < nums[i]) {
			maxK = Math.max(maxK, stack.pop());
		}
		
		stack.push(nums[i]);
	}

	return false;
};
