/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 */
var mostCompetitive = function(nums, k) {
	const n = nums.length;
	const result = new Array(k);
	const stack = [];
	for (let i = 0; i < n; i++) {
		while (stack.length > 0 && nums[i] < stack[stack.length - 1] && stack.length - 1 + n - i >= k) {
			stack.pop();
		}
		if (stack.length < k) {
			stack.push(nums[i]);
		}
	}
	for (let i = 0; i < k; i++) {
		result[i] = stack[i];
	}
	return result;
};
