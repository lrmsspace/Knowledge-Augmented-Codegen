/**
 * @param {number} n
 * @param {number} k
 * @param {number} target
 * @return {number}
 */
var numRollsToTarget = function(n, k, target) {
	const MOD = 1e9 + 7;
	const dp = Array.from({ length: n + 1 }, () => Array(target + 1).fill(0));
	dp[0][0] = 1; // Base case: 1 way to achieve sum of 0 with 0 dice

	for (let dice = 1; dice <= n; dice++) {
		for (let sum = dice; sum <= target; sum++) {
			for (let face = 1; face <= k; face++) {
				if (face > sum) break; // No need to consider faces greater than the current sum
				dp[dice][sum] = (dp[dice][sum] + dp[dice - 1][sum - face]) % MOD;
			}
		}
	}

	return dp[n][target];
};
