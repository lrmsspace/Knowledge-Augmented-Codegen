/**
 * @param {number[][]} grid
 * @return {number}
 */
var equalPairs = function(grid) {
    const n = grid.length;
    const rowMap = new Map();
    let count = 0;
    
    // Store rows in a map with their string representation as the key
    for (const row of grid) {
        const key = row.join(',');
        rowMap.set(key, (rowMap.get(key) || 0) + 1);
    }
    
    // Check columns against the map of rows
    for (let col = 0; col < n; col++) {
        const columnArray = [];
        for (let row = 0; row < n; row++) {
            columnArray.push(grid[row][col]);
        }
        const key = columnArray.join(',');
        
        if (rowMap.has(key)) {
            count += rowMap.get(key);
        }
    }
    
    return count;
};
