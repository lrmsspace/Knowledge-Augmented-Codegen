/**
 * @param {character[]} tasks
 * @param {number} n
 * @return {number}
 */
var leastInterval = function(tasks, n) {
	const taskCounts = new Map();
	for (const task of tasks) {
		taskCounts.set(task, (taskCounts.get(task) || 0) + 1);
	}

	const counts = Array.from(taskCounts.values());
	const maxCount = Math.max(...counts);
	let maxCountTasks = counts.filter(count => count === maxCount).length;

	// The formula to calculate the minimum intervals is:
	// (maxCount - 1) * (n + 1) + maxCountTasks
	// This accounts for the most frequent tasks and the idle time needed between them.
	return Math.max(tasks.length, (maxCount - 1) * (n + 1) + maxCountTasks);
};
