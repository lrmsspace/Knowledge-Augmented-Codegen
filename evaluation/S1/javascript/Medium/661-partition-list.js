/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @param {number} x
 * @return {ListNode}
 */
var partition = function(head, x) {
    const dummyLess = new ListNode(0);
    const dummyGreater = new ListNode(0);
    let less = dummyLess;
    let greater = dummyGreater;
    let current = head;

    while (current) {
        if (current.val < x) {
            less.next = current;
            less = less.next;
        } else {
            greater.next = current;
            greater = greater.next;
        }
        current = current.next;
    }

    // Connect the two partitions and terminate the greater partition
    greater.next = null; // Important to avoid cycle
    less.next = dummyGreater.next;

    return dummyLess.next;
};
