/**
 * @param {number[][]} mat
 * @return {number}
 */
var numSubmat = function(mat) {
	const m = mat.length;
	const n = mat[0].length;
	let count = 0;

	for (let i = 0; i < m; i++) {
		for (let j = 0; j < n; j++) {
            if (mat[i][j] === 1) {
                let width = Infinity;
                for (let k = i; k < m; k++) {
                    width = Math.min(width, mat[k][j]);
                    if (width === 0) break;
                    count += width;
                }
            }
        }
    }
    return count;
};
