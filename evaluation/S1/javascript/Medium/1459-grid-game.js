/**
 * @param {number[][]} grid
 * @return {number}
 */
var gridGame = function(grid) {
    const n = grid[0].length;

    const topPrefixSum = new Array(n + 1).fill(0);
    const bottomPrefixSum = new Array(n + 1).fill(0);

    for (let i = 0; i < n; i++) {
        topPrefixSum[i + 1] = topPrefixSum[i] + grid[0][i];
        bottomPrefixSum[i + 1] = bottomPrefixSum[i] + grid[1][i];
    }

    let result = Infinity;

    for (let col = 0; col < n; col++) {
        const topRemaining = topPrefixSum[n] - topPrefixSum[col + 1];
        const bottomCollected = bottomPrefixSum[col];
        const secondRobotPoints = Math.max(topRemaining, bottomCollected);
        result = Math.min(result, secondRobotPoints);
    }

    return result;
};
