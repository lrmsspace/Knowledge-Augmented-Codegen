/**
 * @param {number[]} nums
 * @return {number}
 */
var deleteAndEarn = function(nums) {
	const maxNum = Math.max(...nums);
	const points = new Array(maxNum + 1).fill(0);

	// Calculate total points for each number
	for (const num of nums) {
		points[num] += num;
	}

	let earn1 = 0; // Max points without taking the previous number
	let earn2 = 0; // Max points including the previous number

	for (let i = 0; i <= maxNum; i++) {
		const currentEarn = Math.max(earn1 + points[i], earn2); // Choose to take or skip the current number
		earn1 = earn2; // Update earn1 to the previous earn2
		earn2 = currentEarn; // Update earn2 to the current maximum
	}

	return earn2; // The maximum points earned
};
