/**
 * @param {string} text
 * @return {number}
 */
var maxRepOpt1 = function(text) {
	const count = {};
	for (const c of text) {
		count[c] = (count[c] || 0) + 1;
	}

	let maxLen = 0;
	let i = 0;

	while (i < text.length) {
		const c = text[i];
		let j = i + 1;

		while (j < text.length && text[j] === c) {
			j++;
		}

		const currentLen = j - i;
		const remainingCount = count[c] - currentLen;
		maxLen = Math.max(maxLen, Math.min(currentLen + remainingCount, count[c]));

		i = j;
	}

	return maxLen;
};
