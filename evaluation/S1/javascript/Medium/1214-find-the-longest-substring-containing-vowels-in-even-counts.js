/**
 * @param {string} s
 * @return {number}
 */
var findTheLongestSubstring = function(s) {
	const vowelIndex = {
		a: 0,
		e: 1,
		i: 2,
		o: 3,
		u: 4
	};

	const seen = new Map();
	let state = 0;
	let maxLength = 0;

	seen.set(state, -1);

	for (let i = 0; i < s.length; i++) {
		const char = s[i];
		if (vowelIndex.hasOwnProperty(char)) {
			state ^= (1 << vowelIndex[char]);
		}

		if (seen.has(state)) {
			maxLength = Math.max(maxLength, i - seen.get(state));
		} else {
			seen.set(state, i);
		}
	}

	return maxLength;
};
