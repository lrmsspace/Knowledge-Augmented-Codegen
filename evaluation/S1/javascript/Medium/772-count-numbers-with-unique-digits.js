/**
 * @param {number} n
 * @return {number}
 */
var countNumbersWithUniqueDigits = function(n) {
	// For n = 0, we have only one number: 0.
	if (n === 0) return 1;

	// For n > 10, the count is the same as for n = 10, since we can only use digits 0-9.
	if (n > 10) n = 10;

	let count = 10; // Start with the count for n = 1 (digits 0-9)
	let availableDigits = 9; // Digits available for selection (1-9 for the first digit, then 0-9 for others)

	for (let i = 2; i <= n; i++) {
		count += availableDigits * (availableDigits - 1); // Choose a digit for the current position and multiply by remaining choices
		availableDigits--; // Decrease the available digits for the next iteration
	}

	return count;
};
