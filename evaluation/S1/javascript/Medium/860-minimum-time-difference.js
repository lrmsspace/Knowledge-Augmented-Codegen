/**
 * @param {string[]} timePoints
 * @return {number}
 */
var findMinDifference = function(timePoints) {
	// Convert time points to minutes and sort them
	const minutes = timePoints.map(time => {
		const [hh, mm] = time.split(':').map(Number);
		return hh * 60 + mm;
	}).sort((a, b) => a - b);

	let minDiff = 1440; // Maximum possible difference (24 hours * 60 minutes)

	// Calculate the difference between adjacent time points
	for (let i = 1; i < minutes.length; i++) {
		minDiff = Math.min(minDiff, minutes[i] - minutes[i - 1]);
	}

	// Also check the difference between the last and first time point across midnight
	minDiff = Math.min(minDiff, (minutes[0] + 1440) - minutes[minutes.length - 1]);

	return minDiff;
};
