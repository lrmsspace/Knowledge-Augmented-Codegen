/**
 * @param {number[]} nums
 * @return {number}
 */
var maxSubArray = function(nums) {
    let globalMax = -Infinity;
    let currentMax = 0;

    for (const num of nums) {
        currentMax += num;

        if (currentMax > globalMax) {
            globalMax = currentMax;
        }

        if (currentMax < 0) {
            currentMax = 0;
        }
    }

    return globalMax;
};
