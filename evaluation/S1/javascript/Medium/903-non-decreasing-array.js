/**
 * @param {number[]} nums
 * @return {boolean}
 */
var checkPossibility = function(nums) {
	let modified = false;

	for (let i = 0; i < nums.length - 1; i++) {
		if (nums[i] > nums[i + 1]) {
			if (modified) {
				return false;
			}
			modified = true;

			// Try modifying nums[i] or nums[i + 1]
			if (i > 0 && nums[i - 1] > nums[i + 1]) {
				nums[i + 1] = nums[i];
			} else {
				nums[i] = nums[i + 1];
			}
		}
	}

	return true;
};
