/**
 * @param {string[]} strs
 * @param {number} m
 * @param {number} n
 * @return {number}
 */
var findMaxForm = function(strs, m, n) {
     const dp = Array.from({ length: m + 1 }, () => new Array(n + 1).fill(0));

    for (const str of strs) {
        const zeros = str.split('0').length - 1; // Count of '0's in the string
        const ones = str.split('1').length - 1; // Count of '1's in the string

        // Update the dp array from bottom-right to top-left to avoid overwriting results we still need to use
        for (let i = m; i >= zeros; i--) {
            for (let j = n; j >= ones; j--) {
                dp[i][j] = Math.max(dp[i][j], dp[i - zeros][j - ones] + 1);
            }
        }
    }

    return dp[m][n];
};
