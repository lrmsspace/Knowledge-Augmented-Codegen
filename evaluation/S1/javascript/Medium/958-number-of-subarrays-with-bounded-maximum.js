/**
 * @param {number[]} nums
 * @param {number} left
 * @param {number} right
 * @return {number}
 */
var numSubarrayBoundedMax = function(nums, left, right) {
	let count = 0;
	let lastInvalidIndex = -1;
	let lastValidIndex = -1;

	for (let i = 0; i < nums.length; i++) {
		if (nums[i] > right) {
			lastInvalidIndex = i;
		} else if (nums[i] >= left) {
			lastValidIndex = i;
		}

		count += Math.max(0, lastValidIndex - lastInvalidIndex);
	}

	return count;
};
