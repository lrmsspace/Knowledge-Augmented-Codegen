/**
 * @param {number[]} ranks
 * @param {number} cars
 * @return {number}
 */
var repairCars = function(ranks, cars) {
    let low = 0;
    let high = Math.max(...ranks) * cars * cars; // upper bound on time
    
    while (low < high) {
        const mid = Math.floor((low + high) / 2);
        let repaired = 0;
        
        for (const r of ranks) {
            repaired += Math.floor(Math.sqrt(mid / r));
            if (repaired >= cars) break; // early stop if we can already repair enough cars
        }
        
        if (repaired >= cars) {
            high = mid; // try to find a smaller time
        } else {
            low = mid + 1; // need more time
        }
    }
    
    return low;
};
