/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {number} n
 * @return {TreeNode[]}
 */
var allPossibleFBT = function(n) {
	if (n % 2 === 0) return [];

	const memo = new Map();

	const generateFBT = (nodes) => {
		if (memo.has(nodes)) return memo.get(nodes);
		if (nodes === 1) return [new TreeNode(0)];

		const result = [];
		for (let leftNodes = 1; leftNodes < nodes; leftNodes += 2) {
			const rightNodes = nodes - 1 - leftNodes;
			const leftTrees = generateFBT(leftNodes);
			const rightTrees = generateFBT(rightNodes);

			for (const left of leftTrees) {
				for (const right of rightTrees) {
					result.push(new TreeNode(0, left, right));
				}
			}
		}

		memo.set(nodes, result);
		return result;
	};

	return generateFBT(n);
};
