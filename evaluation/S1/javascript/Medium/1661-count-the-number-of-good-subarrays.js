/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var countGood = function(nums, k) {
    let count = 0;
    const freq = new Map();
    
    for (const num of nums) {
        const occurrences = freq.get(num) || 0;
        count += occurrences;
        freq.set(num, occurrences + 1);
    }
    
    let result = 0;
    let left = 0;
    
    for (let right = 0; right < nums.length; right++) {
        while (count >= k) {
            result += nums.length - right;
            const leftNum = nums[left];
            const occurrences = freq.get(leftNum);
            count -= occurrences - 1;
            freq.set(leftNum, occurrences - 1);
            left++;
        }
    }
    
    return result;
};
