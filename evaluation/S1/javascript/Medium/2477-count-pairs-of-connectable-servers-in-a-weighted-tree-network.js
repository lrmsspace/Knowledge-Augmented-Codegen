/**
 * @param {number[][]} edges
 * @param {number} signalSpeed
 * @return {number[]}
 */
var countPairsOfConnectableServers = function(edges, signalSpeed) {
    const n = edges.length + 1;
    const graph = Array.from({ length: n }, () => []);
    
    // Build the graph from edges
    for (const [u, v, weight] of edges) {
        graph[u].push([v, weight]);
        graph[v].push([u, weight]);
    }
    
    const count = new Array(n).fill(0);
    
    // DFS function to calculate distances and count pairs
    function dfs(node, parent, distance) {
        let pairCount = 0;
        
        for (const [neighbor, weight] of graph[node]) {
            if (neighbor === parent) continue;
            const newDistance = distance + weight;
            pairCount += dfs(neighbor, node, newDistance);
        }
        
        // Check if the distance from this node is divisible by signalSpeed
        if (distance % signalSpeed === 0) {
            pairCount += 1; // Count this server as a connectable pair
        }
        
        count[node] += pairCount; // Update the count for this server
        return pairCount; // Return the count to the parent call
    }
    
    dfs(0, -1, 0); // Start DFS from an arbitrary root (e.g., server 0)
    
    return count;
};
