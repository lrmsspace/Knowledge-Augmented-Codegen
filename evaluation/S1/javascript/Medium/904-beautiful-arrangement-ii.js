/**
 * @param {number} n
 * @param {number} k
 * @return {number[]}
 */
var constructArray = function(n, k) {
	const result = [];
	for (let i = 1; i <= n - k; i++) {
		result.push(i);
	}

	let left = n - k;
	let right = n;

	while (left < right) {
		result.push(left++);
		if (left < right) {
			result.push(right--);
		}
	}

	return result;
};
