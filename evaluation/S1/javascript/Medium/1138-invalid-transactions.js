/**
 * @param {string[]} transactions
 * @return {string[]}
 */
var invalidTransactions = function(transactions) {
	const n = transactions.length;
	const trans = new Array(n);
	for (let i = 0; i < n; i++) {
		const parts = transactions[i].split(',');
		trans[i] = {
			name: parts[0],
			time: Number(parts[1]),
			amount: Number(parts[2]),
			city: parts[3],
			orig: transactions[i],
			idx: i
		};
	}

	const invalid = new Array(n).fill(false);

	// Mark transactions with amount > 1000 as invalid
	for (let i = 0; i < n; i++) {
		if (trans[i].amount > 1000) invalid[i] = true;
	}

	// Group by name and check pairwise time/city condition
	const byName = new Map();
	for (let i = 0; i < n; i++) {
		const name = trans[i].name;
		if (!byName.has(name)) byName.set(name, []);
		byName.get(name).push(i);
	}

	for (const idxs of byName.values()) {
		const m = idxs.length;
		if (m <= 1) continue;
		for (let a = 0; a < m; a++) {
			for (let b = a + 1; b < m; b++) {
				const i = idxs[a], j = idxs[b];
				if (Math.abs(trans[i].time - trans[j].time) <= 60 && trans[i].city !== trans[j].city) {
					invalid[i] = true;
					invalid[j] = true;
				}
			}
		}
	}

	const res = [];
	for (let i = 0; i < n; i++) if (invalid[i]) res.push(trans[i].orig);
	return res;
};
