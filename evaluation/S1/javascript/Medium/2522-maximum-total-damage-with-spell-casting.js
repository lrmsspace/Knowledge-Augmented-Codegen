/**
 * @param {number[]} power
 * @return {number}
 */
var maximumTotalDamage = function(power) {
    const count = new Map();
    for (const p of power) {
        count.set(p, (count.get(p) || 0) + 1);
    }

    const sortedPowers = Array.from(count.keys()).sort((a, b) => b - a);
    let totalDamage = 0;

    for (const p of sortedPowers) {
        if (count.has(p)) {
            const occurrences = count.get(p);
            totalDamage += occurrences * p;

            // Remove conflicting powers
            count.delete(p - 2);
            count.delete(p - 1);
            count.delete(p + 1);
            count.delete(p + 2);
        }
    }

    return totalDamage;
};
