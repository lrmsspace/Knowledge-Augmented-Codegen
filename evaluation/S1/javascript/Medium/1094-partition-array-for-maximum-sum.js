/**
 * @param {number[]} arr
 * @param {number} k
 * @return {number}
 */
var maxSumAfterPartitioning = function(arr, k) {
	const n = arr.length;
	const dp = new Array(n).fill(0);

	for (let i = 0; i < n; i++) {
		let maxInPartition = arr[i];

		for (let j = i; j >= Math.max(0, i - k + 1); j--) {
			maxInPartition = Math.max(maxInPartition, arr[j]);
			const partitionSize = i - j + 1;
			const partitionSum = maxInPartition * partitionSize;
			const totalSum = partitionSum + (j > 0 ? dp[j - 1] : 0);
			dp[i] = Math.max(dp[i], totalSum);
		}
	}

	return dp[n - 1];
};
