/**
 * @param {number} amount
 * @param {number[]} coins
 * @return {number}
 */
var change = function(amount, coins) {
    // We can use a 1D DP array where dp[i] represents the number of combinations to make up amount i. 
	// We initialize the dp array with 0 and set dp[0] to 1 since there is one way to make up amount 0 (using no coins).
	const dp = new Array(amount + 1).fill(0);
	dp[0] = 1;

	// For each coin, we iterate through the amounts from coin value to the target amount and update the dp array. 
	// For each amount i, we add the number of combinations that make up amount i - coin (i.e., dp[i - coin]) to dp[i].
	for (const coin of coins) {
        for (let i = coin; i <= amount; i++) {
            dp[i] += dp[i - coin];
        }
    }

	return dp[amount];
};
