/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var maximumLength = function(nums, k) {
    let maxLength = 1; // At least one element is a valid subsequence
    for (let i = 1; i < nums.length; i++) {
        if ((nums[i - 1] + nums[i]) % k === (nums[i - 2] + nums[i - 1]) % k) {
            maxLength++;
        } else {
            maxLength = 2; // Reset to at least two elements for a new valid subsequence
        }
    }
    return maxLength;
};
