/**
 * @param {number[]} arr
 * @return {number}
 */
var sumSubarrayMins = function(arr) {
	const stack = [];
	const mod = 1e9 + 7;
	let sum = 0;

	for (let i = 0; i <= arr.length; i++) {
		while (stack.length && (i === arr.length || arr[stack[stack.length - 1]] > arr[i])) {
			const j = stack.pop();
			const k = stack.length ? stack[stack.length - 1] : -1;
			sum += arr[j] * (i - j) * (j - k);
		}

		stack.push(i);
	}

	return sum % mod;
};
