/**
 * @param {string} s
 * @return {number}
 */
var longestPalindromeSubseq = function(s) {
    const n = s.length;
    const dp = new Array(n).fill(0).map(() => new Array(n).fill(0));

    for (let i = n - 1; i >= 0; i--) {
        dp[i][i] = 1; // A single character is a palindrome of length 1
        for (let j = i + 1; j < n; j++) {
            if (s[i] === s[j]) {
                dp[i][j] = dp[i + 1][j - 1] + 2; // If characters match, extend the palindrome by 2
            } else {
                dp[i][j] = Math.max(dp[i + 1][j], dp[i][j - 1]); // Otherwise, take the maximum by excluding one character
            }
        }
    }

    return dp[0][n - 1]; // The length of the longest palindromic subsequence is in the top-right cell
};
