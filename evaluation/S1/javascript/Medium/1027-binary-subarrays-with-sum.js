/**
 * @param {number[]} nums
 * @param {number} goal
 * @return {number}
 */
var numSubarraysWithSum = function(nums, goal) {
	const prefixSumCount = new Map();
	prefixSumCount.set(0, 1); // Base case: one way to have a prefix sum of 0 (empty subarray)

	let currentPrefixSum = 0;
	let count = 0;

	for (const num of nums) {
        currentPrefixSum += num;
        const complement = currentPrefixSum - goal;

        if (prefixSumCount.has(complement)) {
            count += prefixSumCount.get(complement);
        }
        prefixSumCount.set(currentPrefixSum, (prefixSumCount.get(currentPrefixSum) || 0) + 1);
    }

    return count;   
};
