/**
 * @param {number[]} nums
 * @return {number}
 */
var minMoves2 = function(nums) {
      const n = nums.length;
    const mid = Math.floor((n - 1) / 2);
    const median = quickselect(nums, mid);
    
    let moves = 0;
    for (const num of nums) {
        moves += Math.abs(num - median);
    }
    
    return moves;
};
