/**
 * @param {number[]} nums
 * @param {number} indexDifference
 * @param {number} valueDifference
 * @return {number[]}
 */
var findIndices = function(nums, indexDifference, valueDifference) {
    const n = nums.length;
    const sortedIndices = Array.from({ length: n }, (_, i) => i).sort((a, b) => nums[a] - nums[b]);

    let left = 0;
    for (let right = 1; right < n; right++) {
        while (left < right && nums[sortedIndices[right]] - nums[sortedIndices[left]] >= valueDifference) {
            if (Math.abs(sortedIndices[right] - sortedIndices[left]) >= indexDifference) {
                return [sortedIndices[left], sortedIndices[right]];
            }
            left++;
        }
    }

    return [-1, -1];
};
