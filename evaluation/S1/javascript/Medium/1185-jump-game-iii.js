/**
 * @param {number[]} arr
 * @param {number} start
 * @return {boolean}
 */
var canReach = function(arr, start) {
	const visited = new Array(arr.length).fill(false);
	const queue = [start];
	visited[start] = true;

	while (queue.length) {
		const index = queue.shift();

		if (arr[index] === 0) {
			return true;
		}

		const nextIndices = [index + arr[index], index - arr[index]];

		for (const next of nextIndices) {
			if (next >= 0 && next < arr.length && !visited[next]) {
				visited[next] = true;
				queue.push(next);
			}
		}
	}

	return false;
};
