/**
 * @param {number[]} encoded
 * @return {number[]}
 */
var decode = function(encoded) {
	const n = encoded.length + 1;
	let totalXOR = 0; // This will hold perm[1] XOR perm[2] XOR ... XOR perm[n]
	for (let i = 1; i <= n; i++) {
		totalXOR ^= i; // XOR of all numbers from 1 to n
	}

	let oddXOR = 0; // This will hold perm[2] XOR perm[4] XOR ... (all even indexed elements in encoded)
	for (let i = 1; i < encoded.length; i += 2) {
		oddXOR ^= encoded[i]; // XOR of all odd indexed elements in encoded
	}

	const perm = new Array(n);
	perm[0] = totalXOR ^ oddXOR; // This gives us perm[0]

	// Now we can reconstruct the entire perm array
	for (let i = 1; i < n; i++) {
		perm[i] = perm[i - 1] ^ encoded[i - 1];
	}

	return perm;
};
