/**
 * @param {string} s
 * @return {string}
 */
var removeStars = function(s) {
    const stack = [];
    
    for (const char of s) {
        if (char === '*') {
            stack.pop(); // Remove the closest non-star character to the left
        } else {
            stack.push(char); // Add non-star characters to the stack
        }
    }
    
    return stack.join(''); // Join the remaining characters to form the final string
};
