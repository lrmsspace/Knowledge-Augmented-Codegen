/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var numSubarrayProductLessThanK = function(nums, k) {
	if (k <= 1) return 0; // Since all nums[i] are positive, if k <= 1, no subarray can have a product less than k.

	let product = 1;
	let count = 0;
	let left = 0;

	for (let right = 0; right < nums.length; right++) {
		product *= nums[right];

		while (product >= k) {
			product /= nums[left];
			left++;
		}

		count += right - left + 1; // All subarrays ending at 'right' and starting from 'left' to 'right' are valid.
	}

	return count;
};
