/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {ListNode} head
 * @return {TreeNode}
 */
var sortedListToBST = function(head) {
    if (!head) {
        return null;
    }

    if (!head.next) {
        return new TreeNode(head.val);
    }

    // Find the middle node using the slow and fast pointer technique
    let slow = head;
    let fast = head;
    let prev = null;

    while (fast && fast.next) {
        prev = slow;
        slow = slow.next;
        fast = fast.next.next;
    }

    // Disconnect the left half from the middle node
    if (prev) {
        prev.next = null;
    }

    // The middle node becomes the root of the BST
    const root = new TreeNode(slow.val);

    // Recursively build the left subtree from the left half of the list
    root.left = sortedListToBST(head);

    // Recursively build the right subtree from the right half of the list
    root.right = sortedListToBST(slow.next);

    return root;
};
