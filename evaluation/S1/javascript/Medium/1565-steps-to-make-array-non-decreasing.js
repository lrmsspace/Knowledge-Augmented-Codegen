/**
 * @param {number[]} nums
 * @return {number}
 */
var totalSteps = function(nums) {
    const n = nums.length;
    const stack = [];
    const steps = new Array(n).fill(0);
    let maxSteps = 0;
    
    for (let i = 0; i < n; i++) {
        let currentSteps = 0;
        
        while (stack.length > 0 && nums[stack[stack.length - 1]] <= nums[i]) {
            currentSteps = Math.max(currentSteps, steps[stack.pop()]);
        }
        
        if (stack.length > 0) {
            steps[i] = currentSteps + 1;
            maxSteps = Math.max(maxSteps, steps[i]);
        }
        
        stack.push(i);
    }
    
    return maxSteps;
};
