/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @param {number} startValue
 * @param {number} destValue
 * @return {string}
 */
var getDirections = function(root, startValue, destValue) {
    let lca = null;

    const findLCA = (node) => {
        if (!node) return false;

        const left = findLCA(node.left) ? 1 : 0;
        const right = findLCA(node.right) ? 1 : 0;
        const mid = (node.val === startValue || node.val === destValue) ? 1 : 0;

        if (left + right + mid >= 2) {
            lca = node;
        }

        return (left + right + mid) > 0;
    };

    findLCA(root);

    const pathToStart = [];
    const pathToDest = [];

    const findPath = (node, target, path) => {
        if (!node) return false;
        if (node.val === target) return true;

        path.push('L');
        if (findPath(node.left, target, path)) return true;
        path.pop();

        path.push('R');
        if (findPath(node.right, target, path)) return true;
        path.pop();

        return false;
    };

    findPath(lca, startValue, pathToStart);
    findPath(lca, destValue, pathToDest);

    const upMoves = 'U'.repeat(pathToStart.length);
    const downMoves = pathToDest.join('');

    return upMoves + downMoves;
};
