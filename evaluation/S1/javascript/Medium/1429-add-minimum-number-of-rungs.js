/**
 * @param {number[]} rungs
 * @param {number} dist
 * @return {number}
 */
var addRungs = function(rungs, dist) {
    let prev = 0;
    let addedRungs = 0;

    for (const rung of rungs) {
        const gap = rung - prev;
        if (gap > dist) {
            addedRungs += Math.floor((gap - 1) / dist);
        }
        prev = rung;
    }

    return addedRungs;
};
