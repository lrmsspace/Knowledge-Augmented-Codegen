/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @param {number} k
 * @return {number}
 */
var numberOfPairs = function(nums1, nums2, k) {
    const freq = new Map();
    let count = 0;

    for (const num of nums2) {
        const target = num * k;
        freq.set(target, (freq.get(target) || 0) + 1);
    }

    for (const num of nums1) {
        for (const [target, freqCount] of freq.entries()) {
            if (num % target === 0) {
                count += freqCount;
            }
        }
    }

    return count;
};
