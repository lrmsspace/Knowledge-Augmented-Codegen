/**
 * @param {number[]} arr
 * @return {number}
 */
var numOfSubarrays = function(arr) {
	const MOD = 1e9 + 7;
	let oddCount = 0;
	let evenCount = 1; // There's one even prefix sum (the empty subarray)
	let prefixSum = 0;
	let result = 0;

	for (const num of arr) {
		prefixSum += num;

		if (prefixSum % 2 === 0) {
			result = (result + oddCount) % MOD;
			evenCount++;
		} else {
			result = (result + evenCount) % MOD;
			oddCount++;
		}
	}

	return result;
};
