/**
 * @param {number[]} matchsticks
 * @return {boolean}
 */
var makesquare = function(matchsticks) {
    const totalLength = matchsticks.reduce((sum, len) => sum + len, 0);
    if (totalLength % 4 !== 0) return false; // Total length must be divisible by 4

    const sideLength = totalLength / 4;
    const sides = new Array(4).fill(0);

    // Sort matchsticks in descending order to optimize backtracking
    matchsticks.sort((a, b) => b - a);

    const backtrack = (index) => {
        if (index === matchsticks.length) {
            return sides.every(side => side === sideLength);
        }

        for (let i = 0; i < 4; i++) {
            if (sides[i] + matchsticks[index] <= sideLength) {
                sides[i] += matchsticks[index]; // Choose the current matchstick for side i
                if (backtrack(index + 1)) return true; // Recurse to place the next matchstick
                sides[i] -= matchsticks[index]; // Backtrack
            }
            // If this side is empty and we can't place the stick, no need to try other sides
            if (sides[i] === 0) break;
        }

        return false;
    };

    return backtrack(0);
};
