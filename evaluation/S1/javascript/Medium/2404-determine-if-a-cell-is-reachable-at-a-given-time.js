/**
 * @param {number} sx
 * @param {number} sy
 * @param {number} fx
 * @param {number} fy
 * @param {number} t
 * @return {boolean}
 */
var isReachableAtTime = function(sx, sy, fx, fy, t) {
    const dx = Math.abs(fx - sx);
    const dy = Math.abs(fy - sy);
    const manhattanDistance = dx + dy;

    // Check if the Manhattan distance is greater than t
    if (manhattanDistance > t) {
        return false;
    }

    // Check if the difference between t and the Manhattan distance is even
    return (t - manhattanDistance) % 2 === 0;
};
