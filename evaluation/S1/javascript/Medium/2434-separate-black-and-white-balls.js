/**
 * @param {string} s
 * @return {number}
 */
var minimumSteps = function(s) {
    let steps = 0;
    let countOnes = 0;

    for (const char of s) {
        if (char === '1') {
            countOnes++;
        } else {
            steps += countOnes;
        }
    }

    return steps;
};
