/**
 * @param {number[]} arr
 * @return {number}
 */
var countTriplets = function(arr) {
	const n = arr.length;
	const prefixXor = new Array(n + 1).fill(0);

	for (let i = 0; i < n; i++) {
		prefixXor[i + 1] = prefixXor[i] ^ arr[i];
	}

	let count = 0;

	for (let i = 0; i < n; i++) {
		for (let k = i + 1; k < n; k++) {
			if (prefixXor[i] === prefixXor[k + 1]) {
				count += (k - i);
			}
		}
	}

	return count;
};
