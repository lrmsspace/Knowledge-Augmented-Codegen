/**
 * @param {number[]} piles
 * @param {number} h
 * @return {number}
 */
var minEatingSpeed = function(piles, h) {
	let low = 1;
	let high = Math.max(...piles);

	while (low < high) {
		const mid = low + Math.floor((high - low) / 2);
		let hoursNeeded = 0;

		for (const pile of piles) {
			hoursNeeded += Math.ceil(pile / mid);
		}

		if (hoursNeeded > h) {
			low = mid + 1;
		} else {
			high = mid;
		}
	}

	return low;
};
