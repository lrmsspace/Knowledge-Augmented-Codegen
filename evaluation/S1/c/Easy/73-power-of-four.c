bool isPowerOfFour(int n) {
    // A number is a power of four if:
    // 1. It is greater than 0.
    // 2. It is a power of two (only one bit set in binary representation).
    // 3. The only set bit is in an even position (0-based index).
    
    // Check if n is greater than 0
    if (n <= 0) {
        return false;
    }
    
    // Check if n is a power of two
    if ((n & (n - 1)) != 0) {
        return false;
    }
    
    // Check if the only set bit is in an even position
    // The mask 0x55555555 has bits set at all even positions
    if ((n & 0x55555555) == 0) {
        return false;
    }
    
    return true;        
}
