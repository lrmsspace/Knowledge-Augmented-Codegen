/**
 * Definition for a Node.
 * struct Node {
 *     int val;
 *     int numChildren;
 *     struct Node** children;
 * };
 */

/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* preorder(struct Node* root, int* returnSize) {
    // Helper function to perform preorder traversal
    void dfs(struct Node* node, int** result, int* size, int* capacity) {
        if (!node) return;

        // Resize result array if needed
        if (*size >= *capacity) {
            *capacity *= 2;
            *result = realloc(*result, (*capacity) * sizeof(int));
        }

        // Add current node's value
        (*result)[(*size)++] = node->val;

        // Recur for each child
        for (int i = 0; i < node->numChildren; i++) {
            dfs(node->children[i], result, size, capacity);
        }
    }

    // Initialize result array and variables
    int initialCapacity = 100; // Initial capacity for the result array
    int* result = malloc(initialCapacity * sizeof(int));
    int size = 0;
    int capacity = initialCapacity;

    // Perform DFS preorder traversal
    dfs(root, &result, &size, &capacity);

    // Set return size
    *returnSize = size;

    return result;      
}
