int countCharacters(char** words, int wordsSize, char* chars) {
    int char_count[26] = {0};
    for (char* p = chars; *p != '\0'; p++) {
        char_count[*p - 'a']++;
    }

    int total_length = 0;

    for (int i = 0; i < wordsSize; i++) {
        int word_count[26] = {0};
        char* word = words[i];
        int can_form = 1;

        for (char* p = word; *p != '\0'; p++) {
            word_count[*p - 'a']++;
            if (word_count[*p - 'a'] > char_count[*p - 'a']) {
                can_form = 0;
                break;
            }
        }

        if (can_form) {
            total_length += strlen(word);
        }
    }

    return total_length;        
}
