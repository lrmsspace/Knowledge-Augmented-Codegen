bool checkIfExist(int* arr, int arrSize) {
    bool seen[2001] = { false }; // To cover range -1000 to 1000

    for (int i = 0; i < arrSize; i++) {
        int val = arr[i];
        if (val % 2 == 0 && seen[val / 2 + 1000]) {
            return true;
        }
        if (val * 2 <= 1000 && seen[val * 2 + 1000]) {
            return true;
        }
        seen[val + 1000] = true;
    }

    return false;       
}
