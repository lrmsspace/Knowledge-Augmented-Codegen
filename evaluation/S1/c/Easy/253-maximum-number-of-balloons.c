int maxNumberOfBalloons(char* text) {
    int count[26] = {0};
    for (char* p = text; *p; ++p) {
        count[*p - 'a']++;
    }
    int res = count['b' - 'a'];
    res = res < count['a' - 'a'] ? res : count['a' - 'a'];
    res = res < count['l' - 'a'] / 2 ? res : count['l' - 'a'] / 2;
    res = res < count['o' - 'a'] / 2 ? res : count['o' - 'a'] / 2;
    res = res < count['n' - 'a'] ? res : count['n' - 'a'];
    return res;     
}
