char* decodeMessage(char* key, char* message) {
    char map[26];
    int mapped[26] = {0};
    char currentChar = 'a';
    for (int i = 0; key[i] != '\0'; i++) {
        if (key[i] != ' ') {
            int index = key[i] - 'a';
            if (!mapped[index]) {
                mapped[index] = 1;
                map[index] = currentChar;
                currentChar++;
            }
        }
    }

    for (int i = 0; message[i] != '\0'; i++) {
        if (message[i] != ' ') {
            int index = message[i] - 'a';
            message[i] = map[index];
        }
    }

    return message;     
}
