bool uniqueOccurrences(int* arr, int arrSize) {
    int count[2001] = {0};  // To store occurrences of each number from -1000 to 1000
    for (int i = 0; i < arrSize; i++) {
        count[arr[i] + 1000]++;  // Shift index by 1000 to handle negative numbers
    }

    int freq[1001] = {0};  // To store frequency of occurrences
    for (int i = 0; i < 2001; i++) {
        if (count[i] > 0) {
            freq[count[i]]++;
            if (freq[count[i]] > 1) {
                return false;  // If any occurrence count appears more than once
            }
        }
    }

    return true;  // All occurrence counts are unique       
}
