// Given the two positive integers n and time, return the index of the person holding the pillow after time seconds.
//
// Example:
// Input: n = 4, time = 5
// Output: 2
// Explanation: People pass the pillow in the following way: 1 -> 2 -> 3 -> 4 -> 3 -> 2.
