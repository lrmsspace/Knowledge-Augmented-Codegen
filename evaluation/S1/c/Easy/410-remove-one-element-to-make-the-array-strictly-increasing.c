bool canBeIncreasing(int* nums, int numsSize) {
    int removeCount = 0;
    int prev = nums[0];

    for (int i = 1; i < numsSize; i++) {
        if (nums[i] <= prev) {
            removeCount++;
            if (removeCount > 1) {
                return false;
            }
            if (i == 1 || nums[i] > nums[i - 2]) {
                prev = nums[i];
            }
        } else {
            prev = nums[i];
        }
    }

    return true;        
}
