bool rotateString(char* s, char* goal) {
    int len_s = 0, len_goal = 0;
    while (s[len_s] != '\0') len_s++;
    while (goal[len_goal] != '\0') len_goal++;
    if (len_s != len_goal) return false;

    // Check if goal is a substring of s concatenated with itself
    for (int i = 0; i < len_s; i++) {
        bool match = true;
        for (int j = 0; j < len_goal; j++) {
            if (s[(i + j) % len_s] != goal[j]) {
                match = false;
                break;
            }
        }
        if (match) return true;
    }
    return false;       
}
