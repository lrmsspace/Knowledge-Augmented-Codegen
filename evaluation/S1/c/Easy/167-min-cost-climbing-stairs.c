int minCostClimbingStairs(int* cost, int costSize) {
    int first = cost[0];
    int second = cost[1];
    for (int i = 2; i < costSize; i++) {
        int current = cost[i] + (first < second ? first : second);
        first = second;
        second = current;
    }
    return first < second ? first : second;     
}
