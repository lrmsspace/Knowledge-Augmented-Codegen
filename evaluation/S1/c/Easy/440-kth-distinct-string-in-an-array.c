char* kthDistinct(char** arr, int arrSize, int k) {
    // Frequency array to count occurrences of each string
    int freq[1000] = {0};
    
    // First pass: Count frequencies of each string
    for (int i = 0; i < arrSize; i++) {
        freq[i] = 0;
        for (int j = 0; j < arrSize; j++) {
            if (strcmp(arr[i], arr[j]) == 0) {
                freq[i]++;
            }
        }
    }
    
    // Second pass: Find the k-th distinct string
    for (int i = 0; i < arrSize; i++) {
        if (freq[i] == 1) {
            k--;
            if (k == 0) {
                return arr[i];
            }
        }
    }
    
    // If fewer than k distinct strings, return empty string
    return "";      
}
