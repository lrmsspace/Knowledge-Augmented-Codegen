int prefixCount(char** words, int wordsSize, char* pref) {
    int count = 0;
    int prefLen = 0;
    while (pref[prefLen] != '\0') {
        prefLen++;
    }

    for (int i = 0; i < wordsSize; i++) {
        int j = 0;
        while (words[i][j] != '\0' && j < prefLen && words[i][j] == pref[j]) {
            j++;
        }
        if (j == prefLen) {
            count++;
        }
    }

    return count;       
}
