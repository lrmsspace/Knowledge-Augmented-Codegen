int countBalls(int lowLimit, int highLimit) {
    int boxCounts[46] = {0}; // Maximum digit sum for 105 is 9+9+9+9+9=45
    int maxBalls = 0;

    for (int ballNumber = lowLimit; ballNumber <= highLimit; ballNumber++) {
        int sumOfDigits = 0;
        int n = ballNumber;

        // Calculate sum of digits
        while (n > 0) {
            sumOfDigits += n % 10;
            n /= 10;
        }

        boxCounts[sumOfDigits]++;
        if (boxCounts[sumOfDigits] > maxBalls) {
            maxBalls = boxCounts[sumOfDigits];
        }
    }

    return maxBalls;        
}
