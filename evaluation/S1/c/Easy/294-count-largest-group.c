int countLargestGroup(int n) {
    int digitSumCounts[37] = {0}; // Maximum digit sum for n <= 10^4 is 36 (9999 -> 9+9+9+9)
    int maxCount = 0;
    int largestGroupCount = 0;

    for (int i = 1; i <= n; i++) {
        int sum = 0;
        int num = i;
        while (num > 0) {
            sum += num % 10;
            num /= 10;
        }
        digitSumCounts[sum]++;
    }

    for (int i = 0; i < 37; i++) {
        if (digitSumCounts[i] > maxCount) {
            maxCount = digitSumCounts[i];
            largestGroupCount = 1;
        } else if (digitSumCounts[i] == maxCount && maxCount > 0) {
            largestGroupCount++;
        }
    }

    return largestGroupCount;       
}
