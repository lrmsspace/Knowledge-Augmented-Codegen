int countDigitOne(int n) {
    long long count = 0;
    long long factor = 1;
    int lower = 0, curr = 0, higher = 0;

    while (n / factor != 0) {
        lower = n - (n / factor) * factor;
        curr = (n / factor) % 10;
        higher = n / (factor * 10);

        if (curr == 0) {
            count += higher * factor;
        } else if (curr == 1) {
            count += higher * factor + lower + 1;
        } else {
            count += (higher + 1) * factor;
        }

        factor *= 10;
    }

    return (int)count;      
}
