int numberOfArithmeticSlices(int* nums, int numsSize) {
    if (numsSize < 3) {
        return 0;
    }

    int totalSlices = 0;
    int currentSliceCount = 0;

    for (int i = 2; i < numsSize; i++) {
        if (nums[i] - nums[i - 1] == nums[i - 1] - nums[i - 2]) {
            currentSliceCount++;
            totalSlices += currentSliceCount;
        } else {
            currentSliceCount = 0;
        }
    }

    return totalSlices;     
}
