/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */
/**
 * Return an array of arrays of size *returnSize.
 * The sizes of the arrays are returned as *returnColumnSizes array.
 * Note: Both returned array and *columnSizes array must be malloced, assume caller calls free().
 */
int** pathSum(struct TreeNode* root, int targetSum, int* returnSize, int** returnColumnSizes) {
    int **result = NULL;
    *returnSize = 0;
    *returnColumnSizes = NULL;
    
    if (!root) {
        return result;
    }
    
    #define MAX_PATH_LENGTH 5000
    int path[MAX_PATH_LENGTH];
    int pathLength = 0;
    
    void dfs(struct TreeNode* node, int currentSum) {
        if (!node) {
            return;
        }
        
        path[pathLength++] = node->val;
        currentSum += node->val;
        
        if (!node->left && !node->right) { // Leaf node
            if (currentSum == targetSum) {
                result = realloc(result, sizeof(int*) * (*returnSize + 1));
                (*returnColumnSizes) = realloc(*returnColumnSizes, sizeof(int) * (*returnSize + 1));
                
                result[*returnSize] = malloc(sizeof(int) * pathLength);
                memcpy(result[*returnSize], path, sizeof(int) * pathLength);
                (*returnColumnSizes)[*returnSize] = pathLength;
                (*returnSize)++;
            }
        } else {
            dfs(node->left, currentSum);
            dfs(node->right, currentSum);
        }
        
        pathLength--;
    }
    
    dfs(root, 0);
    
    return result;      
}
