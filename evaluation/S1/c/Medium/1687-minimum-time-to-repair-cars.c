long long repairCars(int* ranks, int ranksSize, int cars) {
    long long left = 1, right = 1e14, mid, totalCars, timeNeeded;
    while (left < right) {
        mid = left + (right - left) / 2;
        totalCars = 0;
        for (int i = 0; i < ranksSize; i++) {
            timeNeeded = ranks[i];
            totalCars += (long long)sqrt((double)mid / timeNeeded);
            if (totalCars >= cars) {
                break;
            }
        }
        if (totalCars >= cars) {
            right = mid;
        } else {
            left = mid + 1;
        }
    }
    return left;        
}
