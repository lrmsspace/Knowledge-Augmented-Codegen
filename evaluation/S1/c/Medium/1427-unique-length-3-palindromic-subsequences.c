int countPalindromicSubsequence(char* s) {
    int left[26] = {0}, right[26] = {0};
    for (char* p = s; *p; ++p) {
        right[*p - 'a']++;
    }

    int result = 0;
    for (char* p = s; *p; ++p) {
        int c = *p - 'a';
        right[c]--;
        for (int mid = 0; mid < 26; ++mid) {
            if (left[mid] > 0 && right[mid] > 0) {
                result++;
            }
        }
        left[c]++;
    }
    return result;      
}
