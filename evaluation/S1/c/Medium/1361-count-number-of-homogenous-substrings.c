int countHomogenous(char* s) {
    const int MOD = 1000000007;
    long long result = 0;
    int count = 1;
    int length = 0;

    while (s[length] != '\0') {
        length++;
    }

    for (int i = 1; i <= length; i++) {
        if (s[i] == s[i - 1]) {
            count++;
        } else {
            count = 1;
        }
        result = (result + count) % MOD;
    }

    return (int)result;     
}
