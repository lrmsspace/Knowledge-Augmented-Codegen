char* countAndSay(int n) {
    if (n <= 0) {
        return "";
    }

    // Start with the first term in the sequence
    char* result = (char*)malloc(2 * sizeof(char));
    result[0] = '1';
    result[1] = '\0';

    for (int term = 2; term <= n; term++) {
        int length = strlen(result);
        char* nextResult = (char*)malloc(2 * length * sizeof(char)); // Allocate enough space
        int nextIndex = 0;

        for (int i = 0; i < length; ) {
            char currentChar = result[i];
            int count = 0;

            // Count occurrences of the current character
            while (i < length && result[i] == currentChar) {
                count++;
                i++;
            }

            // Append count and character to the next result
            nextIndex += sprintf(&nextResult[nextIndex], "%d%c", count, currentChar);
        }

        free(result); // Free the previous result
        result = nextResult; // Move to the next result
    }

    return result;      
}
