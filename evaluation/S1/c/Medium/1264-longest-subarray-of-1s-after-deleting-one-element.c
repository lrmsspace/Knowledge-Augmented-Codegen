int longestSubarray(int* nums, int numsSize) {
    int left = 0, right = 0;
    int zero_count = 0;
    int max_length = 0;

    while (right < numsSize) {
        if (nums[right] == 0) {
            zero_count++;
        }

        while (zero_count > 1) {
            if (nums[left] == 0) {
                zero_count--;
            }
            left++;
        }

        max_length = (right - left + 1 - zero_count) > max_length ? (right - left + 1 - zero_count) : max_length;
        right++;
    }

    return max_length;      
}
