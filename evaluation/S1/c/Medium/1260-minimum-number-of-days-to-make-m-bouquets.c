int minDays(int* bloomDay, int bloomDaySize, int m, int k) {
    long long totalFlowers = (long long)m * k;
    if (bloomDaySize < totalFlowers) {
        return -1;
    }

    int left = 1;
    int right = 1000000000;
    int result = -1;

    while (left <= right) {
        int mid = left + (right - left) / 2;

        int bouquets = 0;
        int flowers = 0;

        for (int i = 0; i < bloomDaySize; i++) {
            if (bloomDay[i] <= mid) {
                flowers++;
                if (flowers == k) {
                    bouquets++;
                    flowers = 0;
                }
            } else {
                flowers = 0;
            }
        }

        if (bouquets >= m) {
            result = mid;
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    return result;      
}
