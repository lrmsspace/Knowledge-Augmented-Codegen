char* smallestPalindrome(char* s) {
    int count[26] = {0};
    for (char* p = s; *p; ++p) {
        count[*p - 'a']++;
    }

    int left = 0;
    char* result = (char*)malloc((strlen(s) + 1) * sizeof(char));
    char* right = result + strlen(s) - 1;

    for (int i = 0; i < 26; ++i) {
        while (count[i] > 1) {
            result[left++] = 'a' + i;
            *right-- = 'a' + i;
            count[i] -= 2;
        }
    }

    for (int i = 0; i < 26; ++i) {
        if (count[i] == 1) {
            result[left] = 'a' + i;
            break;
        }
    }

    result[strlen(s)] = '\0';
    return result;      
}
