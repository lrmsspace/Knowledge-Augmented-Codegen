int findSubstringInWraproundString(char* s) {
    int count[26] = {0};
    int n = 0;
    for (int i = 0; s[i]; i++) {
        if (i > 0 && (s[i] - s[i - 1] + 26) % 26 == 1) {
            n++;
        } else {
            n = 1;
        }
        int index = s[i] - 'a';
        if (n > count[index]) {
            count[index] = n;
        }
    }
    int result = 0;
    for (int i = 0; i < 26; i++) {
        result += count[i];
    }
    return result;      
}
