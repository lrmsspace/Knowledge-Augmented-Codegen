/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */
struct ListNode* swapPairs(struct ListNode* head) {
    if (head == NULL || head->next == NULL) {
        return head;
    }

    struct ListNode* newHead = head->next;
    struct ListNode* prevTail = NULL;
    struct ListNode* current = head;

    while (current != NULL && current->next != NULL) {
        struct ListNode* first = current;
        struct ListNode* second = current->next;
        struct ListNode* nextPair = second->next;

        // Swap the pair
        second->next = first;
        first->next = nextPair;

        // Connect the previous pair's tail to the current swapped pair's head
        if (prevTail != NULL) {
            prevTail->next = second;
        }

        // Update prevTail to the tail of the current swapped pair
        prevTail = first;

        // Move to the next pair
        current = nextPair;
    }

    return newHead;     
}
