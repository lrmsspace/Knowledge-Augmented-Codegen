int maxSubarraySumCircular(int* nums, int numsSize) {
    int total = 0;
    int maxSum = nums[0];
    int curMax = 0;
    int minSum = nums[0];
    int curMin = 0;

    for (int i = 0; i < numsSize; i++) {
        curMax = fmax(curMax + nums[i], nums[i]);
        maxSum = fmax(maxSum, curMax);
        curMin = fmin(curMin + nums[i], nums[i]);
        minSum = fmin(minSum, curMin);
        total += nums[i];
    }

    return maxSum > 0 ? fmax(maxSum, total - minSum) : maxSum;      
}
