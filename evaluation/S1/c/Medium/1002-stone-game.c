bool stoneGame(int* piles, int pilesSize) {
    // Since the number of piles is even, Alice can always choose either all the even-indexed piles or all the odd-indexed piles.
    // She can calculate the total stones in both options and choose the one with more stones.
    int evenSum = 0;
    int oddSum = 0;

    for (int i = 0; i < pilesSize; i++) {
        if (i % 2 == 0) {
            evenSum += piles[i];
        } else {
            oddSum += piles[i];
        }
    }

    // Alice wins if she can secure more stones than Bob
    return evenSum != oddSum;       
}
