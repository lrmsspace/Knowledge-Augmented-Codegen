char* kthLargestNumber(char** nums, int numsSize, int k) {
    // Helper function to compare two numeric strings
    int compare(const char* a, const char* b) {
        int lenA = 0, lenB = 0;
        while (a[lenA]) lenA++;
        while (b[lenB]) lenB++;
        
        if (lenA != lenB) {
            return lenA - lenB; // Longer string is larger
        }
        return strcmp(a, b); // Lexicographical comparison
    }

    // Simple selection sort to find the kth largest
    for (int i = 0; i < k; i++) {
        int maxIndex = i;
        for (int j = i + 1; j < numsSize; j++) {
            if (compare(nums[j], nums[maxIndex]) > 0) {
                maxIndex = j;
            }
        }
        // Swap the found maximum element with the first element
        if (maxIndex != i) {
            char* temp = nums[i];
            nums[i] = nums[maxIndex];
            nums[maxIndex] = temp;
        }
    }
    
    return nums[k - 1];     
}
