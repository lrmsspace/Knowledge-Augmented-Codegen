bool increasingTriplet(int* nums, int numsSize) {
    if (numsSize < 3) {
        return false;
    }

    int first = __INT_MAX__;
    int second = __INT_MAX__;

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] <= first) {
            first = nums[i];
        } else if (nums[i] <= second) {
            second = nums[i];
        } else {
            return true;
        }
    }

    return false;       
}
