long long maxScore(int* a, int aSize, int* b, int bSize) {
    long long dp[bSize];
    for (int i = 0; i < bSize; ++i) {
        dp[i] = (long long)a[0] * b[i];
    }

    for (int i = 1; i < aSize; ++i) {
        long long max_prev = dp[0];
        for (int j = i; j < bSize; ++j) {
            if (dp[j - 1] > max_prev) {
                max_prev = dp[j - 1];
            }
            dp[j] = max_prev + (long long)a[i] * b[j];
        }
    }

    long long result = dp[aSize - 1];
    for (int i = aSize; i < bSize; ++i) {
        if (dp[i] > result) {
            result = dp[i];
        }
    }

    return result;      
}
