int integerReplacement(int n) {
    int steps = 0;
    unsigned int num = (unsigned int)n; // Use unsigned to handle INT_MAX case

    while (num != 1) {
        if ((num & 1) == 0) {
            num >>= 1; // n is even
        } else {
            if (num == 3 || ((num & 2) == 0)) {
                num -= 1; // n is odd and either n == 3 or the second least significant bit is 0
            } else {
                num += 1; // n is odd and the second least significant bit is 1
            }
        }
        steps++;
    }

    return steps;       
}
