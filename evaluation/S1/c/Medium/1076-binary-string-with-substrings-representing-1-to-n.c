bool queryString(char* s, int n) {
    int len = 0;
    while (s[len]) len++;

    for (int i = 1; i <= n; i++) {
        int bits = 0;
        for (int x = i; x > 0; x >>= 1) bits++;
        int found = 0;

        for (int j = 0; j + bits <= len; j++) {
            int match = 1;
            for (int k = 0; k < bits; k++) {
                if (s[j + k] != ((i >> (bits - k - 1)) & 1) + '0') {
                    match = 0;
                    break;
                }
            }
            if (match) {
                found = 1;
                break;
            }
        }

        if (!found) return 0;
    }

    return 1;       
}
