int numTrees(int n) {
    // Dynamic Programming array to store the number of unique BSTs for each count of nodes
    int dp[n + 1];
    
    // Base cases
    dp[0] = 1; // An empty tree
    dp[1] = 1; // A single node tree

    // Fill the DP array for counts from 2 to n
    for (int nodes = 2; nodes <= n; nodes++) {
        dp[nodes] = 0; // Initialize the count for 'nodes' to zero
        // Consider each number as root
        for (int root = 1; root <= nodes; root++) {
            // Number of unique BSTs with 'root-1' nodes on the left and 'nodes-root' nodes on the right
            dp[nodes] += dp[root - 1] * dp[nodes - root];
        }
    }

    // The result is the number of unique BSTs that can be formed with 'n' nodes
    return dp[n];       
}
