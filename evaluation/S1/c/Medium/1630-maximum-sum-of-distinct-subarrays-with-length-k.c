long long maximumSubarraySum(int* nums, int numsSize, int k) {
    long long max_sum = 0;
    long long current_sum = 0;
    int count[100001] = {0};
    int distinct_count = 0;

    for (int i = 0; i < numsSize; i++) {
        current_sum += nums[i];
        count[nums[i]]++;
        if (count[nums[i]] == 1) {
            distinct_count++;
        }

        if (i >= k) {
            current_sum -= nums[i - k];
            count[nums[i - k]]--;
            if (count[nums[i - k]] == 0) {
                distinct_count--;
            }
        }

        if (i >= k - 1 && distinct_count == k) {
            if (current_sum > max_sum) {
                max_sum = current_sum;
            }
        }
    }

    return max_sum;     
}
