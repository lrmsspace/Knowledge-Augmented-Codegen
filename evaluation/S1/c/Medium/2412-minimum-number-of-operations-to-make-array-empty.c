int minOperations(int* nums, int numsSize) {
    #define MAX_NUM 1000000
    int freq[MAX_NUM + 1] = {0};
    for (int i = 0; i < numsSize; ++i) {
        freq[nums[i]]++;
    }

    int operations = 0;
    for (int i = 0; i <= MAX_NUM; ++i) {
        if (freq[i] == 0) {
            continue;
        }
        if (freq[i] == 1) {
            return -1;
        }
        operations += freq[i] / 3;
        if (freq[i] % 3 != 0) {
            operations++;
        }
    }

    return operations;      
}
