/**
 * Note: The returned array must be malloced, assume caller calls free().
 */
int* beautifulArray(int n, int* returnSize) {
    int *res = (int *)malloc(n * sizeof(int));
    int size = 1;
    res[0] = 1;

    while (size < n) {
        int newSize = 0;
        for (int i = 0; i < size; ++i) {
            int val = res[i] * 2 - 1;
            if (val <= n) {
                res[newSize++] = val;
            }
        }
        for (int i = 0; i < size; ++i) {
            int val = res[i] * 2;
            if (val <= n) {
                res[newSize++] = val;
            }
        }
        size = newSize;
    }

    *returnSize = size;
    return res;     
}
