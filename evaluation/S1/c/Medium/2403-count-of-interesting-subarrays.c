long long countInterestingSubarrays(int* nums, int numsSize, int modulo, int k) {
    long long result = 0;
    int* count = (int*)calloc(modulo, sizeof(int));
    count[0] = 1;  // Base case: empty subarray

    int prefixMod = 0;
    for (int i = 0; i < numsSize; i++) {
        if (nums[i] % modulo == k) {
            prefixMod = (prefixMod + 1) % modulo;
        }

        int targetMod = (prefixMod - k + modulo) % modulo;
        result += count[targetMod];
        count[prefixMod]++;
    }

    free(count);
    return result;      
}
