int maxProduct(char* s) {
    int n = 0;
    while (s[n]) n++;

    int total_masks = 1 << n;
    int* dp = (int*)calloc(total_masks, sizeof(int));

    for (int mask = 1; mask < total_masks; mask++) {
        int left = 0, right = n - 1;
        int length = 0;

        while (left < right) {
            if (!(mask & (1 << left))) {
                left++;
                continue;
            }
            if (!(mask & (1 << right))) {
                right--;
                continue;
            }
            if (s[left] == s[right]) {
                length += (left == right) ? 1 : 2;
                left++;
                right--;
            } else {
                break;
            }
        }

        if (left >= right) {
            dp[mask] = length;
        }
    }

    int max_product = 0;
    for (int mask = 1; mask < total_masks; mask++) {
        if (dp[mask] == 0) continue;

        int submask = mask;
        while (submask) {
            submask = (submask - 1) & mask;
            int complement_mask = ((total_masks - 1) ^ mask);
            if (dp[complement_mask & (~submask)] > 0) {
                int product = dp[mask] * dp[complement_mask & (~submask)];
                if (product > max_product) {
                    max_product = product;
                }
            }
        }
    }

    free(dp);
    return max_product;     
}
