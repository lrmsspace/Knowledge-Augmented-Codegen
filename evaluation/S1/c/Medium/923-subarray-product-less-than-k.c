int numSubarrayProductLessThanK(int* nums, int numsSize, int k) {
    if (k <= 1) return 0;

    int left = 0, right;
    int product = 1;
    int count = 0;

    for (right = 0; right < numsSize; right++) {
        product *= nums[right];

        while (product >= k) {
            product /= nums[left];
            left++;
        }

        count += right - left + 1;
    }

    return count;       
}
