int brokenCalc(int startValue, int target) {
    int operations = 0;

    while (target > startValue) {
        operations++;
        if (target % 2 == 1) {
            target++;  // Reverse of subtracting 1
        } else {
            target /= 2;  // Reverse of multiplying by 2
        }
    }

    // If startValue is greater than target, we can only subtract
    operations += startValue - target;

    return operations;      
}
