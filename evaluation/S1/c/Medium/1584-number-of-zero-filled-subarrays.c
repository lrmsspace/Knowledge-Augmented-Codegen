long long zeroFilledSubarray(int* nums, int numsSize) {
    long long result = 0;
    long long count = 0;

    for (int i = 0; i < numsSize; i++) {
        if (nums[i] == 0) {
            count++;
            result += count;
        } else {
            count = 0;
        }
    }

    return result;      
}
