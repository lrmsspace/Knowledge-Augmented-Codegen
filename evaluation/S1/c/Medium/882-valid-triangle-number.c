int triangleNumber(int* nums, int numsSize) {
    // Sort the array to facilitate the triangle inequality check
    for (int i = 0; i < numsSize - 1; i++) {
        for (int j = 0; j < numsSize - i - 1; j++) {
            if (nums[j] > nums[j + 1]) {
                int temp = nums[j];
                nums[j] = nums[j + 1];
                nums[j + 1] = temp;
            }
        }
    }

    int count = 0;

    // Iterate through each pair (i, j) and find the maximum k
    for (int i = 0; i < numsSize - 2; i++) {
        for (int j = i + 1; j < numsSize - 1; j++) {
            int k = j + 1;
            while (k < numsSize && nums[i] + nums[j] > nums[k]) {
                k++;
            }
            count += k - j - 1;
        }
    }

    return count;       
}
