bool canConstruct(char* s, int k) {
    int char_count[26] = {0};
    int odd_count = 0;

    // Count the frequency of each character in the string
    for (char* p = s; *p != '\0'; p++) {
        char_count[*p - 'a']++;
    }

    // Count how many characters have an odd frequency
    for (int i = 0; i < 26; i++) {
        if (char_count[i] % 2 != 0) {
            odd_count++;
        }
    }

    // We can construct k palindrome strings if the number of odd frequency characters
    // is less than or equal to k and k is less than or equal to the length of s
    int length = 0;
    for (char* p = s; *p != '\0'; p++) {
        length++;
    }

    return odd_count <= k && k <= length;       
}
