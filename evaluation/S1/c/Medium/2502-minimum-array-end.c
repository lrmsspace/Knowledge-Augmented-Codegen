long long minEnd(int n, int x) {
    long long left = x, right = 1e15, answer = -1;

    while (left <= right) {
        long long mid = left + (right - left) / 2;
        int count = 0;

        for (long long num = mid; num >= x; num--) {
            if ((num & x) == x) {
                count++;
            }
            if (count >= n) {
                break;
            }
        }

        if (count >= n) {
            answer = mid;
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    return answer;      
}
