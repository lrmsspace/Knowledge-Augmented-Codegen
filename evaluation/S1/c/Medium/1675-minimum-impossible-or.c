int minImpossibleOR(int* nums, int numsSize) {
    // Create a boolean array to track which powers of two are present
    int maxPower = 31; // Since nums[i] <= 10^9, we only need to check up to 2^30
    int present[32] = {0}; // Initialize all to 0 (false)

    // Mark the presence of each power of two in the input array
    for (int i = 0; i < numsSize; i++) {
        int num = nums[i];
        for (int j = 0; j < maxPower; j++) {
            if (num & (1 << j)) {
                present[j] = 1; // Mark this power of two as present
            }
        }
    }

    // Find the smallest power of two that is not present
    for (int i = 0; i < maxPower; i++) {
        if (!present[i]) {
            return 1 << i; // Return the missing power of two
        }
    }

    // If all powers of two up to 2^30 are present, return 2^31
    return 1 << maxPower;       
}
