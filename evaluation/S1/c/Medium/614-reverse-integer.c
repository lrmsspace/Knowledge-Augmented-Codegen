int reverse(int x){
    int reversed = 0;
    while (x != 0) {
        int digit = x % 10;
        x /= 10;

        // Check for overflow before actually updating reversed
        if (reversed > (2147483647 - digit) / 10) return 0; // Positive overflow
        if (reversed < (-2147483648 - digit) / 10) return 0; // Negative overflow

        reversed = reversed * 10 + digit;
    }
    return reversed;
}
