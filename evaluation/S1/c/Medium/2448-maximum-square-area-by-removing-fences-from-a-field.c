int maximizeSquareArea(int m, int n, int* hFences, int hFencesSize, int* vFences, int vFencesSize) {
    // Helper function to find the maximum gap in a sorted array
    int maxGap(int* fences, int size, int limit) {
        // Sort the fences
        for (int i = 0; i < size - 1; i++) {
            for (int j = 0; j < size - i - 1; j++) {
                if (fences[j] > fences[j + 1]) {
                    int temp = fences[j];
                    fences[j] = fences[j + 1];
                    fences[j + 1] = temp;
                }
            }
        }

        // Calculate maximum gap
        int max_gap = fences[0] - 1; // Gap before the first fence
        for (int i = 1; i < size; i++) {
            int gap = fences[i] - fences[i - 1] - 1;
            if (gap > max_gap) {
                max_gap = gap;
            }
        }
        int last_gap = limit - fences[size - 1]; // Gap after the last fence
        if (last_gap > max_gap) {
            max_gap = last_gap;
        }

        return max_gap;
    }

    int max_h_gap = maxGap(hFences, hFencesSize, m);
    int max_v_gap = maxGap(vFences, vFencesSize, n);

    int side_length = (max_h_gap < max_v_gap) ? max_h_gap : max_v_gap;
    if (side_length <= 0) {
        return -1;
    }

    long long area = (long long)side_length * (long long)side_length;
    return (int)(area % 1000000007);        
}
