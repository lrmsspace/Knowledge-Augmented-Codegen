int maxChunksToSorted(int* arr, int arrSize) {
    int maxChunks = 0;
    int currentMax = -1;

    for (int i = 0; i < arrSize; i++) {
        if (arr[i] > currentMax) {
            currentMax = arr[i];
        }
        if (currentMax == i) {
            maxChunks++;
        }
    }

    return maxChunks;       
}
