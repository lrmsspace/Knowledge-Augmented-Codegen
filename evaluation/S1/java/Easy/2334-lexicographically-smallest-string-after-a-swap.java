class Solution {
    private boolean hasSameParity(char a, char b) {
        return ((a - '0') % 2) == ((b - '0') % 2);
    }
    public String getSmallestString(String s) {
        char[] arr = s.toCharArray();
        int n = arr.length;

        for (int i = 0; i < n - 1; i++) {
            for (int j = i + 1; j < n; j++) {
                if (hasSameParity(arr[i], arr[j])) {
                    // Swap
                    char temp = arr[i];
                    arr[i] = arr[j];
                    arr[j] = temp;

                    String candidate = new String(arr);
                    if (candidate.compareTo(s) < 0) {
                        s = candidate;
                    }

                    // Swap back
                    temp = arr[i];
                    arr[i] = arr[j];
                    arr[j] = temp;
                }
            }
        }

        return s;           
    }
}
