class Solution {
    public int maxPower(String s) {
        int maxPower = 1;
        int currentPower = 1;

        for (int i = 1; i < s.length(); i++) {
            if (s.charAt(i) == s.charAt(i - 1)) {
                currentPower++;
            } else {
                maxPower = Math.max(maxPower, currentPower);
                currentPower = 1;
            }
        }

        maxPower = Math.max(maxPower, currentPower);

        return maxPower;            
    }
}
