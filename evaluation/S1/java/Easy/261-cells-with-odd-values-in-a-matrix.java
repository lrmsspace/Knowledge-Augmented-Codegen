class Solution {
    public int oddCells(int m, int n, int[][] indices) {
        int[] rowIncrements = new int[m];
        int[] colIncrements = new int[n];

        // Apply increments based on indices
        for (int[] index : indices) {
            rowIncrements[index[0]]++;
            colIncrements[index[1]]++;
        }

        int oddCount = 0;

        // Calculate the number of odd-valued cells
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if ((rowIncrements[i] + colIncrements[j]) % 2 != 0) {
                    oddCount++;
                }
            }
        }

        return oddCount;            
    }
}
