class Solution {
    public boolean hasMatch(String s, String p) {
        String[] parts = p.split("\\*", -1);
        String prefix = parts[0];
        String suffix = parts[1];

        if (s.length() < prefix.length() + suffix.length()) {
            return false;
        }

        if (!s.startsWith(prefix)) {
            return false;
        }

        if (!s.endsWith(suffix)) {
            return false;
        }

        return true;
    }
}
