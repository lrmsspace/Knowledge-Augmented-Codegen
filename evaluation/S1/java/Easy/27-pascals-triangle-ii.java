class Solution {
    public List<Integer> getRow(int rowIndex) {
        List<Integer> row = new ArrayList<>();
        row.add(1); // The first element is always 1

        for (int k = 1; k <= rowIndex; k++) {
            // Compute the next element using the previous one
            long prev = row.get(k - 1);
            long curr = prev * (rowIndex - k + 1) / k;
            row.add((int) curr);
        }

        return row;         
    }
}
