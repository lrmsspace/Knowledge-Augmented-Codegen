class Solution {
    public int numRookCaptures(char[][] board) {
        int rookRow = -1, rookCol = -1;
        // Locate the rook's position
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                if (board[i][j] == 'R') {
                    rookRow = i;
                    rookCol = j;
                    break;
                }
            }
            if (rookRow != -1) break;
        }

        int captures = 0;
        // Directions: up, down, left, right
        int[][] directions = {{-1, 0}, {1, 0}, {0, -1}, {0, 1}};

        for (int[] dir : directions) {
            int x = rookRow + dir[0];
            int y = rookCol + dir[1];

            while (x >= 0 && x < 8 && y >= 0 && y < 8) {
                if (board[x][y] == 'B') {
                    // Bishop blocks the path
                    break;
                } else if (board[x][y] == 'p') {
                    // Pawn can be captured
                    captures++;
                    break;
                }
                x += dir[0];
                y += dir[1];
            }
        }

        return captures;            
    }
}
