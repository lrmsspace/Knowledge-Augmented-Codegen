class Solution {
    private int gcd(int a, int b) {
        if (b == 0) {
            return a;
        }
        return gcd(b, a % b);
    }
    public boolean hasGroupsSizeX(int[] deck) {
        int[] count = new int[10000];
        for (int card : deck) {
            count[card]++;
        }

        int g = -1;
        for (int c : count) {
            if (c > 0) {
                if (g == -1) {
                    g = c;
                } else {
                    g = gcd(g, c);
                }
            }
        }

        return g >= 2;          
    }
}
