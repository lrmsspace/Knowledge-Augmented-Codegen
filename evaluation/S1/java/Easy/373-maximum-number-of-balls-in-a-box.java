class Solution {
    private int getDigitSum(int num) {
        int sum = 0;
        while (num > 0) {
            sum += num % 10;
            num /= 10;
        }
        return sum;
    }
    public int countBalls(int lowLimit, int highLimit) {
        int[] boxCounts = new int[46]; // Maximum sum of digits for numbers up to 10^5 is 45 (99999 -> 9+9+9+9+9=45)
        int maxBalls = 0;

        for (int ballNumber = lowLimit; ballNumber <= highLimit; ballNumber++) {
            int boxNumber = getDigitSum(ballNumber);
            boxCounts[boxNumber]++;
            maxBalls = Math.max(maxBalls, boxCounts[boxNumber]);
        }

        return maxBalls;            
    }
}
