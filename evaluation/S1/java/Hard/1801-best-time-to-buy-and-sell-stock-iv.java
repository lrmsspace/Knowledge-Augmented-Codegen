class Solution {
    public int maxProfit(int k, int[] prices) {
        int n = prices.length;
        if (n == 0) return 0;

        // If k is larger than n/2, we can make maximum number of transactions
        if (k >= n / 2) {
            int maxProfit = 0;
            for (int i = 1; i < n; i++) {
                if (prices[i] > prices[i - 1]) {
                    maxProfit += prices[i] - prices[i - 1];
                }
            }
            return maxProfit;
        }

        // DP array to store the maximum profit at each transaction count
        int[] dp = new int[k + 1];
        int[] minPrices = new int[k + 1];

        // Initialize minPrices with the first price
        for (int i = 0; i <= k; i++) {
            minPrices[i] = prices[0];
        }

        // Fill dp and minPrices arrays
        for (int price : prices) {
            for (int j = 1; j <= k; j++) {
                minPrices[j] = Math.min(minPrices[j], price - dp[j - 1]);
                dp[j] = Math.max(dp[j], price - minPrices[j]);
            }
        }

        return dp[k];           
    }
}
