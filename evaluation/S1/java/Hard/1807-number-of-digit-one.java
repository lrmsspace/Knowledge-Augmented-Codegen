class Solution {
    public int countDigitOne(int n) {
        int count = 0;
        long digit = 1; // Current digit position (1, 10, 100, ...)
        while (digit <= n) {
            long high = n / (digit * 10); // Higher part of the number
            long current = (n / digit) % 10; // Current digit
            long low = n % digit; // Lower part of the number

            if (current == 0) {
                count += high * digit;
            } else if (current == 1) {
                count += high * digit + low + 1;
            } else {
                count += (high + 1) * digit;
            }

            digit *= 10;
        }
        return count;           
    }
}
