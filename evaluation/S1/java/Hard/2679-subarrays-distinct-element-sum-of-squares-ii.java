class Solution {
    public int sumCounts(int[] nums) {
        final int MOD = 1_000_000_007;
        int n = nums.length;

        // last occurrence index of each number
        int[] last = new int[100_001];
        // prefix sum of distinct counts
        long[] prefixSum = new long[n + 1];
        long result = 0;

        for (int i = 0; i < n; i++) {
            int num = nums[i];
            int lastIndex = last[num];
            // distinct count for subarray ending at i
            long distinctCount = (i - lastIndex) + prefixSum[i] - prefixSum[lastIndex];
            distinctCount = (distinctCount % MOD + MOD) % MOD; // ensure non-negative

            prefixSum[i + 1] = (prefixSum[i] + distinctCount) % MOD;
            result = (result + distinctCount * distinctCount) % MOD;

            last[num] = i + 1; // update last occurrence index
        }

        return (int) result;            
    }
}
