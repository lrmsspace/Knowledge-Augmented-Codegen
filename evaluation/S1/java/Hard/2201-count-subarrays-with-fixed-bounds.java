class Solution {
    public long countSubarrays(int[] nums, int minK, int maxK) {
        long result = 0;
        int n = nums.length;

        int lastMinKIndex = -1;
        int lastMaxKIndex = -1;
        int leftBound = -1;

        for (int i = 0; i < n; i++) {
            if (nums[i] < minK || nums[i] > maxK) {
                leftBound = i;
            }
            if (nums[i] == minK) {
                lastMinKIndex = i;
            }
            if (nums[i] == maxK) {
                lastMaxKIndex = i;
            }

            int validStartIndex = Math.min(lastMinKIndex, lastMaxKIndex);
            if (validStartIndex > leftBound) {
                result += validStartIndex - leftBound;
            }
        }

        return result;          
    }
}
