class Solution {
    public long maxScore(int[] a, int[] b) {
        int n = b.length;
        long[][] dp = new long[5][n + 1];

        for (int i = 1; i <= 4; i++) {
            long maxDiff = Long.MIN_VALUE;
            for (int j = i; j <= n; j++) {
                maxDiff = Math.max(maxDiff, dp[i - 1][j - 1] - (long) a[i - 1] * b[j - 1]);
                dp[i][j] = maxDiff + (long) a[i - 1] * b[j - 1];
            }
        }

        return dp[4][n];            
    }
}
