class Solution {
    public long coloredCells(int n) {
        // The number of colored cells after n minutes forms a pattern based on the expansion of a diamond shape.
        // The formula for the total number of colored cells after n minutes is derived from the sum of the series:
        // Total cells = 2*n*(n-1) + 1
        // This accounts for the initial cell and the expansion in all four directions.

        return 2L * n * (n - 1) + 1;            
    }
}
