class Solution {
    public List<Integer> partitionLabels(String s) {
        // Map to store the last occurrence index of each character
        Map<Character, Integer> lastOccurrence = new HashMap<>();
        for (int i = 0; i < s.length(); i++) {
            lastOccurrence.put(s.charAt(i), i);
        }

        List<Integer> partitions = new ArrayList<>();
        int start = 0; // Start index of the current partition
        int end = 0;   // End index of the current partition

        for (int i = 0; i < s.length(); i++) {
            // Update the end index of the current partition
            end = Math.max(end, lastOccurrence.get(s.charAt(i)));

            // If the current index reaches the end of the partition
            if (i == end) {
                // Add the size of the partition to the result list
                partitions.add(end - start + 1);
                start = i + 1; // Update start for the next partition
            }
        }

        return partitions;          
    }
}
