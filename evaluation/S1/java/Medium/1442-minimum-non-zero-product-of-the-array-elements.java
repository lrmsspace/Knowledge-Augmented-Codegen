class Solution {
    private long modularExponentiation(long base, long exponent, long mod) {
        long result = 1;
        base = base % mod;
        while (exponent > 0) {
            if ((exponent & 1) == 1) {
                result = (result * base) % mod;
            }
            base = (base * base) % mod;
            exponent >>= 1;
        }
        return result;
    }
    public int minNonZeroProduct(int p) {
        long MOD = 1_000_000_007;
        long maxNum = (1L << p) - 1;
        long base = maxNum - 1;
        long exponent = (maxNum - 1) / 2;

        return (int) ((modularExponentiation(base, exponent, MOD) * maxNum) % MOD);         
    }
}
