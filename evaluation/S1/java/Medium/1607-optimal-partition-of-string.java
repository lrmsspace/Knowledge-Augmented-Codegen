class Solution {
    public int partitionString(String s) {
        int count = 1;
        boolean[] seen = new boolean[26];

        for (char c : s.toCharArray()) {
            if (seen[c - 'a']) {
                count++;
                seen = new boolean[26];
            }
            seen[c - 'a'] = true;
        }

        return count;           
    }
}
