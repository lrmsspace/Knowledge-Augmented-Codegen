class Solution {
    public long maximumTripletValue(int[] nums) {
        long maxValue = 0;
        int n = nums.length;

        // Step 1: Precompute the maximum value to the right of each index
        int[] maxRight = new int[n];
        maxRight[n - 1] = nums[n - 1];
        for (int i = n - 2; i >= 0; i--) {
            maxRight[i] = Math.max(maxRight[i + 1], nums[i]);
        }

        // Step 2: Iterate through the array to find the maximum triplet value
        int minLeft = nums[0];
        for (int j = 1; j < n - 1; j++) {
            // Update the minimum value to the left of index j
            minLeft = Math.min(minLeft, nums[j - 1]);

            // Calculate the triplet value if possible
            if (maxRight[j + 1] > 0) {
                long tripletValue = (long)(nums[j] - minLeft) * maxRight[j + 1];
                maxValue = Math.max(maxValue, tripletValue);
            }
        }

        return Math.max(maxValue, 0);           
    }
}
