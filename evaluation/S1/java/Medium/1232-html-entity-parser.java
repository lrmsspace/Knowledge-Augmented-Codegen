class Solution {
    public String entityParser(String text) {
        String[][] entities = {
            {"&quot;", "\""},
            {"&apos;", "'"},
            {"&amp;", "&"},
            {"&gt;", ">"},
            {"&lt;", "<"},
            {"&frasl;", "/"}
        };

        for (String[] entity : entities) {
            text = text.replace(entity[0], entity[1]);
        }

        return text;            
    }
}
