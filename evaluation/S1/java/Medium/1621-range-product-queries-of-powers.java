class Solution {
    private long modExp(long base, long exp, long mod) {
        long result = 1;
        base = base % mod;
        while (exp > 0) {
            if ((exp & 1) == 1) {
                result = (result * base) % mod;
            }
            base = (base * base) % mod;
            exp >>= 1;
        }
        return result;
    }
    private long modInverse(long a, long mod) {
        // Using Fermat's Little Theorem for mod inverse since mod is prime
        return modExp(a, mod - 2, mod);
    }
    public int[] productQueries(int n, int[][] queries) {
        final int MOD = 1_000_000_007;

        // Step 1: Decompose n into its powers of 2
        List<Integer> powers = new ArrayList<>();
        for (int i = 0; i < 31; i++) {
            if ((n & (1 << i)) != 0) {
                powers.add(i);
            }
        }

        int m = powers.size();
        long[] prefixProducts = new long[m + 1];
        prefixProducts[0] = 1;
        for (int i = 0; i < m; i++) {
            prefixProducts[i + 1] = (prefixProducts[i] * modExp(2, powers.get(i), MOD)) % MOD;
        }

        // Step 2: Process each query
        int qLen = queries.length;
        int[] results = new int[qLen];
        for (int i = 0; i < qLen; i++) {
            int left = queries[i][0];
            int right = queries[i][1];
            long product = (prefixProducts[right + 1] * modInverse(prefixProducts[left], MOD)) % MOD;
            results[i] = (int) product;
        }

        return results;         
    }
}
