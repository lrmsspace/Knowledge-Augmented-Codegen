class Solution {
    public Node connect(Node root) {
        if (root == null) {
            return null;
        }

        Node head = root; // head of the current level
        Node prev = null; // the leading node on the next level
        Node curr = null; // current node of current level

        while (head != null) {
            curr = head;
            head = null;
            prev = null;

            while (curr != null) {
                if (curr.left != null) {
                    if (prev != null) {
                        prev.next = curr.left;
                    } else {
                        head = curr.left;
                    }
                    prev = curr.left;
                }
                if (curr.right != null) {
                    if (prev != null) {
                        prev.next = curr.right;
                    } else {
                        head = curr.right;
                    }
                    prev = curr.right;
                }
                curr = curr.next;
            }
        }

        return root;            
    }
}
