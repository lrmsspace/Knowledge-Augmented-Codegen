class Solution {
    public int longestSquareStreak(int[] nums) {
        java.util.Arrays.sort(nums);
        java.util.Set<Integer> numSet = new java.util.HashSet<>();
        for (int num : nums) {
            numSet.add(num);
        }

        int maxLength = -1;
        for (int num : nums) {
            int length = 1;
            long current = num;
            while (numSet.contains((int)(current * current))) {
                current = current * current;
                length++;
            }
            if (length >= 2) {
                maxLength = Math.max(maxLength, length);
            }
        }

        return maxLength;           
    }
}
