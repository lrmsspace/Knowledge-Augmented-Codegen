class Solution {
    private int digitSum(long n) {
        int sum = 0;
        while (n > 0) {
            sum += n % 10;
            n /= 10;
        }
        return sum;
    }
    public long makeIntegerBeautiful(long n, int target) {
        long addition = 0;
        long factor = 1;

        while (digitSum(n) > target) {
            long next = factor * 10;
            long remainder = n % next;
            addition += next - remainder;
            n += next - remainder;
            factor *= 10;
        }

        return addition;            
    }
}
