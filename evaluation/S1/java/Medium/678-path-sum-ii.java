/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    private void backtrack(TreeNode node, int remainingSum, List<Integer> path, List<List<Integer>> result) {
        if (node == null) {
            return;
        }

        path.add(node.val);
        remainingSum -= node.val;

        // Check if it's a leaf node and the remaining sum is zero
        if (node.left == null && node.right == null && remainingSum == 0) {
            result.add(new ArrayList<>(path));
        } else {
            // Continue the search on left and right subtrees
            backtrack(node.left, remainingSum, path, result);
            backtrack(node.right, remainingSum, path, result);
        }

        // Backtrack: remove the last added node value
        path.remove(path.size() - 1);
    }
    public List<List<Integer>> pathSum(TreeNode root, int targetSum) {
        List<List<Integer>> result = new ArrayList<>();
        if (root == null) {
            return result;
        }
        backtrack(root, targetSum, new ArrayList<>(), result);
        return result;
    }
}
