class Solution {
    public int findMinDifference(List<String> timePoints) {
        // There are only 1440 minutes in a day, so if there are more than 1440 time points,
        // at least two must be the same, resulting in a minimum difference of 0.
        if (timePoints.size() > 1440) {
            return 0;
        }

        // Convert time points to minutes and sort them
        List<Integer> minutesList = new ArrayList<>();
        for (String time : timePoints) {
            String[] parts = time.split(":");
            int hours = Integer.parseInt(parts[0]);
            int minutes = Integer.parseInt(parts[1]);
            minutesList.add(hours * 60 + minutes);
        }
        Collections.sort(minutesList);

        // Find the minimum difference between consecutive time points
        int minDiff = Integer.MAX_VALUE;
        for (int i = 1; i < minutesList.size(); i++) {
            minDiff = Math.min(minDiff, minutesList.get(i) - minutesList.get(i - 1));
        }

        // Check the difference between the last and first time point across midnight
        minDiff = Math.min(minDiff, (1440 - minutesList.get(minutesList.size() - 1)) + minutesList.get(0));

        return minDiff;         
    }
}
