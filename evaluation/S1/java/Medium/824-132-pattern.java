class Solution {
    public boolean find132pattern(int[] nums) {
        int n = nums.length;
        if (n < 3) {
            return false;
        }

        // min[i] = minimum of nums[0..i]
        int[] min = new int[n];
        min[0] = nums[0];
        for (int i = 1; i < n; i++) {
            min[i] = Math.min(min[i - 1], nums[i]);
        }

        // Use a stack to keep track of potential "2" values in the "132" pattern
        java.util.Deque<Integer> stack = new java.util.ArrayDeque<>();

        // Traverse from the end to the beginning
        for (int j = n - 1; j >= 0; j--) {
            // We want to find a "3" (nums[j]) and a "2" (stack top) such that min[j] < stack top < nums[j]
            if (nums[j] > min[j]) {
                // Pop elements from the stack that are less than or equal to min[j]
                while (!stack.isEmpty() && stack.peek() <= min[j]) {
                    stack.pop();
                }
                // If the current top of the stack is less than nums[j], we found a "132" pattern
                if (!stack.isEmpty() && stack.peek() < nums[j]) {
                    return true;
                }
                // Push the current number as a potential "2" for future iterations
                stack.push(nums[j]);
            }
        }

        return false;           
    }
}
