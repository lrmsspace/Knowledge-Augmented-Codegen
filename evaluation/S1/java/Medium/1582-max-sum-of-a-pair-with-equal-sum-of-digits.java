class Solution {
    public int maximumSum(int[] nums) {
        // Map to store the two largest numbers for each digit sum
        Map<Integer, int[]> digitSumMap = new HashMap<>();
        int maxSum = -1;

        for (int num : nums) {
            int digitSum = getDigitSum(num);
            int[] topTwo = digitSumMap.getOrDefault(digitSum, new int[]{-1, -1});

            // Update the top two largest numbers for this digit sum
            if (num > topTwo[0]) {
                topTwo[1] = topTwo[0];
                topTwo[0] = num;
            } else if (num > topTwo[1]) {
                topTwo[1] = num;
            }

            digitSumMap.put(digitSum, topTwo);
        }

        // Calculate the maximum sum of pairs with equal digit sums
        for (int[] topTwo : digitSumMap.values()) {
            if (topTwo[1] != -1) {
                maxSum = Math.max(maxSum, topTwo[0] + topTwo[1]);
            }
        }

        return maxSum;          
    }
}
