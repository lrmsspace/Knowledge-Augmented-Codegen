class Solution {
    public String getSmallestString(int n, int k) {
        char[] result = new char[n];
        // Start from the end of the string
        for (int i = n - 1; i >= 0; i--) {
            // Determine the maximum value we can assign to this position
            int maxCharValue = Math.min(26, k - i);
            result[i] = (char) ('a' + maxCharValue - 1);
            k -= maxCharValue;
        }
        return new String(result);          
    }
}
