class Solution {
    public long countGood(int[] nums, int k) {
        long result = 0;
        int left = 0;
        long count = 0;
        int n = nums.length;
        int[] freq = new int[100001];

        for (int right = 0; right < n; right++) {
            count += freq[nums[right]];
            freq[nums[right]]++;

            while (count >= k) {
                freq[nums[left]]--;
                count -= freq[nums[left]];
                left++;
            }

            result += left;
        }

        return result;          
    }
}
