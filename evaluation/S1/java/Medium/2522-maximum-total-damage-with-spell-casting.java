class Solution {
    public long maximumTotalDamage(int[] power) {
        Map<Integer, Long> damageMap = new HashMap<>();
        int maxPower = 0;
        for (int p : power) {
            damageMap.put(p, damageMap.getOrDefault(p, 0L) + p);
            maxPower = Math.max(maxPower, p);
        }

        long[] dp = new long[maxPower + 1];
        for (int i = 1; i <= maxPower; i++) {
            long damage = damageMap.getOrDefault(i, 0L);
            dp[i] = Math.max(dp[i - 1], dp[Math.max(0, i - 3)] + damage);
        }

        return dp[maxPower];            
    }
}
