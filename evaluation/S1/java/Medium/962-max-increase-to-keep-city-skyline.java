class Solution {
    public int maxIncreaseKeepingSkyline(int[][] grid) {
        int n = grid.length;
        int[] maxRow = new int[n];
        int[] maxCol = new int[n];

        // Calculate max heights for each row and column
        for (int r = 0; r < n; r++) {
            for (int c = 0; c < n; c++) {
                if (grid[r][c] > maxRow[r]) {
                    maxRow[r] = grid[r][c];
                }
                if (grid[r][c] > maxCol[c]) {
                    maxCol[c] = grid[r][c];
                }
            }
        }

        int totalIncrease = 0;

        // Calculate the total possible increase
        for (int r = 0; r < n; r++) {
            for (int c = 0; c < n; c++) {
                int allowedHeight = Math.min(maxRow[r], maxCol[c]);
                totalIncrease += allowedHeight - grid[r][c];
            }
        }

        return totalIncrease;           
    }
}
