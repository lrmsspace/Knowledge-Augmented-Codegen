class Solution {
    public boolean validateStackSequences(int[] pushed, int[] popped) {
        java.util.Stack<Integer> stack = new java.util.Stack<>();
        int j = 0;

        for (int x : pushed) {
            stack.push(x);
            while (!stack.isEmpty() && stack.peek() == popped[j]) {
                stack.pop();
                j++;
            }
        }

        return stack.isEmpty();         
    }
}
