class Solution {
    public List<Integer> sequentialDigits(int low, int high) {
        List<Integer> result = new ArrayList<>();
        String digits = "123456789";

        int lowLength = Integer.toString(low).length();
        int highLength = Integer.toString(high).length();

        for (int length = lowLength; length <= highLength; length++) {
            for (int start = 0; start <= digits.length() - length; start++) {
                String subStr = digits.substring(start, start + length);
                int num = Integer.parseInt(subStr);
                if (num >= low && num <= high) {
                    result.add(num);
                }
            }
        }

        return result;          
    }
}
