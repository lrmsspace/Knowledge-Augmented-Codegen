class Solution {
public:
    int maximumSum(vector<int>& nums) {
        // Map from digit sum to the largest two numbers with that digit sum
        unordered_map<int, pair<int, int>> digitSumMap;
        int maxSum = -1;

        for (int num : nums) {
            int sumDigits = 0;
            int temp = num;
            while (temp > 0) {
                sumDigits += temp % 10;
                temp /= 10;
            }

            auto& entry = digitSumMap[sumDigits];
            if (num > entry.first) {
                entry.second = entry.first;
                entry.first = num;
            } else if (num > entry.second) {
                entry.second = num;
            }

            if (entry.second != 0) {
                maxSum = max(maxSum, entry.first + entry.second);
            }
        }

        return maxSum;          
    }
};
