class Solution {
public:
    vector<int> findOriginalArray(vector<int>& changed) {
        int n = changed.size();
        if (n % 2 != 0) return {};

        const int MAX_VAL = 100000;
        vector<int> freq(MAX_VAL + 1, 0);
        for (int val : changed) {
            freq[val]++;
        }

        vector<int> original;
        for (int val = 0; val <= MAX_VAL; ++val) {
            if (freq[val] > freq[2 * val]) {
                return {};
            }
            for (int count = 0; count < freq[val]; ++count) {
                original.push_back(val);
            }
            freq[2 * val] -= freq[val];
        }

        return original;            
    }
};
