class Solution {
public:
    vector<int> findAnagrams(string s, string p) {
        vector<int> res;
        if (s.length() < p.length()) return res;

        vector<int> pCount(26, 0);
        vector<int> sCount(26, 0);

        for (char c : p) {
            pCount[c - 'a']++;
        }

        int pLen = p.length();
        for (int i = 0; i < s.length(); ++i) {
            sCount[s[i] - 'a']++;

            if (i >= pLen) {
                sCount[s[i - pLen] - 'a']--;
            }

            if (sCount == pCount) {
                res.push_back(i - pLen + 1);
            }
        }

        return res;         
    }
};
