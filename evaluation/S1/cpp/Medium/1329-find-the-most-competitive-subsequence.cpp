class Solution {
public:
    vector<int> mostCompetitive(vector<int>& nums, int k) {
        int n = nums.size();
        vector<int> stack;
        stack.reserve(k);
        int to_remove = n - k;

        for (int i = 0; i < n; ++i) {
            while (!stack.empty() && to_remove > 0 && stack.back() > nums[i]) {
                stack.pop_back();
                --to_remove;
            }
            stack.push_back(nums[i]);
        }

        // If there are still elements to remove, remove from the end
        while (to_remove > 0) {
            stack.pop_back();
            --to_remove;
        }

        // Resize to k elements in case we added extra
        stack.resize(k);
        return stack;           
    }
};
