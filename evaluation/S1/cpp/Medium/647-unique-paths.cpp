class Solution {
public:
    int uniquePaths(int m, int n) {
        // DP array to store the number of unique paths to each cell
        vector<vector<int>> dp(m, vector<int>(n, 0));

        // Base case: There's only one way to reach any cell in the first row or first column
        for (int i = 0; i < m; ++i) {
            dp[i][0] = 1;
        }
        for (int j = 0; j < n; ++j) {
            dp[0][j] = 1;
        }

        // Fill the DP table
        for (int i = 1; i < m; ++i) {
            for (int j = 1; j < n; ++j) {
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
            }
        }

        // The bottom-right cell contains the number of unique paths to reach it
        return dp[m - 1][n - 1];            
    }
};
