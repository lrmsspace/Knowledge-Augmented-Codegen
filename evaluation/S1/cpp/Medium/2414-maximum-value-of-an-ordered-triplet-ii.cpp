class Solution {
public:
    long long maximumTripletValue(vector<int>& nums) {
        long long max_value = 0;
        long long max_suffix = nums.back();
        long long min_prefix = nums[0];

        // Iterate from the second last element down to the second element
        for (int j = nums.size() - 2; j >= 1; --j) {
            // Calculate potential triplet value with current j
            long long current_value = (min_prefix - nums[j]) * max_suffix;
            if (current_value > max_value) {
                max_value = current_value;
            }

            // Update max_suffix and min_prefix
            if (nums[j] > max_suffix) {
                max_suffix = nums[j];
            }
            if (nums[j - 1] < min_prefix) {
                min_prefix = nums[j - 1];
            }
        }

        return max_value > 0 ? max_value : 0;           
    }
};
