class Solution {
public:
    int maxTotalReward(vector<int>& rewardValues) {
        int n = rewardValues.size();
        int maxReward = 0;
        for (int val : rewardValues) {
            if (val > maxReward) {
                maxReward += val;
            }
        }
        return maxReward;           
    }
};
