/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int maxProduct(TreeNode* root) {
        long long totalSum = 0;
        function<long long(TreeNode*)> computeTotalSum = [&](TreeNode* node) -> long long {
            if (!node) return 0;
            long long leftSum = computeTotalSum(node->left);
            long long rightSum = computeTotalSum(node->right);
            long long subtreeSum = leftSum + rightSum + node->val;
            return subtreeSum;
        };
        totalSum = computeTotalSum(root);

        long long maxProd = 0;
        const int MOD = 1e9 + 7;
        function<long long(TreeNode*)> findMaxProduct = [&](TreeNode* node) -> long long {
            if (!node) return 0;
            long long leftSum = findMaxProduct(node->left);
            long long rightSum = findMaxProduct(node->right);
            long long subtreeSum = leftSum + rightSum + node->val;

            // Calculate product for the split at this edge
            long long product = subtreeSum * (totalSum - subtreeSum);
            if (product > maxProd) {
                maxProd = product;
            }

            return subtreeSum;
        };
        findMaxProduct(root);

        return static_cast<int>(maxProd % MOD);         
    }
};
