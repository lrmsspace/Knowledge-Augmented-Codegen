class Solution {
public:
    int deleteAndEarn(vector<int>& nums) {
        const int MAX_NUM = 10000;
        vector<int> points(MAX_NUM + 1, 0);
        for (int num : nums) {
            points[num] += num;
        }

        int n = points.size();
        vector<int> dp(n, 0);
        dp[0] = points[0];
        dp[1] = max(points[0], points[1]);

        for (int i = 2; i < n; ++i) {
            dp[i] = max(dp[i - 1], dp[i - 2] + points[i]);
        }

        return dp[n - 1];           
    }
};
