class Solution {
public:
    int maxWidthRamp(vector<int>& nums) {
        int n = nums.size();
        vector<int> leftMin(n);
        leftMin[0] = nums[0];
        for (int i = 1; i < n; ++i) {
            leftMin[i] = min(leftMin[i - 1], nums[i]);
        }

        int maxWidth = 0;
        int j = 0;
        for (int i = 0; i < n; ++i) {
            while (j < n && nums[j] >= leftMin[i]) {
                maxWidth = max(maxWidth, j - i);
                ++j;
            }
        }

        return maxWidth;            
    }
};
