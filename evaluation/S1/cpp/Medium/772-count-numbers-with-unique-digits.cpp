class Solution {
public:
    int countNumbersWithUniqueDigits(int n) {
        if (n == 0) return 1;
        if (n == 1) return 10;

        int count = 10; // for n = 1
        int uniqueDigits = 9; // choices for the first digit (1-9)
        int availableDigits = 9; // choices for the remaining digits (0-9 excluding the first digit)

        for (int i = 2; i <= n && i <= 10; ++i) {
            uniqueDigits *= availableDigits;
            count += uniqueDigits;
            --availableDigits;
        }

        return count;           
    }
};
