class Solution {
public:
    bool canMakeSubsequence(string str1, string str2) {
        int n = str1.size();
        int m = str2.size();
        int j = 0; // Pointer for str2

        for (int i = 0; i < n && j < m; ++i) {
            char c1 = str1[i];
            char c2 = str2[j];

            // Check if we can match str2[j] with str1[i] or str1[i] incremented
            if (c1 == c2 || (c1 == 'z' && c2 == 'a') || (c1 + 1 == c2)) {
                j++; // Move to the next character in str2
            }
        }

        return j == m; // If we have matched all characters in str2         
    }
};
