class Solution {
public:
    int minDeletions(string s) {
        vector<int> freq(26, 0);
        for (char c : s) {
            freq[c - 'a']++;
        }

        unordered_set<int> usedFrequencies;
        int deletions = 0;

        for (int f : freq) {
            while (f > 0 && usedFrequencies.count(f)) {
                f--;
                deletions++;
            }
            if (f > 0) {
                usedFrequencies.insert(f);
            }
        }

        return deletions;
    }
};
