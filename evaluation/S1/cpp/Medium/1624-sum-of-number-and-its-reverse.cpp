class Solution {
public:
    bool sumOfNumberAndReverse(int num) {
        for (int i = 0; i <= num; ++i) {
            int rev = 0, x = i;
            while (x > 0) {
                rev = rev * 10 + (x % 10);
                x /= 10;
            }
            if (i + rev == num) return true;
        }
        return false;           
    }
};
