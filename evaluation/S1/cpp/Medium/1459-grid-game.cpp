class Solution {
public:
    long long gridGame(vector<vector<int>>& grid) {
        int n = grid[0].size();
        vector<long long> prefixRow0(n + 1, 0);
        vector<long long> prefixRow1(n + 1, 0);

        for (int i = 0; i < n; ++i) {
            prefixRow0[i + 1] = prefixRow0[i] + grid[0][i];
            prefixRow1[i + 1] = prefixRow1[i] + grid[1][i];
        }

        long long result = LLONG_MAX;

        for (int col = 0; col < n; ++col) {
            long long topRight = prefixRow0[n] - prefixRow0[col + 1];
            long long bottomLeft = prefixRow1[col];
            long long secondRobotScore = max(topRight, bottomLeft);
            result = min(result, secondRobotScore);
        }

        return result;          
    }
};
