class Solution {
public:
    int longestSubsequence(vector<int>& arr, int difference) {
        unordered_map<int, int> dp;
        int best = 1;
        for (int val : arr) {
            int prev = val - difference;
            int currLen = dp.count(prev) ? dp[prev] + 1 : 1;
            dp[val] = currLen;
            if (currLen > best) best = currLen;
        }
        return best;            
    }
};
