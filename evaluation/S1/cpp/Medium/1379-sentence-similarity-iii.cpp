class Solution {
public:
    bool areSentencesSimilar(string sentence1, string sentence2) {
        vector<string> words1, words2;
        split(sentence1, words1);
        split(sentence2, words2);

        int n1 = words1.size();
        int n2 = words2.size();
        if (n1 < n2) {
            return areSentencesSimilar(sentence2, sentence1);
        }

        int l = 0, r = 0;
        while (l < n2 && words1[l] == words2[l]) {
            ++l;
        }
        while (r < n2 - l && words1[n1 - 1 - r] == words2[n2 - 1 - r]) {
            ++r;
        }
        return (l + r >= n2);           
    }
};
