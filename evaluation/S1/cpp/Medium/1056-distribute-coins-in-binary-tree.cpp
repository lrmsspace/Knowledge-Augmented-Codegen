/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int distributeCoins(TreeNode* root) {
        int moves = 0;

        // Helper function to perform DFS and calculate moves
        std::function<int(TreeNode*)> dfs = [&](TreeNode* node) -> int {
            if (!node) return 0;

            // Post-order traversal: process left and right children first
            int left_balance = dfs(node->left);
            int right_balance = dfs(node->right);

            // Current node's balance: coins - 1 + left_balance + right_balance
            int current_balance = node->val - 1 + left_balance + right_balance;

            // Accumulate moves needed for left and right subtrees
            moves += abs(left_balance) + abs(right_balance);

            return current_balance;
        };

        dfs(root);
        return moves;           
    }
};
