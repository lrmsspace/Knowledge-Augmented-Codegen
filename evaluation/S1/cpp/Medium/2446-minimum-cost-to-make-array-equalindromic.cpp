class Solution {
public:
    long long minimumCost(vector<int>& nums) {
        int n = nums.size();
        long long total = 0;
        for (int i = 0; i < n / 2; ++i) {
            total += abs(nums[i] - nums[n - 1 - i]);
        }
        return total;           
    }
};
