class Solution {
public:
    int maxAbsoluteSum(vector<int>& nums) {
        int maxSum = 0;
        int minSum = 0;
        int currentMax = 0;
        int currentMin = 0;

        for (int num : nums) {
            currentMax += num;
            if (currentMax < 0) {
                currentMax = 0;
            }
            if (currentMax > maxSum) {
                maxSum = currentMax;
            }

            currentMin += num;
            if (currentMin > 0) {
                currentMin = 0;
            }
            if (currentMin < minSum) {
                minSum = currentMin;
            }
        }

        return std::max(maxSum, -minSum);           
    }
};
