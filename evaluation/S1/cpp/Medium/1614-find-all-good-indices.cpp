class Solution {
public:
    vector<int> goodIndices(vector<int>& nums, int k) {
        int n = nums.size();
        vector<int> left(n, 1), right(n, 1);
        for (int i = 1; i < n; ++i) {
            if (nums[i] <= nums[i - 1]) {
                left[i] = left[i - 1] + 1;
            }
        }
        for (int i = n - 2; i >= 0; --i) {
            if (nums[i] <= nums[i + 1]) {
                right[i] = right[i + 1] + 1;
            }
        }

        vector<int> result;
        for (int i = k; i <= n - k - 1; ++i) {
            if (left[i - 1] >= k && right[i + 1] >= k) {
                result.push_back(i);
            }
        }
        return result;          
    }
};
