class Solution {
public:
    int maxProduct(vector<string>& words) {
        int n = words.size();
        std::vector<int> masks(n, 0);
        for (int i = 0; i < n; ++i) {
            for (char c : words[i]) {
                masks[i] |= (1 << (c - 'a'));
            }
        }

        int best = 0;
        for (int i = 0; i < n; ++i) {
            for (int j = i + 1; j < n; ++j) {
                if ((masks[i] & masks[j]) == 0) {
                    int product = static_cast<int>(words[i].size()) * static_cast<int>(words[j].size());
                    if (product > best) {
                        best = product;
                    }
                }
            }
        }

        return best;            
    }
};
