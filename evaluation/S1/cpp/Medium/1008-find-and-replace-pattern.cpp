class Solution {
public:
    vector<string> findAndReplacePattern(vector<string>& words, string pattern) {
        auto encode = [](const string &s) -> string {
            string code;
            code.reserve(s.size());
            int next = 0;
            int mapping[26];
            for (int i = 0; i < 26; ++i) mapping[i] = -1;
            for (char ch : s) {
                int idx = ch - 'a';
                if (mapping[idx] == -1) {
                    mapping[idx] = next++;
                }
                code.push_back('a' + mapping[idx]);
            }
            return code;
        };

        string pattern_code = encode(pattern);
        vector<string> result;
        for (const string &word : words) {
            if (encode(word) == pattern_code) {
                result.push_back(word);
            }
        }
        return result;          
    }
};
