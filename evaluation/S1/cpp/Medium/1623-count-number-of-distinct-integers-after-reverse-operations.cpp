class Solution {
public:
    int countDistinctIntegers(vector<int>& nums) {
        const int MAX_NUM = 1000000;
        bool seen[MAX_NUM + 1] = { false };

        for (int num : nums) {
            seen[num] = true;

            // Reverse the digits of num
            int reversed = 0;
            int temp = num;
            while (temp > 0) {
                reversed = reversed * 10 + (temp % 10);
                temp /= 10;
            }
            seen[reversed] = true;
        }

        int distinctCount = 0;
        for (int i = 0; i <= MAX_NUM; ++i) {
            if (seen[i]) {
                distinctCount++;
            }
        }

        return distinctCount;           
    }
};
