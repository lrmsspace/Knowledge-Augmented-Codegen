class Solution {
public:
    string modifyString(string s) {
        int n = s.size();
        for (int i = 0; i < n; i++) {
            if (s[i] == '?') {
                for (char c = 'a'; c <= 'z'; c++) {
                    bool canUse = true;
                    if (i > 0 && s[i - 1] == c) {
                        canUse = false;
                    }
                    if (i < n - 1 && s[i + 1] == c) {
                        canUse = false;
                    }
                    if (canUse) {
                        s[i] = c;
                        break;
                    }
                }
            }
        }
        return s;           
    }
};
