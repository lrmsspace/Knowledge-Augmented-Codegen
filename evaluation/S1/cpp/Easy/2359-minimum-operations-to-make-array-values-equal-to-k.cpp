class Solution {
public:
    int minOperations(vector<int>& nums, int k) {
        unordered_set<int> unique_greater;
        for (int num : nums) {
            if (num < k) {
                return -1;  // Impossible to make all elements equal to k
            } else if (num > k) {
                unique_greater.insert(num);
            }
        }
        return unique_greater.size();           
    }
};
