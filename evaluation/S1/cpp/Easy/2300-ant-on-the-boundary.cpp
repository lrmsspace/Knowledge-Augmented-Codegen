class Solution {
public:
    int returnToBoundaryCount(vector<int>& nums) {
        int position = 0;
        int returnCount = 0;

        for (int move : nums) {
            position += move;
            if (position == 0) {
                returnCount++;
            }
        }

        return returnCount;         
    }
};
