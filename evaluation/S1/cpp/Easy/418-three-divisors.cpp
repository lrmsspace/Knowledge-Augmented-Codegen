class Solution {
public:
    bool isThree(int n) {
        if (n < 4) return false;  // The smallest number with exactly 3 divisors is 4 (1, 2, 4)
        
        int root = static_cast<int>(sqrt(n));
        if (root * root != n) return false;  // n must be a perfect square
        
        // Check if root is prime
        for (int i = 2; i * i <= root; ++i) {
            if (root % i == 0) {
                return false;  // root has a divisor other than 1 and itself
            }
        }
        
        return true;  // n has exactly three divisors: 1, root, and n           
    }
};
