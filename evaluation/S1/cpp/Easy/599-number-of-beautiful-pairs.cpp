class Solution {
public:
    int countBeautifulPairs(vector<int>& nums) {
        int count = 0;
        int n = nums.size();
        
        auto firstDigit = [](int num) {
            while (num >= 10) {
                num /= 10;
            }
            return num;
        };
        
        auto lastDigit = [](int num) {
            return num % 10;
        };
        
        for (int i = 0; i < n; i++) {
            int fd = firstDigit(nums[i]);
            for (int j = i + 1; j < n; j++) {
                int ld = lastDigit(nums[j]);
                if (__gcd(fd, ld) == 1) {
                    count++;
                }
            }
        }
        
        return count;           
    }
};
