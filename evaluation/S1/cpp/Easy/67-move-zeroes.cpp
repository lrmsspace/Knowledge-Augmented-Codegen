class Solution {
public:
    void moveZeroes(vector<int>& nums) {
        int n = nums.size();
        int lastNonZeroIndex = 0;

        // Move all non-zero elements to the front
        for (int i = 0; i < n; i++) {
            if (nums[i] != 0) {
                nums[lastNonZeroIndex++] = nums[i];
            }
        }

        // Fill the remaining positions with zeros
        for (int i = lastNonZeroIndex; i < n; i++) {
            nums[i] = 0;
        }           
    }
};
