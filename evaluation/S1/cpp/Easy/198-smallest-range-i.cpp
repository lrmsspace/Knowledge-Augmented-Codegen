class Solution {
public:
    int smallestRangeI(vector<int>& nums, int k) {
        int minNum = INT_MAX;
        int maxNum = INT_MIN;
        for (int num : nums) {
            if (num < minNum) minNum = num;
            if (num > maxNum) maxNum = num;
        }
        int range = maxNum - minNum;
        if (range <= 2 * k) {
            return 0;
        } else {
            return range - 2 * k;
        }           
    }
};
