class Solution {
public:
    int longestPalindrome(string s) {
        unordered_map<char, int> charCount;
        for (char c : s) {
            charCount[c]++;
        }

        int length = 0;
        bool hasOddCount = false;

        for (const auto& entry : charCount) {
            int count = entry.second;
            length += (count / 2) * 2;  // Add the largest even number <= count
            if (count % 2 == 1) {
                hasOddCount = true;  // At least one character has an odd count
            }
        }

        // If there's at least one character with an odd count, we can place one in the center
        if (hasOddCount) {
            length += 1;
        }

        return length;          
    }
};
