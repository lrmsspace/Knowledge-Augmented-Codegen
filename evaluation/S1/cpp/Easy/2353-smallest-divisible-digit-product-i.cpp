class Solution {
public:
    int smallestNumber(int n, int t) {
        if (t == 0) {
            return 10;
        }
        if (t == 1) {
            return n;
        }
        
        for (int num = n; ; num++) {
            int prod = 1;
            int temp = num;
            while (temp > 0) {
                int digit = temp % 10;
                prod *= digit;
                temp /= 10;
            }
            if (prod % t == 0) {
                return num;
            }
        }
        
        return -1; // Should never reach here           
    }
};
