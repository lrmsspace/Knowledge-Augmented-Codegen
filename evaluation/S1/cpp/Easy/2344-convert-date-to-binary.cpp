class Solution {
public:
    string convertDateToBinary(string date) {
        stringstream ss(date);
        string yearStr, monthStr, dayStr;
        getline(ss, yearStr, '-');
        getline(ss, monthStr, '-');
        getline(ss, dayStr, '-');

        int year = stoi(yearStr);
        int month = stoi(monthStr);
        int day = stoi(dayStr);

        return toBinary(year) + "-" + toBinary(month) + "-" + toBinary(day);            
    }
};
