class Solution {
public:
    int differenceOfSum(vector<int>& nums) {
        int elementSum = 0;
        int digitSum = 0;

        for (int num : nums) {
            elementSum += num;

            // Calculate digit sum for the current number
            while (num > 0) {
                digitSum += num % 10;
                num /= 10;
            }
        }

        return abs(elementSum - digitSum);          
    }
};
