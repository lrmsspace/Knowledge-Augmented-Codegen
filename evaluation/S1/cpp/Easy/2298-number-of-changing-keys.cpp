class Solution {
public:
    int countKeyChanges(string s) {
        int changes = 0;
        for (size_t i = 1; i < s.length(); ++i) {
            if (tolower(s[i]) != tolower(s[i - 1])) {
                changes++;
            }
        }
        return changes;         
    }
};
