class Solution {
public:
    bool findSubarrays(vector<int>& nums) {
        unordered_set<int> seenSums;
        for (size_t i = 0; i + 1 < nums.size(); ++i) {
            int currentSum = nums[i] + nums[i + 1];
            if (seenSums.count(currentSum)) {
                return true;
            }
            seenSums.insert(currentSum);
        }
        return false;           
    }
};
