class Solution {
public:
    int minTimeToType(string word) {
        int totalTime = 0;
        char currentChar = 'a';

        for (char targetChar : word) {
            int clockwiseDist = abs(targetChar - currentChar);
            int counterClockwiseDist = 26 - clockwiseDist;
            totalTime += min(clockwiseDist, counterClockwiseDist) + 1; // +1 for typing the character
            currentChar = targetChar;
        }

        return totalTime;           
    }
};
