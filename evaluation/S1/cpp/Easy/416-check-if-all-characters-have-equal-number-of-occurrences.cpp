class Solution {
public:
    bool areOccurrencesEqual(string s) {
        unordered_map<char, int> freq;
        for (char c : s) {
            freq[c]++;
        }
        
        int expected = freq.begin()->second;
        for (const auto& entry : freq) {
            if (entry.second != expected) {
                return false;
            }
        }
        
        return true;            
    }
};
