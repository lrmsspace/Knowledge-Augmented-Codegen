class Solution {
public:
    int maximumPopulation(vector<vector<int>>& logs) {
        vector<int> population(101, 0);  // Years from 1950 to 2050

        // Count population changes
        for (const auto& log : logs) {
            int birth = log[0];
            int death = log[1];
            population[birth - 1950]++;   // Person is born
            population[death - 1950]--;   // Person dies
        }

        int maxPopulation = 0;
        int currentPopulation = 0;
        int earliestYear = 1950;

        // Calculate the year with maximum population
        for (int year = 0; year <= 100; ++year) {
            currentPopulation += population[year];
            if (currentPopulation > maxPopulation) {
                maxPopulation = currentPopulation;
                earliestYear = year + 1950;
            }
        }

        return earliestYear;            
    }
};
