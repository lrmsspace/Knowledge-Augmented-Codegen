class Solution {
public:
    double average(vector<int>& salary) {
        int minSalary = INT_MAX;
        int maxSalary = INT_MIN;
        int totalSalary = 0;
        int n = salary.size();

        for (int sal : salary) {
            minSalary = std::min(minSalary, sal);
            maxSalary = std::max(maxSalary, sal);
            totalSalary += sal;
        }

        return static_cast<double>(totalSalary - minSalary - maxSalary) / (n - 2);
    }
};
