class Solution {
public:
    int rearrangeCharacters(string s, string target) {
        vector<int> countS(26, 0);
        vector<int> countTarget(26, 0);
        
        for (char c : s) {
            countS[c - 'a']++;
        }
        
        for (char c : target) {
            countTarget[c - 'a']++;
        }
        
        int maxCopies = INT_MAX;
        
        for (int i = 0; i < 26; i++) {
            if (countTarget[i] > 0) {
                maxCopies = min(maxCopies, countS[i] / countTarget[i]);
            }
        }
        
        return maxCopies;           
    }
};
