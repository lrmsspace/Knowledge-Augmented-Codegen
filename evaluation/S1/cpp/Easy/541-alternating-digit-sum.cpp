class Solution {
public:
    int alternateDigitSum(int n) {
        int sum = 0;
        bool add = true;
        
        while (n > 0) {
            int digit = n % 10;
            if (add) {
                sum += digit;
            } else {
                sum -= digit;
            }
            add = !add;
            n /= 10;
        }
        
        return -sum;  // Reverse the sign since we processed from least significant digit           
    }
};
