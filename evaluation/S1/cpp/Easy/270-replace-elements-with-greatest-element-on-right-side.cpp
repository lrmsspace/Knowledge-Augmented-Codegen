class Solution {
public:
    vector<int> replaceElements(vector<int>& arr) {
        int n = arr.size();
        vector<int> result(n, -1);
        int maxRight = -1;

        for (int i = n - 1; i >= 0; --i) {
            result[i] = maxRight;
            if (arr[i] > maxRight) {
                maxRight = arr[i];
            }
        }

        return result;          
    }
};
