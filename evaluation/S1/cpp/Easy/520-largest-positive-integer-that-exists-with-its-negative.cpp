class Solution {
public:
    int findMaxK(vector<int>& nums) {
        unordered_set<int> numSet;
        int maxK = -1;

        for (int num : nums) {
            numSet.insert(num);
        }

        for (int num : nums) {
            if (num > 0 && numSet.count(-num)) {
                maxK = max(maxK, num);
            }
        }

        return maxK;            
    }
};
