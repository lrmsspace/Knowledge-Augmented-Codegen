class Solution {
public:
    int maxProduct(int n) {
        int digits[10];
        int count = 0;
        while (n > 0) {
            digits[count++] = n % 10;
            n /= 10;
        }
        int max1 = -1, max2 = -1;
        for (int i = 0; i < count; i++) {
            if (digits[i] > max1) {
                max2 = max1;
                max1 = digits[i];
            } else if (digits[i] > max2) {
                max2 = digits[i];
            }
        }
        return max1 * max2;         
    }
};
