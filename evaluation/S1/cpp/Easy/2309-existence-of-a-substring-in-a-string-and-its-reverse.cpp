class Solution {
public:
    bool isSubstringPresent(string s) {
        unordered_set<string> substrings;
        int n = s.length();

        // Store all substrings of length 2
        for (int i = 0; i < n - 1; ++i) {
            substrings.insert(s.substr(i, 2));
        }

        // Check for reversed substrings of length 2
        for (int i = n - 1; i > 0; --i) {
            string revSub = "";
            revSub += s[i];
            revSub += s[i - 1];
            if (substrings.find(revSub) != substrings.end()) {
                return true;
            }
        }

        return false;           
    }
};
