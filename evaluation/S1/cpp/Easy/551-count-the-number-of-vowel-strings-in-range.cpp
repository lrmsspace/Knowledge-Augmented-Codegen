class Solution {
public:
    int vowelStrings(vector<string>& words, int left, int right) {
        int count = 0;
        for (int i = left; i <= right; ++i) {
            const string& word = words[i];
            if (isVowel(word.front()) && isVowel(word.back())) {
                ++count;
            }
        }
        return count;           
    }
};
