class Solution {
public:
    long long maximumTripletValue(vector<int>& nums) {
        int n = nums.size();
        long long maxValue = 0;

        for (int j = 1; j < n - 1; ++j) {
            int maxLeft = 0;
            for (int i = 0; i < j; ++i) {
                if (nums[i] > maxLeft) {
                    maxLeft = nums[i];
                }
            }

            int maxRight = 0;
            for (int k = j + 1; k < n; ++k) {
                if (nums[k] > maxRight) {
                    maxRight = nums[k];
                }
            }

            long long currentValue = static_cast<long long>(maxLeft - nums[j]) * maxRight;
            if (currentValue > maxValue) {
                maxValue = currentValue;
            }
        }

        return maxValue > 0 ? maxValue : 0;         
    }
};
