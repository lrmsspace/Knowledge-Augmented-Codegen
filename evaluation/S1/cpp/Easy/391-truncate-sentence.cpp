class Solution {
public:
    string truncateSentence(string s, int k) {
        int spaceCount = 0;
        int pos = 0;
        while (pos < s.size() && spaceCount < k - 1) {
            if (s[pos] == ' ') {
                spaceCount++;
            }
            pos++;
        }
        // If we reached the end of the string, return the whole string
        if (pos == s.size()) {
            return s;
        }
        return s.substr(0, pos - 1); // Exclude the last space          
    }
};
