class Solution {
public:
    vector<int> runningSum(vector<int>& nums) {
        int n = nums.size();
        vector<int> running_sum(n, 0);
        int current_sum = 0;
        for (int i = 0; i < n; ++i) {
            current_sum += nums[i];
            running_sum[i] = current_sum;
        }
        return running_sum;         
    }
};
