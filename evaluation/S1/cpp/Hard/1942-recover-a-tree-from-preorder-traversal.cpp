/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    TreeNode* recoverFromPreorder(string traversal) {
        int pos = 0;
        return recover(traversal, pos, 0);          
    }
private:
    TreeNode* recover(const string& traversal, int& pos, int depth) {
        int n = traversal.size();
        int curr_depth = 0;
        int start = pos;

        // Count dashes to determine depth
        while (pos < n && traversal[pos] == '-') {
            curr_depth++;
            pos++;
        }

        // If current depth does not match expected depth, backtrack
        if (curr_depth != depth) {
            pos = start; // Reset position
            return nullptr;
        }

        // Read the node value
        int val_start = pos;
        while (pos < n && isdigit(traversal[pos])) {
            pos++;
        }
        int val = stoi(traversal.substr(val_start, pos - val_start));
        TreeNode* node = new TreeNode(val);

        // Recursively build left and right subtrees
        node->left = recover(traversal, pos, depth + 1);
        node->right = recover(traversal, pos, depth + 1);

        return node;
    }
};
