class Solution {
public:
    string orderlyQueue(string s, int k) {
        if (k > 1) {
            sort(s.begin(), s.end());
            return s;
        } else {
            string ss = s + s;
            string res = s;
            int n = s.size();
            for (int i = 1; i < n; ++i) {
                string candidate = ss.substr(i, n);
                if (candidate < res) {
                    res = candidate;
                }
            }
            return res;
        }           
    }
};
