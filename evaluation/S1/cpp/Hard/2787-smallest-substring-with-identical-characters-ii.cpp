class Solution {
public:
    int minLength(string s, int numOps) {
        int n = s.size();
        int left = 1, right = n;

        auto can = [&](int len) {
            int count = 0;
            for (int i = 0; i <= n - len; ) {
                char c = s[i];
                int j = i;
                while (j < n && s[j] == c) j++;
                int segmentLen = j - i;
                if (segmentLen < len) {
                    count += len - segmentLen;
                    i = j;
                } else {
                    i += len;
                }
            }
            return count <= numOps;
        };

        while (left < right) {
            int mid = left + (right - left) / 2;
            if (can(mid)) {
                right = mid;
            } else {
                left = mid + 1;
            }
        }

        return left;            
    }
};
