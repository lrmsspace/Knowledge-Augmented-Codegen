class Solution {
public:
    int numSubmatrixSumTarget(vector<vector<int>>& matrix, int target) {
        int rows = matrix.size();
        if (rows == 0) return 0;
        int cols = matrix[0].size();
        int result = 0;

        // Precompute prefix sums for each row
        for (int r = 0; r < rows; ++r) {
            for (int c = 1; c < cols; ++c) {
                matrix[r][c] += matrix[r][c - 1];
            }
        }

        // Iterate over all pairs of columns
        for (int left = 0; left < cols; ++left) {
            for (int right = left; right < cols; ++right) {
                unordered_map<int, int> prefixSumCount;
                prefixSumCount[0] = 1; // Base case: one way to have sum zero
                int currSum = 0;

                // Calculate the sum of submatrices between columns left and right
                for (int r = 0; r < rows; ++r) {
                    currSum += matrix[r][right] - (left > 0 ? matrix[r][left - 1] : 0);
                    result += prefixSumCount[currSum - target];
                    prefixSumCount[currSum]++;
                }
            }
        }

        return result;          
    }
};
