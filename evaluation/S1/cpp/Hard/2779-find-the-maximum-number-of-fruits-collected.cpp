class Solution {
public:
    int maxCollectedFruits(vector<vector<int>>& fruits) {
        int n = fruits.size();
        vector<vector<int>> dp1(n, vector<int>(n, 0));
        vector<vector<int>> dp2(n, vector<int>(n, 0));
        vector<vector<int>> dp3(n, vector<int>(n, 0));

        // DP for child starting at (0,0)
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < n; ++j) {
                int fromTop = (i > 0) ? dp1[i - 1][j] : 0;
                int fromLeft = (j > 0) ? dp1[i][j - 1] : 0;
                int fromTopLeft = (i > 0 && j > 0) ? dp1[i - 1][j - 1] : 0;
                dp1[i][j] = fruits[i][j] + max({fromTop, fromLeft, fromTopLeft});
            }
        }

        // DP for child starting at (0,n-1)
        for (int i = 0; i < n; ++i) {
            for (int j = n - 1; j >= 0; --j) {
                int fromTop = (i > 0) ? dp2[i - 1][j] : 0;
                int fromRight = (j < n - 1) ? dp2[i][j + 1] : 0;
                int fromTopRight = (i > 0 && j < n - 1) ? dp2[i - 1][j + 1] : 0;
                dp2[i][j] = fruits[i][j] + max({fromTop, fromRight, fromTopRight});
            }
        }

        // DP for child starting at (n-1,0)
        for (int i = n - 1; i >= 0; --i) {
            for (int j = 0; j < n; ++j) {
                int fromBottom = (i < n - 1) ? dp3[i + 1][j] : 0;
                int fromLeft = (j > 0) ? dp3[i][j - 1] : 0;
                int fromBottomLeft = (i < n - 1 && j > 0) ? dp3[i + 1][j - 1] : 0;
                dp3[i][j] = fruits[i][j] + max({fromBottom, fromLeft, fromBottomLeft});
            }
        }
        int maxFruits = 0;
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < n; ++j) {
                int total = dp1[i][j] + dp2[i][j] + dp3[i][j] - 2 * fruits[i][j];
                maxFruits = max(maxFruits, total);
            }
        }
        return maxFruits;
    }
};
