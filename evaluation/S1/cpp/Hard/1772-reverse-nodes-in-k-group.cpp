/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* reverseKGroup(ListNode* head, int k) {
        if (k <= 1 || !head) return head;

        ListNode dummy(0, head);
        ListNode* prevGroupEnd = &dummy;

        while (true) {
            ListNode* groupStart = prevGroupEnd->next;
            ListNode* groupEnd = prevGroupEnd;

            // Check if there are k nodes left in the list
            for (int i = 0; i < k && groupEnd; ++i) {
                groupEnd = groupEnd->next;
            }
            if (!groupEnd) break; // Not enough nodes to reverse

            ListNode* nextGroupStart = groupEnd->next;

            // Reverse the k nodes
            ListNode* prev = nextGroupStart;
            ListNode* curr = groupStart;
            while (curr != nextGroupStart) {
                ListNode* temp = curr->next;
                curr->next = prev;
                prev = curr;
                curr = temp;
            }

            // Connect the previous part with the reversed group
            prevGroupEnd->next = groupEnd;
            prevGroupEnd = groupStart; // Move to the end of the reversed group
        }

        return dummy.next;          
    }
};
