class Solution {
public:
    int getXORSum(vector<int>& arr1, vector<int>& arr2) {
        int xor1 = 0;
        for (int num : arr1) {
            xor1 ^= num;
        }
        
        int xor2 = 0;
        for (int num : arr2) {
            xor2 ^= num;
        }
        
        return xor1 & xor2;         
    }
};
