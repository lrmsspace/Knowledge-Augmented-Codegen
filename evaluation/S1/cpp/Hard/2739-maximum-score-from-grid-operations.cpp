class Solution {
public:
    long long maximumScore(vector<vector<int>>& grid) {
        int n = grid.size();
        int m = grid[0].size();
        vector<int> colMin(n, INT_MAX);
        for (int j = 0; j < m; ++j) {
            int colMinVal = INT_MAX;
            for (int i = 0; i < n; ++i) {
                if (grid[i][j] < colMinVal) {
                    colMinVal = grid[i][j];
                }
            }
            for (int i = 0; i < n; ++i) {
                if (grid[i][j] == colMinVal) {
                    if (grid[i][j] < colMin[i]) {
                        colMin[i] = grid[i][j];
                    }
                }
            }
        }
        long long maxScore = 0;
        for (int i = 0; i < n; ++i) {
            if (colMin[i] != INT_MAX) {
                maxScore += colMin[i];
            }
        }
        return maxScore;            
    }
};
